<blockquote>
  <h4>An appreciation by David Scantlebury </h4>
  <p>John Edwin Oakley Mayne, always know as &quot;Jack&quot;, was born on the 21st of May
  1909 the only child of Sidney Mayne and Annie Oakley. He grew up in Luton, attending
  Dunstable Grammar School. He had a great sense of family and family traditions and often
  recalled the occasion when his grandfather, the then Mayor of Luton, was called upon to
  read the Riot Act after celebrations at the end of the war got out of hand and the Town
  Hall was burnt down. His grandfather and great uncle were provisions merchants with a
  stall at Luton market. They were both elected mayor on various occasions and always
  ensured that they did not serve on the market stall during terms of office. </p>
  <p>He was sure from an early age that he wanted to study chemistry and indeed read
  chemistry at the Royal College of Science, University of London obtaining his BSc in 1930.
  He received a PhD in 1934 and took his DSc in 1953. He joined the Wire Rope Section of the
  (then) Safety in Mines Research Board in 1930. He was still located at the Royal College
  of Science and his studies were submitted for a PhD which was entitled &quot;The Corrosion
  of Colliery Ropes&quot; the examiners being Sir Harold Carpenter and F.G.Donnan. This was
  the beginning of his lifetime's work in corrosion. His first publication was in 1936 on
  Water Line Corrosion. He became recognised internationally for his work, and has presented
  plenary lectures at many conferences around the world. In 1986 he was awarded the U.R.
  Evans Award by the Institute of Corrosion, a sword much admired by his grandchildren, and
  in 1993 the prestigious Cavallaro Medal for his research and publication in corrosion. </p>
  <p>He was offered a part time research position in the Corrosion group in the University
  of Cambridge, Department of Metallurgy by its leader Dr U.R. Evans in 1938 and he has
  remained in Cambridge ever since. When Evans retired in 1954, he took charge of the
  corrosion group and began supervising research students. </p>
  <p>Jack always hated waste and since his position with Evans was only part time, he
  decided to occupy the other part of his time by embarking on a parallel career. He chose
  to invest his time and energy in setting up a factory to produce water based paints and in
  particular emulsion polymerised vinyl acetate which were just beginning to replace
  white-wash as a white domestic finish. In 1939, Mayne was co-founder of Vinyl Products Ltd
  which subsequently became part of Reichhold Chemicals Ltd, and remained on the board of
  both companies until they were taken over in the 1970's by Unilever. From the
  &quot;proceeds&quot; of the takeover, Mayne donated a generous sum to the University of
  Cambridge, Department of Metallurgy, which together with the residue of the U.R.Evans
  estate forms the U.R.Evans fund devoted to encouraging research on aqueous corrosion. This
  fund has been used to support Tim Burstein during much of his time at Cambridge. </p>
  <p>Jack never let up on his research activities and has published a substantial corpus. He
  continued experimenting, writing, publishing and thinking about his subject until his
  death. Indeed formal retirement at seventy five did not slow down his rate of publication;
  the rate increased! His contribution to Corrosion Science was seminal in two subject
  areas; corrosion inhibitors and corrosion control by organic coatings. </p>
  <p>His work on inhibitors concerned inhibition by non-oxidising and oxidising anodic
  inhibitors in neutral solutions mainly on iron and steel. Jack attended the first Ferrara
  Congress in 1960 and presented his work on inhibitors at Ferrara every five years until
  1990. </p>
  <p>Jack's early work on paints with Evans looked at zinc-rich paints and the effect of
  pigment volume concentration the coating performance. He was also very interested in the
  role of contaminants and underfilm corrosion and much of his early work concerned the
  problems of painting rusty steel and subsequent paint performance, a research problem
  which is receiving much attention at the moment since traditional anti-corrosive pigments
  are now becoming environmentally unacceptable. His work on Resistance Inhibition in the
  50's forms the core of present day thinking on corrosion inhibition by paints. He showed
  that coatings are sufficiently permeable to oxygen and water so as not to stifle the
  cathodic reaction under the film and that the important processes are the movement of ions
  through the paint. Together with his research students this work continued by considering
  the ion permeability of coatings and conduction mechanisms, coating structure, solvents,
  inert and active pigments and soap formation. </p>
  <p>He was also an outstanding supervisor of research students both in his intellectual
  input to their projects, and his care for their further training. We all found him
  personally very approachable and unassuming, a most kind and generous man. Jack not being
  a Cambridge man always felt slightly outside the system; for many years the University of
  Cambridge did not recognise the University of London PhD and Jack was known as Mr Mayne.
  He never had a formal appointment at the University; indeed he never drew a salary for his
  activities. Both he and Mrs Mayne always went out of their way to welcome strangers to the
  Department; many times these strangers ended up as close friends, accepting an offer of
  accommodation in the next door flat, or a visit to his boat on the Norfolk Broads, or to
  his flat at Woolacombe. His annual Christmas dinner party, for the whole group including
  the technician, cleaner and wives were splendid events. Mrs Mayne would prepare a
  delicious meal in her tiny kitchen and Jack would select the wines from his collection in
  the garage. The term &quot;Mayne criterion&quot; was coined to describe the situation when
  the number of empty bottles exceeded the number of persons sitting round the table. Jack
  would end up in the early hours imagining he could speak French and listening to Tom
  Lehrer. </p>
  <p>Lionel Shreir first met Jack in 1951; Lionel had just been appointed as a lecturer to
  the Battersea College of Technology and was planning to initiate research and write
  lecture courses in corrosion. Two weeks in Cambridge in Evans' laboratory, being looked
  after by Jack was the start of a long friendship. Anyone seeking an introduction to Jack's
  work is well advised to consult the latest edition of Shreir's book (1994) where there are
  articles by Jack on organic coatings and Thomas on inhibitors. </p>
  <p>Costi Edeleanu has said that Jack &quot;has provided both moral and in many cases
  material support to individuals who are now well known in the field of corrosion. There
  are any number of us who regard Jack as the most generous man we have ever met and even
  people like U.R.Evans and Sam Hoar would not have been as productive as they were, had it
  not been for the help Jack had given them.&quot; </p>
  <p>Throughout his life he had great enthusiasm for a wide range of subjects. He read
  widely in philosophy and history. He and Mrs Mayne were deeply interested in modern art.
  One of the joys of visiting his flat was to see original Picassos and Braques on the wall
  and Henry Moore bronzes on the shelves. Jack had an excellent wine collection, mainly
  French and red. When I was a research student, there was a family pilgrimage in the Rover
  to Bordeaux when they explored the various Chateaux Mayne and returned with samples which
  &quot;needed further study&quot;. </p>
  <p>His wife Margaret died in 1991, they had been married fifty one years. He remained
  remarkably fit and active: still maintaining and riding his bike around Cambridge until a
  few weeks ago. Jack's passing seems like the end of an era, the severing of the connection
  with the prewar Evans corrosion school. He will be sadly missed by his children and
  grandchildren, and his very many friends and colleagues in the United Kingdom and around
  the world. </p>
