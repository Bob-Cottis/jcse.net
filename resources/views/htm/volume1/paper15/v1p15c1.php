  <p>I have in hand clear experimental evidence that the story published in JCSE is false, not of original nature<sup>1</sup> and contains some serious,
  principal violations on sound corrosion protection science and technology.</p>
  <p>For example figure 2 ( the protection mechanism ) converts 1 atom oxygen into 2 hydoxyl ions, so being an real atom reactor. This is not
  acceptable.</p>
  <p>Fig. 3 is showing a case of contact corrosion, because Wessling used only one steel sample in that test. So, the panel was half painted with
  a zinc based coating and half with his PANI. Because the Pani produces a potential shift of about 150 mV, the respective surface was
  electrochemically more noble than the zinc coated one. Obviously contact corrosion takes place in such an
  arrangement. The result obtained on corrosion protection with PANI is thus
  completely wrong and contain no scientific information, except for the fact that the author has no idea on what he
  is dealing with.</p>
