  <h4>Scab Test:</h4>
  <p>&sect;18 A weakly-accelerated field test known as the &#145;SCAB-test&#146;, Swedish
  Standard SS 117211, was performed on the scribed samples. Prior to testing,
  samples were scribed down to the steel by cutting through the paint with a
  hand-held 1-mm wide stainless steel blade held against a steel ruler. The
  resultant scribe is 1 mm in width, oriented horizontally and symmetrically
  located, on each sample. In the SCAB-test, samples are set out at an outdoor
  exposure station at SCI&#146;s facilities in Stockholm, and sprayed twice-weekly
  with a 3% NaCl solution. Samples were placed 45<font face="Symbol">ï¿½</font>
  from the vertical, facing west, with the scribe horizontal. Testing was begun
  in July 1995 and ended in July 1997. After the field exposure was completed,
  samples were evaluated for maximum one-way creep from scribe, blistering,
  cracking, flaking, and rust intensity.</p>
  <h4>Salt Spray:</h4>
  <p>&sect;19 The salt spray test, ASTM B-117, was <u>not</u> performed in order to rank
  the coatings or provide any sort of comparison of the coating-substrate
  combinations used in this study. Rather, it was performed in order to see if
  exposure to a different corrosion environment - with constant wetness instead
  of cycling - would provide any additional clues interactions between
  substrates and polymers.</p>
  <p>&sect;20 Samples scribed as described previously were run in the salt spray test for
  ten days. The test was carried out in accordance with ASTM B-117; scribes were
  placed parallel to the ground. At the end of ten days, samples were evaluated
  for maximum one-way creep from scribe, blistering, cracking, flaking, and rust
  intensity.</p>
  <h4>Grazing-Angle Ftir:</h4>
  <p>&sect;21 FT-IR studies were performed to look for evidence of saponification. Two
  methods were used, the KBr (Potassium Bromide)-pellet method and the ATR
  (Attenuated Total Reflectance) method. The KBr-pellet method was used to
  analyse corrosion products and the ATR method was used to analyse the paint
  surface. For the KBr-pellet studies, the apparatus was a Biorad FTS-175C and
  for the ATR studies the apparatus was an UMA500 FT-IR microscope with an ATR
  objective.</p>
