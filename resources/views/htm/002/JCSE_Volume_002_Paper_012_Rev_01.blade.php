  <p>&sect;32 <b><u>Behavior of the acrylics:</b></u> The results for acrylics were quite
  surprising. Whereas the acrylics as a group experience creep from scribe and
  edge corrosion on blast-cleaned hot-rolled steel, the same paints degrade
  completely and flake off of the hot-dipped galvanized substrates. This is a
  complete reverse of what had been expected, and indeed does not agree with
  results from other trials [2].</p>
  
  <p>&sect;33 <b><u>Are acrylics vulnerable to saponification?</b></u> A possible clue is found
  in the Swedish pulp and paper industry standard SSG 1009, &quot;Systems for
  initial painting of hot dip zinc coated steel&quot; [3], which advises that
  zinc-coated surfaces should be painted with paints based on non-saponifiable
  binders. What is of interest here is that zinc-coated surfaces, either
  initially or as they undergo corrosion, can be expected to provide an alkali
  environment. The question then becomes, are acrylics vulnerable to
  saponification?</p>
  <p>&sect;34 Acrylics can be divided into two groups, acrylates (or polyacrylates) and
  methacrylates, depending upon the original monomer from which the polymer was
  built. (The difference lies in a methyl group attached to the backbone of the
  polymer molecule of a methacrylate in place of the hydrogen atom found in the
  acrylate.)</p>
  <p>&sect;35 Billmeyer, in his classic text on polymer science, states that methyl
  methacrylates are quite resistant to saponification -&#150;but that polyacrylates
  are not [4]. If an acrylic paint, therefore, is composed entirely of methyl
  methacrylate, it should not saponify. If, however, it is a polyacrylate or
  mixture of the two types, it may be vulnerable to saponification.</p>
