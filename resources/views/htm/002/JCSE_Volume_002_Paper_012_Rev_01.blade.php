  <p>&sect;38 A &#145;softer&#146; acrylate used as a copolymer could improve the ability of
  the binder to flex and bend to some extent, which is desirable in a coating
  for cold crack resistance [6] or dimensionally active substrates such as wood
  [4].</p>
  <p>&sect;39 It seems reasonable to postulate that the binder in the acrylic paints is
  not composed entirely of methyl methacrylate, but of at least a copolymer; and
  that the particular binders used in this study saponified in the presence of
  zinc.</p>
  <p>&sect;40 However, this should not lead to the conclusion that all acrylic emulsion
  polymers on the market saponify, unless they are pure methacrylates. Other
  compositional variations, like the type of acrylate monomer used for
  co-polymerization, the polymer morphology, the molecular weight and the
  methods used to stabilize the emulsion in water are of major importance. And
  in fact all the paint ingredients, not merely the binder, must be carefully
  selected in order to achieve good performance on zinc.</p>
  
  <p>&sect;41 <b><u>What role does the zinc play?</b></u> The four waterborne acrylic paint
  systems showed markedly worse performance on galvanized steel than on carbon
  steel; the trend was just the opposite for the four epoxies (as expected).
  Therefore, it seems reasonable to conclude that these results are not due to
  some mistake in preparing the galvanized samples which resulted in an entirely
  unpaintable surface. It also seems reasonable to conclude that no drastic
  mistakes in application of the acrylics were made, since these paints showed a
  normal behavior on the other ferrous substrates. The degradation of the
  acrylics on the galvanized steel seems, therefore, to be due to an interaction
  between the zinc and the acrylic coatings.</p>
  <p>&sect;42 As discussed above, it is quite possible that the copolymers used in this
  study underwent saponification and failed. But what role, precisely, did the
  zinc play in this? There are several possiblities:</p>
  <p>&sect;43 The initial adhesion to the zinc was poor. The polymer compositions used
  for these coatings were not optimized for zinc, but rather for carbon steel.
  The reactive groups on the polymers which are intended to adhere to the
  substrate, did not react well enough with zinc. The zinc does not receive
  sufficient corrosion protection from the coating; water and oxygen can reach
  the zinc surface and initiate corrosion of the zinc. This corrosion causes an
  alkali environment at the zinc-paint interface, causing saponification of the
  polymer.</p>
  <p>&sect;44 The zinc substrate was not wetted, due to paint formulation rather than
  polymer design. The reactive groups on the polymer may be ideally suited to
  adhere to zinc; but if the paint does not flow &#150; e.g., due to high viscosity
  &#150; enough across zinc to wet the substrate surface, the polymer never has a
  chance to adhere tightly. As in the previous case, water and oxygen can reach
  the zinc surface and initiate corrosion of the zinc. This causes an alkali
  environment at the zinc-paint interface, causing saponification of the
  polymer.</p>
  <p>&sect;45 The zinc causes further crosslinking of the binder during cure, depending
  on which monomers are used. Crosslinking goes too far and the coating becomes
  brittle. Weathering stresses such as temperature change and wetting/drying
  cycles break down the cured coating. Saponification is not the major mechanism
  of degradation in this theory.</p>
  <p>&sect;46 Zn<sup>++</sup> ions destroyed the coulombic stability of the dispersion,
  leading to flocculation. The paint is in effect destroyed after being applied
  but before a coherent film can form.</p>
  <p>&sect;47 Zinc ions, together with organic acids in the paint (from additives, rather
  than the binder), form a zinc soap which in turn breaks down the cured paint.
  The formation of metal soaps by the reaction of metal compounds with organic
  acids is a well known phenomenon. It is known, for example, that this is the
  mechanism by which red-lead paint protects steel. The acids may be present in
  these paints already at its production, due to additives used rather than the
  binder. Different metal soaps have, however, different properties. The lead
  soaps formed in red lead paints based on linseed oil are known to be elastic,
  adhering and very resistant to water. Zinc soaps, in contrast to lead soaps,
  are known to be brittle and to be water soluble [7]; the latter can be
  expected to increase the driving force for the water transport through the
  paint film towards the metal surface.</p>
  
  <p>&sect;48 <b><u>Has evidence of saponification been observed?</b></u> Grazing-angle FTIR
  studies were performed on the samples which had been exposed at the outdoor
  station for two years, and on the samples which had been in the salt spray
  test for ten days. The paint surface which had been immediately adjacent to
  the metal was examined for evidence of zinc soaps. The problem with this
  technique was that to examine the paint at this interface, it was necessary to
  look at areas where delamination had occurred; and since delamination was
  always associated with corrosion of the underlying substrate in these samples,
  these were areas heavily contaminated with corrosion products. The only
  identifiable spectra observed, were those attributable to the corrosion
  products Zn<sub>5</sub>(OH)<sub>8</sub>Cl<sub>2<font face="Symbol">ï¿½</font> </sub>H<sub>2</sub>O
  and ZnO. An interesting peak was seen on all the samples at 1385cm<sup>-1</sup>.
  Paints all have a peak here, but for these samples it was sharper than normal.</p>
  <p>&sect;49 Samples with only the primer of the system which had shown the most
  degradation, Acrylic K, were studied with the FTIR before and after exposure
  to solutions containing ZnCl, and NaOH. A solution of ZnCl in distilled water
  (pH 5 after addition of ZnCl) was used; the other solution was sodium
  hydroxide in water, with pH 11. A solution of zinc ions in an <u>alkali </u>solution
  would have been of interest, of course, but was not possible; at alkali
  conditions the zinc precipitates out of solution and the pH drops sharply.
  After 1340 hours of exposure to the ZnCl solution, no change was seen in the
  FTIR spectra of the Acrylic K primer. The sample of Acrylic K primer which was
  exposed to NaOH at pH 11, on the other hand, showed a change in peaks at
  1380cm<sup>-1</sup> and 1029cm<sup>-1</sup>. After 113 hours' exposure to the
  alkali solution, these peaks had had increased height in comparison to the
  other peaks. After 1340 hours' exposure to the solution, both the height and
  width were acccentuated. The conclusion seems to be that the mere presence of
  zinc is not enough to affect the <i>cured</i> coating; an alkali environment
  is needed.</p>
  <p>&sect;50 The salt spray results hint that zinc, or corrosion products, or both, are
  responsible for the acrylics' degradation. The pattern of blistering seen on
  the acrylics after 10 days&#146; salt spray testing is quite different from that
  seen after two years&#146; SCAB exposure. After the salt spray testing, blisters
  are dramatically concentrated under the scribe, and seem to follow the lines
  of the liquid running down from the scribe. Because of the constant wetness in
  the salt spray chamber, the zinc in galvanized or galvalume coatings undergoes
  a different corrosion mechanism from that which would occur in a field
  exposure; the zinc rapidly dissolves without forming corrosion products in the
  salt spray chamber.<sup> </sup>Thus the runoff liquid under the scribe may be
  expected to contain zinc, and alkaline corrosion products, from the inorganic
  coating exposed at the scribe. It is not known whether the blistering seen in
  the runoff lines under the scribe is due to a reaction in these areas between
  the paint and the zinc in the runoff liquid, or between the paint and the
  alkaline zinc corrosion products.</p>
  <h3>Conclusions</h3>
  <p>&sect;51 Waterborne acrylic coatings are frequently used on galvanized substrates
  with good results; however, the acrylic coatings used here performed much
  worse on galvanized steel than on carbon steel. A possible explanation is that
  the copolymers used for the binder were vulnerable to saponification.</p>
  <p>&sect;52 More work should be done in this area to identify more clearly the
  processes by which these acrylics degrade on the zinc surfaces. Evidence
  confirming or eliminating saponification as the cause of acrylic degradation,
  the actual mechanism of degradation (zinc surface producing an alkali
  environment leading to saponification, or zinc plus binder creating zinc
  soaps, et cetera), and which acrylic polymer chemistries and pretreatments
  before painting lead to a tendency to this, are areas which could benefit from
  future research.</p>
