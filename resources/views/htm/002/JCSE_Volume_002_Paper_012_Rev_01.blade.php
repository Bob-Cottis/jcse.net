<h3>&sect;1 Abstract</h3>
  <p>Trials have been conducted of four types of waterborne paints on various
  ferrous substrates. Samples were subjected to either a Scab test for two
  years, or ten days of salt spray testing.</p>
  <p>&sect;2 Interestingly, the acrylic paints all performed worse on galvanized steel
  than on carbon steel. This contradicts previous experience of waterborne
  acrylics at the Swedish Corrosion Institute and elsewhere. It was not
  possible, using grazing-angle FTIR, to establish what had happened; infrared
  results seem to indicate that an alkali environment is necessary to the
  degradation of the coatings; but the role which the zinc plays is not clearly
  defined.</p>
  <p>&sect;3 The conclusion drawn is not that waterborne acrylic coatings are unsuitable
  for galvanized substrates, but that further work is necessary to understand
  the interactions of zinc and waterborne acrylic polymers. Theories for why the
  zinc caused this include:</p>
  <p>&sect;4 Either the initial adhesion to the zinc was poor (a polymer composition
  problem) or the zinc substrate was not wetted (a paint formulation problem).
  In either case, water and oxygen can reach the zinc surface and initiate
  corrosion of the zinc. This causes an alkali environment at the zinc-paint
  interface, causing saponification of the polymer.</p>
  <p>&sect;5 The zinc is causing further crosslinking of the binder, depending on which
  monomers are used. Crosslinking goes too far and the coating becomes brittle.
  Weathering stresses such as temperature change and wetting/drying cycles break
  down the cured coating.</p>
  <p>&sect;6 Zn<sup>++</sup> ions destroyed the coulombic stability of the dispersion,
  leading to flocculation. The paint is in effect destroyed after being applied
  but before a coherent film can form.</p>
  <p>&sect;7 Zinc ions, together with organic acids in the paint (from additives, rather
  than the binder), form a zinc soap which in turn breaks down the cured paint.</p>
  <p>&sect;8 <b>Keywords: </b>acrylic waterbornes, zinc, galvanized steel, corrosion</p>
  <h3>Introduction</h3>
  <p>&sect;9 Waterborne paints are not simply solventborne paints in which the organic
  solvent has been replaced with water; the paint chemist must design an
  entirely new system from the ground up. Additives to keep the pigment from
  clumping, for example, may be completely different for a dispersion in a polar
  liquid such as water than in a nonpolar organic solvent. The same can be said
  for the chemicals added to make the pigments integrate with the binder, so
  that pigment particles are not simply occupying voids in the binder. The
  binder itself most frequently exists as a dispersion in waterborne paints. To
  keep the solid polymer in a stable and even dispersion, rather than in clumps
  at the bottom of the paint can, requires some creative use of additives. And
  of course, additives may be used to prevent flash rusting of the steel before
  enough water has evaporated for the polymer to form a coherent film which can
  provide corrosion protection.</p>
  <p>&sect;10 The implications of all this paint chemistry for coating-substrate
  interactions, and hence coating-substrate compatibility, is not well known.
  The subject is of more than academic interest: experience from the field shows
  that waterborne acrylic paints on galvanized surfaces sometimes perform
  extremely well &#150; but sometimes degrade quickly, for unknown reasons.</p>
  <p>&sect;11 The aim of this project was to examine the compatibility of various ferrous
  substrates with acrylic waterborne resins. Four different paints were studied.</p>
