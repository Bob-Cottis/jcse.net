  and oxygen permeability was relatively small compared to the effect of
  aluminium on cathodic disbonding. The transport of charge through the film
  increased with increasing aluminium content, i.e. opposite to the effect of
  the pigments on cathodic disbonding. Neither of the transport parameters
  correlates well with the disbonding rate. This indicates that the mechanism of
  the effect of the aluminium pigments on cathodic disbonding is something else
  than the barrier effect.</p>
  <h4><a name="_Toc414433478">Active Aluminium Pigments</a></h4>
  <p>&sect;32 The aluminium pigments are covered with oxide. According to the supplier
  the pigments contain 1-3% aluminium oxide. Leidheiser <i>et al</i>. have
  suggested that the oxide on the pigments may act as a buffer that decreases
  the pH beneath the film [1]. Hydroxide generated by the cathodic reaction at
  the steel/coating interface is generally accepted as being responsible for
  cathodic disbonding, as discussed in the introduction in Part 1. If hydroxide
  is removed from the steel/coating interface by buffering reactions with the
  coating, disbonding may also decrease. According to the Pourbaix-diagram for
  aluminium [10], solid aluminium oxide is not stable above pH 9-10 and will
  react with hydroxide (Equation 5-2). A simple experiment was performed to test
  the buffer theory. Epoxy 0 and Epoxy 10 were applied in two-coat films in the
  four possible combinations of the two paints. The application and coating
  thickness is illustrated in Figure 6. If there is a buffer effect from the
  aluminium pigments, it will be important to have the aluminium pigments in the
  first coat. Then only system 3 and 4 will show decreased cathodic disbonding.
  If the effect of the aluminium pigments on cathodic disbonding is a general
  barrier effect, then system 4 will perform best, system 2 and 3 will perform
  equally and system 1 will perform worst. The disbonding rates are given in
  Figure 7.</p>
