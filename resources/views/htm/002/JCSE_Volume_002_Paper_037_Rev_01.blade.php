  that the positive effect of the aluminium pigments is linked to their presence
  at the steel/coating interface.</p>
  <h4><a name="_Toc414433479">Corrosion of the Aluminium Pigments</a></h4>
  <p>&sect;36 Aluminium is not passive in aqueous solutions with pH above 9-10, and will
  corrode according to Reaction 5-3. Ritter <i>et al.</i>[11]<i> </i>and McLeod
  <i>et al.</i>[12]<i> </i>have found that the pH under the disbonded film can
  be as high as 14. We can then ask whether the aluminium pigments will corrode
  or be protected by the binder. If the pigments corrode they may gradually
  disappear and leave large voids in the film. The corrosion of the pigments may
  also have a positive effect on e.g. cathodic disbonding. Both oxygen and
  hydroxide are consumed in the corrosion reaction, which may decrease the
  disbonding rate. The buffer theory for the reduction in cathodic disbonding
  presented above presupposes that the sodium hydroxide under the coating has
  access to the pigments. If we can verify that the pigments corrode, it will
  also support the buffer theory.</p>
  <p>&sect;37 Figure 8 shows a micrograph of a cross section of Epoxy 10 after cathodic
  disbonding. The coating was 360 �m thick and the sample had been exposed for
  three months in substitute seawater at 25�C, polarised to -1050 mV SCE with a
  potentiostat. The disbonding propagated along the cross section, and the
  disbonded distance was about 10 mm. The section of the film shown in Figure 8
  is about 3 mm, and the disbonding front is to the right. The dark area in the
  cross section is filled with metallic aluminium flakes, while in the bright
  areas the aluminium pigments have corroded and are converted to aluminium
  oxide. In the left end of the section, the film has probably been disbonded
  for several weeks. Here the aluminium pigments have corroded into about 1/4 of
  the film thickness. Towards the disbonding front at right end of the picture,
  the corrosion of the pigments is less severe. Near the front, the bright area
  is only a thin line. Here the film has disbonded recently and only the
  pigments close to the interface have had time to corrode. Cross sections of
  Epoxy 10 outside the disbonded region (adhering coating), taken after two
  years exposure in the long term test in Part 1, showed no sign of pigment
  corrosion. The corrosion of pigments deep inside the film therefore seems to
  take place mainly after the coating has disbonded. Under an intact and
  adhering film, the cathodic reaction is probably too slow to give significant
  corrosion of the aluminium pigments.</p>
