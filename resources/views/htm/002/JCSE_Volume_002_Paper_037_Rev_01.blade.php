  <h4><a name="_Toc414433473">Cathodic Disbonding</a></h4>
  <p>&sect;15 Steel samples, 80 x 80 x 5 mm, were cut from mild steel. The samples were
  ground with emery paper to roughness R<sub>y</sub> = 5 �m. The paints were
  applied with an applicator in two coats. The first coat was allowed to cure
  for about 24 hours at room temperature before the second coat was applied.
  Coating thickness was measured with a magnetic gauge. The coatings were given
  a circular damage in the middle of the samples, 6 mm in diameter. Cathodic
  disbonding was tested in substitute seawater [3] at room temperature. The
  samples were polarised to -1050 mV SCE by a potentiostat. The disbonded
  diameter was measured in four directions, and the disbonded area was
  calculated from the average diameter.</p>
  <h4><a name="_Toc414433474">TEM Analysis of the Aluminium Pigments</a></h4>
  <p>&sect;16 Epoxy 10 was applied on ground steel plates in two coats with a total dry
  film thickness of 200 �m. The samples were exposed in substitute seawater at
  25�C, and polarised to -1050 mV SCE by a potentiostat. The panels were
  exposed until the disbonding front had moved about 10 mm from the initial
  holiday in the coating. TEM samples were prepared by ultramicrotomy [7]. Cross
