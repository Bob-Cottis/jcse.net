<h3>&sect;1 Abstract</h3>

  <p>In Part 1 it was shown that aluminium flake pigments decreased cathodic
  disbonding of epoxy coatings significantly. In this article the mechanism of
  the effect of the aluminium pigments is studied. Two possible mechanisms have
  been investigated: a diffusion barrier mechanism and a mechanism where the
  aluminium pigments are chemically active. Water uptake, water diffusion rate,
  oxygen diffusion rate and ionic resistance were measured as a function of
  aluminium content in the paint. Adding 10% aluminium pigments decreased the
  oxygen diffusion rate about six times. However, the oxygen permeability was
  much less affected. The water diffusion rate decreased only slightly. The
  ionic conductivity increased when the aluminium pigment concentration
  increased. It was shown that the aluminium pigments only decrease the rate of
  cathodic disbonding when they are applied in the first coat, directly on the
  steel. The effect was therefore related to their presence at the steel/coating
  interface. This indicates that the aluminium pigments were chemically active
  in decreasing cathodic disbonding, i.e. that they affected the environment on
  the steel/coating interface. It was also shown that the aluminium pigments in
  the disbonded film corroded. The alkaline environment under the disbonded
  coating therefore reached the pigment particles in the coating.</p>
  <p>&sect;2 <b>Keywords</b>: Organic coatings, cathodic disbonding, aluminium pigments</p>
  <h3><a name="_Toc414433468">I</a>ntroduction</h3>
  <p>&sect;3 In Part 1 we reported about the effect of aluminium and glass barrier
  pigments on cathodic disbonding of epoxy coatings. We then found that the
  barrier pigments had a large effect on cathodic disbonding. Epoxy and epoxy
  mastic binders were pigmented with between 0% and 20% (wt.) aluminium
  pigments. Coatings with 10% or more aluminium pigments applied on blast
  cleaned steel had no disbonding after 18 months exposure. After two years of
  testing the coating with 10% aluminium had disbonded about 3 mm. In the same
  test period the coatings without aluminium pigments disbonded 65 mm on
  average. When the coatings were applied on ground steel the disbonding of the
  coating without aluminium was two decades faster than the coating with 10%
  aluminium. The purpose with this paper is to investigate the mechanism for the
  effect of the aluminium pigments on cathodic disbonding.</p>
