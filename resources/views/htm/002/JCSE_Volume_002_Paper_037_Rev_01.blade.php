  <h3><a name="_Toc414433480">C</a>onclusion</h3>
  <p>&sect;44 In a previous article we have shown that aluminium pigments decrease the
  rate of cathodic disbonding for two different epoxy binders. In this article
  we have investigated two possibilities for mechanism behind the effect: A
  barrier mechanism and a mechanism where the aluminium pigments are chemically
  active.</p>
  <p>&sect;45 The aluminium pigments decreased the oxygen diffusion rate through the
  epoxy coating. However, the oxygen permeability coefficient was much less
  affected. The transport of water was only slightly affected by the aluminium
  pigments. The ionic conductivity of the epoxy increased when the aluminium
  pigment concentration increased.</p>
  <p>&sect;46 The aluminium pigments had insignificant effect on cathodic disbonding
  unless the aluminium containing film was applied directly on the steel
  surface. The effect was therefore connected to their presence near the
  steel/coating interface. This indicates that the aluminium pigments had an
  additional effect on cathodic disbonding besides any barrier effects. Others
  have suggested that buffering reactions with the oxide on the surface of the
  pigments may decrease cathodic disbonding. We have also shown that the
  aluminium pigments in the disbonded film corroded after cathodic disbonding.
  The alkaline environment under the disbonded coating therefore had access to
  the pigment particles in the coating.</p>
