  disbonding implies that the pigments decrease the transport of at least one of
  the reactants, and that this reactant affects the rate of disbonding. The rate
  of cathodic disbonding should then correlate with the transport rate of this
  species. Figure 5 shows the transport properties and disbonding rate for the
  films as function of aluminium pigment concentration. The disbonding rates are
  taken from the disbonding experiments on ground steel substrate in Part 1. The
  responses are normalised to the results for Epoxy 0.</p>
