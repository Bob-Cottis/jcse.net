  <p>&sect;5 The aluminium pigments are shaped as flakes and tend to orient themselves
  parallel to the substrate. They are therefore expected to improve the barrier
  properties of the coating [2]. The aluminium pigments may have decreased
  cathodic disbonding by decreasing the transport through the coating of either
  of the reactants in Equation 5-1. A barrier mechanism implies that the
  aluminium pigments reduce the transport through the coating of at least one
  reactant, and that this reactant affects the rate of the disbonding. We have
  therefore studied the transport of oxygen, water and ions through an epoxy
  film as function of the concentration of aluminium pigments. The amide cured
  epoxy binder used in the previous paper was used again, with the same
  concentrations of aluminium: 0%, 5%, 10% and 20% by weight in the wet paint.
  The paints are called Epoxy 0, Epoxy 5, Epoxy 10 and Epoxy 20.</p>
