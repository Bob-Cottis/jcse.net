  <h2>Blistering and Delamination Processes on Coated Steel</h2>
  <p>D. Greenfield &amp; J. D. Scantlebury<br>
  <i>Corrosion and Protection Centre, UMIST, PO Box 88, Manchester M60 1QD<br>
  <script>mailto2('Scantlebury','manchester.ac.uk')</script></i></p>
<h3>&sect;1 Abstract</h3>
  <p>The alkaline nature of the environment at the paint/metal interface has
  been addressed.&nbsp; A pH indicator has been incorporated into an alkyd
  coating and the alkalinity of the environment at the interface monitored as a
  function of time using time lapse photography.</p>
  <p>&sect;2 <b>Keywords:</b> Blistering, Delamination, Alkyd, Marine Environment.
  <h3>Introduction</h3>
  <p>&sect;3 When a coating is exposed to an aggressive medium, such as that found in a
  marine environment, the associated failure mechanisms of blistering and
  delamination are amongst the most important to be considered.&nbsp; These two
  modes of failure are often treated separately, but have so many common
  features that it could be argued that the differences are a matter of degree
  rather than type.&nbsp; There is a general consensus over the integrity of a
  typical coating and it is fair to assume that one will find defects, due to
  porosity or damage in service.&nbsp; Therefore the following considers the
  situation of an organically coated steel structure containing a defect, which
  is subjected to aggressive, immersed conditions.</p>
  <p>&sect;4 Due to the presence of imperfections in the coating, the steel substrate is
  directly exposed to its surroundings.&nbsp; This initiates a corrosion
  process, with the anodic reaction occurring at the defect, this reaction
  follows that shown in equation (1)</p>
