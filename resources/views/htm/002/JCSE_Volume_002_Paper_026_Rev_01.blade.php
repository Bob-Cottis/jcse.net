  <h3 align="left">Discussion</h3>
  <p>&sect;33 The results presented here show quite clearly that, for the coating
  considered, not only do blistering and delamination processes share many
  common features but that they may be considered as constituent parts of one
  overall process.&nbsp;</p>
  <p>&sect;34 The process starts with the initiation of blisters around the region of the
  point of damage.&nbsp; These blisters become larger as time progresses until
  they eventually coalesce.&nbsp; Once this has occurred, the delamination
  process takes over and the front progresses rapidly.&nbsp; As the coating
  delaminates from the fault, the pH at the interface behind it falls as the
  process continues.</p>
  <p>&sect;35 Figure 7 indicates that the processes occurring under the film are cyclic
  in nature, starting with an initiation time where there is no apparent
  activity.&nbsp; Of course, the indicator used only functions at higher pHs so
  there would be a delay in its response.&nbsp; Notwithstanding this, the
  initial delay period coupled with the apparent inactivity indicated by the
  plateau at about 100 hours does provide evidence of a cyclic process in line
  with that proposed by Scantlebury [10].</p>
  <h3>Conclusions</h3>
  <p>&sect;36 Blistering and delamination act in conjunction with each other in the
  failure of organic coatings.&nbsp;</p>
  <p>&sect;37 As blisters develop and grow, they coalesce to form a large disbonded
  region in the vicinity of a fault in the coating.&nbsp; This disbonded area
  provides the site for the delamination process to initiate and propagate.</p>
  <p>&sect;38 The initial delay time is repeated in a cyclic nature once the delamination
  reaches a critical point.&nbsp; At this point, the pH of the solution under
  the film at the disbonded area falls as the oxygen reduction reaction moves
  away from the fault.&nbsp; At this point, the delamination process gives way
  to further blistering under the intact coating.&nbsp; One could view the
  delamination front needing the blisters to act as stepping-stones in order to
  progress.</p>
