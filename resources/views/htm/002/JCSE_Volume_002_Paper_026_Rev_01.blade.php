  <p>&sect;18 The average DFT of the final coatings was 22mm.&nbsp; A 2mm hole was
  introduced into the cured coatings and the panels were fitted with a 30mm
  diameter perspex cylinder, 50mm high, which was filled with a 0.5M NaCl
  solution.&nbsp; The panels were left at ambient temperature for 10 days and
  were photographed at regular intervals.&nbsp; On the second run of the
  experiment, time-lapse photography was employed using a digital camera linked
  to a PC.&nbsp; Computerised videos were produced showing the progress of the
  breakdown of alkyd coatings, stills from these videos are included in the
  result section.</p>
