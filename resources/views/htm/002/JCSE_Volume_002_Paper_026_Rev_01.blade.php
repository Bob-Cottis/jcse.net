  <p>&sect;6 The ferrous ions produced in equation (1) go into solution and react to
  produce electrically neutral compounds through combination with cations in the
  medium.&nbsp; This leaves us with a charge imbalance.&nbsp; The environment
  has a surfeit of positive charge, in the form of whatever cations are present
  and the environment at the cathode is producing hydroxyl ions, resulting in an
  excess of negative charge.&nbsp; In the case of uncoated steel, the route for
  the counter-cations is straightforward.&nbsp; However, when the steel is
  coated the situation becomes more complicated.&nbsp; The path from the
  exposure environment to the cathodic site is either restricted or blocked
  completely.&nbsp;</p>
  <p>&sect;7 It was shown by Mayne [1] that coatings were so permeable to water and
  oxygen that their rate of arrival at the cathodic region was greater than that
  required for corrosion to proceed.&nbsp; Figure 1 shows a schematic
  representation of the results of the corrosion reactions, along the lines
  proposed by Schwenk [2].&nbsp; It can be seen that the cathodically produced
  hydroxyl is present at both the blistering and the delamination sites.&nbsp;
  The alkaline nature of this resultant solution at these sites is considered to
  be a major contributory factor in the failure of the coating.</p>
