  after [2]</b></p>
  <p>&sect;9 The reason this alkalinity causes such failure has been variously ascribed
  to saponification of the coating [3], dissolution of the oxide layer at the
  interface [4], alteration of the ionic resistance of the film [5, 6].</p>
  <p>&sect;10 One particular feature that has been identified by a number of workers, is
  a delay time or initiation period between a coated substrate first being
  exposed to a corrosive environment and the start of the blistering or
  delamination process taking place.&nbsp; Leidheiser [7] ascribes this delay as
  due to the time required to set up a steady state diffusion in the film;
  whether this diffusion was of water, oxygen or ions was unclear.&nbsp; Nguyen
  [8], on the other hand, suggests that the important route for diffusion is
  that of the cations along the paint/metal interface.&nbsp; Both
  interpretations provide explanations for the observed phenomena.</p>
  <p>&sect;11 The work described here is concerned with blistering and delamination of an
  alkyd coating. Part of the essential quality of this research is to use,
  wherever possible, non-destructive test methods.&nbsp; To this end, some work
  has been carried out to determine the feasibility of incorporating pH
  indicators into the organic coating under investigation.&nbsp;</p>
  <p>&sect;12 The initial tests, described below, were carried out on an alkyd
  resin.&nbsp; The hypothesis of the tests was that cathodic areas under a clear
  resin coating may be identified by the high pH generated due to the presence
  of OH<sup>-</sup> produced by the oxygen reduction reaction; it should
  therefore be possible to track the delamination front as it proceeds along the
  interface.&nbsp;</p>
  <h3>Experimental</h3>
  <h4>Specimen preparation</h4>
  <p>&sect;13 The substrates used for all the experiments were cold rolled, mild steel Q
  Panels.&nbsp; The panels were solvent de-greased and dried with paper towels
  before being coated in a dust free, ventilated, coating cabinet.&nbsp; The
  coatings were cured at room temperature for 48 hours and then stoved at 65-70<sup>o</sup>C
  for a further 48 hours. &nbsp;</p>
  <p>&sect;14 In order to provide an indication of the degree of protection offered by
  this coating, a control experiment was carried out to demonstrate the effect
  of immersing an unprotected substrate in the test solution of 0.5M NaCl.&nbsp;
  This was recorded with time lapse photography and the result is available as
  video1 in MPEG format in either <a href="v2p26vid1.mpg">medium</a> (2.6 Mb) or <a href="v2p26vid1_small.mpg">small</a>
  (0.9 Mb) size.&nbsp;<i>[Editor's note: there is always a compromise between
  file size and image quality for video clips - these are the
  best we can do at present - note that it will take about 1 hour to retrieve 10
  Mb using a V90 modem].</i></p>
  <p>&sect;15 In order to track the development of alkaline and therefore cathodic
  regions under the film, phenolphthalein pH indicator was incorporated into the
  wet coating, as detailed below.&nbsp;</p>
  <p>&sect;16 After testing various preparations of the indicator for incorporation into
  the coating, the optimum solution for the alkyd coating was determined to be
  1g phenolphthalein in 100ml EtOH + 100 ml water.&nbsp; Three panels were
  selected at random to test if the indicator would leach out of the
  coating.&nbsp; A drop of pH 13.5 buffer solution was placed on the surface of
  the coating.&nbsp; Within a minute, the droplet had turned pink as seen in
  figure 2.</p>
