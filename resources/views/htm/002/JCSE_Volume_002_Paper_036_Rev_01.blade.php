  <h2>Filiform Corrosion of Aluminium Alloy 3003 H14 under Humid and Immersed
  Conditions</h2>
  <p>Z. Marsh, J. Marsh* and J.D. Scantlebury</p>
  <i>
  <p>Corrosion and Protection Centre, UMIST, Manchester 60 1QD, United Kingdom.<br>
  *CAPCIS Ltd, CAPCIS House, 1 Echo Street, Manchester M1 7DP, United Kingdom.<br>
