<p align="JUSTIFY">&sect;30 It was found that for the reaction in CH<sub>3</sub>OH
  system aluminium corroded very severely whereas in the PE solution, no
  reaction took place. The corrosion rates conducted in the CH<sub>3</sub>OH and
  PE solution were 1,179.5 and 0.015 mdd, respectively. This is due to the fact
that HgCl<sub>2</sub> is very soluble in either water or alcohol as methanol or
ethanol, but not in non-polar solvents. PE is thus not a good solvent to
dissolve HgCl<sub>2</sub>. However, the solvent PE used in the experiment
contained 0.02% of water, as a result, very little reaction between aluminium
and HgCl<sub>2</sub> occurred in the PE system.</p>
<p align="JUSTIFY">&sect;31 c) <i><b>DMM Solution</b></i> : In the flasks containing
