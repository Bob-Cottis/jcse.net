  <h3 align="JUSTIFY">Methods</h3>
<SCRIPT>comment(11)</SCRIPT>  <h4 align="JUSTIFY">1. Specimen Preparation</h4>
  <p align="JUSTIFY">Commercial carbon steel/aluminium bars were cut to make
  short strips with dimensions of 11.2 x 25 x 2.75 mm for carbon steel
  and 12.3 x 25 x 3 mm for aluminium. A hole was then drilled in the
  strips near one end for mounting. The diameter of the hole was 3.5 mm for
  carbon steel and 5.0 mm for aluminium. A few of these strip coupons were
  chosen for elemental composition analysis by vacuum emission spectrometer.</p>
<p align="JUSTIFY">&sect;12 Specimens were wetted and rubbed with a No. 120 abrasive
  paper until their surface was cleaned and smooth. The sizes of carbon steel
  and aluminium specimens became 11.1 x 24 x 2.75 mm and 12.0 x 24 x
  2.9 mm, respectively. All of the strip coupons were subsequently stamped with numbers on the upper left hand side near the hole. Coupons were then degreased
  by scrubbing with a bleach-free scouring powder, followed by rinsing with
  distilled water and a mixture of 1:1 ratio of methanol : diethyl ether, and
  finally dried with air. They were then weighed with
  an accuracy of 0.0001 g, and kept in a dessicator until experiments were
  started.</p>
<SCRIPT>comment(13)</SCRIPT>  <h4 align="JUSTIFY">2. Preparation of Corrosive Solution</h4>
  <p align="JUSTIFY">The minimum solution volume-to-specimen area ratio was 20
  ml/cm<sup>2</sup> of specimen surface, as recommended in ASTM A G31-72. The
  total solution volume was therefore fixed at 150 ml. The selected solvents
  were CH<sub>3</sub>OH and PE.</p>
<p align="JUSTIFY">&sect;14 a) <i>Hg Solution</i> : Since the solubility of elemental
  Hg in cyclohexane is about 3 ppm., 5 mg of Hg was thus weighed and dissolved
  in 1 liter of cyclohexane.</p>
<p align="JUSTIFY">&sect;15 b) <i>HgCl<sub>2</sub> Solution </i>: The selected solvents
  were used to dissolve HgCl<sub>2</sub>. The HgCl<sub>2</sub> concentration used
  was 150 ppm.</p>
<p align="JUSTIFY">&sect;16 c) <i>DMM Solution</i> : The same selected solvents were
  used to dissolve DMM at concentrations
  of 0, 50, 100, 150, 200 and
  250 ppm.</p>
<p align="JUSTIFY">&sect;17 d). <i>DMM + HCl Solution</i> : DMM concentrations, as set in
the method 2 c), were carried out, and 150 ppm of conc. HCl was added into each
flask containing either methanol or PE. The concentrated HCl was a 37% by wt.
solution, so introduction of HCl could thus not be separated from that of water.
The volumes of conc. HCl and DMM required to give 150 ppm concentration in 150
ml is on the order of 0.05 ml, therefore, the total solution volume did not
change significantly.</p>
<p align="JUSTIFY">&sect;18 e) <i>DMM + H<sub>2</sub>S Solution</i> : Saturated H<sub>2</sub>S
  solution was prepared by passing H<sub>2</sub>S gas, which was synthesized
  from the reaction of conc. H<sub>2</sub>SO<sub>4</sub> and Na<sub>2</sub>S
  powder, through the solvent for 1 h. at ambient temperature. In case of CH<sub>3</sub>OH,
  1000 mg of H<sub>2</sub>S is soluble in 943 ml of CH<sub>3</sub>OH<sup><i>15</i></sup>.
  The solubility of H<sub>2</sub>S in diethyl ether is 6.86 x 10<sup>-4</sup>
  mol/g while that of H<sub>2</sub>S in PE is 3.70 x 10<sup>-4</sup> mol/g <sup>16</sup>.
  Thus, in the case of PE, H<sub>2</sub>S gas was first dissolved into 10 ml of
  diethyl ether to saturation, then 140 ml of PE was added. The following corrosive solutions were prepared and studied using
  the method of&nbsp; ref. <i>16</i>:</p>
  <p align="JUSTIFY">- CH<sub>3</sub>OH or PE + saturated H<sub>2</sub>S</p>
  <p align="JUSTIFY">- CH<sub>3</sub>OH or PE + saturated H<sub>2</sub>S + 200 ppm
  DMM</p>
  <p align="JUSTIFY">- CH<sub>3</sub>OH + 300 ppm H<sub>2</sub>S solution + 200
  ppm DMM</p>
  <p align="JUSTIFY">- CH<sub>3</sub>OH or PE + 150 ppm HCl</p>
  <p align="JUSTIFY">- CH<sub>3</sub>OH or PE + 150 ppm HCl + 200 ppm DMM</p>
<SCRIPT>comment(19)</SCRIPT>  <h4 align="JUSTIFY">3. Procedure</h4>
  <p align="JUSTIFY">One pair of specimens was
  immersed in an Erlenmyer flask
  containing corrosive solution. The flask was closed loosely by a stopper to
  release gas produced during the corrosion process and prevent the suppression
  of the reaction. Temperatures studied were varied from -10 to 70 &deg;C with an increment of 20 &deg; C. At low
  temperatures, at or below 0 &deg;C, the flask was stored in a
  refrigerator, whereas at higher temperatures a temperature controlled bath was
  used. Duration of exposure depended on temperature. At low temperatures, the
  corrosion process took a long time, 700-1,000 h. while at ambient and higher
  temperatures, the reaction took place very rapidly, 74.6 mdd, and needed to be
  observed at all time.</p>
<SCRIPT>comment(20)</SCRIPT>  <h4 align="JUSTIFY">4. Specimen Cleaning after the Exposure</h4>
  <p align="JUSTIFY">The cleaning process was done by following the methods of
  ASTM G1-72 (Reapproved 1979). Procedures were varied depending on type of
  metal being cleaned, as follows;</p>
<p align="JUSTIFY">&sect;21 a) <i>Aluminium Specimen Cleaning </i>: Coupons were
  cleaned as well as possible with a plastic knife. Oily or greasy deposits were
  removed by soaking in trichloroethylene followed by the cleaning solution
  containing chromic acid, phosphoric acid, and water at 80 &deg;
  C for 25 min. The coupons were then rinsed with distilled water, isopropanol,
  and benzene. Finally, they were dried between paper towels and placed in a
  dessicator for 1 h. before weighing.</p>
<p align="JUSTIFY">&sect;22 b) <i>Carbon Steel Specimen Cleaning </i>: After cleaning
  the coupons with plastic knife and soaking in trichloroethylene, remaining
  corrosion products needed to be removed by a bristle brush. They were then
  immersed in Clarke&#146;s solution at room temperature for 25 min.
  The specimens were rinsed with water followed by isopropanol and dried between
  paper towels followed by warm air drying.</p>
