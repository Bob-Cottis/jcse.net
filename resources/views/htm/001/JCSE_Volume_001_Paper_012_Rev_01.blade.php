<p align="JUSTIFY">&sect;52 As expected, when using PE instead of CH<sub>3</sub>OH, the
  corrosion rates were much lower. The system of PE + 300 ppm H<sub>2</sub>S +
  200 ppm DMM also gave a lower rate than that of PE + 150 ppm HCl + 200 ppm DMM.
  This is due to the content of water in HCl since HCl used in the system was
  37% of HCl in water while H<sub>2</sub>S used was in the form of gas purged
  into the PE solvent directly. The reactivity between H<sub>2</sub>S
  or HCl and DMM was then low. However, in gas phase as in natural gas, the gas
  molecules move around easier and faster, resulting in higher chance for those
  molecules to collide with each other. The reactivity between H<sub>2</sub>S
  and DMM should thus be better and the corrosion rate would be higher.</p>
