  <h3>&sect;1 Abstract</h3>
  <p>The effects of sub-ppm levels of H<sub>2</sub>S, oxygen and the adsorbed
  water on the atmospheric corrosion of silver were studied. The concentration
  of H<sub>2</sub>S and the relative humidity affecting the tarnish rate were
  carefully controlled by permeation tube and monitoring the humidity. The
  quartz crystal microbalance (QCM), an <em>in situ</em> mass measuring
  technique, was used in order to measure the tarnish rate of silver in various
  exposure conditions and varying amounts of adsorbed water depending on
  relative humidity. The tarnish films were analyzed by XPS, the analyzed data
  were compared to the thermodynamic calculation used to predict the stable
  phases and the equilibrium potentials such as <font face="Symbol">f</font> <sub>Ag+/Ag</sub>,
  <font face="Symbol">f</font> <sub>O/O2-</sub> and pH. QCM results showed that
  oxygen was required for the sulfidation of silver exposed to 100ppb H<sub>2</sub>S
  environments. This agrees with the predicted results that silver was not
  oxidized by hydrogen ion but by dissolved oxygen in the condensed water. In
  addition, the water adsorbed from the highly humidified environments enhanced
  the tarnish rate. This could be explained by the fact that the adsorbed water
  provided a medium for the oxygen, which oxidizes silver metal.</p>
<h3>&sect;2 Introduction</h3>
  <p>Silver has been used for electronic connector materials, solder, batteries
  and decorating materials because of its high electrical and thermal
  conductivities. Hence, the atmospheric corrosion of silver has been studied
  with an emphasis on the effect of humidity, the effect of gas and the kinetic
  mechanism.</p>
<p>&sect;3 Fiaud and Guinement<sup>1</sup> conducted indoor laboratory studies on the
  corrosion of silver in atmospheric environments of controlled relative
  humidity and pollutant gases, reporting that the corrosion product was silver
  sulfide and that the kinetic behavior conformed to a parabolic law. This does
  not agree with D. Simon&#146;s result<sup>2</sup> that the kinetic behavior
  conformed to a linear law. Kuhn and Kelsall<sup>3</sup> calculated the stable
  phase in Ag - H<sub>2</sub>S - O<sub>2</sub> and Ag - H<sub>2</sub>S - O<sub>2</sub>
  - H<sub>2</sub>O with their thermodynamic data, indicating that silver sulfate
  was stable instead of silver sulfide. This difference between Kuhn &amp;
  Kelsall&#146;s result and Fiaud &amp; Guinement&#146;s experimental result was
  explained by a large barrier for the formation of sulfate from sulfide.</p>
