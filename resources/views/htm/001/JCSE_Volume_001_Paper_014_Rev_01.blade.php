<h3>&sect;9 Experimental</h3>
  <p>Silver was deposited on the front surface of a crystal by evaporation at
  Midwest Research Technologies, Inc. to a thickness of 5000 <font face="Courier New">�</font>
  . Before the deposition, the back surface was masked with crystal bond. Gold,
  previously deposited on the front side was stripped by aquaregia solution,
  cleaned with distilled water and acetone alternatively three times and dried
  with N<sub>2</sub> gas. The silver coated crystal was kept in desiccant before
  and after exposure. Before the exposure, the surface of silver was analyzed
  with XPS. Adsorbed species, such as adventitious hydrocarbon, (OH)<sub>ad</sub>
  and adsorbed oxygen were detected, but the oxide peak was not detected.</p>
<p>&sect;10 The sub-ppm level of concentration was obtained using a permeation tube. In
  order to get reproducible results, the concentrations of H<sub>2</sub>S, the
  relative humidity and the temperature in the chamber were monitored during the
  exposure. The temperature of the chamber was kept at 25 <font face="Symbol">�</font>
  0.1 <font face="Symbol">�</font> C and the flow rate was 1 <font face="Times New Roman">l</font>
  /min. Purified air supplied by an air supply, or nitrogen (with 99.99% purity)
  from nitrogen cylinder was used as a carrier gas. The concentration of H<sub>2</sub>S
  was maintained at 5% during the exposure, while the concentrations of
  undesired gases in the purified air were less than 1ppb for SO<sub>2</sub>,
  0.1ppb for H<sub>2</sub>S, 1ppb for NO, 1ppb for NO<sub>2</sub>, 1ppb for O<sub>2</sub>,
  1.8<font face="Symbol">�</font> 10<sup>3</sup>ppb for CO<sub>2</sub> and
  100ppb for CO. The details of the mixed flowing gas system were described in
  other papers<sup>18</sup>.</p>
<p>&sect;11 The quartz crystal microbalance, an<i> in situ</i> mass measurement
  technique with high mass sensitivity (<font face="Symbol">@</font> 10ng/cm<sup>2</sup>),
  was employed to monitor the mass change during exposure to part per billion
  pollutants and during the transition between wet and dry environments. The
  block diagram of the circuitry elements and the description of the particular
  circuitry employed are given elsewhere<sup>17</sup>. The circuitry designed
  for the oscillation of a 5 MHz quartz crystal was modified for the oscillation
  of a 6 MHz quartz crystal. In addition, the newly designed QCM was calibrated
  by the potentiostatic method with the following experimental conditions<sup>7</sup>:
  0.2M HClO<sub>4</sub> + 10<sup>-3</sup> M AgNO<sub>3</sub> at 0.0V (SHE). The
  result (12.21ng/Hz-cm<sup>2</sup>) agreed well with the theoretically
  calculated value (12.26ng/Hz-cm<sup>2</sup>). The holder was made of
  polychlorotrifluoroethylene to minimize the mass change due to the chemical
  reaction between the holder, silver and gases. Viton O-rings were placed on
  each side of the crystal to ensure proper sealing and to minimize the residual
  stress since the frequency can be affected by pressure as well as mass change.</p>
<p>&sect;12 The tarnish film after the exposure was analyzed on a PHI 5400 XPS system (Perkin
  Elmer Corp.) equipped with a water cooled Mg-anode (Mg-K<font face="Symbol">a</font>
  : 1253.6eV and FWHM (full width at half maximum): 0.7eV) at total power
  dissipation of 400 watts (15kV, 26.7mA). The sample stage was positioned such
  that the photoelectron emitted at an angle of 45 degrees to the plane of
  silver surface entered the analyzer through a 1 mm <font face="Symbol">�</font>
  4 mm aperture. The spectrometer was calibrated with two reference points
  (Ag-3d<sub>5/2</sub>: 368.3eV and C-1s: 284.5eV) to minimize the error due to
  the drift of the spectrometer with assumption that the shift of energy due to
  charging effects was negligible. In order to analyze the inside of the tarnish
  film, the differentially pumped argon ion gun (4kV, 2mA ions) of the XPS
  system was utilized. The calibration of the removal rate was accomplished by
  sputtering through the thickness of a Ta<sub>2</sub>O<sub>5</sub> standard
  (1.8nm/min).</p>
<p>&sect;13 The surface morphology after exposure was observed by Field Emission
  Scanning Electron Microscopy (Model S-4500, Hitachi., Ltd.) with an
  accelerating voltage of 5kV to provide information on the tarnishing process.
  The sample stage was positioned such that the incident electron collided with
  the silver surface at an angle of 40 degrees to the plane of the surface to
  maximize the spatial resolution. The morphology could be observed with a
  maximum magnification of 100kX because of utilization of field emission as the
  electron source.</p>
<h3>&sect;14 Experimental Results</h3>
  <h4>Analysis of tarnish film with XPS</h4>
  <p>Figure 1 shows the XPS spectrum of the tarnish film formed on silver
  exposed to 100ppb H<sub>2</sub>S with air containing 75% R.H. for 72 hours.
  The peaks of sulfur-2p were detected at 161.8 and 161.0eV from the film
  surface indicating sulfide. No peak indicating sulfite or sulfate was detected
  before or after sputtering. The Auger parameter (724.8eV) calculated from the
  silver-3d<sub>5/2</sub> peak (B.E. = 368.2eV) and Ag-M<sub>4</sub>VV (K.E. =
  357.7eV) detected correspond to that of silver sulfide. The Auger parameter is
  shifted to that for silver metal as the intensity of sulfur-2p peak reduced
  with sputtering. The broad peak of oxygen-1s on the surface disappeared after
  sputtering. This peak is thought to result from (OH)<sub>ad</sub>, water and
  adsorbed oxygen. No oxygen-1s peak indicating the presence of oxide was
  detected before or after sputtering. Adventitious carbon also appeared on the
  surface and disappeared after sputtering. The XPS spectra of the tarnish film
  formed on silver exposed to 100ppb H<sub>2</sub>S with air containing 15% R.H.
  for 90 hours were similar to the above except for the sulfur-2p shown in
  Figure 2. The silver peaks and sulfur peaks identify the tarnish film as
  silver sulfide and the adsorbed species on the surface were also the same as
  the above. A small amount of sulfite (or sulfate) was detected on the surface.
  However, it was not detected after the exposure to 100ppb H<sub>2</sub>S with
  air containing 15% R.H. for 40 hours. Hence, the sulfite (or sulfate) may be
  produced after longer exposures than 40 hours. The XPS spectra of the tarnish
  film formed on the silver exposed to 100ppb H<sub>2</sub>S with nitrogen gas
  containing 75% R.H. for 72 hours were similar to the above except for the
  silver peak shown in Figure 3. The Auger parameter of silver did not match
  that of silver or silver sulfide and was positioned in the middle of these
  Auger parameters. Hence, the mismatch between the Auger parameter obtained and
  that of silver sulfide is due to the fact that the film is thinner than the
  escape depth of electrons having the kinetic energy of approximately 360eV (<font face="Symbol">�</font>
  2nm)<sup>16</sup>.</p>
<p>&sect;15 Hence, the tarnish film after exposure to environments containing H<sub>2</sub>S
  consisted of only silver sulfide covered by adventitious carbon, adsorbed
  water and possibly adsorbed oxygen and (OH)<sub>ad</sub>.</p>
