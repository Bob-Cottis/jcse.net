  <h3>Conclusions</h3>
<p>&sect;29 The aim of this investigation was the study of the effects of relative
  humidity and oxygen on the tarnish process of silver in various environments
  containing 100ppb H<sub>2</sub>S. The mass change was monitored during
  exposure and the tarnish film was analyzed by XPS. From the experimental
  results, the conclusions are as follows:</p>
<p>&sect;30 Tarnish film formed consisted of only silver sulfide covered by
  adventitious carbon, adsorbed water and possibly adsorbed oxygen and (OH)<sub>ad</sub>.</p>
<p>&sect;31 Silver was not tarnished without oxygen, which proved by QCM results and
  thermodynamic calculation.</p>
<p>&sect;32 The amount of adsorbed water in 75% R.H. environment was as much as 2.5
  monolayers and the presence of H<sub>2</sub>S increased the amount of adsorbed
  water to 5.3 monolayers. It is enough to act as bulk water. Hence, the high
  tarnish rate in humid environment is due to the presence of bulk water layer
  in which oxygen is dissolved to oxidize silver.</p>
<p>&sect;33 In humid environment, the kinetic behavior was due to the high electrical
  conductivity of the tarnish film and the high oxidizing power due to adsorbed
  water.</p>
