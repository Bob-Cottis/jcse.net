<h3>&sect;19 Measurement of mass gain with QCM</h3>
  <p>Figure 4 shows the mass gain with time when silver coated crystals were
  exposed to environments containing hydrogen sulfide. The mass loss occurred
  after the exposure condition changed from 100ppb H<sub>2</sub>S with 75% R.H.
  air (or N<sub>2</sub>) to 15% R.H. air, indicating water desorption (120ng/cm<sup>2</sup>)
  which was 1.7 times greater than that of adsorbed water (70ng/cm<sup>2</sup>)
  on as-received silver metal. The reason that water was more adsorbed may be
  due to the difference of hydrophilicity<sup>19</sup> between silver metal
  surface and the surface covered by H<sub>2</sub>S which is soluble in water.
  The mass gain after exposure of 100ppb H2S with 75% R.H. air for 72 hours was
  two times higher than after 100ppb H<sub>2</sub>S with 15% R.H. air,
  indicating that adsorbed water enhanced the reaction rate. In addition, the
  mass gain in 100ppb H<sub>2</sub>S with 75% R.H. N<sub>2</sub> was negligible,
  indicating that the presence of oxygen is required for the sulfidation of
  silver. It agreed with the XPS result that the observed Auger parameter of
  silver was between the Auger parameter of elemental silver and that of silver
  sulfide. The curve - (a) and (b) in Fig. 4 were reploted versus time<sup>1/2</sup>
  scale instead of time scale. The mass gain in 100ppb H<sub>2</sub>S with 75%
  R.H. air increased linearly until 8 hours after the exposure, but its rate was
  reduced after this period, conforming to parabolic behavior. However, in
  100ppb H<sub>2</sub>S with 15% R.H. air, the mass gain increased only
  parabolically without an initial linear region. In 100ppb H<sub>2</sub>S with
  air containing 75% R.H., the kinetic behavior is due to the change of the rate
  determining step from surface reaction or mass transport in the gas phase or
  the mixed step to mass transport through the tarnish film. The reaction
  mechanism or transport mechanism through the tarnish film will be discussed in
  the next section.</p>
