<h2>&sect;26 Discussion</h2>
  <p>In 100ppb H<sub>2</sub>S, silver was oxidized by oxygen, as was proved by
  the <i>in&nbsp;situ</i> measurements of mass showing that the tarnish rate of
  silver was negligible in the environments without oxygen. This corresponded to
  the results, obtained from thermodynamic calculation, that the equilibrium
  potential between Ag and Ag<sup>+</sup> being higher than that for hydrogen
  gas evolution as shown in Fig. 6-(b). In addition to oxygen, the amount of
  adsorbed water affected the tarnish rate. This effect was explained by the
  change of ionic path on surface<sup>8</sup> or the change of activity of other
  gas<sup>9</sup>. It could also be explained by the change of properties of
  adsorbed water with humidity<sup>6, 10</sup>. The formation of a few
  monolayers makes the behavior of adsorbed water be that of the bulk water. In
  the humid environments used in this tests (75% R.H.), the water was adsorbed
  as much as 2.5 monolayers. Furthermore, the presence of H<sub>2</sub>S
  increased the amount of water from 2.5 monolayers to 5.3 monolayers. Hence,
  the tarnish process in humid environments is supposed to be regarded as the
  reaction in aqueous solution. The following reactions in humid environments
  are suggested:</p>
