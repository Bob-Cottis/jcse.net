  <h2>Tarnish Process of Silver in 100ppb H<sub>2</sub>S Containing Environments</h2>
  <p>H. Kim* and J.H. Payer<sup><font face="Symbol">+</font></sup><br>
  <em>* Technical Research Lab. Pohang Iron &amp; Steel Co., P. O. Box 36,
  pohang 790-785, Korea<br>
