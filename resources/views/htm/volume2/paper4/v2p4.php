<SCRIPT>comment(23)</SCRIPT><h3>Conclusions</h3>
<SCRIPT>comment(24)</SCRIPT><p>The conditions of electroreductive polymerisation of <i>trans</i>-[RuCl<sub>2</sub>(vpy)<sub>4</sub>]
on Nd-Fe-B magnets associated to improved reproducibility and to the formation
of homogeneous film were CV at potentials ranging from 0.2 to &#150;2.8V vs. SCE.</p>
<SCRIPT>comment(25)</SCRIPT><p>EIS experiments performed during the film growth indicated a decrease in the
resistance R<sub>CT</sub> after the 30<sup>th</sup> cycle, which could be due to
the degradation of the polymeric film.</p>
<SCRIPT>comment(26)</SCRIPT><p>The impedance diagrams showed that the polymeric film offers a temporary
protection to the corrosion process.</p>
<SCRIPT>comment(27)</SCRIPT><p>The gravimetric tests in Na<sub>2</sub>SO<sub>4</sub> solution indicated that
the poly-{<i>trans</i>-[RuCl<sub>2</sub>(vpy)<sub>4</sub>]} coating was not
highly effective, probably due to its low thickness and high permeability.</p>
