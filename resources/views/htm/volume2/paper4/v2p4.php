<SCRIPT>notify();</script><SCRIPT>comment(1)</SCRIPT><h3>Abstract</h3>
<p>This work reports the reductive electrochemical polymerisation of the <i>trans</i>-[RuCl<sub>2</sub>(vpy)<sub>4</sub>]
monomer, where vpy = 4-vinylpyridine. Its potentiostatic and potentiodynamic
(cyclic voltammetry) electropolymerisation produced strongly adhesive and
electroactive films on platinum electrodes. However the films deposited on Nd-Fe-B
permanent magnets did not show electroactivity in several solvents/electrolytes
tested. The efficiency of the film formation indicated the existence of a strong
correlation between the upper-limit value of the potential range applied and the
charge density consumed in the polymerisation. Electrochemical Impedance
Spectroscopy studies in Na<sub>2</sub>SO<sub>4</sub> 0,5 mol.dm<sup>-3</sup>
solution showed that the charge transfer resistance (R<sub>CT</sub>) associated
to the Nd-Fe-B magnets coated with poly-<i>trans</i>-[RuCl<sub>2</sub>(vpy)<sub>4</sub>],
was four times higher than that of uncoated magnets. Gravimetric tests indicated
no significant weight losses of the coated magnets. Morphology and thickness
measurement studies were performed by Scanning Electron Microscopy (SEM). Energy
Dispersive Spectroscopy (EDS) confirmed the presence of Ru in the polymeric
film.</p>
<SCRIPT>comment(2)</SCRIPT><p><b>Keywords: </b>electropolymerisation, Nd-Fe-B magnets, corrosion.</p>
<SCRIPT>comment(3)</SCRIPT><h3>Introduction</h3>
<p>The electrocoating from monomers with redox centres constitute an alternative
way of application in electrocatalysis and corrosion protection, partially due
to the low oxidation-state of immobilised transition metals in the polymeric
matrix. Organic coatings catodically electrodeposited on inert substrates, such
as Pt, C and TiO, have been reported in the literature [1]. Reductive
electropolymerisation has been used in our laboratory as an alternative in the
corrosion protection of active substrates, such as Nd-Fe-B magnets. Our
laboratory has synthesised and characterised a novel complex with general
formula <i>trans</i>-[RuCl<sub>2</sub>(L)<sub>4</sub>], where L = vpy = 4-vinyl
pyridine, with reductive electropolymerised sites. In recent reports the
feasibility of reductive electropolymerisation with the ligand L =
4-vinylpyridine on inert electrodes of Pt and Pd, as well as on sintered
substrates of Fe+5%Ni and Fe+10%Ni alloys, has been demonstrated [2,3]. Results
concerning the electrodeposition of this monomer on stainless steels are
underway [4,5].</p>
<SCRIPT>comment(4)</SCRIPT><p>Nd-Fe-B permanent magnets shows outstanding magnetic properties. Due to their
high-energy product, applications of this material have increased in the last
decade. The main applications include consumer electronics, computer
peripherals, acoustics, office automation, and magnetic resonance image [6].
Nevertheless, poor corrosion resistance and low thermal stability are the main
drawbacks for the use of Nd-Fe-B in some applications. Efforts have been
concentrated in increasing the magnetic properties of this kind of material,
based on evolution of corrosion resistance through the application of surface
coatings [2,7-9]. Poly-{<i>trans</i>-[RuCl<sub>2</sub>(ypy)<sub>4</sub>]} films
are under investigation as an alternative surface coating for NdFeB magnets.</p>
<SCRIPT>comment(5)</SCRIPT><h3>Experimental</h3>
<p>Reagents and Synthesis<i>:</i> Commercially available reagents and solvents
with analytical grade were employed throughout this work. RuCl<sub>3</sub>.3H<sub>2</sub>O
(Jonhson-Matthey) and 4-vinylpyridine (Aldrich) were used without further
purification. The synthesis of Ruthenium blue solution and<i> trans</i>-[RuCl<sub>2</sub>(vpy)<sub>4</sub>]
was carried out employing a method described in the literature [10].</p>
<SCRIPT>comment(6)</SCRIPT><p>Preparation of samples: Nd-Fe-B magnets produced by Sumitomo were prepared
for coating by polishing on 500 grit emery paper followed by an ethanol wash.
The exposed sample area to coating was 0.95 cm<sup>2</sup>.</p>
<SCRIPT>comment(7)</SCRIPT><p>Electrocoating:<i> </i>Nd-Fe-B magnets were coated with<i> </i>poly-{<i>trans</i>-[RuCl<sub>2</sub>(vpy)<sub>4</sub>]}
films, electrodeposited by Cyclic voltammetry (CV) and potentiostatic technique.
The monomer concentration was 5 mmol.cm<sup>-3</sup> in CH<sub>3</sub>CN/CH<sub>2</sub>Cl<sub>2</sub>
(4/1) and the electrolyte used was TBHP (tetrabutilamoniun hexafluorphosfate)
0.1 mol.cm<sup>-3</sup> [11]. Electrodepositions were carried out in a
electrochemical cell with one compartment using a 273A Princeton Applied
Research (PARC) Potentiostat/Galvanostat, interfaced with a DOS-compatible
computer through a National Instrument General Purpose Interface Board (GPIB).
Instrumental control, data acquisition, and processing were performed by a 270
EG&amp;G Research Electrochemistry Software. All the electrodepositions were
performed at room temperature and the electrolyte was quiescent.</p>
<SCRIPT>comment(8)</SCRIPT><p>Electrochemical Impedance Spectroscopy (EIS): EIS tests were carried out with
a frequency response analyser (Solartron SI 1255) and a potentiostat ( EG&amp;G
273A) controlled by an electrochemical impedance software. These measurements
were carried out at room temperature in a flat cell, in Na<sub>2</sub>SO<sub>4</sub>
0.5 mol.cm<sup>-3</sup> solutions at the open circuit potential. Measurements on
Nd-Fe-B magnets without poly-{<i>trans</i>-[RuCl<sub>2</sub>(vpy)<sub>4</sub>]}
films were performed for the sake of comparison.</p>
<SCRIPT>comment(9)</SCRIPT><h3>Results</h3>
<p>Several experiments aimed at obtaining electropolymer films from the monomer <i>trans-</i>[RuCl<sub>2</sub>(vpy)<sub>4</sub>]<sup>
</sup>were conducted using electrodes of different materials [4,5,10-12]. The
film formation on Nd-Fe-B magnets occurs almost at the same range of applied
potential (&#150;2.6 and &#150;2.8V vs. SCE) found by Paula <i>et al</i>. [13] to
electropolymerise successfully <i>trans</i>-[RuCl<sub>2</sub>(vpy)<sub>4</sub>]
on Fe-5%Ni and Fe-10%Ni sintered alloys [10]. However, electrodepositions on Nd-Fe-B
occur preferentially at &#150;2.75 and &#150;2.8V. In this work, electropolymerised
films were produced by either cyclic voltammetry (CV) or at fixed potentials.</p>
<SCRIPT>comment(10)</SCRIPT><p>The samples coated by films electrodeposited at &#150;2.75V (fixed potential)
showed no satisfactory reproducibility. SEM images obtained from these films are
suggestive of discontinuities and consequently some parts of the substrate were
not totally coated (fig.1a). The cyclic voltammetry electrodepositions were more
reproducible and the coatings more homogeneous (fig.1b), although the
polymerisation only occurred successfully when positive potentials were scanned
The redox polymer poly(vinyl ferrocene) exhibits a maximum of conductivity when
the concentration of oxidised and reduced sites in the polymer matrix matches.
This occurs at the standard potential (E<sup>0</sup>) of the redox centres in
the polymer [14]. In agreement with the studies of poly(vinyl ferrocene), the
electropolymerisation of <i>trans-</i>[RuCl<sub>2</sub>(vpy)<sub>4</sub>] on Nd-Fe-B
magnets are successfully when the range of applied potential reaches the region
of the standard potential of the redox centres, that is, nearly 0.2V vs. SCE for
poly-{<i>trans-</i>[RuCl<sub>2</sub>(vpy)<sub>4</sub>]} on Pt electrodes.</p>
<SCRIPT>comment(11)</SCRIPT><p>Poly-{<i>trans</i>-[RuCl<sub>2</sub>(vpy)<sub>4</sub>]} films could be formed
at the following conditions: 0.2 to &#150;2.8V and 0.4 to &#150;2.8V at 50 mV.s<sup>-1</sup>,
30 to 50 cycles. No significant differences were found in the films polymerised
in either potential range, 0.2 to &#150;2.8V or 0.4 to &#150;2.8V vs. SCE, apparently
because E<sup>0</sup> of Ru<sup>2+/3+</sup> on Nd-Fe-B magnets, occurs before
0.2 V.</p>
