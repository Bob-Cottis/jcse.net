<SCRIPT>comment(17)</SCRIPT><p>The corrosion resistance of the coated samples, coated by using the optimum
conditions stablished above, was analysed by gravimetric tests as a function of
immersion time and by EIS in Na<sub>2</sub>SO<sub>4</sub> 0.5 mol.dm<sup>-3</sup>
solution. Comparative studies were made between poly-{<i>trans</i>-[RuCl<sub>2</sub>(vpy)<sub>4</sub>]}
coated Nd-Fe-B magnets and uncoated magnets.</p>
<SCRIPT>comment(18)</SCRIPT><p>Figure 3 shows the impedance diagrams corresponding to the coated and
uncoated Nd-Fe-B magnets investigated. The R<sub>CT</sub> of the coated magnet
after 2 hours in the sulphate solution (ca. 2.5 k<font face="Symbol">W</font>
.cm<sup>2</sup> ) was approximately 4 times higher than that of the uncoated
magnet (ca. 0.6 k<font face="Symbol">W</font> .cm<sup>2</sup> ) in the same
solution. The double layer capacitances associated to the coated (2 hours of
immersion) and to the uncoated magnets, were around 930 and 2200 <font face="Symbol">m</font>
F.cm<sup>-2</sup>, respectively. These results show that the electrodeposited
film provides temporary protection to the substrate. The protective
characteristics of the film could decrease with the immersion time. Comparing
the weight losses and EIS results, this behaviour could be attributed to the
permeation of the thin film by aggressive species of the electrolyte.</p>
<SCRIPT>comment(19)</SCRIPT><p>The solution resistance (R<sub>S</sub>) estimated from the electrochemical
response of the uncoated NdFeB magnet in the Na<sub>2</sub>SO<sub>4</sub>
electrolyte was approximately 15 <font face="Symbol">W</font> .cm<sup>2</sup>.
However, higher R<sub>S</sub> was found (44 <font face="Symbol">W</font> .cm<sup>2</sup>)
when the magnet was tested in the same electrolyte, after been coated. That
behaviour can be rationalised in terms of the electronic resistance of the film
(electrons transport between the redox centres Ru<sup>2+/3+</sup>) which are
associated in series with R<sub>S</sub>, producing that ohmic drop.</p>

