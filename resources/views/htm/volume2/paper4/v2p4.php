<SCRIPT>comment(13)</SCRIPT><p>Since the range of potential scanned was too large, the experiments took more
than one hour per deposition. In order to gather information on the working
conditions, electrochemical impedance spectroscopy (EIS) was carried out during
the film electropolymerisation, to monitor the film growth. The impedance
diagrams (fig.2) showed that the film growth reaches a maximum after 30 cycles.
The diagrams corresponding to the 2<sup>nd</sup>, 5<sup>th</sup> and 12<sup>th</sup>
cycles suggested a diffusion control, related to monomer mass transport from the
bulk solution to the magnet sample surface. For a higher number of cycles, a
capacitive arc starts to delineate, and a complete arc could be firstly seen at
the 15<sup>th</sup> cycle. The capacitive arc increased progressively for
further cycles up to the 30<sup>th</sup> cycle. For additional cycles, the film
resistance decreased, as indicated in fig.2, probably due to cracks or other
kinds of degradation.</p>
<SCRIPT>comment(14)</SCRIPT><p>Based on these results the electrodepositions on Nd-Fe-B were carried out by
CV up to 30 cycles in the potential range between 0.2 and &#150;2.8V vs. SCE.</p>
