  polarization of an uncoated sample. The value of R<sup>0</sup><sub>pol</sub>
  is assumed to be constant. A<sub>d</sub> has been calculated for the plasma-polymerised
  coating obtained from the HMDSO/O<sub>2</sub> 80/20 mixture. The results are
  reported in table 2 and is coherent with the corroding area observed. The
  delaminated area seems to stabilize around 0.015 cm<sup>2</sup> after 4 days
  of immersion in NaCl.</p>
