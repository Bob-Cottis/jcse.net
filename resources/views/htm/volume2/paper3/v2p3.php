<SCRIPT>comment(7)</SCRIPT>  <h4>Coating characterization by IR spectroscopy</h4>
  <p>The structure and bondings in the deposited films have been studied by
  means of IR spectroscopy (Figure 1). According to the litterature <font size="3">[11-13]</font>,
  the absortion bands can be assigned as follow : 790-800 cm<sup>-1</sup> (Si-C
  stretching and CH<sub>3</sub> rocking in Si-(CH<sub>3</sub>)<sub>2</sub>), 835
  cm<sup>-1</sup> (Si-C stretching and CH<sub>3</sub> rocking in Si-(CH<sub>3</sub>)<sub>3</sub>),
  880 cm<sup>-1</sup> (Si-(CH<sub>3</sub>)<sub>2</sub> rocking and bending),
  1000-1100 cm<sup>-1 </sup>(Si-O asymmetric stretching), 1260 cm<sup>-1 </sup>(CH<sub>3
  </sub>symmetric bending in Si-(CH<sub>3</sub>)<sub>X</sub>), a weak band at
  1350 cm<sup>-1</sup> (-CH<sub>2</sub>- scissoring and wagging vibrations in
  disilymethylene Si-CH<sub>2</sub>-Si), 1408 cm<sup>-1 </sup>( CH<sub>3 </sub>symmetric
  bending in<sup> </sup>Si-(CH<sub>3</sub>)<sub>X</sub>), 1458 cm<sup>-1</sup>
  ((CH)<sub>X</sub> asymmetric bending), 1710 cm<sup>-1 </sup>(C=O groups),
  2900-2960 cm<sup>-1 </sup>(CH<sub>X</sub> symmetric and asymmetric
  stretching), 2100-2250 cm<sup>-1</sup> (Si-H stretching vibration). With
  increasing O<sub>2</sub> in the HMDSO/O<sub>2</sub> mixture, the relative
  intensities of the absortion band at 1260 cm<sup>-1 </sup>(characteristic of
  Si-(CH<sub>3</sub>)<sub>X</sub>) is seen to decrease and the one of the band
  at 1000-1100 cm<sup>-1 </sup>(Si-O) increase. We consider that this is because
  of a decrease in the relative amount of organic content and to the high degree
  of crosslinking in the coating. This view is supported by measurement of the
  film density. The highest density is obtained at high O<sub>2</sub> content in
  the feed. One may conclude that plasma-polymerised coatings obtained from
  HMDSO/O<sub>2</sub> could be considered as polymer like or silica like as a
  function of O<sub>2</sub> content in the feed.&nbsp;</p>
