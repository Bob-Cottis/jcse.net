  provide to a coating less crosslinked. And hence this coating possesses the
  lower barrier properties. The increase of C<sub>c</sub> in the first day of
  exposure is considered due to water uptake by the coating. In fact, the
  coating capacitance increases with increasing water uptake [14]. The volume
  fraction V of electrolyte absorbed by the coating can be generally determined
  from the experimental values of C<sub>c</sub> [18] as follow: v = log(C<sub>c</sub>(t)/C<sub>c</sub>(0))/log80
