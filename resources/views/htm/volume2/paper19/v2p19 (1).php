<h3><b>Results and
Discussion</b></h3>

<SCRIPT>comment(23)</SCRIPT><p>The dependence of the corrosion potential E<sub>corr</sub>
on immersion time (Fig. 1) in artificial sea water illustrates changes in the
protective mechanisms supplied to the steel substrates by the studied final
coating. At the beginning of the exposure
the oscillations of the open circuit potential indicate that E<sub>corr</sub>
remains undefined due to an initial barrier protective mechanism. </p>

<SCRIPT>comment(24)</SCRIPT><p>With increasing immersion time, (t &gt; 1h) E<sub>corr</sub>
attains a potential that lies in the range of the corrosion potential of pure
Zn electrodes in sea water [15]. This fact indicating that water has reached
the ZRP layer, giving rise to the electric contact among the Zn particles and
with the steel substrate. In this way the cathodic protection mechanism period
is established, at relatively short immersion times. </p>

