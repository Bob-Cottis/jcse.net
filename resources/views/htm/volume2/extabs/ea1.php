<h2>The Influence of Cleaning and Surface Treatment on Filiform Corrosion of
Aluminium Alloys</h2>
  A.J.Kayes, M.J.Robinson &amp; S.Impey<i>
  <p>School of Industrial &amp; Manufacturing Science Cranfield University,
  Bedford MK43 OAL, UK<br>
  <script>mailto2('Offshore','cranfield.ac.uk')</script></i><b>
  <p>Keywords: filiform corrosion, cleaning, surface treatment, microstructure,
  contamination, electropolish
</b>
  <h4>Introduction</h4>
  Filiform corrosion is a localised form of attack that usually occurs beneath
  paint films and leads to the appearance of thread-like filaments. It is
  generally considered to result from contamination on the metal surface and
  will only propagate in humid conditions. The type of pretreatment used prior
  to painting has a very important influence on the subsequent occurrence of
  filiform corrosion. Chromate containing pretreatments have been widely used
  commercially, although alternatives are being actively sought on health and
  environmental grounds. A considerable range of susceptibility can be displayed
  between different alloys. It has been unclear whether this range of behaviour
  is caused by differences in the alloy compositions or by some feature of their
  microstructure or surface condition. In the work described in this paper the
  individual factors that influence an alloy&#146;s susceptibility to filiform
  corrosion have been studied in detail. In particular, the effects of various
  cleaning methods and chromating treatments have been investigated, together
  with the influence of surface contamination, grain structure and mechanical
  and electrochemical polishing.<u>
  <p>Surface Treatments.</u> Filiform corrosion tests were performed on 3003 and
  3105 aluminium alloys. Panels 145 mm x 80 mm of the mill finished alloys were
  prepared using a variety of cleaning and pretreatment methods. The standard
  pretreatment was [1] Clean in acetone [2] Alkaline clean in Ridoline 1806
  (30s) [3] Rinse in tap water [4] Acid rinse in 23% HNO<sub>3</sub> (15s) [5]
  Distilled water rinse. The influence of each experimental variable was
  investigated separately by omitting or adding steps in the standard treatment
  as shown in Table 1. For example, some panels were prepared by cleaning in
  acetone alone and others had the additional step of cleaning in 1M NaOH. The
  effects of different chromating treatments were investigated by varying the
  time of exposure to the Alochrom bath. In general, two replicate specimens
  were produced for each condition. The panels were then painted by bar coating
  with 3-5 microns of polyester primer, dried at 120C and given 13-15 microns of
  polyester top coat.
