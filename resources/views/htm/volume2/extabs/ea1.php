<b>
  <h4>Conclusions</h4>
</b>[1] The filiform corrosion susceptibility of aluminium sheet was shown to be
more closely related to the condition of the original surface layer than to the
microstructure of the substrate. This was due to the presence on the surface of
high temperature oxides, rolling lubricants and other contaminants from
processing.
<p>[2] Solvent cleaning of the aluminium prior to painting could reduce some of
the subsequent development of corrosion filaments but those treatments that
removed the original mill finished surface layer were found to be more
effective.
<p>[3] The use of appropriate alkaline cleaning, together with mechanical or
electrochemical polishing, was able to control filiform corrosion to a similar
extent to a chromate conversion coating.
<p>[4] Electropolishing the surface gave the greatest reduction in filament
development. This was attributed both to the efficient removal of the surface
layer and to the production of a microscopically flat surface, free from the
polishing irregularities that promote filiform corrosion.
