</b><u>
Filiform Corrosion Testing.</u> The painted panels were tested using the
standard filiform method BS X 32: 1991. A scribe 2mm wide and 0.15 mm deep was
produced in the longitudinal and transverse directions. Filiform corrosion was
initiated by exposure to HCl vapour for 1 hour and the panels were then tested
at 42C and 82% RH for 1000 hours. The extent of filiform corrosion was
quantitatively assessed by measuring the lengths of the filaments that developed
on each scribe using a Leica 500QMC image analyser.
<b>
  <h4>Results</h4>
</b>
  It was found that the most useful measure of the extent of filiform corrosion
  was the total length of the filaments that developed on the 70 mm long scribe
  during the 1000 hours exposure. In each case, these total lengths were
  expressed as Relative Filament Lengths (%) by comparing them with those that
  formed on 3003A when tested in the painted, mill finished condition, without
  any form of pretreatment. The results of the filiform testing are summarised
  in Fig 1.
  By performing tests in which different areas of the panels were polished in
  either the longitudinal or transverse direction it was possible to distinguish
  between effects caused by the surface layer and those due to the substrate. In
  other tests, the elongated grains were recrystallised by heat treatment at
  535C to investigate the influence of the substrate microstructure. The results
  indicate that filiform corrosion behaviour is influenced most strongly by the
  properties of the original surface layer.
