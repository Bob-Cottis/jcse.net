  <h4>Introduction</h4>

  In the last few years, the corrosion protective properties of plasma-polymerised coatings based on organosilicone precursor have been studied [1-6]. These films show interesting properties: they are highly crosslinked, insoluble, pinhole-free and very adherent. Particularly, the organosilicone plasma-polymerised seems to be a good candidate to form priming layer for subsequent coating [6, 7-9].
  <p>In some of our previous works [10-11], we studied the effects of hexamethyldisiloxane (HMDSO)/O<font SIZE="2"><sub>2</sub></font> mixture on the excited species in the plasma and on the structure of the coatings. The plasma polymerisation occured in a microwave reactor which are characterized by high rates of charged and neutral species (atoms, radicals ...) and consequently provided short time of deposition.
  <p>The aim of this paper is to study the influence of HMDSO/O<font SIZE="2"><sub>2</sub></font> mixture on the corrosion protection properties. Plasma-polymerised films were deposited on iron samples under different HMDSO/O<font SIZE="2"><sub>2</sub></font> ratios mixture and provide to a large wide of materials, from silica to polymers. The coatings characterization was investigated by IR spectroscopy. By using electrochemical impedance spectroscopy (EIS) measurements and appropriate model (equivalent circuit), it is possible to obtain parameters which can be correlated to the protective properties of several plasma-polymerised coatings.
<b>
<p>Materials and experimental procedure
</b>
  <p>The substrat materials, iron disks (purity 99,5%) 1,5 cm in diameter and 1 mm in thickness, was supplied by Goodfellow. Hexamethyldisiloxane (purity 98%) was purchased from Sigma-Aldrich.
  <p>Plasma polymerisation was carried out in a large microwave plasma reactor sustained by a surface wave ; this apparatus is described in detail elsewhere [11].The microwave is excited at a 2.45 GHz frequency and put through a quartz tube thanks to an excitator built on the surfaguide principle. The microwave (MW) power delivered is 600 W. Furthermore, the substrate holder was biased by means of a 13.56 MHz radio-frequency (RF) generator at a fixed power 50W. The feed gases, oxygen and argon, are injected at the top of the tube whereas the HMDSO is injected in the flowing afterglow via a ring. The total pressure varied from 50 mTorr to 80 mTorr depending on the HMDSO to O<font SIZE="2"><sub>2</sub></font> ratios. The flow rates were fixed at 76 sccm for (HMDSO + O<font SIZE="2"><sub>2</sub></font>) and at 5 sccm for the argon. Four different percentage in the HMDSO/O<font SIZE="2"><sub>2</sub></font> mixture have been studied : 20/80, 50/50, 80/20 and 100/0.
  <p>Before coating deposition, samples were treated in the microwave reactor without RF bias by oxygen plasma (150 sccm) during 10 min. The duration of coating deposition were varied from 6 min to 45 min. After coating, microwave and RF powers were turned off and samples were left in the reactor during 15 min in an argon flow (900 sccm) for cooling.
  <p>The film structure was analysed by means of reflection absorption IR (RAIR) measurements performed on a Br�cker IFS28 spectrometer. The spectra of the plasma-polymerized coatings were typically obtained by averaging 20 scans and then substracting the spectrum obtained from the substrate before deposition of the film. The film thickness was measured with a profilometer (Dektak 3030, Sloan). The samples were weighted before and after coating with a micro-scale balance (Sartorius MC210). The film weight was deduced by difference. The corrosion protection of the film was analysed by means of electrochemical impedance spectroscopy (EIS) measurement. EIS data were obtained using a Schlumberger solartron 1255 frequency response analyser coupled to an EGG PAR 273 potenstiostat/galvanostat. Impedance data were collected at frequencies ranging from 10 mHz to 100 kHz. Typically measurements were made at 5 points/decade, 10 mV a.c. amplitude. An electrochemical cell with three electrodes was used. Coated iron sample was working electrode (area 0,8 cm<font SIZE="2">2</font>) and a platinum grille served as counter electrode. A saturated calomel reference electrode was employed. Experiments were conducted at room temperature with NaCl 0,1 mol.L<font SIZE="2">-1</font> electrolyte under Ar atmosphere. The experimental impedance spectra were interpretated on the basis of equivalent electrical circuits using a fitting software (EGG).
