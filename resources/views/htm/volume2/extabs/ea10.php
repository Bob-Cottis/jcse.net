  <h2>Organic Coatings for Cathodic Protection in Atmospheric Condition</h2>
  <p>Tooru TSURU, Takao YAMAZAKI and Atsushi NISHIKATA<i>
  <p>Dept. Metallurgy and Ceramics Science, Tokyo Institute of Technology,
  2-12-1, O-okayama, Meguro-ku, Tokyo, 152-8552 JAPAN<br>
  <SCRIPT>mailto2('tsuru','mtl.titech.ac.jp');</SCRIPT></i><b>
  <p>Keywords: </b>Cathodic protection, atmospheric corrosion, electrical
  conductive layer, oxygen diffusion, contamination, water film, moisture layer</p>
  <h3>Introduction</h3>
  <p>Organic coatings for steels are effective to prevent corrosion in many
  environments by insulating the steel surface from corrosive media. When the
  coating layer is damaged by mechanical scratches or delamination, corrosion
  under the film proceeds gradually and covered by red rusts. Corrosion rate
  under the film is not so fast, however, appearance of rusted steel is not
  acceptable in usual use.
  <p>Cathodic protection for steel structures is widely applied in seawater,
  fresh water and soils. Since a current path for the protection current is not
  available in air and on coating film surface, cathodic protection is not
  adopted for these environments. There are some trials for cathodic protection
  of coated steels in air atmosphere where anode is positioned on the coating
  film surface. In this case, current path formed when the film surface was
  covered by moisture layer.
  <p>In this paper, we propose a new coating system, which more effectively
  protects the steel surface in atmospheric corrosion condition, and describe
  some results of corrosion tests in laboratory and fields.</p>
  <h3>Experimental</h3>
  <b>
  <p>Coating System:</b> The coating system applied onto a steel surface
  consisted three layers and an electrical conductive layer was sandwiched by
  insulating layers. For the laboratory tests, the first insulating layer was
  epoxy layer of 200 <font face="Symbol">m</font>m, the second layer was
  polyester layer of 100 <font face="Symbol">m</font>m containing carbon powders
  for electrical conduction and the top layer was epoxy layer of 100 <font face="Symbol">m</font>m.
  Sizes of specimens were 5cm x 5cm. For field test, almost same coating but the
  top coat was fluoride resin type of 20<font face="Symbol">m</font>m and the
  size was 45cm x 90cm.<b>
  <p>Cathodic Protection:</b> Protection current was applied between the steel
  surface and the electrical conducting layer. Applied voltage was 10V or 5V.<b>
  <p>Experimental Procedure:</b> After insulating test, the coating of specimen
  was scratched as 1mm width and 10mm length or removed the coatings some other
  shapes. Specimens were exposed in a constant humidity or in a cyclic wet/dry
  condition. In some cases, defects were contaminated by sodium chloride. The
  cathodic protection current was monitored with temperature and relative
  humidity. The defect surface was checked after the experiment.</p>
  <h3>Results and Discussion</h3>
  <b>
  <p>Results of Lab Test: </b>The damaged samples with different contamination
  levels from 0 to 32mg/m<sup>2</sup> were exposed in a constant humidity of 50
  to 90% at 298K. Only the specimens that have wide bare surface or very low
  contamination level were difficult to protect from red rusting. Most of the
  specimens were protected perfectly. Rough estimation of the minimum cathodic
  current density for protection was 60 to 70<font face="Symbol">m</font>A/cm<sup>2</sup>.
  This is several times larger than the cathodic limiting current of oxygen
  diffusion in bulk solution. This is understood as an increase in the diffusion
  limiting current with decreasing the water film thickness.
  <p>In case of large defects, such as several mm square or diameter, the
  protection current is not enough at the central part of the defect especially
  for low moisture layer condition. It was seemed that the thickness of the
  first insulate layer affected the minimum thickness of the water layer.<b>
  <p>Field Test: </b>The specimens were exposed in urban atmosphere with applied
  voltage of 10V. The specimens exposed against sunshine were protected
  cathodically except for large defects. In case of large defects, the moisture
  layer dried up too fast to distribute the protection current to the bare
  surface apart from the edge of coating. For the specimens facing downward, the
  moisture layer at the defects dried up gradually, so the cathodic protection
  continued for longer time and more widely. When the defects were contaminated
  with a NaCl solution, it was found that the protection current flowed for
  longer time in day cycles compared with that of clean defects. The distance or
  location of the defects from the current drain of the coating film did not
  affect the current densities at the defects. This indicates that the ohmic
  drop is negligible within the film, and suggests that the protection system
  does not require so many current drains for a huge steel structure.
  <p>The results showed that the cathodic protection for new coating system is
  performed unless the moisture layer keeps the electrical conduction between
  the substrate and the electrical conductive coating layer. This coating system
  will be suitable to apply the steel structures close to seashore or polluted
  air atmosphere.<b>
  <p>Current Needs for Protection: </b>The minimum current density for cathodic
  protection in a neutral solution is nearly equal to the diffusion limiting
  current of oxygen. It is about 20<font face="Symbol">m</font>A/cm<sup>2</sup>
  in bulk stagnant solution. The required current observed in laboratory tests
  was several times larger than this. In case of thin water layer, less than the
  thickness of the stable diffusion layer, the diffusion limiting current
  increases with decreasing the water layer thickness.</p>
  <h3>Summary</h3>
  <p>New coating system to be able to use cathodic protection of coating defects
  was proposed. An electrical conducting layer was sandwiched by insulating
  layers and cathodic protection current was applied between this layer and
  steel substrate. Coated steels with some artificial defects were tested in
  laboratory and field. Unless moisture layer covered the defects surface, no
  red rusts appeared for long period. It is suggested that the new system is
  applicable to steel structures at or near seashore.
