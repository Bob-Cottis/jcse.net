<h2>Using Electrical Impedance Spectroscopy (EIS) to Support Conventional Artificial Perspiration Testing.</h2>
J. Marsh&nbsp;
<i><br>
CAPCIS Ltd, CAPCIS House, 1 Echo Street, Manchester M1 7DP.<br>
<script>mailto2('Jon.Marsh','fs1.capcis.umist.ac.uk')</script>&nbsp; 
</i>
<p><b>Keywords: EIS, Artificial Perspiration, Lacquer Coatings, Zinc Electroplated Steel.&nbsp;
</b></p>
<p>Electrical impedance spectroscopy (EIS) was compared with the conventional testing regime for examining the resistance to attack by artificial perspiration of a number of water based finishing lacquers.  The lacquers were used as finishing treatments for lacquer coated steel shopping trolleys.  EIS was found to be superior to the conventional regime (exposure followed by abrasion).  EIS was able to provide quantitative data about the degradation process, whereas the conventional regime could only provide a pass/fail result.  EIS was also able to differentiate between the performance of different lacquers capable of obtaining a pass result in the conventional test.</p>
<h4>Introduction</h4>
Resistance to perspiration exposure is especially important for items fabricated with the requirement of being physically handled.  A prime example is shopping trolleys.  In Britain, shopping trolleys are usually constructed of lacquer coated zinc electroplated steel.  The lacquer used is usually colourless, transparent and approximately 5<font FACE="Symbol">m</font>
m thickness.  One of the largest problems associated with this product is discoloration and zinc loss of the handles and top rail. This leads to an unattractive dull grey/black appearance, as opposed to the initial bright metallic finish.  In today&#146;s world, maintaining the appearance of the product can be as important as maintaining its function.  
One testing method used to examine this problem is exposure to artificial perspiration.  Examples are the ANSI artificial perspiration test[1] for coating degradation, and the Volvo leaching test[2] for chromate loss from surfaces.  This article describes the use of electrical impedance spectroscopy (EIS)[3,4,5] to support conventional artificial perspiration exposure testing.  The substrate examined is zinc electroplated steel coated with a variety of thin water based finishing lacquers.
<h4>Experimental</h4>
Three lacquer systems are discussed.  These are representative of the different responses obtained from a number of water based finishing lacquers from a variety of sources.  The lacquers have been designated as A, Band C.
Samples of lacquer coated zinc electroplated steel (2cm length pieces of 3mm wire or testing panels of dimensions 6cm by 10cm) were examined using the ANSI artificial perspiration exposure test[1].  This consists of exposing the samples to synthetic perspiration for four 15 minute cycles, with hardness testing of the lacquer coating by abrasion with a 2B pencil after each exposure cycle.  If the coating can be removed, this is defined as a test failure.  The cycle after which the failure occurred is also noted.  The formulation of the ANSI artificial perspiration solution is given in Table 1.
Samples were also examined using EIS spectroscopy, with testing undertaken after 0, 15, 30, 45, and 60 minutes of exposure.  The samples were abraded using a 2B pencil after each 15 minute exposure period, before EIS measurements were taken.  The instrument used was a single channel ACM autoAC DSP (1996 model).  The frequency range examined was 30 KHz to 0.1 Hz., and the potential perturbation used was <font FACE="Symbol">±</font>
 10mV around the rest potential.  
The high frequency coating response was determined from a Nyquist plot[3,4,5], and the coating resistance calculated.  All samples were examined in triplicate.  Values varied within each sample group by up to <font FACE="Symbol">±</font>
30% with respect to the mean value.
<h4>Results</h4>
The results of the conventional test are given in Table 2.  The thin and colourless nature of the lacquer coatings made it extremely difficult to visually determine the failure point of the test.  
Figure 1 is an example of the Nyquist response obtained from lacquer C after 60 minutes of exposure.  The high frequency coating response is clearly differentiated from the lower frequency response associated with the charge transfer processes associated with substrate corrosion[3,4,5].
Figure 2 shows the coating response of zinc electroplated steel coated with the three lacquers, A, B and C.  Lacquer A shows no degradation during the test.  Lacquer B shows no penetration over the time period of the test, but clearly shows coating degradation.  Lacquer C shows a sudden drop in coating resistance during the test, indicative of penetration of the pencil through to the metallic substrate.
<h4>Discussion</h4>
Figure 1 shows an example of an EIS Nyquist response obtained from lacquer C after 60 minutes of exposure.  The response covers the frequency range of 30KHz down to 1Hz.  Two semicircles are clearly visible.  The high frequency semicircle, with Z imaginary max occurring at a value of about 10,000Hz, can be attributed to the response of the lacquer coating[3,4,5].  The lower frequency circle, with Z imaginary max at about 100Hz, can be attributed to the corrosion of the substrate, presumably the zinc electroplate film.  A value for the coating resistance can be calculated from the first semicircle.
From the coating resistance EIS results presented in Figure 2, it can be noted that the three lacquer coatings discussed here produce different modes of behaviour.  As stated previously, these are typical examples from a larger group of lacquers.  The three modes of behaviour are:
1. Lacquers showing little or no degradation (reduction in coating resistance) with exposure time.  This is shown by lacquer A, where little or no decline from the initial coating resistance of about 4000<font FACE="Symbol" COLOR="#000000">W</font>.cm<sup>2</sup>.  

<ol START="2">

<li>Lacquers showing a steady decline in coating resistance with exposure, but no sharp drop to a very low value.  This is shown by lacquer B, where a decline in coating resistance from 2000</font><font FACE="Symbol" COLOR="#000000"> W</font>.cm<sup>2</sup> to approximately 450</font><font FACE="Symbol" COLOR="#000000"> W</font>.cm<sup>2</sup> is observed.  The environment is progressively degrading the coating, indicating that the coating would be unsuitable for use in conditions where contact with perspiration would occur.  However, no penetration of the 2B pencil through to the substrate occurred, and the lacquer would have passed the standardised conventional test.</li>
<li>Lacquers which show a rapid decline in coating resistance to a value below 100</font><font FACE="Symbol" COLOR="#000000"> W</font>.cm<sup>2 </sup>after a given period of exposure, associated with coating degradation, and penetration of the 2B pencil through to the substrate.  An example is lacquer C.  After 45 minutes of exposure a significant decline in coating resistance can be observed.  This is followed by a fall to less than 100</font><font FACE="Symbol" COLOR="#000000"> W</font>.cm<sup>2</sup> after 60 minutes of exposure as the 2B pencil penetrates through the lacquer coating.</li></ol>

Examining the conventional test results (Table 2), it can be noted that there are substantial problems associated with using this test to examine thin colourless transparent finishing lacquers.  The first problem is that the test failure definition is based on the visual observation of coating removal.  We have found that with the lacquer types examined it was usually impossible to detect this.  Secondly, the test regime is a pass/fail test.  It is not generally possible to differentiate between different &quot;pass&quot; coatings.  Finally, the test is a qualitative test, in that a judgement of coating softening is entirely dependant on the person undertaking the test as they abrade the coating with the 2B pencil.  
The use of EIS as a supporting technique provides substantial advantages.  A failure, associated with penetration of the 2B pencil through to the metal substrate is immediately apparent, with the coating resistance dropping to a low level, usually less than 100<font FACE="Symbol" COLOR="#000000">W</font>.cm<sup>2</sup>.  This can be seen in Figure 2, for lacquer C after 60 minutes of exposure.  The second advantage over the conventional technique is that it enables differentiation between coatings that would be defined as passing the conventional test.  The conventional test could not differentiate between lacquers A and B.  However, EIS can clearly show that lacquer A has a lower initial porosity (higher initial coating resistance) than B.  Also, lacquer B degrades over the lifetime of the test, whereas lacquer A does not.

<h4>Conclusions</h4>

<ol>

<li>EIS is a useful supporting technique for use with artificial perspiration testing.  </li>
<li>EIS was better able to determine the failure point of the test, in comparison to the conventional testing regime.  </li>
<li>EIS was able to differentiate between different lacquer coatings that would be defined as passing the artificial perspiration exposure test.  Both the initial porosity and the resistance to degradation of the coating could be determined quantitatively.</li></ol>


