therefore enhancing material transport along the interface to assist
kinetically this interface instability process.</p>

<h4>Nickel Aluminides</h4>

<p>&sect;33 The time dependence of interfacial segregation has not been
studied as extensively on these alloys as on the other two types.  Limited data
on how the interface chemistry changes with oxidation time can be found in a
recent publication <a name="_Ref42584749"></a><a href="#_edn52"
name="_ednref52" title="">[52]</a>.
The results reported here concentrate on comparing the behaviour of
stoichiometric NiAl, Ni-rich Ni-40Al and a S-doped Ni-40Al.</p>

<p>&sect;34 The interface microstructures on
NiAl and Ni40Al after scale removal were similar to those shown for the iron aluminides, consisting of &alpha;-Al<sub>2</sub>O<sub>3</sub>
grain imprinted areas (identified as 'interface') and interfacial voids that
deepened into the alloy, an example can be seen in Fig. 5a.  The only
difference is that there were fewer small voids between the &alpha;-Al<sub>2</sub>O<sub>3</sub> grains.</p>

<p>&sect;35 Segregation to the surface of NiAl
was very dependent on its crystallographic orientation.  This is indicated by
more than one group of data given for each segregant in Table 3.  Since carbon
adsorption onto clean alloy surfaces often took place under the electron beam,
especially if the sample surface was not very clean, the background C level
could sometimes be rather high, giving rise to large standard deviations.
After 26 hrs at 1000&deg;C, C was the major
impurity on void faces.  There was no S; B and P tended to segregate on different
orientations.  After 100 hrs, S began to segregate to the voids; consequently,
the amount of C decreased.  The presence of S also reduced B segregation, but
did not affect that of P.</p>

