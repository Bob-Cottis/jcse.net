sulphur level at the interface.  Is it possible that more S was present at the
band/alloy interface, or is this relationship purely coincidental?  It is
difficult to imaging why a nearly epitaxial oxide/alloy interface should
accommodate S more favourably.  Instead, maybe the change in S content was
related to the change of the Al<sub>2</sub>O<sub>3</sub> phase in the scale
from predominantly &theta; to &gamma;.  However, due to their small sizes and the
overlapping diffraction rings of the three aluminas, phase identification has
been difficult.  Work is currently under way to better study the phases in
different regions and to examine the interface with TEM on cross-sections of
these scales.</p>

