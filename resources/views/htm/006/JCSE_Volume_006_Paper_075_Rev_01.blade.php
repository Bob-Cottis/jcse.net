<h2>Segregation Phenomena at Growing Alumina/Alloy Interfaces</h2>

<p>P.Y. Hou<br>
<i>Materials Sciences Division, Lawrence Berkeley National Laboratory, 1 Cyclotron Rd., MS: 62-203, Berkeley, Ca 94720, USA, <script>mailto2('pyhou','lbl.gov')</script></i></p>



<h3>Abstract</h3>

<p>&sect;1 The chemistry and structure at the scale/alloy interface are
important factors governing scale adhesion.  The chemical changes can occur
from segregation of impurities in the alloy, such as sulphur and carbon, or
alloying elements such as chromium, aluminium and reactive elements.  This
paper reviews studies of the changes of interfacial composition with oxidation
time for Al<sub>2</sub>O<sub>3</sub> formed on several model alumina-forming
alloys, and tries to relate that to the interfacial strength.  Results show
that sulphur segregation to oxide/metal interfaces can indeed occur, but the
type and amount of segregants at the interface depend on the alloy composition
and the interface structure.  Co-segregation of impurities with alloying
elements can also occur, resulting in multi-layer segregants at the interface.
Sulphur-containing interfaces are indeed weaker, but the major role of sulphur
is to enhance interfacial void formation.  Reactive elements in the alloy not
only gather sulfur but also exert an additional positive effect on scale
adhesion.</p>

<p>&sect;2 <b>Keywords: </b>oxidation, segregation, oxide/metal
interface, sulphur, reactive element, alumina, adhesion.</p>

<h3>Introduction</h3>

<p>&sect;3 Many advances have been made in the past two decades on the
growth and development of Al<sub>2</sub>O<sub>3</sub> scales.  It is now known
that the first-formed Al<sub>2</sub>O<sub>3</sub> are almost never the
thermodynamically most stable &alpha; form,
but are aluminas that have been identified as the &theta; and/or the &gamma; forms <a href="#_edn1" name="_ednref1" title="">[1]</a>,<a href="#_edn2" name="_ednref2" title="">[2]</a>,<a href="#_edn3" name="_ednref3" title="">[3]</a>,<a href="#_edn4" name="_ednref4"
title="">[4]</a><a name="_Ref482086107">,</a><a name="_Ref42491401"></a><a href="#_edn5"
name="_ednref5" title="">[5].</a><a name="_Ref42491819"></a><a href="#_edn6" name="_ednref6" title="">[6]</a>.
These are often referred to as &quot;transition&quot; or &quot;metastable&quot;
aluminas.  The more stable &alpha;-Al<sub>2</sub>O<sub>3</sub>
later nucleates at the transition alumina/alloy interface <a name="_Ref482086108"></a><a href="#_edn5">[5]</a>,<a href="#_edn6">[6]</a>,<a href="#_edn7" name="_ednref7" title="">[7]</a>,
and the initially formed  alumina transforms to the &alpha; form with time.  Limited
high resolution
TEM studies <a name="_Ref42498530"></a><a href="#_edn8" name="_ednref8"
title="">[8]</a> have shown that the interface between the transition alumina
and the alloy, in this case a single crystal NiAl, is coherent, but became incoherent
once &alpha;-Al<sub>2</sub>O<sub>3</sub> nucleates.  The
development of &alpha;-Al<sub>2</sub>O<sub>3</sub>
is faster at higher temperatures <a href="#_edn9" name="_ednref9" title="">[9]</a> and with the presence of chromium in the alloy <a href="#_edn10"
name="_ednref10" title="">[10]</a>,
but seems slower with higher concentrations of Al or the presence of a reactive
element (RE), such as Hf, Y or Zr, in the alloy <a href="#_edn11"
name="_ednref11" title="">[11]</a>.
The transition alumina grows predominantly by aluminium outward transport <a href="#_edn12" name="_ednref12" title="">[12]</a>,<a href="#_edn13" name="_ednref13" title="">[13]</a>,
but the &alpha;-Al<sub>2</sub>O<sub>3</sub>
by predominantly oxygen inward transport with a non-trivial amount of aluminium
outward diffusion <a href="#_edn14" name="_ednref14" title="">[14]</a> unless
a reactive element is present, then the Al outward transport is greatly
reduced <a href="#_edn15" name="_ednref15" title="">[15]</a>.
The growth rate of the transition alumina is about one order of magnitude
faster than that of the &alpha;-Al<sub>2</sub>O<sub>3</sub>.
Extensive void formation at scale/alloy interfaces is observed without any
reactive element, particularly on Ni or Fe aluminides <a href="#_edn16"
name="_ednref16" title="">[16]</a>,<a href="#_edn17" name="_ednref17" title="">[17]</a>.</p>

<p>&sect;4 The adherence of Al<sub>2</sub>O<sub>3</sub> scales on
alloys is an important issue for practical applications.  It has been known for
over 60 years <a href="#_edn18" name="_ednref18" title="">[18]</a>that the presence
of small amounts of RE in the alloy greatly improves Al<sub>2</sub>O<sub>3</sub>
scale adhesion.  Many mechanisms have been proposed for this phenomenon <a href="#_edn19" name="_ednref19" title="">[19]</a>,
but in recent years, the most widely accepted one is what is known as the
&quot;sulphur effect&quot; <a name="_Ref471800009"></a><a href="#_edn20"
name="_ednref20" title="">[20]</a>,<a href="#_edn21" name="_ednref21" title="">[21]</a>.
The hypothesis is that the oxide/metal interface is intrinsically strong, but
indigenous sulphur that is present in the alloys, often around 10-30 ppm,
segregates to the interface during oxidation, thus weakens the bonding and
renders the scale nonadherent.  The role of the reactive element, as a result
of their strong sulphide forming ability, is to react and tie up the sulphur;
therefore, preventing it from segregating to the interface.  Evidence of the
detrimental effect of sulphur on scale adhesion has been provided by the
performance of many different desulphurized or low S alloys <a href="#_edn22"
name="_ednref22" title="">[22]</a>,<a href="#_edn23" name="_ednref23" title="">[23]</a><a name="_Ref42652564">,</a><a href="#_edn24" name="_ednref24" title="">[24]</a>.
When the sulphur content in the alloy is reduced to less than 3 ppm or so,
often by a high temperature H<sub>2</sub>-annealing process, scale spallation
under thermal cycling conditions becomes much less <a href="#_edn24">[24]</a>.</p>

<p>&sect;5 The proposed &quot;sulphur effect&quot; points to the importance
of how interface chemistry may affect scale adhesion.  Sulphur is well known
to
segregate to alloy grain boundaries and weakens them <a href="#_edn25"
name="_ednref25" title="">[25]</a>,
and theoretical studies have shown bond weakening effects of S on Al<sub>2</sub>O<sub>3</sub>/metal
interfaces <a name="_Ref42569963"></a><a href="#_edn26" name="_ednref26"
title="">[26]</a>,<a href="#_edn27" name="_ednref27" title="">[27]</a>.
Limited mechanical testing on the strength of diffusion bonded Al<sub>2</sub>O<sub>3</sub>/metal
interfaces with the presence of a foreign element has also demonstrated strong
effects of interface chemistry <a name="_Ref42569965"></a><a href="#_edn28"
name="_ednref28" title="">[28],</a><a href="#_edn29" name="_ednref29" title="">[29]</a>,<a href="#_edn30" name="_ednref30" title="">[30]</a>.
However, most of these studies did not characterize the effect these segregants
had on interface microstructure, particularly on defect type, size and distribution.
This is especially important, since more debonded areas seem to exist when
impurities are present at these interfaces <a href="#_edn26">[26]</a>,<a href="#_edn28">[28]</a>.</p>

<p>&sect;6 Since all commercial metals and alloys contain ppm levels of
non-metallic impurities that readily segregate to surfaces when the alloy is
heated, it is often assumed that the same kind of segregation takes place at
oxide/metal interfaces.  Whether this is always the case is not known.  The
purpose of this paper is to summarize work done in recent years in our
laboratory on the chemical changes at several Al<sub>2</sub>O<sub>3</sub>/alloy
interfaces in order to provide a better understanding of the segregation
phenomena that occur during scale growth.</p>

<p>&sect;7 Results and discussions are presented separately for three
groups of alloys: iron aluminides, nickel aluminides and MCrAl, where M is Fe
or Ni.  In each section, the segregation behaviour is first discussed and
related to surface segregation, alloy composition and the oxide growth process.
Subsequently, the relationship between segregation, interface morphology and any
existing data on interfacial strengths is addressed.</p>

<h3>Experimental</h3>

<p>&sect;8 Several different types of model alloys were studied; these
include nickel and iron aluminides, Zr-doped iron aluminide and MCrAl type
alloy with M being Fe or Ni.  The stoichiometric NiAl and the iron aluminides
were made at Oak Ridge National Laboratory (ORNL).  The NiCrAl was supplied by
NASA; other alloys were made at LBNL.  All the alloys were induction or arc
melted, followed by a high temperature anneal in inert atmospheres.  15mm x 10
mm x 1mm sized specimens were either cut from the ingot or from a hot rolled
sheet.  Hydrogen-annealing of a few alloy specimens was performed in
Zr-gettered hydrogen at 1200&deg;C for 100
hours.  The alloy identifications, compositions and sulphur and carbon impurity
levels are given in Table 1.  It is seen that the nickel alumnides have much
lower sulphur contents than the other alloys, indicating that the starting powders
of Fe and Cr must contain more S impurity than Ni.  Other than C and S
impurities, the NiAl and FeAl alloys also contain tens of ppm of B and P
respectively.<i>  </i>In order to increase the S content of the NiAl, a batch
of Ni-40at%Al was made with small amounts of NiS, and it is identified as the S-doped
alloy, i.e., Ni40Al(S).</p>

