<p>&sect;10 One face of the specimen was polished to a 1 &micro;m finish with diamond paste prior to
oxidation in 1 atmosphere flowing dry oxygen.  Most tests were conducted at
1000<sup>o</sup>C, with some at 900&deg;C
or 1100<sup>o</sup>C.  The specimen was placed in an alumina boat with a
thermocouple at its back to monitor its surface temperature, and then pushed
into the hot zone of a horizontal furnace to start the oxidation.  At the end
of the desired oxidation time the boat and specimen were quickly pulled out of
the furnace and cooled in ambient air.  A few specimens were also
water-quenched or furnace-cooled in order to establish different cooling rates.</p>

<p>&sect;11 The chemistry of the scale/alloy interface after oxidation
was examined using a PHI 660 Scanning Auger Electron Microscope (AES), where
  a specially designed linear translator mounted with a Vickers micro-indenter
  had
been placed in its ultra high vacuum (UHV) chamber.  To make a scratch, the
oxidized specimen was first pushed into the indenter using the Z-control of the
specimen stage, followed by a drag along the X or the Y direction for several
millimetres <a href="#_edn31" name="_ednref31" title="">[31]</a>.
This motion often produced numerous fractures adjacent to the scratch mark and
induced spallation of the oxide scale, which exposed the scale/alloy
interface.  The vacuum condition during and after the scratch was usually
better than 2&times;10<sup>-10</sup> torr.  A 0.5-1 &micro;m
diameter electron beam was placed on the exposed alloy surface to study its
composition.  Usually 10-15 areas on each specimen were surveyed at 10 kV and
90&deg; incident angle.  With the attached
scanning electron microscope (SEM), the morphology of the surveyed area, such
as interfacial pores, alloy grains and convoluted ridges or valleys of the
interface could be distinguished.  The scratch motion also caused pieces of
oxide scales to flip over such that the underside of the spalled oxide could
be
analyzed as well.  The composition at the scale underside was always only O and
Al.  All segregants remained on the alloy side after scale spallation.</p>

<p>&sect;12 One specimen was examined using a PHI model 670 field
emission AES (FEAES), which has a typical probe size of 30 nm <a name="_Ref41372417"></a><a href="#_edn32" name="_ednref32" title="">[32]</a>.
This small probe provided the chemical analysis of individual facets on the
alloy surface without any possible inclusion of nearby interfacial pores.  A
few alloys were also studied using a micro X-ray photoelectron spectrometer (&micro;XPS)
to distinguish the chemical states of
the segregants <a name="_Ref42580363"></a><a href="#_edn33" name="_ednref33"
title="">[33]</a>.</p>

<p>&sect;13 X-ray diffraction (XRD) was used to identify the scale and
alloy phases, and more detailed examination of the scale and interface
morphology was performed using field emission SEM after the Auger analysis.</p>

<h3>Results and Discussions</h3>

<h4>Iron Aluminides</h4>

<p>&sect;14 The only segregant found at the
Al<sub>2</sub>O<sub>3</sub>/FeAl interfaces was sulphur.  The amount of S as
a
function of oxidation time for the Fe40Al and the Fe<sub>3</sub>Al are
presented in Figure 1(a) and (b) respectively.  The surface atomic percent was
calculated using peak heights and tabulated Auger sensitivity factors <a href="#_edn34" name="_ednref34" title="">[34]</a>.
The coverage in monolayer was determined from the attenuation of the Fe signal
compared to that of the starting alloy.  On Fig. 1(a), results from FEAES are
also included along with a calculated S content (interface sweeping) assuming
the interface grows entirely inward with time and all the S impurity in the
alloy accumulates at the interface.</p>


