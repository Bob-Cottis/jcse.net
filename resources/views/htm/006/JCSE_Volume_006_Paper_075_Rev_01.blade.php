<p>&sect;32 One last point that is worth discussing is the flatness of
the scale on the H<sub>2</sub>-annealed and the Zr-doped alloy compared to that
of the undoped starting alloy (Fig. 4).  Similar results have also been
reported for FeCrAl, where the scale/alloy interface on the normal purity alloy
