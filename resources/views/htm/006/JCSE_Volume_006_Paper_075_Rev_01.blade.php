<p>The void faces of Ni40Al, on the other hand, were covered
with a high concentration of S much earlier than NiAl.  Since both the normal
purity NiAl and Ni40Al have similar bulk sulphur contents (2-4 ppm), the
difference in saturation rate must be the result of faster S diffusion in
Ni40Al than in NiAl.  From these segregation results, approximate S diffusion
coefficients in the two alloys were calculated to be 6&times;10<sup>-9</sup> cm<sup>2</sup>/s
for Ni40Al and 1&times;10<sup>-9</sup> cm<sup>2</sup>/s for NiAl.  The much slower
rate in stoichiometric NiAl is consistent with the fact that Ni diffusion in
NiAl is almost 10 times slower than that in Ni40Al <a href="#_edn53"
name="_ednref53" title="">[53]</a>.
The magnitude of S diffusion calculated here is also in agreement with recent
data from S segregation studies on NiAl surfaces <a href="#_edn54"
name="_ednref54" title="">[54]</a>.</p>

<p>&sect;38 The interface areas on the NiAl were mostly free from any
sulphur or carbon.  Some areas contained boron, or phosphorus and boron, but
SEM observations were not able to associate any interface features with their
presence.  The occurrence of P and B decreased with longer oxidation time, up
to 100 hours, and higher temperatures, up to 1150<sup>o</sup>C, yet the
interface continued to be free from carbon or sulphur, even when S already
segregated to the void surfaces.</p>

