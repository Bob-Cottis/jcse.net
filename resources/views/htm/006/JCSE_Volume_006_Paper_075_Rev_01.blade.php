<h3>Conclusions</h3>

<p>&sect;51 Studies of impurity segregation to growing Al<sub>2</sub>O<sub>3</sub>/alloy
interfaces have shown the following:</p>

<ol start=1 type=1>
 <li>Sulphur can surely segregate to intact Al<sub>2</sub>O<sub>3</sub>/alloy
     interfaces during scale growth.</li>
 <li>While NiAl and Ni40Al contain similar levels of indigenous
     sulphur impurity, S segregated to the &alpha;-Al<sub>2</sub>O<sub>3</sub>/Ni40Al
     interface but was never detected at the &alpha;-Al<sub>2</sub>O<sub>3</sub>/NiAl
     interface.  The difference is probably related to a higher interface
     energy of the former.</li>
 <li>Sulphur was the only segregant found on the &alpha;-Al<sub>2</sub>O<sub>3</sub>/Fe<sub>3</sub>Al,
     Fe-40Al interfaces.  It appeared after the &alpha;-Al<sub>2</sub>O<sub>3</sub>
     formed a complete layer at the interface.  Prior to that, the Al<sub>2</sub>O<sub>3</sub>/Fe40Al
     interface was free from segregants, but S segregated to the Al<sub>2</sub>O<sub>3</sub>/Fe<sub>3</sub>Al
     interface upon oxidation but later desegregated.  This dependence of
     interface S content on scale growth may be related to the continually
     changing interface microstructure between the first-formed alumina and
     those that developed at later stages.</li>
 <li>On FeCrAl and NiCrAl alloys, Cr co-segregated with sulphur
     causing a higher than 1 monolayer S coverage at the Al<sub>2</sub>O<sub>3</sub>/alloy
     interface at all times.  Carbon also segregated to the alumina-FeCrAl, but
     not NiCrAl, interface during cooling.</li>
</ol>

<p>&sect;52 These results clearly demonstrate that impurity segregation
to growing oxide/alloy interfaces can vary significantly on alloy compositions
and on interface structures that result from the oxidation process.</p>

<p>&sect;53 Relating segregation to limited interface fracture studies
show that</p>

<ul type=disc>
 <li>The major effect of sulphur, as least in NiAl alloys, was
     to enhance interfacial void formation.  This essentially created a weaker
     interface by increasing its defect concentration.</li>
 <li>Sulphur-containing interfaces were weaker than S-free
     interfaces (&lt;0.04 monolyer), although the latter can still fail
     adhesively.  The interface formed on reactive element containing alloys
     was also S-free but much stronger, suggesting a positive effect of RE on
     scale adhesion, rather than just preventing S from segregating to the
     interface.</li>
</ul>

