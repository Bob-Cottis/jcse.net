<p>&sect;41 The segregation behaviour at &alpha;-Al<sub>2</sub>O<sub>3</sub>/NiAl
and Ni40Al interfaces again showed some interesting phenomena.  First of all,
here is an interface, Al<sub>2</sub>O<sub>3</sub>/NiAl, which did not show any
S segregation, even when S already covered most of its void faces.  Instead,
some locations of this interface were found with small amounts of B or B and
P.  Secondly, an alloy with less Al, Ni40Al, but with the same phase and
similar bulk impurity contents, forming an interface with the same type of
oxide under the same oxidation conditions, consistently showed sulphur at its
interface.  Why should sulphur prefer one but not the other?  Wetting studies
of NiAl on sapphire <a href="#_edn55" name="_ednref55" title="">[55]</a>,
and theoretical analysis on metal/ceramic bonding <a href="#_edn56"
name="_ednref56" title="">[56]</a>have shown that the interface energy between Ni(Al)/Al<sub>2</sub>O<sub>3</sub>
decreases with increasing Al activity near the alumina dissociation pO<sub>2</sub>.
This would imply that the Al<sub>2</sub>O<sub>3</sub>/Ni40Al interface formed
during oxidation has a higher energy than the Al<sub>2</sub>O<sub>3</sub>/NiAl
interface, such that sulphur segregation may be more favoured on the Ni40Al to
lower its energy.  Whether the same difference will be observed between Fe40Al
and FeAl would be worth investigating, especially since S has already been shown
to segregate to Al<sub>2</sub>O<sub>3</sub>/Fe40Al interfaces (Fig. 1a).
Another difference that might exist between the Fe and the Ni aluminides, which
is currently being evaluated, is the build up of S with oxidation time,
especially at the initial stage while the Al<sub>2</sub>O<sub>3</sub> at the
interface has not yet transformed to the &alpha;-form.</p>

<p>&sect;42 Using a simple tensile pull-test <a href="#_edn52">[52]</a>, the adherence of Al<sub>2</sub>O<sub>3</sub>
scales formed on these nickel aluminide alloys was found to be dictated by the
number of interfacial pores, as seen in Fig. 6.   The test was done by pulling
a 3mm diameter stub under continually increasing loads until failure occurred,
which often took place at the scale/alloy interface.  The stub was bonded to
the oxide surface via an epoxy that has a maximum failure stress of 103 MPa.
Analysis of interfacial pore density was conducted on each failed area from SEM
micrographs.  The data points in Fig. 6 were obtained from several NiAl and
Ni40Al alloys.  Although S was present at the Ni40Al but not at the NiAl
interfaces, its presence did not make a difference within the scatter of the data.
The S-doped NiAl with a high pore density of 11/&micro;m<sup>2</sup>
after only 10 min oxidation failed at 4.9 MPa, had the lowest interface
strength tested so far.  These results show very clearly that the dominating
effect of S is to enhance interfacial pore formation.  These defects
deteriorate the interface strength more dramatically than any bond weakening
effect caused by the presence of S at the interface.  There seems to be some
indications that for the same pore density, the interfaces under thicker scales
are weaker.  This may be a pore size effect, which was not taken into account.
More work is being conducted on the effect of oxidation time, and to relate
that to the interface microstructure and chemistry.</p>

