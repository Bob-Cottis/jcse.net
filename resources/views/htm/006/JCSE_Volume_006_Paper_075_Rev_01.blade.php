<p>Representative microstructures indicated
in Fig. 1 are given in Figure 2.  The initially formed scale, Fig. 2(a),
consisted of fine-grained &theta; and &gamma;-Al<sub>2</sub>O<sub>3</sub>, leaving a
rather smooth and featureless interface.  Isolated &alpha;-Al<sub>2</sub>O<sub>3</sub> grains formed randomly at the
