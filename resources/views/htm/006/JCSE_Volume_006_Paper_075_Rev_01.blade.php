<p>&sect;18 At the free surfaces, or void faces, saturation was reached
early.  Using the model of Lea and Seah <a href="#_edn35" name="_ednref35"
title="">[35]</a>and the bulk S content reported in Table 1, the rate to reach
saturation gave
an approximate S diffusivity of 3 x 10<sup>-8 </sup>cm<sup>2</sup>/s at 1000&deg;C,
which falls within the range reported for S diffusion in Fe and Ni-based alloys
<a href="#_edn36" name="_ednref36"
title="">[36]</a>,<a href="#_edn37" name="_ednref37" title="">[37]</a>,<a href="#_edn38" name="_ednref38" title="">[38]</a><i>.</i>
Although S usually segregates on metals at a saturation level of 0.5 monolyer
<a href="#_edn39" name="_ednref39" title="">[39]</a>,
the amount found here on void faces is higher, especially on the large voids.
Although the data points have been grouped together here with error bars, it
has been shown in an earlier publication <a href="#_edn32">[32]</a> that the
amount of S increased with void growth and that S co-segregated with Al to the
void
surfaces, but similar co-segregation was not found at the interface.</p>

<p>&sect;19 The small probe of FEAES placed on individual imprinted
grain facets on Fe40Al detected lower S content than that obtained from the
conventional AES with a 0.5-1 &micro;m probe,
indicating that the larger probe included sub-micron voids that had higher S
contents (Fig. 1a).  Nevertheless, the trend of S built up with oxidation time
was not affected by the probe size.  For the Fe40Al, no S was detected at the
interface between the first formed &gamma; and/or &theta;-Al<sub>2</sub>O<sub>3</sub>, nor was it
found at the base of the first nucleated, isolated &alpha;-Al<sub>2</sub>O<sub>3</sub> grain imprints.  S started to
appear at the interface only after a complete &alpha;-Al<sub>2</sub>O<sub>3</sub>
layer developed at the interface at about 1hr at 1000<sup>o</sup>C.  As the
scale grew, the interfacial S content increased slowly until a saturation level
of about half a monolayer was reached.  The amount of S detected at the
interface was significantly higher than that calculated from interface
sweeping.  This indicates that sulphur indeed segregated to the interface
during scale growth.</p>

<p>&sect;20 The observed slow sulphur built up at Al<sub>2</sub>O<sub>3</sub>/Fe40Al
interfaces is rather surprising.  The rate is definitely not limited by S
diffusion in the alloy, in light of what was observed on the void surfaces.
Since the transition from the absence of S at the interface to its first
appearance is related to the transformation of the metastable transition Al<sub>2</sub>O<sub>3</sub>
to &alpha;-Al<sub>2</sub>O<sub>3</sub>, it is
possible that the interfacial segregation behaviour is related to the type of
interface formed between the alloy and the different aluminas.  This is
especially plausible because past results have indicated that the interface
between &gamma;-Al<sub>2</sub>O<sub>3</sub>
and a single crystal NiAl is coherent, but becomes incoherent once &alpha;-Al<sub>2</sub>O<sub>3</sub> forms <a href="#_edn8">[8]</a>.
However, whether the same is true for Fe40Al is unknown.</p>

<p>&sect;21 The fact that S was not detected beneath isolated, first
nucleated &alpha;-Al<sub>2</sub>O<sub>3</sub>
grains, but only appeared after the &alpha;-Al<sub>2</sub>O<sub>3</sub>
had impinged onto one another to form a complete layer at the interface
suggests that segregation is not related to the phase of the alumina per se,
but to the interface structure that must be changing continuously in response
to the growth process, and in this case, probably the growth stress that arose
from the impingement of &alpha;-Al<sub>2</sub>O<sub>3</sub>
into a complete layer.  To relax this stress, grains at the interface may
undergo reorientation or rearrangements.  These processes should alter the
interface microstructure.  As the microstructure changes, segregation energy
will also change.  The less coherent the interface, the more favourable it is
for S to segregate.  The process would be similar to that observed at alloy
grain boundaries, where the extent of solute segregation often depends on the
boundary structure <a href="#_edn40" name="_ednref40" title="">[40]</a>,<a name="_Ref482083922"></a><a href="#_edn41" name="_ednref41" title="">[41]</a>,
with the tilt angle probably being the most significant factor <a name="_Ref482083924"></a><a href="#_edn42" name="_ednref42" title="">[42]</a>.</p>

<p>&sect;22 For the Fe<sub>3</sub>Al, the segregation behaviour at the
interface was even more surprising (Fig. 1b).  At both 1000 and 900&deg;C, the S content was initially high, went
through a minimum of almost zero, then increased slowly to a steady state level
similar to that found on the Fe40Al alloy.  Greater spatial variations, not
related to alloy grains, exist at shorter oxidation times, as indicated by the
larger error bars.  This means that the sulphur content at the alloy surface
under these initial scales varied locally within the probe size of a 0.5-1 &micro;m diameter area.  After the minimum point,
the amount of S at different locations of the interface became much more
uniform.  The minimum for both temperatures corresponded again to the
development of a complete layer of &alpha;-Al<sub>2</sub>O<sub>3</sub>
at the interface.</p>

