scheduled maintenance.</p>

<h4>Converter and Heat Exchanger Configuration </h4>

<script>comment();</script><p>The hot gas converter system is designed to
optimize the reaction whereby SO<sub>2</sub> is converted to SO<sub>3</sub>. It
is a system of vessels which consists of the converter, usually containing four
beds of V<sub>2</sub>O<sub>5</sub> catalysts, with heat exchangers located
after each catalyst bed, which utilise the heat of the reaction to preheat the
incoming gas to the reaction temperature, and cool the reacted gas.� </p>

<script>comment();</script><p>A schematic is shown in Figure
1.� For simplicity only the one of the heat exchangers is shown. This heat
exchanger is located in the gas stream directly after the 1<sup>st</sup> bed,
where the majority of the conversion takes place, the majority of heat is
generated, and thus is called the �hot� heat exchanger.� It is the only process
vessel between the 1<sup>st</sup> and 2<sup>nd </sup>catalyst beds, and therefore
the tubes of this heat exchanger, along with the 1<sup>st</sup> bed support
structure and the associated gas ducts, represent the sources of the scale
which foul the catalyst bed. </p>

