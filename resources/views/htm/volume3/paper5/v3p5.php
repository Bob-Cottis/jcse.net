    <h4><a name="_Toc478818773">Osmotic Blistering</a></h4>
    <SCRIPT>comment(40)</SCRIPT><p>This form of failure is one consequence of a contaminated substrate.<a href="#_edn30" name="_ednref30" title>[30]</a>,
    <a href="#_edn31" name="_ednref31" title>[31]</a>, <a href="#_edn32" name="_ednref32" title>[32]</a>.
    Soluble salts at the interface can form a concentrated salt solution that,
    due to decreased water activity, acts to draw water through the coating,
    which behaves as a semi-permeable membrane, from the exposure
    environment.&nbsp; The osmotic pressures exerted can be expected to range
    between 2500 and 3000 kPa compared with a mechanical resistance to
    deformation of a coating ranging from 6 to 40 kPa<a href="#_edn33" name="_ednref33" title>[33]</a>&nbsp;
    In addition, the pressure within the blister becomes greater than the
    atmosphere on the free side.<a href="#_edn34" name="_ednref34" title>[34]</a>&nbsp;
    So, provided the film is not ruptured in the process, the conditions exist
    for a continually expanding blister.&nbsp;</p>
    <SCRIPT>comment(41)</SCRIPT><p>Figure 5 schematically represents the stages in the formation of an
    osmotic blister.&nbsp; In Figure 5A a layer of interfacial water in contact
    with soluble salts is shown, in B the salts at the interface have formed a
    concentrated solution and finally C represents the stage when the osmotic
