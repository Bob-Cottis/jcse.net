    Watts<sup>55</sup>.&nbsp; A number of factorsdrew them to this
    conclusion.&nbsp; Firstly, EPMA examination of a cross section of the
    polymer showed Na ingress into the coating from both the interface and the
    bulk solution; however there was no Na detected in the bulk of the polymer,
    suggesting that Na<sup>+</sup> did not fully penetrate the film.&nbsp; Also,
    the disbondment velocity (Dk) was related to the surface profile of the
    substrate and the parameter referred to as tortuosity (<i>&#964;</i>) as
    follows:</p>
