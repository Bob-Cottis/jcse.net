    however, it has been shown29 that electroosmosis plays a predominant role in
    this form of blistering with osmosis only contributing to about 6%.&nbsp;</p>
    <SCRIPT>comment(42)</SCRIPT><p>Gowers and Scantlebury<a href="#_edn35" name="_ednref35" title>[35]</a>
    were able to measure currents flowing between different areas of the
    substrate under artificial blisters.&nbsp; They found that contaminating the
    surface of the substrate with NaCl promoted the corrosion process, the
    current flow and the blister formation.</p>
    <SCRIPT>comment(43)</SCRIPT><p>One suggestion that has been made to reduce the tendency for a coating to
    suffer osmotic blistering hinges on the formulation of the film.&nbsp; In a
    solvent borne coating, it is normal to find that a combination of different
    solvents is employed.&nbsp; According to Storfer and Yuhas, hydrophilic
    solvents can facilitate and accelerate several of the generally recognized
    blister-formation mechanisms, particularly osmotic blistering.<a href="#_edn36" name="_ednref36" title>[36]</a>&nbsp;
    An inverse relationship was determined between the solubility of water in
    the last solvent in the formulation to evaporate (the tail solvent) and the
    blister resistance of the coating.&nbsp; Hydrophilic solvents are more prone
    to cause blistering.</p>
    <h4><a name="_Toc478818774">Anodic Blistering</a></h4>
    <SCRIPT>comment(44)</SCRIPT><p>This mode of failure was addressed by Koehler,<a href="#_edn37" name="_ednref37" title>[37]</a>
    who considered liquid filled blisters to be anodic in nature.&nbsp;
    Experiments were conducted an on epoxy phenolic resin exposed to 0.01N
    calcium chloride.&nbsp; Calcium was chosen as the cation because it does not
    produce an alkaline environment.&nbsp; No deliberate faults or holidays were
    introduced into the coating.&nbsp; The samples were anodically polarised and
    blisters appeared after seven days.&nbsp; The substrate in the centre of the
    blister was dished and corroded while the periphery was bright, indicating
    cathodic detachment.&nbsp; The explanation of these results was that during
    the test, chloride ions passed through the coating at thin points and
    created an acid environment under the coating within the blister.&nbsp; The
    cathodic reaction was postulated as being the reduction of the ferric oxide
    on the steel to the soluble ferrous state.&nbsp; This type of failure
    requires a low pH environment to proceed.&nbsp; Such low values may be found
    in many canned foods and drinks.&nbsp;</p>
    <h4><a name="_Toc478818775">Cathodic Blistering</a></h4>
    <SCRIPT>comment(45)</SCRIPT><p>It is to this form of blistering that the majority of the literature is
    directed.&nbsp; Cathodic blistering is the result of an alkaline environment
    under the coating caused by the cathodic reaction, associated with corrosion
    that occurs at a damaged site of the film<a name="_Ref398475679">.</a><a href="#_edn38" name="_ednref38" title>[38]</a><sup>,</sup>
    <a href="#_edn39" name="_ednref39" title>[39]</a>, <a href="#_edn40" name="_ednref40" title>[40]</a>&nbsp;
    Early work by Kittelberger <a href="#_edn41" name="_ednref41" title>[41]</a>
    identified a relationship between bare areas of steel and the occurrence of
    blistering on immersed panels; it was shown that an exposed area of <sub><img src="v3p5_image014.gif" width="33" height="25"></sub>&nbsp;of
    the coated specimen was enough to exert a significant influence on the
    blistering of the coating.&nbsp; Figure 6 shows the proposed failure
    mechanisms, which may occur when a film containing a fault is exposed to a
    corrosive medium.&nbsp;</p>
    <SCRIPT>comment(46)</SCRIPT><p>The fault may take the form of mechanical damage to the coating or may be
    an inherent fault of the coating i.e. pores or holidays.&nbsp; The primary
    pre-requisite for this form of failure is that the substrate should support
    a cathodic reaction.&nbsp; In the case of a neutral or alkaline environment the cathodic reaction would be the
    reduction of oxygen.&nbsp; Tests with cathodically polarised steel coated
    with polybutadiene, exposed to a NaCl solution,<a href="#_edn42" name="_ednref42" title>[42]</a>
    developed blisters containing a solution of high pH after 7 days, which
    coalesced in 18 days; no intentional damage was made to the coatings in this
    experiment.&nbsp;</p>
