    <h3><a name="_Toc478818783">Adhesion</a></h3>
    <SCRIPT>comment(101)</SCRIPT><p>Within the debate regarding the important features of the protection
    offered by organic coatings, the argument over the importance of adhesion is
    amongst the most contentious.&nbsp; It is often claimed (or at least
    inferred) that the adhesion is of paramount importance, to the exclusion of
    all other features.&nbsp; According to Bullett and Prosser �the ability to
    adhere to the substrate throughout the desired life of the coating is one of
    the basic requirements of a surface coating, second only to the initial need
    to wet the substrate�,<a href="#_edn85" name="_ednref85" title>[85]</a>
    Funke describes adhesion as the most important and decisive property of a
    coating.<a href="#_edn86" name="_ednref86" title>[86]</a></p>
    <SCRIPT>comment(102)</SCRIPT><p>In order to enter into the debate as to the importance of adhesion in the
    protective mechanisms of paint systems, it is necessary to consider the
    process of adhesion itself.&nbsp; In particular, the principle of adhesive
    failure needs to be addressed.&nbsp; For two surfaces to adhere to each
    other, be they of the same or of different materials, there is a need for
    intimate contact on an atomic scale between them.&nbsp; In the case of a
    liquid coating, the degree of intimate contact is governed by the ability of
    the coating to wet the substrate.&nbsp; This wetting ability needs to be
    present not only in the static sense, where the contact angle between the
    substrate and the liquid may be used to determine how efficient the wetting
    is; but also in a dynamic sense, where the rate of wetting of the substrate
    is not countered by the rate of shear involved in the application of the
    coating.<a href="#_edn87" name="_ednref87" title>[87]</a>&nbsp;&nbsp;</p>
    <SCRIPT>comment(103)</SCRIPT><p>According to Bikerman<a href="#_edn88" name="_ednref88" title>[88]</a> a
    true adhesion failure is improbable, a view shared by others<a name="_Ref477923931">.</a><a href="#_edn89" name="_ednref89" title>[89]</a><sup>,
    <a href="#_edn90" name="_ednref90" title>[90]</a></sup>&nbsp; If one
    subscribes to this point of view, then it must be the case that all failures
    that involve detachment of the coating from the substrate, must include some
    element of either cohesive failure of the coating or the oxide layer on the
    surface of the steel.&nbsp; This view is reinforced by XPS work which has
    been carried out by several workers, who have found that traces of polymeric
    material is normally found on the �metal� surface of a metal/polymer
    interfacial fracture, which may have appeared to be a purely adhesive
    failure from a visual examination.<a href="#_edn54" name="_ednref54" title>[54]</a>,
    <a href="#_edn55" name="_ednref54" title>[55]</a>, <a href="#_edn91" name="_ednref91" title>[91]</a>&nbsp;
    Although the explanation of a cohesive polymer or oxide failure may account
    for the observed failure modes, this is probably still an
    oversimplification.</p>
    <SCRIPT>comment(104)</SCRIPT><p>The function of the adhesive joint is to effectively remove the interface
    from between the two different materials.&nbsp; In order to do this, a
    gradual change must occur in the chemical composition of the coating and the
    substrate in the interfacial area.&nbsp; Plato elucidated this concept in
    his explanation of how the different elements in the universe could exist as
    one:</p>
    <SCRIPT>comment(105)</SCRIPT><p>It is not possible for two things to be fairly united without a third for
    they need a bond between them which shall join them both, that as the first
    is to the middle, so is the middle to the last, then since the middle
    becomes the first and the last, and the last and the first become the
    middle, of necessity, all will come to be the same. And being the same with
    one another, all will be a unity.<a href="#_edn92" name="_ednref92" title>[92]</a></p>
    <SCRIPT>comment(106)</SCRIPT><p>The inference of this is that in a true adhesive joint, there is no
    clearly defined interface and that one needs to see the interfacial area as
    a gradual transitory phase where the coating and the substrate affect and
    are effected by each other.&nbsp; So, in the same way that the atomic
    structure of the metal gradually changes from that of the bulk phase to that
    of the oxide on the surface � it is proposed that the properties of the
    surface of the uncoated metal are changed when a polymeric coating is
    applied to it.&nbsp; In the same way, Leidheiser suggests that the structure
    of the coating varies as one passes through the bulk of the film to the
    interfacial region with the metal.&nbsp; This region, which extends into the
    polymer, is known as the interphase.&nbsp; The size of the interphase is not
    known, but is thought to be in the nanometer range<a name="_Ref477924448">.</a><a href="#_edn93" name="_ednref93" title>[93]</a>&nbsp;</p>
    <SCRIPT>comment(107)</SCRIPT><p>Whilst loss of adhesion may occur under a number of situations, a major
    form of this type of failure is loss of adhesion resulting from exposure to
    aqueous or humid environments.<a href="#_edn94" name="_ednref94" title>[94]</a>,
    <a href="#_edn95" name="_ednref95" title>[95]</a>, <a href="#_edn96" name="_ednref96" title>[96]</a>,
    <a href="#_edn97" name="_ednref97" title>[97]</a>&nbsp; As a result of work
    into loss of adhesion due to water ingress, it has been concluded that
    adhesion tests in the dry state are of little use for applications in
    immersed or humid environments.<a href="#_edn98" name="_ednref98" title>[98]</a>,
    <a href="#_edn99" name="_ednref99" title>[99]</a> Indeed, it has been
    proposed that, in isolation, adhesion tests are insufficient to determine
    the ability of a coating to control corrosion.<a href="#_edn100" name="_ednref100" title>[100]</a>&nbsp;</p>
    <SCRIPT>comment(108)</SCRIPT><p>It is true that the adhesion plays an important role in the protective
    mechanism of coatings, but one should take care when claiming that it is the
    overriding consideration.&nbsp; When one considers the process of cathodic
    delamination it is clear that, once the paint has become detached from the
    substrate, the underlying metal is exposed to the environment and is no
    longer afforded any protection from the coating system.&nbsp; One should,
    however, be careful to ensure that the whole process is viewed in
    context.&nbsp; Whilst the loss of adhesion, resulting from the delamination
    process, effectively negates the protection afforded by the coating, it is
    important to consider whether the original adhesion is the deciding factor
    in the delamination process.&nbsp; Gowers and Scantlebury suggested that the
    beneficial role of the adhesion of a paint coating is due to the impairment
    of the formation of a layer of electrolyte at the coating/substrate
    interface, preventing ionic current flow and the spread of corrosion over
    the surface.<a href="#_edn101" name="_ednref101" title>[101]</a></p>
    <SCRIPT>comment(109)</SCRIPT><p>A case in point is that of the use of silanes as adhesion
    promoters.&nbsp; There has been a considerable amount of work carried out in
    this field and the results may, at first sight, appear contradictory.&nbsp;
    The fundamental reason why the results of various investigations lead to
    different conclusions is the starting point and the assumptions made by the
    investigators.&nbsp;</p>
    <SCRIPT>comment(110)</SCRIPT><p>Some work has been carried out which has shown that the use of silane
    coupling agents leads to an improvement in wet adhesion.<a href="#_edn89" name="_ednref54" title>[89]</a>&nbsp;
    This result has been used to assert that this treatment will therefore
    result in enhanced protection against such phenomena as underfilm corrosion
    or cathodic delamination.&nbsp; This claim is based on the assumption that
    good adhesion to the substrate will necessarily lead to improvement in the
    protection afforded.</p>
    <SCRIPT>comment(111)</SCRIPT><p>The work of Marsh<a href="#_edn102" name="_ednref102" title>[102]</a> et
    al has tested this assumption and has shown that although silanisation does
    in fact provide better wet adhesion, the treatment afforded no improvement
    in resistance to delamination and that underfilm corrosion was significantly
    worse on the silane treated specimens.&nbsp; It was pointed out by Marsh
    that the use of the cathodic delamination test as a determinant of adhesive
    strength is inappropriate, as the work showed that improved adhesion played
    no part in the rate of delamination.&nbsp; Furthermore, the cathodic
    delamination test is therefore a measure of cationic mobility rather than
    adhesive strength.&nbsp; Other work on salt spray exposure of coated panels
    has found that no adhesion loss had occurred although severe underfilm
    corrosion had taken place.<a href="#_edn103" name="_ednref103" title>[103]</a></p>
    <SCRIPT>comment(112)</SCRIPT><p>Good surface preparation is the key to good adhesion but the type, as
    well as the condition, of the substrate has been found to have a strong
    influence upon the initial dry, and the subsequent wet, adhesion of a
    metal/polymer system.&nbsp; Gosselin showed that the metal used as the
    substrate affected the strength of the final dry bond and Arslanov found
    that the adhesive strength of a coating on aluminium initially decreased
    upon exposure to water, but subsequently got stronger.<a href="#_edn104" name="_ednref104" title>[104]</a></p>
    <SCRIPT>comment(113)</SCRIPT><p>One method that has been suggested by Funke to increase the adhesive
    strength of coated systems is the application of thin layer technology.<a href="#_edn105" name="_ednref105" title>[105]</a>,
    <a href="#_edn106" name="_ednref106" title>[106]</a> Here, it is suggested
    that the adhesion to the substrate could be enhanced as a result of
    co-operative bonding between the intermediate thin polymer layer and the
    main bulk of the coating.&nbsp;</p>
    <SCRIPT>comment(114)</SCRIPT><p>The proposed thickness of these coatings is in the nanometer range. <a href="#_edn107" name="_ednref107" title>[107]</a>&nbsp;
    While this is in conflict with common coating practice whereby the base
    primer coat is thicker than the surface profile of the metal, when viewed in
    conjunction with Leidheiser�s contention regarding the interfacial region,<a href="#_edn93" name="_ednref54" title>[93]</a>
    and Kumins� model of restricted chain mobility within thin layers;<a href="#_edn108" name="_ednref108" title>[108]</a>
    this may well be an avenue worthy of investigation.</p>
    <SCRIPT>comment(115)</SCRIPT><p>In conclusion, though the adhesion of a coating to the substrate is an
    important factor, which must be taken into consideration when addressing the
    mechanisms of corrosion protection afforded by the coating, the overall
    picture is much more complex and the overall mechanism that must be
    considered is a combination of various contributory factors.&nbsp; Karyakina
    and Kuzmak have produced an extensive overview of the processes of adhesion
    and other contributory factors, together with a review of experimental
    techniques for the evaluation of coating properties.<a href="#_edn109" name="_ednref109" title>[109]</a></p>
