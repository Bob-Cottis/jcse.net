    <SCRIPT>comment(48)</SCRIPT><p>As no defects were introduced into the coating material, the question of
    the route taken by the reactants is raised.&nbsp; Mayne12 showed that the
    transmission of water and oxygen through the film was in excess of that
    needed for corrosion and attributed the protective mechanism to the film�s
    high ionic resistance.&nbsp; If we accept this hypothesis, some pathway must
    exist through the film to allow the sodium ions to the interface in order to
    produce the alkaline environment.&nbsp; These pathways could be due to
    pores.&nbsp; However, an alternative theory is proposed by Leidheiser,<a href="#_edn43" name="_ednref43" title>[43]</a>
    who in a cautionary technical note suggests that above a given
    concentration, alkali cations may have a deleterious effect on the coating,
    which leads to morphological changes hence introducing conductive pathways
    to the interface.</p>
    <h3><a name="_Toc478818776">Cathodic Delamination</a></h3>
    <SCRIPT>comment(49)</SCRIPT><p>Akin to cathodic blistering, cathodic delamination is also the result of
    alkalinity at the interface.&nbsp; Again, this alkalinity is the result of
    cathodic activity under the coating.&nbsp; It is associated with faults,
    either inherent or induced, in the coating. Cathodic polarisation may be a
    consequence of either corrosion at the point of damage or the application of
    cathodic protection.&nbsp; Resulting from experiments carried out by Smith
    and Dickie on primer failure,<a href="#_edn44" name="_ednref44" title>[44]</a>
    it has been shown that under impressed cathodic conditions, corrosion
    inhibitive pigments play no part in the reduction of disbonding.&nbsp; Under
    such conditions, the performance of the system reflects the resistance of
    the primer resin system to alkali displacement.&nbsp; During salt spray
    exposure, it was found that the corrosion inhibitive pigments did exhibit a
    degree of control of the rate of failure by inhibiting the anodic reaction.</p>
    <h3><a name="_Toc478818777">The Mechanism Of Delamination</a></h3>
    <SCRIPT>comment(50)</SCRIPT><p>A number of explanations have been put forward whereby the alkaline
    environment under the film affects the integrity of the metal - polymer
    interface, or perhaps more properly the interface between the oxide and the
    polymer.&nbsp; Koehler,<a href="#_edn45" name="_ednref45" title>[45]</a>
    showed that this form of failure, which he called �halo detachment�,
    only occurs when there are alkali metal cations available in the environment
    to act as counter ions to the cathodically generated OH<sup>-</sup>.</p>
    <SCRIPT>comment(51)</SCRIPT><p>One explanation is that dissolution of the air-formed oxide layer is
    responsible for the loss of adhesion.&nbsp; Various techniques have been
    adopted to confirm this view.&nbsp; Ritter<a href="#_edn46" name="_ednref46" title>[46]</a>
    carried out ellipsometric studies on a range of coatings in tandem with an
    investigation of uncoated steel exposed to saturated NaOH.&nbsp; The
    coatings studied were collodion, polystyrene and two proprietary acrylics,
    all of which were cured at room temperature.&nbsp; Additionally, a
    heat-cured acrylic was examined.&nbsp; The argument put forward was that
    delamination is a direct result of oxide dissolution and this is supported
    by the presence of iron ions in the interfacial region of the polymer in the
    heat cured specimens.&nbsp; The oxide layer present on the metal under these
    circumstances, is thicker than the normal air-formed layer, as a result of
    the curing regime.&nbsp;</p>
    <SCRIPT>comment(52)</SCRIPT><p>Further work carried out using this technique<a href="#_edn47" name="_ednref47" title>[47]</a>
    concluded that the high pH generated under the coating was conducive to
    oxide growth and surface roughening of the substrate.&nbsp; This conclusion
    was the result of tests carried out on uncoated substrates in a high pH
    environment.&nbsp; Taken in conjunction with the findings cited in the
    previous paragraph, the inference of this could be that the thicker oxide
    layer, developed due to the high pH generated under the film, could cause
    the oxide film to become mechanically weaker and result in a cohesive
    failure.&nbsp;</p>
    <SCRIPT>comment(53)</SCRIPT><p>Castle and Watts,<a href="#_edn48" name="_ednref48" title>[48]</a> in an
    XPS investigation of cathodic delamination, concluded that dissolution of
    the oxide layer was not a significant factor in the process.&nbsp; It was
    found that the oxide within the disbonded region was reduced only in
    localised patches and the disbonded area extended well beyond this.</p>
    <SCRIPT>comment(54)</SCRIPT><p>In a similar vein, Wiggle et al<a href="#_edn49" name="_ednref49" title>[49]</a>
    considered different pre-treatments and the effect upon them of cathodically
    produced hydroxide.&nbsp; Both zinc and iron phosphate pre-treatments along
    with bare metal was included in the study.&nbsp; Initial tests were carried
    out to determine the porosity of the conversion coatings, which yielded
    results of 0.2% for the zinc and 23% for the iron phosphate; these porosity
    values were linked to the poor resistance of the iron phosphate to anodic
    undermining.&nbsp; Experiments were carried out under both anodic and
    cathodic polarisation as well as at open circuit.&nbsp; Cathodic
    polarisation trials resulted in dissolution of the zinc phosphate and hence
    loss of adhesion of the coating.&nbsp; Knaster and Parks <a href="#_edn50" name="_ednref50" title>[50]</a>
    also identified dissolution of zinc phosphate under paint coatings on
    pre-treated steel but considered this to be a secondary reaction.&nbsp; The
    contention in this case is that delamination of coatings from phosphated
    panels, due to a defect, is caused by oxygen depolarisation along the
    phosphate-paint interface and hydrogen evolution at the delamination front
    that results in a force, which disrupts the adhesion of the polymer to the
    phosphate layer.&nbsp;</p>
    <SCRIPT>comment(55)</SCRIPT><p>Dissolution of phosphate conversion coatings on steel was also examined
    by Sommer and Leidheiser.<a href="#_edn51" name="_ednref51" title>[51]</a>&nbsp;
    The variables they considered were the effect of the pH and also the alkali
    cation involved.&nbsp; The pH effects were investigated by exposing test
    panels to 0.01, 0.1 and 1.0-M solutions of NaOH for ten minute intervals up
    to one hour.&nbsp; The importance of the cation involved was determined with
    0.1-M solutions of hydroxides of Na, Cs, K and Li.&nbsp; It was found that
    increasing pH values from 11.5 to 13.5 resulted in a higher rate of
    dissolution of the phosphate ions from the conversion coating.&nbsp;</p>
    <SCRIPT>comment(56)</SCRIPT><p>With regard to the cation type, the investigators were surprised to find
    that Na was the most effective of the cations in the dissolution of the
    coating (1.6, 2.6, &amp; 4.1 times K, Cs &amp; Li respectively).&nbsp;
    Additionally, the cation type not only affected the rate but also the mode
    of dissolution.&nbsp; With Na, Cs and K, the phosphate ions were dissolved
    more rapidly than the zinc ions, whereas in the case of Li, the zinc was the
    one to be dissolved at a higher rate.</p>
    <SCRIPT>comment(57)</SCRIPT><p>Dissolution of the conversion layer is also suggested by Dickie,<a href="#_edn52" name="_ednref52" title>[52]</a>
    after an investigation of the failure of an epoxy coating.&nbsp; This
    mechanism is proposed to account for the slower delamination and longer
    delay time observed with alkali resistant coatings.</p>
    <SCRIPT>comment(58)</SCRIPT><p>An alternative view to the dissolution of the substrate theory is that
    the alkaline solution under the film attacks the coating itself.&nbsp;
    Koehler�s investigation <a href="#_edn53" name="_ednref53" title>[53]</a>
    of oleoresinous and polybutadiene coatings on steel concluded that
    delamination was due to saponification of the polymers.&nbsp; In his
    discussion, he states that disbonding may occur at pH 11.7, which is well
    below that required to dissolve the oxide layer.&nbsp; That higher pH�s
    may form under the coating at cathodic sites is not disputed, however it is
    suggested that this may take place subsequent to the loss of coating
    adhesion.&nbsp; Furthermore, it is proposed that regardless of any other
    phenomena a film of water is required under the coating for disbonding to
    proceed.&nbsp;</p>
    <SCRIPT>comment(59)</SCRIPT><p>Attack on the polymer is also a mechanism supported by Hammond et al <a href="#_edn54" name="_ednref54" title>[54]</a>
    who examined three different epoxy systems on steel with a coating range of
    10 - 30 �m.&nbsp; In their examination of the interfacial composition of
    delaminated surfaces, using XPS, predominant traces of polymer were in
    evidence on the metal surface relative to iron oxide; this was in
    conjunction with little or no traces of iron in the interfacial
    polymer.&nbsp; Their conclusion from this was that the failure mechanism was
    due to resin degradation and that the locus of failure was within the
    polymer, resulting in a failure that was cohesive in nature.&nbsp;</p>
    <SCRIPT>comment(60)</SCRIPT><p>Another XPS investigation was undertaken by Castle and Watts <a href="#_edn55" name="_ednref55" title>[55]</a>,
    who looked at fusion-bonded epoxy coated mild steel.&nbsp; The results of
    this study suggested that the failure was essentially adhesive in
    nature.&nbsp; One of the variables in the experiments carried out was
    surface texture, ranging from grit blasted (R<sub>a</sub> 3.80 �m) to a
    mirror finish polish with 1�m diamond paste (R<sub>a</sub> 0.15 �m).&nbsp;
    It was found that as the R<sub>a</sub> values increased, there was a slight
    tendency toward cohesive failure of the oxide layer.&nbsp; However, although
    some iron was transferred to the polymer, the amount was a trivial component
    of the locus of failure and was consistent with cohesive failure of the
    oxide covering at protruding asperities.&nbsp;</p>
    <h3><a name="_Toc478818778">Transport Pathways for Aggressive Species</a></h3>
    <SCRIPT>comment(61)</SCRIPT><p>The nature of the delamination mechanism is not the only area of debate
    within the overall subject of cathodic delamination.&nbsp; Whilst there is
    agreement as to the species involved in the process, it is still open to
    conjecture how those elements arrive at the delamination front.&nbsp;
    Considering a coated steel substrate, immersed in an electrolyte of neutral
    or near neutral pH, the half-cell reaction responsible for the delamination
    process is generally agreed to be oxygen reduction.&nbsp; This reaction
    generates OH<sup>-</sup> at the cathodic site and is responsible for the
    alkaline environment at the delamination front.&nbsp; The elements required
    for the process to proceed are water, oxygen and free electrons.&nbsp; The
    electrons may be generated by either an anodic reaction or through the
    application of cathodic protection.&nbsp; Additionally, in order to maintain
    electroneutrality, some form of counter ion is required; the high pH values
    recorded at the reaction zone indicate that these counter-ions are alkali
    cations rather than protons<a name="_Ref393638768">.</a><a href="#_edn56" name="_ednref56" title>[56]</a>&nbsp;</p>
    <SCRIPT>comment(62)</SCRIPT><p>If we assume that cathodic delamination is a result of a damaged coating,
    there are two possible routes that the reactants for the cathodic reaction
    may take.&nbsp; The two alternatives are either through the coating or along
    the polymer-metal interface.&nbsp; An extensive review of the delamination
    process was carried out by Leidheiser et al <a href="#_edn56" name="_ednref56" title>[56]</a>.&nbsp;
    The results of the experiments carried out indicated that water was
    transported to the reaction zone through the coating.&nbsp; It was suggested
    that a certain fraction of this could be in the form of a cation such as H<sub>3</sub>O<sup>+</sup>,
    as the cathodic nature of the front may favour the transmission of water
    associated with an ion possessing a positive charge.&nbsp; The supply of
    oxygen to the cathodic site was found to be largely through the coating,
    with a small contribution from interfacial transport, in the case of the
    epoxy coating studied.&nbsp; Other work on the subject of the supply of
    reactants to the delamination front with pigmented coatings<a name="_Ref476987668">,</a><a href="#_edn57" name="_ednref57" title>[57]</a>
    suggests that supply of oxygen along the interface may be the
    rate-controlling factor.&nbsp;</p>
    <SCRIPT>comment(63)</SCRIPT><p>The major unresolved factor in the overall picture is the route taken by
    the charge-neutralising cations.&nbsp; Leidheiser�s study identified a
    linear relationship between the diffusion coefficient in aqueous solutions
    of the different cations, used in his experiments, and the delamination
    rate.&nbsp; This suggested that the cations were supplied along the
    interface.&nbsp; However, as delamination rates showed a strong dependence
    on film thickness,<a href="#_edn58" name="_ednref58" title>[58]</a> through
    film migration of cations could not be discounted.&nbsp;</p>
    <SCRIPT>comment(64)</SCRIPT><p>An important feature identified in Leidheiser�s work was an incubation
    time needed before delamination commenced.&nbsp; This was dubbed the �delay
    time� and the factor controlling this parameter was considered to be
    cation ingress into the coating, due to an observed correlation between
    ionic mobility and the delay time.&nbsp;</p>
