    addressed various different factors affecting ionic conduction in polymer
    films. <a name="_Ref444419341">The first</a><a name="_Ref445626064">,</a><a href="#_edn20" name="_ednref20" title>[20]</a>
    examined the influence of the electrolyte on the resistance of films.&nbsp;
    It was found that the films investigated followed the general trend
    exhibited in Figure 4.&nbsp; However, when an I Type film was exposed to a
    solution of very low electrolyte concentration, and therefore a high water
    activity, the film displayed D Type characteristics over a small range of
    concentrations, at the lower end of the scale. Once the electrolyte
    concentration reached a critical level, the film began to exhibit more
    typical I Type characteristics.&nbsp; This led to the oft quoted conclusion that the
    difference between D and I Type conduction was �more one of degree rather
    than of kind�.</p>
    <SCRIPT>comment(28)</SCRIPT><p>Scantlebury examined the distribution of D and I Type areas across a
    given film.<a href="#_edn21" name="_ednref21" title>[21]</a>&nbsp; It was
    found that the films investigated were all very inhomogeneous and had a
    distribution of both D and I Type areas over the film.&nbsp; Two
    conclusions, relating to the mechanisms responsible for the different modes
    of conduction were drawn from this investigation.&nbsp; Firstly, that D Type
    conduction could not be attributed to the presence of pores unless they were
    of molecular dimensions.&nbsp;</p>
    <SCRIPT>comment(29)</SCRIPT><p>The second conclusion that was drawn from this study, was that the
    resistance of both D and I Type areas bore a relationship to their hardness
    values, as determined via microhardness tests.&nbsp; D Type areas were found
    overall to have a lower hardness value than that obtained for I Type.&nbsp;
    The hardness of the film was equated to its crosslinking density.&nbsp; This
    result was reinforced by swelling experiments, which showed that D Type
    areas swelled to a greater degree than I Type.&nbsp; Therefore, it was
    concluded that D Type conduction was a function of lower crosslink density
    in the film.&nbsp;</p>
    <SCRIPT>comment(30)</SCRIPT><p>This conclusion reinforced Kinsella�s view <a href="#_edn20" name="_ednref20" title>[20]</a>
    that the initial drying conditions had a marked effect on the final
    properties of the film.&nbsp; Other work on crosslink densities of polymer
    films has shown that the crosslink density appears only to affect the ionic
    conductivity of the film.<a href="#_edn22" name="_ednref22" title>[22]</a>&nbsp;
    The study was concerned with the effect of crosslink density upon water
    permeation through films and concluded that the permeation of water was not
    noticeably affected by alteration of the crosslink density.&nbsp; The final
    two papers in the series by Mayne and co-workers considered the influence of
    temperature<a href="#_edn23" name="_ednref23" title>[23]</a> and finally the
    effect on the barrier properties of a coating due to the inclusion of inert
    pigments.<a href="#_edn24" name="_ednref24" title>[24]</a></p>
    <SCRIPT>comment(31)</SCRIPT><p>Evidence that D Type areas are detrimental to the protective properties
    of organic coatings was produced by Mills<a name="_Ref477745245">,</a> <a href="#_edn25" name="_ednref25" title>[25]</a>
    who showed a direct relationship between the presence of D Type areas in the
    film and the occurrence of underfilm corrosion.&nbsp; With regard to the
    term �virtual pores� put forward by Cherry<sup>17</sup> to explain the
    mode of conduction in an apparently continuous film, Mills suggests the term
    �conductive polymer phase� as more appropriate<a name="_Ref477745338">.</a><a href="#_edn26" name="_ednref26" title>[26]</a>&nbsp;
    This term is intended to describe more accurately the apparent properties of
    D Type films.&nbsp;</p>
    <SCRIPT>comment(32)</SCRIPT><p>Mills further observed that the proportion of D Type areas was a function
    of the thickness of the film.<a href="#_edn27" name="_ednref27" title>[27]</a>&nbsp;
    It was found that the incidence of D Type areas decreased significantly once
    the film�s thickness was increased to around 75&#956;m, this finding was
    used to conclude that D Type areas were spherical and approximately&nbsp; 75&#956;m
    diameter.</p>
    <h3>Breakdown Mechanisms of Coatings</h3>
    <h4><a name="_Toc478818771">Blistering and Delamination</a></h4>
    <SCRIPT>comment(33)</SCRIPT><p>Amongst the most common forms of failure found in organic coatings are
    those of blistering and delamination.&nbsp; Much debate has ensued as a
    result of the research into these failure modes and a number of governing
    mechanisms have been put forward.&nbsp; The mechanisms involved in the two
    modes of failure are similar, but it is unclear whether they are the same
    phenomena.&nbsp;</p>
    <SCRIPT>comment(34)</SCRIPT><p>Within each of these failure modes, there are sub-classifications, which
    may or may not be important for a particular system.&nbsp; The components of
    a system are the substrate, the coating and the exposure environment.&nbsp;
    Factors affecting the performance of a system include surface preparation,
    coating application, cure regime and film integrity.</p>
    <h4><a name="_Toc478818772">Factors Affecting Blister Initiation</a></h4>
    <SCRIPT>comment(35)</SCRIPT><p>The blistering mechanism of paint coatings upon exposure to aqueous
    environments was addressed by Gay<a name="_Ref477745433">,</a><a href="#_edn28" name="_ednref28" title>[28]</a>
    who found that all the systems examined displayed four common features:</p>
    <ol>
      <li>The higher the osmotic pressure of the immersion liquid, the smaller
        the amount of blistering.</li>
      <li>The fluid in blisters formed on seawater immersion is almost
        invariably alkaline and its chloride content is lower than that of the
        seawater.</li>
      <li>The steel under such blisters is usually bright and free from
        corrosion.</li>
      <li>Areas of blistering are frequently associated with adjacent areas of
        corrosion.</li>
    </ol>
    <SCRIPT>comment(36)</SCRIPT><p>In the majority of cases, before any blistering occurred, some corrosion
    was observed at weak points in the coating and no substantial amount of
    blistering was found where corrosion was not present.&nbsp; The suggested
    sequence of events leading to the formation of blisters is as follows.&nbsp;</p>
    <ul>
      <li>The paint film imbibes water from the solution, possibly containing
        some dissolved salts.</li>
      <li>Eventually, sufficient liquid containing chloride ions passes through
        to the underlying metal and activates primary corrosion sites at the
        interface.</li>
      <li>As the corrosion proceeds at the anodic areas under the film, hydroxyl
        ions build up at the cathodic sites.</li>
      <li>The alkaline environment at the cathodic sites weakens or destroys the
        adhesion of the film whilst producing osmotically active substances at
        the paint metal interface.</li>
      <li>The presence of these substances at the interface is favourable to
        either osmotic or endosmotic passage of water through the film from the
        immersion liquid.</li>
    </ul>
    <SCRIPT>comment(37)</SCRIPT><p>The principle of endosmotic transfer of water through the coating was
    supported by Mayne <a href="#_edn29" name="_ednref29" title>[29]</a> who
    concluded that the contribution of osmotic transfer of water was much
    smaller; 6% in the case of the coatings studied.</p>
