    clear varnish coated substrates; this was unexpected as the former had
    higher ionic permeabilities.&nbsp; This situation was considered by
    pre-soaking the pigmented coatings in water prior to testing; this resulted
    in a reduction in the delay times from 5-7 hours to 1-2 hours.&nbsp; This
    result pointed to ingress of either water or oxygen as the factor
    responsible for the duration of the delay time.&nbsp;</p>
    <SCRIPT>comment(73)</SCRIPT><p>With regard to the species responsible for the delamination process and
    the route taken, different mechanisms were seen to be responsible for the
    two systems.&nbsp; For the unpigmented coating, interfacial cation migration
    was considered to be the determining factor as the delamination rate
    constant increased in a linear fashion with cation mobility.&nbsp; With the
    pigmented coating, which displayed a greater through-film ionic transport
    rate, coupled with lower oxygen permeability, interfacial migration of
    oxygen was suggested as a likely rate determining mechanism; this was
    suggested to be virtually independent of cation mobility.&nbsp; A further
    result, which reinforced the hypothesis of interfacial migration of a
    species as the rate-determining factor, was that the rate of disbonding
    eventually became zero.&nbsp; The explanation for this was that the
    interfacial pathways became blocked with corrosion products.&nbsp;</p>
    <SCRIPT>comment(74)</SCRIPT><p>Armstrong and Johnson,<a href="#_edn61" name="_ednref61" title>[61]</a>
    working with unpigmented chlorinated rubber coatings subjected to cathodic
    protection, concluded that the corrosion current passed through faults in
    the form of cracks or crevices in the film. It is proposed that degradation
    of the film due to OH<sup>-</sup> was essentially mechanical in
    nature.&nbsp; This argument is supported by a number of factors.&nbsp; Free
    standing films exhibited conductive behaviour only after long exposure to
    concentrated OH<sup>-</sup>; this was lost after the films were dried in a
    dessicator.&nbsp; It was felt this result supported the hypothesis of
    conduction through cracks or crevices rather than through the bulk
    film.&nbsp; FTIR (Fourier Transform Infra Red) spectra of membranes exposed
    to OH<sup>-</sup> displayed no detectable chemical change from the untreated
    film.&nbsp;</p>
    <SCRIPT>comment(75)</SCRIPT><p>The underside of the films (that in contact with the metal), which was
    subjected to the alkaline conditions, was found to contain holes that were
    not visible on the upper face of the coating.&nbsp; This is thought to be
    due to pockets of high solvent or plasticiser concentration; it is suggested
    that the OH<sup>-</sup> opens up such defects by a solvent cracking process,
    which reinforces the assertion that the breakdown is mechanical rather than
    chemical.&nbsp;</p>
    <h3><a name="_Toc478818779">The Substrate</a></h3>
    <SCRIPT>comment(76)</SCRIPT><p>In an ideal world, the coatings that we applied to protect against
    corrosion would be a perfectly homogeneous mix and be free of defects of any
    kind.&nbsp; Also, the surface to which the coatings were applied would be in
    an ideal condition, free of any form of contamination and with an
    appropriate finish.&nbsp;</p>
    <SCRIPT>comment(77)</SCRIPT><p>This utopian situation does not occur in the real world and the
    substrates are commonly far from pristine.&nbsp; There are occasions where
    adequate preparation of the surface to be coated is either technically
    unfeasible or prohibitively expensive.<a href="#_edn62" name="_ednref62" title>[62]</a>
    In such circumstances, there is a need for a coating that will perform well
    on a contaminated substrate.&nbsp; Mayne�s<sup>12</sup> model of underfilm
    corrosion, considered earlier, was applicable to a clean surface.&nbsp; In
    the presence of contaminants on the surface, resistance inhibition does not
    come into play, as all the required elements for corrosion to proceed are
    present at the interface.&nbsp;</p>
    <h3><a name="_Toc478818780">Effects of Contamination</a></h3>
    <SCRIPT>comment(78)</SCRIPT><p>Surface contamination is conventionally considered to be due to such
    factors as salt deposits or corrosion.&nbsp; Indeed, this is the source of a
    great deal of concern and has been the basis of much work<a href="#_edn63" name="_ednref63" title>[63]</a>,
    <a href="#_edn64" name="_ednref64" title>[64]</a> <a href="#_edn65" name="_ednref65" title>[65]</a>
    <a href="#_edn66" name="_ednref66" title>[66]</a> <a href="#_edn67" name="_ednref67" title>[67]</a>.&nbsp;
    Additionally, it has been demonstrated that, even on substrates cleaned to
    laboratory standards, minor inclusions such as sulphides can have a
    detrimental effect on the performance of an organic coating.<a href="#_edn68" name="_ednref68" title>[68]</a></p>
    <SCRIPT>comment(79)</SCRIPT><p>An established method for dealing with the problem of painting over steel
    that is rusted, contaminated with salts or, as is usually the case, a
    combination of the two, has been to coat with an oil-based coating pigmented
    with red lead (Pb<sub>3</sub>O<sub>4</sub>).&nbsp; Environmental concerns
    have limited the use of lead compounds<a href="#_edn69" name="_ednref69" title>[69]</a>
    and forced formulators to look to alternative means of addressing the
    problem.<a href="#_edn70" name="_ednref70" title>[70]</a>&nbsp; In the
    pursuit of a viable alternative, the red lead formulation is still used as a
    benchmark by which to compare the various contenders <a href="#_edn71" name="_ednref71" title>[71]</a>,
    <a href="#_edn72" name="_ednref72" title>[72]</a>, <a href="#_edn73" name="_ednref73" title>[73]</a>.
    The exact mechanisms by which red lead protect a compromised substrate are
    unclear, but its efficiency is undisputed.</p>
    <SCRIPT>comment(80)</SCRIPT><p>Rust, in itself, is not seen as a <a name="_Ref398022695">problem;</a><a href="#_edn74" name="_ednref74" title>[74]</a>
    rather it is the contaminants that the rust contains.&nbsp; Mayne <a href="#_edn75" name="_ednref75" title>[75]</a>
    identified that sulphates were embedded into adherent rust close to the
    metal surface; this rust layer was not easily removed by wire brushing.
    These sulphate �nests� are felt, to a large degree, to be responsible
    for the formation of blisters at anodic sites <a href="#_edn71" name="_ednref71" title>[71]</a>.&nbsp;
    Such blisters lead to failure, due to local bulging and eventual cracking of
    the coating.&nbsp;</p>
    <SCRIPT>comment(81)</SCRIPT><p>Mayne�s experiments<a href="#_edn76" name="_ednref76" title>[76]</a>
    were carried out in Cambridge, over the period 1945 to 1953.&nbsp; The
    contamination of his specimens was determined to be due to the burning of
    fossil fuels, this was justified by a seasonal variation in the degree of
    contamination giving higher levels in the winter months.&nbsp; One could
    possibly argue that with the advent of smokeless zones in Britain that, in
    non-industrial urban areas, this problem should be greatly alleviated.&nbsp;
    Having said this, industrial areas still provide an adequate level of
    contamination to require attention.&nbsp; The problem of sulphates in
    industrial areas becomes one of chlorides in marine environments.&nbsp;</p>
    <h3><a name="_Toc478818781">Surface Tolerant Coatings</a></h3>
    <SCRIPT>comment(82)</SCRIPT><p>In a search for environmentally acceptable coatings that may be applied
    over compromised metal surfaces, attention has been focused on the field of
    �surface tolerant coatings�.&nbsp; Frondistou-Yiannas <a href="#_edn73" name="_ednref73" title>[73]</a>
    surveyed the range of these coatings and their reported modes of
    action.&nbsp; The mechanisms considered were: conversion of rust to
    magnetite, conversion of iron oxides to other compounds and inertisation of
    soluble salts.&nbsp; In addition to these, Thomas <a href="#_edn74" name="_ednref74" title>[74]</a><sup>
    </sup>also considered barrier coatings, the ability of the coating to wet
    and penetrate the rust and anti-corrosive pigments.&nbsp; The field trials
    of the former investigator, which were carried out in both marine and
    industrial areas, concluded that none of the systems tested was expected to
    provide long-term protection in the aggressive environments used in the
    tests.&nbsp;</p>
    <SCRIPT>comment(83)</SCRIPT><p>So called rust converters are mainly formulations based upon tannic
    acid.&nbsp; An extensive review of the mechanisms involved in the action of
    tannin based coatings,<a href="#_edn77" name="_ednref77" title>[77]</a>
    details the reaction mechanisms by which it is claimed that tannins help to
    protect steel.&nbsp; The action of tannin-based treatments is explained by
    the inhibition of the formation of magnetite, which if allowed to develop,
    would increase the area available for oxygen reduction.&nbsp; An alternative
    name of �rust deactivators� is proposed for these formulations, which
    may lead one to think that they have some effect upon the contaminants that
    are invariably present.&nbsp; However, this statement is followed by the
    fact that tannins are unable to neutralise surface contaminants such as
    sulphates.</p>
    <SCRIPT>comment(84)</SCRIPT><p>In her review of rust conversion preparations, Thomas <a href="#_edn74" name="_ednref74" title>[74]</a>
    states that the premise behind their use consists of the stabilisation of
    the rust, so that the redox reaction between Fe(II) and Fe(III) is no longer
    possible.&nbsp; In this case, in principle, corrosion would be
    prevented.&nbsp; It is pointed out, however, that tannic acid reacts with
    Fe(III) within regions underlying thick layers of surface rust; therefore,
    it would be necessary to wire brush the surface prior to treatment.</p>
    <SCRIPT>comment(85)</SCRIPT><p>A further assessment of the action of tannic acid has been conducted by
    Morcillo et al.<a href="#_edn78" name="_ednref78" title>[78]</a>&nbsp;
    Panels that had been exposed outdoors for up to two years were thoroughly
    wire brushed and immersed in tannic acid of various concentrations.&nbsp;
    Fresh panels were also immersed, to assess the effect on clean steel.&nbsp;
    A film of ferric tannate was found to form on the surface of the rusty
    panels, this film was found to be extremely porous.&nbsp; The results from
    these tests showed that tannic acid promotes rusting on clean steel and does
    not significantly reduce that of rusted steel.&nbsp; Furthermore, it was
    suggested that due to the nature of the ferric tannate film formed on the
    surface, that the tannic acid might attack the iron at the base of the
    pores.</p>
    <SCRIPT>comment(86)</SCRIPT><p>Perhaps the most poignant point comes from Frondistou-Yiannas� paper
    where, all the rust converter systems under examination did not make it to
    the field trials, as they failed the initial laboratory screening due to
    blistering and intercoat disbonding.</p>
    <SCRIPT>comment(87)</SCRIPT><p>An alternative method of dealing with contaminated surfaces proposed by
    Sykes<a href="#_edn79" name="_ednref79" title>[79]</a> is the use of
    water-borne paint.&nbsp; The contention here is that if the requirements of
    the coating fall into any of the following criteria:</p>
    <ul>
      <li>Removal of soluble salts from the surface.</li>
      <li>Conversion of salts into safer species.</li>
      <li>Electrochemical alteration of rust involving the underlying steel.</li>
    </ul>
    <SCRIPT>comment(88)</SCRIPT><p>Then an aqueous medium, in the form of a water-borne coating probably
    offers the best chance of success; providing a method to either dissolve or
    bind the soluble ions while the paint is drying.</p>
    <SCRIPT>comment(89)</SCRIPT><p>Experimental work was carried out on a 120�m thick water-borne vinyl
    acrylic latex system applied over pre-rusted steel.&nbsp; The substrates
    were coated in the as-received condition, wire brushed or grit blasted prior
    to application of the coating.&nbsp; The formulation from which the coating
    was taken normally has its pH adjusted to 4.5 - 5.0.&nbsp; During the
    investigation, the pH was taken down to 2.7.&nbsp; This lower pH was found
    to aid in the removal of light deposits of rust resulting in a decrease in
    the degree of blistering of the coating.</p>
