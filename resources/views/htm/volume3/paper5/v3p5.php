    <h4><a name="_Toc478818769">Modes of Ionic Conduction Through Polymer Films</a></h4>
    <SCRIPT>comment(24)</SCRIPT><p>The question of the possibility of conduction through pores was addressed
    by Kinsella<a name="_Ref477745065">.</a><a href="#_edn19" name="_ednref19" title>[19]</a>&nbsp;
    This work identified different modes of ionic conduction through polymer
    films.&nbsp; Two distinct forms of conduction were identified.&nbsp; The
    first was I, or inverse type conduction, where the resistance of the film
    coincided with that of the solution; this type of conduction was in line
    with that identified by Bacon, Smith and Rugg <a href="#_edn9" name="_ednref9" title>[9]</a>.&nbsp;</p>
    <SCRIPT>comment(25)</SCRIPT><p>The second mode of through-film transport that was identified was named
    D, or direct type conduction where the film�s resistance ran counter to
    that of the solution�s. Initially, the films that exhibited D type
    conduction were thought to be due to coating deficiencies such as dust wicks
    passing across the film.&nbsp; However, investigation of the temperature
    coefficients of resistance for these films showed them to be higher than
    that of aqueous conduction.&nbsp; In addition, the films were shown to be
    preferentially cation permeable and thus considered to be continuous.</p>
    <p align="center"><img src="v3p5_image010.gif" align="left" width="357" height="333"></p>
