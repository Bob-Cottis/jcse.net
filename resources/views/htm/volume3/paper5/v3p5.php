    <SCRIPT>comment(6)</SCRIPT><p>The nature of this barrier has been the subject of much work over many
    years by workers in the field of protective organic coatings.&nbsp; The
    original assumption was that organic coatings act as a barrier to water and
    oxygen from the environment<a name="_Ref477516736"></a><a href="#_edn7" name="_ednref7" title>[7]</a>.&nbsp;
    Scientific investigations
    of the subject over the last half century have determined that the limiting factor in the protective
    mechanisms of barrier coatings is frequently their resistance to the flow of ionic
    current.<a href="#_edn8" name="_ednref8" title>[8]</a></p>
    <h3><a name="_Toc478818763">Electrolytic Resistance of Organic Coatings</a></h3>
    <p><img src="v3p5_image002.gif" align="left" width="304" height="301"><a name="_Toc468603853"></p>
    <SCRIPT>comment(7)</SCRIPT><p><b>Figure 1</b></a><b> : Schematic of resistance behaviour of coatings in
    immersed metals after ref <a href="#_edn9" name="_ednref9" title>[9]</a></b></p>
    <SCRIPT>comment(8)</SCRIPT><p>The importance of the electrolytic resistance of organic coatings has
    long been appreciated.&nbsp; Bacon, Smith and Rugg<a name="_Ref443900310">,</a><a href="#_edn9" name="_ednref9" title>[9]</a>
    after measuring the resistances of over 300 coating systems, determined a
    direct correlation between these resistances and the ability of the coating
    to protect the underlying steel from corrosion. Three general
    classifications, based on a sustained resistance value, were established
    during this investigation: good, fair and poor - see Figure 1.&nbsp; The
    classification that a coating was allocated was dependant upon its long term
    resistance value upon immersion in solution �Most coatings, if continuous,
    have resistances in the neighbourhood of Log R = 9 during the first 5 to 10
    minutes of immersion, followed by a decrease in resistance which may vary
    considerably in steepness and duration�, the units of R were Ohms for one
    sq. cm.&nbsp; Their subsequent behaviour determined which category they
    should be placed into.&nbsp; All coatings were found to exhibit an initial
    decrease in resistance, which varied in terms of rate and duration.&nbsp;
    For a good coating, this initial decrease was followed by an abrupt recovery
    to around the original value.&nbsp; The subsequent resistance of the coating
    remained either essentially constant or fluctuated within the high
    resistance region.&nbsp; A coating designated as fair either levelled off or
    displayed a slight increase in resistance.&nbsp; However, a subsequent sharp
    drop in resistance was a pre-cursor to coating failure, which occurred
    within six months of the exposure period.&nbsp; The resistance of a poor
    coating continued to decrease resulting in failure within sixty days.&nbsp;
    A coating that maintained a resistance of 10<sup>8 </sup>Ohm cm<sup>2</sup>
    provided good corrosion protection while one whose resistance fell below 10<sup>6
    </sup>Ohm cm<sup>2</sup> did not.</p>
    <SCRIPT>comment(9)</SCRIPT><p>Although this work identified the relationship between the ionic
    resistance of the coatings and their ability to protect, the permeability of
    other corrosive agents other than ionic species was not considered.&nbsp;
    Therefore, the results were presented as providing a measure of the likely
    protection that could be expected from a particular system rather than
    proposing a mechanism for the protective action of organic coatings.&nbsp;</p>
    <h3>The Effect of the Environment on the Performance of Coatings</h3>
    <SCRIPT>comment(10)</SCRIPT><p>A further important result from the work of Bacon, Smith and Rugg
    identified the effect of the environment on the protective merit of coated
    submerged steel.&nbsp; One of the variables taken into consideration was the
    level of dissolved salts in the test solution.&nbsp; It was found that the
    resistance of the coating fell with increasing salt concentration and from
    this result it was concluded that the dissolution of environmental water
    into the coating was more important than the uptake of salt from the
    solution by the coating. That the level of dissolved salts of the exposure
    environment had an effect on the properties of coatings had been reported
    previously by Kittelberger and Elm <a href="#_edn10" name="_ednref10" title>[10]</a>.&nbsp;
    This work showed a relationship between the level of dissolved salts in the
    solution, expressed as osmotic pressure, and the water uptake of the films
    under investigation, expressed as percentage weight gain.&nbsp; It was shown
    that the nature of the solute did not affect the results; rather it was the
    osmotic pressure that determined the level of imbibition is this a word? of
    water into the coating.&nbsp; When the coatings were exposed to a salt
    solution, after an initial high rate of water absorption, the curve levelled
    off and eventually an equilibrium was attained.&nbsp; Where the films were
    exposed to distilled water, however, no equilibrium was reached and the film
    continued to gain in weight.&nbsp; It was concluded that the degree of water
    absorption was a function of the activity of the water in solution, due to
    the differences in osmotic pressure between the exposure solution and that
    within the film, see Figure 2.</p>
