<h3>&sect;1 Abstract</h3>

<p>Zinc metal containing coatings, especially those based on
silicate binders are very popular anti-corrosive primers. This review explores
the history of their discovery, compares them with more conventional organic
systems, and discusses the proposed mechanisms of cure. The protection
mechanisms are summarised and specific features such as minimum zinc content
are emphasised. Recent electrochemical studies are examined and the various
models are discussed. </p>

<p><b>Keywords: </b>metallic zinc powder, silciate binder, epoxy binder, review,
cure mechanisms, protection mechanisms </p>

<h3>Introduction</h3>

<h4>Early developments</h4>

<p>&sect;2 The extraction of zinc was first carried out commercially in
Bristol in 1743. As part of the process, powdered zinc oxide was obtained by
roasting sulphide ores which was then reduced by distillation with carbon. Thus
early on in the history of zinc, both zinc oxide and zinc metal were available
in a finely divided form and the use of these materials in paints is a natural
extension. Before the rise in rutile titania as an opacifying pigment, zinc
oxide was very popular. It is still used in can lacquers for the containing of
proteinaceous products. Sulphur containing proteins cause black staining with
the iron of the can. Fortunately zinc zulphide is colourless. Zinc phosphate
dihydrate is currently popular as an anti corrosion primer pigment. Zinc metal
powder has always been readily available as zinc is produced by condensation
from the vapour and it has a long history as a pigment is paints. According to
Nelson writing in volume 2 of the classic five volume series edited by
Mattiello <a href="#_edn1" name="_ednref1" title="">[1]</a>
zinc dust pigments have been used since 1840 with very early French and German
work quoted in his article. According to Munger, the use of metallic zinc in
paint coatings for the protection of steel from corrosion dates back to the
1930�s <a href="#_edn2" name="_ednref2" title="">[2]</a>.
These coatings, with a high quantity of zinc included, are used as a primer or
final coat and can be spray applied, dipped or brushed.</p>

<p>&sect;3 The scientific developments of these coatings, predominantly
organic, which contain zinc in high quantities started in Cambridge in the
1940's with Evans &amp; Mayne<a name="_Ref521129880"></a> <a href="#_edn3" name="_ednref3" title="">[3]</a>. This early work incorporated 95% by
weight of zinc into a variety of binders. These included chlorinated rubber
lacquer, polystyrene lacquer, linseed oil, medium stand-oil, and an aqueous
emulsion of linseed oil. Panels were exposed into seawater with a scratch line
in the coating to expose steel. Evans &amp; Mayne explain the excellent
protection being due to the cathodically protective nature of the zinc. This in
part is due to the necessary particle to particle to substrate contact of the
zinc. Evans &amp; Mayne based the incorporation of zinc pigment on the original
work of sacrificial protection of iron or steel in seawater by Davy in 1834 <a href="#_edn4" name="_ednref4" title="">[4]</a>.
However, they also stated that paints pigmented with zinc dust had been
employed against atmospheric attack since the 1840�s <a href="#_edn5" name="_ednref5" title="">[5]</a>.
It was also found earlier that this type of paint gave temporary protection to
uncoated sections of a steel substrate in outdoor exposure <a href="#_edn6" name="_ednref6" title="">[6]</a>.</p>

<p>&sect;4 The first documented use of silicate binders is regularly
referred to be the application of a water-borne sodium silicate zinc dust
coating to the Morgan Wyalla above ground pipeline in Australia <a href="#_edn7" name="_ednref7" title="">[7]</a>.
The coating was cured with the heat and was reported to be in excellent
condition after more than 50 years<a name="_Ref521129197"></a> <a href="#_edn8" name="_ednref8" title="">[8]</a>.</p>

<h4>Zinc silicate coatings </h4>

<p>&sect;5 It is said by Munger<a name="_Ref521128922"></a> <a href="#_edn9" name="_ednref9" title="">[9]</a> that the entire development of zinc
silicate coatings began with the Australian, Victor Nightingall�s dream of
forming a zinc coating that would be the equivalent of galvanising but be
applied by painting techniques. His starting point was to produce a material
similar to the mineral willemite (zinc silicate, Zn<sub>2</sub>SiO<sub>4</sub>)
as a thin film on a metal surface. It was said that the produced material was a
durable and corrosion resistant coating<a name="_Hlt521139698"></a> <a href="#_edn9">[9]</a>.</p>

<h3>Characteristics of zinc-rich coatings</h3>

<p>&sect;6 Coatings that are pigmented with zinc particles can have an
organic or inorganic binder. Organic binders such as epoxies, phenoxies,
chlorinated rubbers, silicones and vinyls have been used<a name="_Hlt521224868"></a>
<a href="#_edn13" name="_ednref13" title="">[13]</a>. Inorganic binders are either sodium
silicates or ethyl silicates. The inorganic binders can be self-curing and
water or solvent reducible.</p>

<p>&sect;7 Inorganic zinc silicate coatings are often used as primers
and are top coated with paints that do not contain zinc.</p>

<h3>Organic and inorganic zinc coatings</h3>

<p>&sect;8 The differences between organic and inorganic zinc coatings
have been summarised by Hafford et al <a
href="#_edn10" name="_ednref10" title="">[10]</a>.
It is said that organic zinc coatings are more compatible with topcoats, more
flexible, less dependant on atmospheric moisture to cure but are difficult to
apply under high or low ambient temperatures. With inorganic zinc coatings it
is said that the surface preparation is more rigorous, there is better
corrosion protection, better toughness and better abrasion resistance than is
seen with organic zinc coatings. Inorganic zinc coatings also dry faster under
optimum conditions and once the coating is formed there is better temperature
resistance.</p>

