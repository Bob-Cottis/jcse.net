mentioned by Ross &amp; Lingard<a name="_Ref521135391"></a> <a href="#_edn27" name="_ednref27" title="">[27]</a> who state that the pore blocking
mechanism is of greater importance than the galvanic stage. It is also stated
that the cathodic protection stage results in a high local pH, which protects
the steel and encourages the formation of insoluble, protective zinc compounds.</p>

<p>&sect;43 O�Connor &amp; Bowen <a href="#_edn11" name="_ednref11" title="">[11]</a> state that following the galvanic stage
the build up of corrosion product of zinc or �white rust� such as zinc
carbonate and zinc oxide fills the pores within the coating. This stops air and
moisture reaching the substrate below.</p>

<p>&sect;44 Scantlebury et al<a name="_Ref521135557"></a> <a href="#_edn28" name="_ednref28" title="">[28]</a> agree that there is a galvanic stage
of protection but state that the second stage of protection is �contentious�.
Two suggested mechanisms are the corrosion products act as chemical inhibitors
or by blocking the pores. By a combination of monitoring current with time and
impedance it was concluded that the second stage of protection is a combination
between the two, i.e. zinc corrosion products acting as chemical inhibitors and
blocking pores. This is a point upon which Armas<a name="_Ref521135687"></a> <a href="#_edn29" name="_ednref29" title="">[29]</a> agrees where it is stated that the
main part of the mechanism is the cathodic protection of the steel by the zinc.
As the exposure time increases the deposition of inhibiting zinc compounds at
the base of the coating pores is followed by the sealing of the porous
structure by zinc corrosion products.</p>

<p>&sect;45 Feliu<a name="_Ref521129795"></a> <a href="#_edn30" name="_ednref30" title="">[30]</a>,<a name="_Ref521135600"></a>
<a href="#_edn31" name="_ednref31" title="">[31]</a> on work that utilises impedance for
both the galvanic and barrier stage states that the galvanic stage becomes
inactive after a short time. The second stage, it is said, is not sufficiently
classified and is dependant on zinc content. The ethyl silicate coatings of the
type studied by Feliu are said to favour cathodic protection.</p>

<p>&sect;46 Other authors<a name="_Ref521135420"></a> <a href="#_edn32" name="_ednref32" title="">[32]</a>, <a name="_Ref521135629"></a><a href="#_edn33" name="_ednref33" title="">[33]</a>, <a name="_Ref521129816"></a><a href="#_edn34" name="_ednref34" title="">[34]</a>
agree that there is a galvanic stage
to the protection. The second stage follows what has already been stated in
this section where there is either a pore sealing or inhibiting barrier formed.</p>

