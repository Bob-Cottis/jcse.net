<h4>Zinc particle size</h4>

<p>&sect;111 The particle size of the zinc within the coatings has an
effect on the amount of zinc that is available to provide cathodic protection <a href="#_edn45" name="_ednref45" title="">[45]</a>.
The zinc content available for cathodic protection is said to be highest when
the mean particle size is 2-3mm.</p>

<p>&sect;112 Montes <a href="#_edn46"
name="_ednref46" title="">[46]</a>
states that the influence of particle size distribution on zinc dust in water
based inorganic zinc-rich coatings is dependent on the desired viscosity of the
coating. Some particle sizes are said to be too fine, others too coarse and it
would appear that a mixture is required.</p>

