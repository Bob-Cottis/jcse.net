<h3>The protection mechanism of zinc rich coatings</h3>

<h4>Introduction</h4>

<p>&sect;37 It is stated a number of times within this document that the
majority of the available literature concerns zinc rich coatings. Very little
is available on low-level zinc containing ethyl silicate coatings aside from
authors <a href="#_edn30" name="_ednref30" title="">[30]</a>, <a href="#_edn34" name="_ednref34" title="">[34]</a> concluding that this type of coating does not
afford any protection to steel. The mechanisms and investigations into them
that appear in this section are almost all concerning zinc rich coatings. The
use of this information forms the basis of understanding to the performance
data that will be obtained for the subject coating.</p>

<h4>Comments on the protection mechanism</h4>

<p>&sect;38 Although there is a general agreement of there being two stages
in the protection mechanism, there appears to be most discussion on the type of
secondary effect. The type of literature that appears comes in the form of
either reviews or experimental papers. Occasionally review papers do include
original experimental work but with others the experience of the author with
the coating type may be simply of literature rather than practical. This
section includes comments by a number of authors, some of whom are very well
represented. Specific aspects of their work are discussed at a later stage;
initially it is the comments on the mechanism that are discussed here.</p>

<p>&sect;39 The above paragraph mentions a general agreement of the
mechanism with discussion concerning the second stage of protection. This is
opposed by Romagnoli &amp; Vetere <a href="#_edn23" name="_ednref23" title="">[23]</a> who state that there is not a universally accepted
theory for the protection mechanism. However, some agreement is with Evans
&amp; Mayne <a href="#_edn3" name="_ednref3" title="">[3]</a> who suggest a cathodic protection stage,
where the zinc acts as a sacrificial anode, followed by a subsequent stage
where the substrate is protected longer than is expected by galvanic action
alone. This is by either blocking of the pores within the coating by zinc
corrosion products; or the inhibitive action of zinc basic compounds on the
steel substrate. Experimental work carried out by Romagnoli &amp; Vetere <a href="#_edn23" name="_ednref23" title="">[23]</a> suggests zinc contents above 85% by weight
are needed for the particle-to-particle-to-substrate electrical contact. A
mention is also made of a 50% by weight zinc containing coating whereby the
authors dismiss this as being too low zinc content. As zinc content decreases
the protective action diminishes. Impedance work carried out suggests a pore
filling action by the zinc basic salts with properties dependant on particle
size and content of zinc.</p>

<p>&sect;40 It is stated by Hare <a href="#_edn12" name="_ednref12" title="">[12]</a> that the zinc rich type of coating is
sacrificial and that the galvanic activity is a consequence of a deliberately
engineered electrochemical cell (figure 11). The principles of this cell are
the same as those that form the basis of hot dip galvanising and anodes such as
zinc and magnesium. When there is no electrolyte (dry conditions or incidental,
light atmospheric wetting) Hare states that zinc corrosion is low and the steel
substrate is protected by the marginal barrier effects of the primer. The cell
is only active when electrolyte is present and when this is the case any
exposed steel is protected by a local cell action and is covered by zinc
corrosion product. Over time, according to Hare, the anode is polarised by zinc
corrosion products such as zinc carbonates, zinc sulphate and zinc hydroxide.
These products are said to fill the pores in the film structure forming a hard
inorganic film. The mechanism is then transformed from cathodic protection to a
barrier effect. Cathodic protection is said to be minimal after approximately 3
months and this lengthens to 3 years with atmospheric exposure. Hare concludes
that there remain questions concerning the barrier stage of protection.</p>

