<h3>Cure of coating</h3>

<p>&sect;13 Coatings of this type that are two-pack solvent borne
usually require no further treatment to attain cure. The cure reaction proceeds
after the mixing of the two packs. This mixing initiates the chemical reaction
that enables the coating to dry and cure forming a film of desired properties.
There is a short, finite pot life of the coatings once mixing has occurred.
Two-pack coatings can be formulated with current and proposed environmental,
health and safety standards including volatile organic compound (VOC)
regulations <a href="#_edn15" name="_ednref15"
