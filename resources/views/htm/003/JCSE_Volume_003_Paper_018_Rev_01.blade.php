<h3>The role of zinc content in the formation of corrosion products</h3>

<p>&sect;77 As stated earlier, the zinc content has a direct affect on
the performance of the zinc silicate coating systems. It has also been
established that the initial protection mechanism of this type of coating is
through cathodic protection and that the level of zinc content is of great
importance. This level of zinc content influences the overall
particle-to-particle-to-substrate contact. Work done by Morcillo et al <a href="#_edn34" name="_ednref34" title="">[34]</a> offers a schematic representation of the
type of conduction pathways within the coating (figure 16). This is dependant
