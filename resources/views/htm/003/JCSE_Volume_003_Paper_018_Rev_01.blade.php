<h3>Extended protection of zinc containing coatings</h3>

<p>&sect;89 It has been said by a number of authors <script>crossref(27)</script>,
<script>crossref(29)</script>, <script>crossref(31)</script>, <script>crossref(32)</script> that the protection afforded by zinc pigmented
coatings is longer than the period of cathodic protection suggests. There are a
number of suggested reasons as to this extended protection.</p>

<p>&sect;90 It has been suggested that there are two stages of
protection: cathodic protection followed by barrier formation. The
investigation into barrier properties is usually carried out using
electrochemical impedance spectroscopy (EIS). EIS is generally used with
organic coatings but its use with inorganic zinc coatings will be shown.</p>

<p>&sect;91 The use of EIS with coatings, especially organic coatings,
is well established <a href="#_edn40"
name="_ednref40" title="">[40]</a>, <a href="#_edn41" name="_ednref41" title="">[41]</a>
and is frequently presented <a href="#_edn42"
name="_ednref42" title="">[42]</a>.
The use of EIS with zinc containing coatings is said to produce results unlike
any other type of coating <script>crossref(30)</script>.</p>

<p>&sect;92 Lindquist <script>crossref(26)</script> in a study of commercial zinc containing
coatings found that, in general, following 60 days immersion the coatings
exhibited little galvanic action. This was founded by a high
impedance/resistance inferring low galvanic activity. The extended protection
by the coatings was attributed to the inhibitive properties of the zinc salts
produced during the cathodic protection stage. The extended performance was
also said to be influenced by a high pH (9) in the film. </p>

<p>&sect;93 Feliu <script>crossref(30)</script>, <script>crossref(31)</script> states that the corrosion protection mechanism
in zinc-rich coatings is in the first instance cathodic protection, followed by
a barrier effect of the coating pores being sealed by corrosion products. </p>

