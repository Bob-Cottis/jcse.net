<p>&sect;18 Before the reaction takes place the material is
prehydrolised to a known degree by adding a small, exact amount of water. The
solution is also slightly acidic, as stated earlier. The alkoxy group goes to
silanol groups with alcohol in the hydrolysis reaction. Along with this alcohol
there is the alcohol in the bulk mixing solution, which begins to evaporate on
application. As the zinc is added the acid is neutralised making the silanol
groups more reactive, they then either react with each other to form the silica
matrix and water; or react with ethoxy groups to produce ethanol. The water
produced in the condensation reaction goes on to promote further hydrolysis.</p>

