effect.</p>

<h3>The effect of zinc content</h3>

<p>&sect;106 The zinc content and its particle size is important in
determining the extent of cathodic protection in a zinc-rich silicate coating.
This is due to the relationship of specific surface area and the desire for
electrical continuity<a name="_Ref521138967"></a> <a
href="#_edn43" name="_ednref43" title="">[43]</a>. The effect that the zinc content
has on the cathodic protection within the coatings has said to be governed by
the binder and pigment material used <script>crossref(28)</script>, <script>crossref(43)</script>.</p>

<p>&sect;107 Theiler <script>crossref(39)</script> states that there is an optimum zinc
content for each type of binder material used.�
Too much zinc results in a large anodic area compared to the cathodic
area and a small current density is drawn from the active zinc. This low
current density allows the zinc to passivate by not changing the pH within the
film and the protective action is lost. A small anode area would result in a
high current density and the zinc would be used rapidly.</p>

<h4>The PVC/CPVC ratio</h4>

<p>&sect;108 The pigment volume concentration/critical pigment volume
concentration (PVC/CPVC) is of great importance to the successful performance
of inorganic zinc-rich coatings. As has been stated, contact between pigment
and substrate is necessary to establish cathodic protection. If the ratio is
too low then there is total encapsulation of the pigment particles by the binder
and inter-particle contact is minimal <script>crossref(12)</script>. The number of internal pores within the
film is also insufficient to promote galvanic activity. Too much zinc would
afford cathodic protection to the steel but with the risk of having too little
binder to keep the pigment on the steel substrate. The zinc pigment would also
be exposed and have an increased corrosion rate that would lead to a shorter
life of the coating.</p>

<p>&sect;109 However, it can be said that inorganic zinc rich primers are
less susceptible to the requirement for specific loading<a name="_Ref521139057"></a><a href="#_edn44" name="_ednref44" title="">[44]</a>. The binder used is said to react
with the coating and substrate rather than encapsulation as with organic
binders. The inorganic film is strong, inert, adhesive and heat resistant as
well as being quite porous. This porosity allows for electrolyte to pass
through before barrier properties take effect. </p>

