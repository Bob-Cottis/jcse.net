<h3>&sect;42 Conclusions</h3>

<p>The
electrochemical and corrosion behaviour of analytically and commercially pure
titanium electrodes in alkaline solutions was investigated in order to
determine the influence of the state of electrode surface, concentration of
NaOH and temperature.</p>

<p>&sect;43 In
order to get the reproductive results, the preparation of the electrode surface
with 1200 grit emery paper immediately before dipping the electrode into
solution is needed.</p>

<p>&sect;44 The
electrode purity does not influence neither the mechanism of hydrogen evolution
reaction nor of the titanium anodic dissolution, but does affect the values of
corrosion and polarization resistances. The lower resistances were found for
the electrodes of commercial purity. The NaOH concentration has an essential
influence on corrosion and polarization resistances causing their increase in
more concentrated solutions. The rate of TiO�<sub>2</sub> growth increases with
increasing temperature up to 35�C. The further rise of temperature
(up to 55�C) does not show any influence. </p>

