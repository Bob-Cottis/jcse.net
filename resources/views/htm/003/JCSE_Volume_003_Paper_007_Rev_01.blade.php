<h3>&sect;1 Abstract</h3>

<p>The electrochemical and corrosion behaviour of
commercially and analytically (99.99%) pure titanium in 0.1 M; 0.5 M and 1.0 M
NaOH (pH=12.73-13.49) were investigated and were found to depend on electrode
purity and solution concentration. The corrosion, passive and transpassive
regions were also analysed. The corrosion and polarization resistances as well
as temperature influence on corrosion behaviour were determined.</p>

<p>&sect;2 <b>Key words: </b>alkaline
solutions, corrosion rate, oxide film, titanium.</p>

<h3>&sect;3 Introduction</h3>

<p>Thanks
to its low density and high mechanical and corrosion resistance, titanium is
widely used as construction material in many fields of
technology. Its corrosion resistance is due to a passivating oxide layer which
is formed either spontaneously in air and/or in water solutions or using anodic
polarization of titanium electrodes in both acid and alkaline solutions.
According to the properties of the oxide film, titanium belongs to the valve
metals. Recently titanium dioxide has
attracted particular attention as one of the
most promising semiconductor materials for solar energy conversion<sup>1-5</sup>.
There are only few works in literature related with corrosion behaviour of
titanium in alkaline solutions<sup>6-7</sup>. </p>

  <p>&sect;4 In
the work of� Braner and Nann<sup>6</sup>
the corrosion behaviour of titanium in 1.0 M and 6.0 M NaOH solutions was
investigated using current-voltage dependence. The transpassive region was
shifted toward more negative potentials compared to one in acid solutions. The
anodic passive film was found to be thicker in 6.0 M than in 1.0 M NaOH, for
the same current densities as well as in case of open circuit.</p>

<p>&sect;5 Wilhelmsen
and Hurlen<sup>7</sup> have investigated the
behaviour of titanium using
potentiostatic and galvanostatic methods. The electrodes were previously
stabilized in alkaline solutions, pH=7-13. The results of capacity measurements
of passive film TiO<sub>2</sub> in potential region between �0.75 and 2.5 V vs
SCE were also presented. They concluded that titanium passes from active to
passive state at potentials more positive than �0.75 V. The polarization
current does not depend on pH in passive region.</p>

<p>&sect;6 In
