<p>&sect;21 For
all cyclovoltammetric investigations the initial potential was -1.1V vs SCE,
which is close to Ti/TiO<sub>2</sub> standard potential (-0.86V).</p>

<p>&sect;22 The
currents which were noticed at this potential were attributed to hydrogen
evolution on thin oxide films, formed during short exposure of the electrodes
to air.</p>

<p>&sect;23 At
more positive potentials, in the vicinity of
the corrosion potential, titanium anodically dissolves to Ti<sup>+</sup> and Ti<sup>2+</sup>. In the potential
region between �0.5 and 0.5 or 0.75 V (depending on titanium purity and NaOH
concentration) vs corrosion potential, corrosion reactions proceed forming
passive oxide layer</p>

<p>Ti +
2OH<sup>- </sup><font face="Symbol">�</font> TiO<sub>2</sub> + H<sub>2</sub> + 4e</p>

<p>Ti +
2H<sub>2</sub>O <font face="Symbol"> �</font> TiO<sub>2</sub> + 2H<sub>2</sub></p>

<p>&sect;24 This
passive region is characterised by the
dominant presence of TiO<sub>2</sub> on the
titanium surface. The TiO<sub>2</sub> layer is formed only partially during the
first cycle, while it is completed after second cycle
(Figures &quot;b&quot;).</p>

<p>&sect;25 The
potential in this passive region is stable and only slightly&nbsp; changes with time due to inhibition
of corrosion reactions that is caused by growing TiO<sub>2</sub> passive layer.
The electrode enters transpassive region at potentials more positive than 0.5
or 0.75 V vs corrosion one. This region is characterized by an electrical
breakthrough and subsequent slow rise of current. The rise is attributed to the
evolution of oxygen that is enabled by transformation of TiO<sub>2</sub>
passive layer to conductive TiOOH form.</p>

<p>&sect;26 TiOOH
is formed through reaction of disproportionation which proceed due to particle
nucleation and simultaneous formation at vacancies. This explanation of Ti
behaviour in transpassive region, together with possible formation of hydroxyl
bridges is accepted in available literature<sup>8-13</sup>.</p>

<p>&sect;27 In
our experiments, using CV method, the applied sweep rate of 50 mV/s was high
enough to provide rapid nucleation and simultaneous formation at two
dimensional vacancies during TiO<sub>2</sub> formation. The intensity of
titanium passivation is found to be determined by purity of Ti electrodes and
NaOH concentration. Both commercial and analytical Ti undergo faster
passivation in more concentrated solutions. The analysis Pourbaix diagram for
Ti confirms this conclusion<sup>14</sup>. </p>

<p>&sect;28 The
TiO<sub>2</sub> passive layer is found to be significantly thicker for
analytical than for commercially pure titanium. This is due to presence of
impurities (Fe, H<sub>2</sub>, N<sub>2 </sub>...) in commercial Ti which also form
their oxides, decreasing the rate of the basic reaction. The voltammograms
(Fig.7a, 7b, and 8a, 8b) were used for determination of kinetics and mechanism
of titanium dissolution reaction as well as of hydrogen evolution reaction<sup>9-10</sup>.
Using the values of current and potential from the voltammograms the Tafel
lines were plotted. The calculated values of the slopes are shown in Tables 5.
On the basis of these values. the Tafel reaction (recombination of hydrogen
adatoms to molecule) is found to be the rate determining step in� NaOH�
concentration� range� between�
0.1 and 0.5 M. The mechanism of titanium anodic dissolution to Ti<sup>+</sup>
and Ti<sup>2+</sup> up to 50 mV vs corrosion potential is determined in a
similar manner, and the following reactions are proposed </p>

<p>Ti <font face="Symbol"> �</font> Ti<sup>+</sup> + e</p>

