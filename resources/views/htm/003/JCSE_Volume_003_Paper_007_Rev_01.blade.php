<h3>&sect;13 Experimental</h3>

<p>Commercial titanium according to
DIN 37025; Fe-0.20%, O<sub>2</sub>-0.10%, N<sub>2</sub>-0.05%,
C-0.008%, H<sub>2</sub>-0.13%, Pd-0.15%, active surface area 0.2cm<sup>2</sup> and analytical
grade titanium (99.99%), active surface
area 0.3cm<sup>2</sup> in a
form of rod was protected by PTFE in such a
manner that only its cross section was exposed to electrolyte. The electrode
preparation consisted only in polishing with 1200 grit emery paper immediately
before dipping into cell solution.� The testing
solutions were� </p>

<p>0.1
M NaOH + 1.9 M NaClO<sub>4</sub>� pH =
12.73, </p>

<p>0.5
M NaOH + 1.5 M NaClO<sub>4</sub>, pH = 13.13 </p>

<p>1 M
NaOH + 1.0 M NaClO<sub>4</sub>, pH = 13.49</p>

  <p>in
order to maintain the ionic strength constant. By an argon stream the solution
was kept of exposure to air. Saturated calomel electrode was used as reference
electrode and wide Pt foil was used as counter electrode. The measurements were
carried out by an original PKS-system with built in software for
electrochemical and corrosion investigation as well as by a PAR EG&amp;G
potentiostat/galvanostat Model 273. A scanning electron microscope (JEOL Model JCXA-733)
  was
also used. The working temperature of the solutions was 25<b>�</b>C, and the
temperature influence was investigated in the range 25 and 55<b>�</b>C.</p>

