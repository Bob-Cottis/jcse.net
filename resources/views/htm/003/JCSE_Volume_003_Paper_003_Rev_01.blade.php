  <p>&sect;24 In Figure 3 the potentiodynamic polarization curves of bare or anodised
  specimens in 3M H<sub>2</sub>SO<sub>4</sub> and with an addition of 0.015 M of
  malonic or citric acids are shown. From these results it follows that in all
  cases the Al-Alloy suffers localized corrosion. This is indicated by E<sub>pit</sub>
  having a more positive value than E<sub>cor</sub> and also from the presence
  of a hysteresis loop between the forward and reverse scans [23,24]. In all
  cases the Al-Alloy is not passivated, as passive regions do not appear for
  potential values more positive than E<sub>pit</sub> and reverse currents are
  always higher than forward currents. It is also observed that the addition of
  carboxylic acids did not significantly influence E<sub>cor</sub> but shifted E<sub>pit</sub>
  in the noble direction (-610 mV for malonic acid) relative to the bare alloy
  (-680 mV) and decreased the anodic current indicating less susceptibility to
  localized corrosion in the free corrosion potential regions. In the absence of
  any carboxylic acid, E<sub>pit</sub> shifted in the opposite direction (-720
  mV for 3 M H<sub>2</sub>SO<sub>4</sub>).</p>
