<h3>Conclusions</h3>

<p>&sect;65 Our experiments have shown that
IPAP system to be a feasible, low temperature deposition method for surface
coatings. A triode created by thermionic emitter showed a significant
improvement in deposition. Any comparisons with conventional systems will
further established the efficient and effective process development of IPAP
over other established methods. When aluminium substrates were exposed to IPAP
conditions, they showed a satisfactory growth of aluminium nitride with an
improved hardness. Although initial sputtering was useful in the removing oxide
layer the results were not satisfactory. One of the major aspects in aluminium
nitride coating was the cleaning. Our experiments showed, a significant
improvement was observed by the use of polished aluminium substrates. The
formed nitride layer did not show any further growth after reaching an optimum
level. The ability to control power independently for the emitter, the specimen
and the positive electrode facilitated the independent control of voltage and
cathode current density. The entire process was successfully completed at
temperature less than 400 &deg;C. Therefore, HEPAP would definitely be
an attractive process for coatings of temperature sensitive materials. The
theoretical model developed will assist to calculate the ion energies, which in
turn will give an indication of optimum parameters that, could be used. The
model will become more reliable as and when the assumptions are confirmed to
reflect ideal situations. This model could be now used for coatings of other
material using IPAP.</p>

<p>&sect;66 One of the draw back of IPAP was
the difficulty of controlling the thickness of the coating. The fact that the
nitride layers did not grow further than a certain limit due to shielding
effect of previous layers which in turn made it difficult to control the
thickness. The author suggest the supply of an extra source of substrate
material viz ; another aluminium disc, the substrate to be connected to a
rotating device or to supply an additional source of the substrate material in
powder form that will enable to enter the cathode fall region. </p>

