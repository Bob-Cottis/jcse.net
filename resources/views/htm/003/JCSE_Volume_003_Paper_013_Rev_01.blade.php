<p>&sect;7 Scanning Electon Microscopy (SEM)
and Electron Dispersive X-ray Analysis (EDS), X-ray diffraction (XRD) and Auger
Electron Spectroscopy (AES) and surface hardness were performed on the coatings
deposited. The quality of the layer produced was affected by the cleanliness of
the surface to be coated . The physical nature of the surface to be treated
determined the pre-treatments required to get the thicker deposition. At this
stage the surface was carefully inspected for signs of contamination or damage.
Any undesirable matter was removed from the surface prior to treatment. This
involved breaking of the bonds between the contaminants and the substrate
surface either by physical or chemical methods. The polishing therefore was
done until a fine mirror-like surface was obtained. After the polishing stage,
the sample was washed with acetone to obtain a clean surface. </p>

<h3>Results</h3>

<p>&sect;8 The layers deposited were
characterised for hardness, structure and composition. The appearances of these
samples were light black due to compositional difference and the surface layers
were highly adherent and dense. In conventional nitriding at temperatures lower
than 400 &deg;C the adhesion is very poor and improves with increasing
temperature. During HEPAP a meta -stable Al<sub>3</sub>N is thought to form due
to the energetic ion bombardment. It is believed that this aspect plays an
important role in promoting adhesion by reducing compressive thermal stress
arising due to differences in the thermal expansion coefficients between the Al<sub>3</sub>N
coating and the Al substrate.</p>

<p>&sect;9 The microstructure of the IPAP
films grown on to Al substrates revealed interesting features. The films
appeared to be growing in a vertical direction as is evident by the columnar
structure shown in figure 2. Following initial deposition, the rate of growth
in the vertical direction appeared to be much greater than the growth rate in
the horizontal direction. It is also evident that outgrowth occurs from the
substrate surface as aluminium atoms are removed due to sputtering caused by
high energy bombardment of the surface. The Al<sub>3</sub>N phase seemed to be
an important feature in this columnar mode of growth in this deposition method.</p>

<p>&sect;10 For engineering applications the
micro-hardness is an important property that determine the in-service
performance. Figure 3 shows the surface micro-hardness of IPAP coatings
compared to conventional plasma coatings. It is clearly evident that the IPAP
process produces coatings which are three times harder than plasma nitride
coating. In general, a greater resistance to wear can be expected with an increase
in micro-hardness which ultimately means longer in-service of the component.</p>

<p>&sect;11 The composition of the AlN
coatings was analysed using a SEM and EDS. Peaks showing the presence of Al ,N,
Si/W, Ti, Fe and Ar were evident. Signals from the base material and the
filament also showed up in the spectrum. Initial examination of the texture
revealed the presence of cubic Al<sub>3</sub>N phases at orientations of
(200),(311),(400) (see figure 5). </p>

