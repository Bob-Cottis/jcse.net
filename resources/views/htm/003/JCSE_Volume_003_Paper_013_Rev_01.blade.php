<p>&sect;61 We suggest any of the following
two possibilities for the mechanism responsible for the formations of AlN.</p>

<p>&sect;62 The first possibility is the
formation of aluminium nitride in the plasma. This happens when active nitrogen
ions react with sputtered aluminium ions. Initially they will be high in
nitrogen (AlN) and upon condensation on to the surface of the cathode they
decomposes and release nitrogen. This nitrogen may diffuse in to the substrate.
Thus the composition becomes Al<sub>3</sub>N type. </p>

<p>&sect;63 The second possibility is that
ionic bombardment induces vacancies and vacancy clusters and therefore
increases nitrogen diffusion. This could generate a coating with a thickness of
few atomic layers and some may have defects. These defects will also promote
the diffusion of nitrogen.</p>

