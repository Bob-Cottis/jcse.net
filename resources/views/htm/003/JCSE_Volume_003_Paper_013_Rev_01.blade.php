<h3>&sect;1 Abstract</h3>

<p>This work describes a theoretical model for the mechanism
involved in a triode glow discharge employed to grow aluminium nitride films. A
series of systematic studies were carried out at varying degrees of cathode
voltage, cathode current density, pressure range and ion collector voltage
values over a fixed period of time. All films developed were characterised
using Scanning Electron Microscopy, Electron Dispersion X- ray Analysis, X- ray
Diffraction and micro hardness tests. This project has collated a basic
theoretical model that can be applied to produce films of different materials.</p>

<p><b>Key Words: </b>Glow discharge, Triode, Aluminium nitride </p>

<h3>Introduction</h3>

<p>&sect;2 Aluminium nitride (AlN) is a
binary III-V compounds with a wurtzite-type structure. The electron to atom
ratio is 4:1. The structure shows an irregular distribution of tetrahedral
bonds with a c/a axis ratio of AlN being 1.6. [<script>crossref(1)</script>] It has an outstanding
combination of properties. They are high thermal conductivity (320 W/mK),
excellent physical and chemical stability at high temperatures (melting point
is about 2400 &deg;C), optical transparency in the visible and near
infrared (IR), piezoelectric and good insulating properties (the band gap is
approximately 6.3eV ) and high hardness (HV 1400). Aluminium nitride is mainly
used as a high strength special material to harden the surface and in the
microelectronics industry as a basic material acting for passivation of
semiconductor surface, transmission of surface acoustic waves etc. [<script>crossref(2)</script>]</p>

<p>&sect;3 The use of an intensified glow
discharge in a triode system to enhance the plasma to deposit aluminium nitride
was initiated in 1991. [<script>crossref(3)</script>] This technique known as Intensified Plasma
Assisted Processing (IPAP), is an improvement from traditional glow discharge
techniques. Plasma intensification is produced by thermionic emission. Meletis  [<script>crossref(3)</script>]
carried out the first High Intensified Plasma Assisted Processing (IPAP) work
to develop coatings on aluminium. Their initial work proved the system to be
advantageous over conventional nitriding using a diode. They carried out the
work in two ranges of the plasma energy spectrum. The first HEPAP work at
Manchester Metropolitan University was started in 1996  [<script>crossref(4)</script>] to extend
the operating parameters using a balanced RF system to the substrate for the
deposition of AlN. This paper describes the details of the optimum conditions
achieved and the quantitative/qualitative model used in IPAP.</p>

<h3>Experimental</h3>

<p>&sect;4 The IPAP system was built
around a water cooled non-magnetic stainless steel vessel ( Figure 1). The
negative emission source with filament and a DC powered positive plate were
positioned facing one another across the inside of the vessel and were
controlled separately. The RF powered substrate holder was connected to a matching
network that enabled the process operation in the automatic mode. The system
was continually pumped and included a heating and pre-mixing nitrogen-argon
container. A plasma was sustained in the system across the pressure range 1.33
x 10<sup>3</sup> Pa to 1.33 x 10 <sup>-1</sup> Pa. The substrate used was 1.5
cm thick and had a diameter of 5 cm. It was air craft quality HE30 grade
aluminium (Al 98% by wt.)</p>

<p>&sect;5 Initially the system was
evacuated to 1.33 x 10 <sup>-4</sup> Pa and flushed several times with argon.
In order to remove the Al<sub>2</sub>O<sub>3</sub> layer formed on the surface
due to oxidation the samples were sputter cleaned with the substrate voltage at
2000V with argon for 10 minutes. The argon gas was phased out gradually whilst
simultaneously introducing nitrogen. All samples after each experiment were
removed from the chamber and characterised. Several sets of experiments were
done using unpolished and polished aluminium grade HE30. In all cases the
experiments were carried out by varying each parameter over 7 stages. There
were four variable parameters (viz: cathode voltage, nitrogen gas pressure,
cathode current density and ion collector voltage) and initially one parameter
was changed whilst the other three parameters were kept constant. Next, two
parameters were changed while the other two were kept constant. Then three
parameters were changed whilst one parameter was kept constant and finally all
parameters were changed to obtain the optimum working range which gave a
thicker deposition. These values were compared with predicted optimum parameters
to establish a possible correlation for the future. </p>

