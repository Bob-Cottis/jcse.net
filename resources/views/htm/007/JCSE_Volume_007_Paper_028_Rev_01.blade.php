<h3>Abstract</h3>

<p>The surface electronic states on nickel-titanium alloy (Ni<i><sub>x</sub></i>Ti<sub>1-<i>x</i></sub>)
electrodes, which have five types of intermetallic compounds (Ti, NiTi<sub>2</sub>,
NiTi, Ni<sub>3</sub>Ti, Ni) and three types of oxides (TiO<sub>2</sub>, NiTiO<sub>3</sub>,
NiO), were investigated by cyclic voltammetry for various nickel mole fraction <i>x</i>.�
An oxidation current peak attributed to the oxidation of the surface states
appeared at 0.53 V vs. SCE in the cyclic voltammogram.� The surface state
density <i>n</i> varied with the alloy composition such as <i>n</i> = (2<i>x</i>- 1) <i>n</i><sub>1</sub>, where <i>n</i><sub>1</sub>
= 5.2 � 10<sup>15</sup> /cm<sup>2</sup>, suggesting they are Ni<sup>2+/3+</sup>
states in the nickel oxide layer.</p>

<p>&sect;1 <b>Keywords:
</b>nickel-titanium alloy, intermetallic compound, metal oxide layer, surface
state.</p>

<h3>Introduction</h3>

<p>&sect;2 A
metal electrode in aqueous solution has an electrical double layer with a
typical capacitance of 26 � 10 mF/cm<sup>2</sup> at the interface in contact
with aqueous solution, across which most of the applied voltage leaves itself
[ref1-ref5].� The surface of the metal contacted with aqueous solution
generally has thin metal oxide layer resulted from reaction with oxygen in air,
followed by water molecules adsorption.� Thus, the actual interface between
metal and aqueous solution can be expressed as metal / thin metal oxide layer /
the Helmholtz layer / aqueous solution, having a capacitance of about 26 mF/cm<sup>2</sup>.</p>

<p>&sect;3 The electron transfer across the interface are characterized thermodynamically
by the metal electrode potential, that is, the position of the Fermi level, but
characterized kinetically by electronic levels in the oxide layer formed on the
metal surface, through which the electrons are transported.� We, therefore,
need to have knowledge about the electronic levels in the metal oxide.</p>

<p>&sect;4 The case for an alloy electrode is more complicated if we take into account the
existence of the oxide layer at the interface.�� Two kinds of metal atoms in an
alloy do not always dissolve to each other unlimitedly, but in many cases, they
form several intermetallic compounds, and the alloy is usually the mixture of
them, i. e. heterogeneous.� The alloy electrode, as well as the metal
electrode, forms an oxide layer at the interface in aqueous solution, which is
a mixture of each metal oxide or complex oxide.� Accordingly, the
characteristics of the electron transfer across the interface will depend on
the alloy composition.</p>

<p>&sect;5 Alloy of metal nickel and titanium, Ni<i><sub>x</sub></i>Ti<sub>1-<i>x</i></sub>,
forms five types of intermetallic compounds, such as Ti (<i>x</i><sub>1</sub> =
0), NiTi<sub>2</sub> (<i>x</i><sub>2</sub> = 1/3), NiTi (<i>x</i><sub>3</sub> =
1/2), Ni<sub>3</sub>Ti (<i>x</i><sub>4</sub> = 3/4), and Ni (<i>x</i><sub>5</sub>
= 1), where <i>x</i> is nickel mole fraction [ref6].� An alloy whose nickel
mole fraction <i>x</i> is <i>x<sub>i</sub></i> &lt; <i>x</i> &lt; <i>x<sub>i</sub></i><sub>+1</sub>
is a mixture of two intermetallic compounds whose <i>x</i> are <i>x<sub>i</sub></i>
and <i>x<sub>i</sub></i><sub>+1</sub>.</p>

<p>&sect;6 On
the other hand, metal titanium (<i>x</i> = 0) has its oxide TiO<sub>2</sub>,
metal nickel (<i>x</i> = 1) has NiO, Ni<i><sub>x</sub></i>Ti<sub>1-<i>x</i></sub>
alloy (0 &lt; <i>x</i> &lt; 1) has NiTiO<sub>3</sub> [ref7].� That is, the Ni<i><sub>x</sub></i>Ti<sub>1-<i>x</i></sub>
alloy has five types of intermetallic compounds (Ti, NiTi<sub>2</sub>, NiTi, Ni<sub>3</sub>Ti,
Ni) and three types of oxides (TiO<sub>2</sub>, NiTiO<sub>3</sub>, NiO).</p>

<p>&sect;7 Therefore, the Ni<i><sub>x</sub></i>Ti<sub>1-<i>x</i></sub> alloy electrodes
are expected to have three types of electron transfer mechanism based on the
three kinds of the oxides forming interface layer.</p>

<p>&sect;8 The metal oxide layer on the electrode surface has electronic levels
corresponding to the valence number change of the metal ions in the metal
oxide.� The electron transfer through these levels can occur depending on these
relative positions with the Fermi level of the alloy bulk.</p>

<p>&sect;9 Each metal oxide has its own electronic levels [ref8], thus we need the
knowledge on the surface oxide layer for understanding the mechanism of the
electrons transfer on the electrode surface.</p>

<p>&sect;10 In
the present study, we measured the interface quality of the Ni<i><sub>x</sub></i>Ti<sub>1-<i>x</i></sub>
alloy using the cyclic voltammetry, and discussed the electronic states on the
Ni<i><sub>x</sub></i>Ti<sub>1-<i>x</i></sub> alloy electrode surface for
various nickel mole fractions <i>x</i>.</p>

