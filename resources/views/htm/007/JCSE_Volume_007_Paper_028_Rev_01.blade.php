<h3>Conclusion</h3>

<p>&sect;40 The
Ni<i><sub>x</sub></i>Ti<sub>1-<i>x</i></sub> alloy showed the oxidation current
peak at 0.53 V vs. SCE in the cyclic voltammography.� It was attributed to the
oxidation of surface states Ni<sup>2+/3+</sup> in Ni(OH)<sub>2</sub> in the
oxide layer formed on the electrode surface.� The surface states density
increased with nickel mole fraction<i> x</i>, proportional to (2<i>x</i>- 1) in the range of 0.5 � <i>x</i>�
1.</p>

<p>&sect;41 The thickness of oxide layer on the electrode surface was estimated as 1.84 nm
from the oxidation current peak.</p>

