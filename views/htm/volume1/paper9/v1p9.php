<SCRIPT>comment(14)</SCRIPT>  <h4 align="left"></b>3 Results and Discussion</h4>                        
  <p align="left">The distribution of chrome and molybdenum, i. e., the main         
  elements determining the passivation stability of the steel is relatively         
  uniform, regardless of the dual-phase structure of metal. The chrome content         
  is any of the analysis point did not drop below the critical content (&lt;         
  12%) at which intergranular corrosion of the steel can develop as a result of         
  depletion of the boundary zones of the metal in chrome with precipitation of         
  high-chrome carbides at the grain boundary [9,10].</p>        
<SCRIPT>comment(15)</SCRIPT>  <p align="left">Investigations by the AM and B methods in accordance with GOST         
