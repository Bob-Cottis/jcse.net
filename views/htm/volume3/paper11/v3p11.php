<p><b><span lang=EN-US>I) INTRODUCTION 
</span></b></p>

<p><span lang=EN-US>  </span></p>

<p><span lang=EN-US>  The characterization and the assessment of
the susceptibility to IGC of austenitic stainless steel, always had recourse to
the normalized classic tests, (ASTM G 28, ASTM A262-86, SEP 1877, AFNOR
A05-159, AFNOR A05-160), of Huey, Strauss and Streicher.  These tests are delicate to put in work on
site and to interpret in the case of a very fine precipitation.  Besides, their implementation often needs to
cut a sample of metal that is harmful to structures in service and to the
integrity of pieces at receipt control. 
It is precisely the need to assure these controls, particularly of the
taken of casting products, by non destructive tests, that French company
(CAFL)', developed since 25 years, an Electro-chemical test under the EPR name
(Electrochemical Potentiokinetic Reactivation) or method potentio-kinetic by
reactivation of sensitivity to intergranular corrosion of austenitic stainless
steels. Since, numerous investigations</span><sup><span lang=EN-US>2-21</span></sup><span
lang=EN-US> contributed to the development of the technique. It exists today under
different variants of which the used more, due to its lower sensitivity at the
surface state</span><sup><span lang=EN-US>13,18,19</span></sup><span lang=EN-US>, is
the double sweep rate method (EPR double loop)</span><sup><span lang=EN-US>2-6</span></sup><span
lang=EN-US>. She is applied currently on sites with a portable EPR cell</span><sup><span
lang=EN-US>14</span></sup><span lang=EN-US> for test in situ of receipt piece either
finished cauldron apparatus</span><sup><span lang=EN-US>7-10</span></sup><span
lang=EN-US>.  </span></p>

<p><span lang=EN-US>The
high sensitivity of this technique in regard to the electrolyte composition and
the  </span></p>

<p><span lang=EN-US>Operative
conditions do not put back in reason its reliability that has been demonstrated
otherwise on numerous classes of austenitic stainless steels and nickel alloys.
However, the great precision to detect the fine precipitation requires research
for every alloy, of a suitable electrolyte composition, temperature, and
potential range and of sweep rate </span><sup><span lang=EN-US>9,13-17</span></sup><span
lang=EN-US>. The basis of the electrolyte being constituted by the sulfuric acid
(H2SO4) with the addition of a dépassivator as the KSCN</span><sup><span
lang=EN-US>13,15</span></sup><span lang=EN-US>, or the NH<sub>4</sub>SCN</span><sup><span
lang=EN-US>9,17</span></sup><span lang=EN-US>, or the HC1</span><sup><span lang=EN-US>14</span></sup><span
lang=EN-US> in the case of the modified EPR method. That permits to become
liberated from the quickly aging of the solution and to offer a better
sensitivity concerning depassivation of Cr-depleted zones.  </span></p>

<p><span lang=EN-US>In
this paper, is presented the application of the EPR DL method, to put in
evidence Cr-depletion zones of consecutive precipitation at grain boundaries of
M</span><sub><span lang=EN-US>23</span></sub><span lang=EN-US>C</span><sub><span
lang=EN-US>6 </span></sub><span lang=EN-US> chromium carbides and intermetallic phases (</span><span
lang=EN-US>, </span><span
lang=EN-US>) during the aging of 316L steel. 
</span></p>

<p><span lang=EN-US>The
efficiency and utility of the method for the detection and the quantitative
follow-up in service of the degree of sensitization to the IGC, will be
discussed while confronting results of the aged microstructure, completed by
Cr-depletion profiles, to results of Strauss and EPR DL tests.  In This work, we intend to contribute to
efforts, aiming the normalization of the EPR test for the determination and the
quantification of impoverished zones in elements of alloy.  </span></p>

