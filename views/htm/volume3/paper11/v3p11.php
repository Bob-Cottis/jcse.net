<p><span lang=FR>  </span></p>

<p><span lang=EN-US>IV-1)
<u>Evolution of the microstructure during aging treatment:</u></span></p>

<p><span lang=EN-US>&nbsp;</span></p>

<p><span lang=EN-US>The
results of observations and identifications achieved by electronic diffraction
are summarized in the <u>figure 4</u>. They show that in the domain of
temperature studied, carbides of type M</span><sub><span lang=EN-US>23</span></sub><span
lang=EN-US>C</span><sub><span lang=EN-US>6</span></sub><span lang=EN-US>
appear the first and then intermetallic phases. These phases appear all,
