<p><b><span lang=EN-US>IV) Discussion</span></b></p>

<p><span lang=EN-US>&nbsp;</span></p>

<p><span lang=EN-US>The oversaturation in Carbon and in Molybdenum
of homogenized austenite governs the intervening structural modifications
during the keeping in temperature of steel 316L.  The low content in Carbon of this steel nuance favors in first
the formation of type M<sub>23</sub>C<sub>6</sub> carbides, essentially formed
of Chromium and Molybdenum.  Their
precipitation in substantial quantity involves the reduction of contents in
Chromium and in Molybdenum grain boundaries. The intermetallic phases </span><span
lang=EN-US>h</span><span lang=EN-US> and </span><span lang=EN-US>s</span><span
lang=EN-US> appear later, in an austenite
impoverished in Carbon and less rich in Molybdenum.  The germination of the </span><span lang=EN-US>s</span><span
lang=EN-US> phase, rich in Chromium, requires a
sufficient repletion of grain boundaries zones previously impoverished by the
precipitation of M<sub>23</sub>C<sub>6</sub> carbides. The prolongation of
keeping  (30 000h) to the elevated
temperatures (&gt; 650°C) assures the repletion required by the precipitation
of this phase.  The fast diffusion of
the Chromium to this temperature guarantees the growth of the </span><span
lang=EN-US>s</span><span lang=EN-US> phase without the creation of new impoverished
zones.  </span></p>

<p><span lang=EN-US>The comparison of corrosion results in the
sulfo-cupric reagent and concentration profiles of Chromium, assigns the
sensitization to the IGC, detected by the Strauss test, to the lowering of the
content in Chromium at grain boundaries below a critical level valued to 12-13%
at and to a width of profile, to this level, superior or equal to 25 nm.  Desensitization appears only controlled by
the critical value of 12-13% at in Chromium, considering in this domain, the
width of the profile is very superior to the critical width due to the duration
or the elevated temperature of the keeping (30 000h at 600°C).  </span></p>

<p><span lang=EN-US>These observations explain the delay to the
detection of the sensitization to the corrosion by this test in relation to the
beginning of apparition of the intergranular precipitate  of M<sub>23</sub>C<sub>6</sub> carbides
(fig. 8). This delay is imputed to the weak width of the Cr-depletion profile
(&lt;25 nm) to the critical value 12-13 % at in Chromium that makes difficult the
assessment of the intergranular penetration depth on micrographic section very
lower to the resolution limit of the optical microscope.  It is for this reason, that aged states
during 1000h at 600°C and 100h at 650°C appear outside of the domain of sensitivity
although the content in Cr of boundary is respectively 11,2% at and 11,5% at.
Besides, The efficient concentration in boundaries of these 2 states is strong
probably lower to these values owing to the experimental overestimate related
to the large size of the probe before the width of the profile and integrate
the extremely steep and narrow bottom of the profil<sup>22</sup>.  The theory<sup>23</sup> foresees a minimal
Chromium concentration at boundary of aging beginning. </span></p>

