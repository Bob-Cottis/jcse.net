<p><b><span lang=EN-US>ABSTRACT:</span></b></p>

<p><span lang=EN-US>  </span></p>

<p><span lang=EN-US>The
resistance to IGC corrosion of austenitic stainless steels revolves during the
aging at temperatures between 500°C and 700°C. This evolution is consecutive to
very known phenomena, of precipitation of M</span><sub><span lang=EN-US>23</span></sub><span
lang=EN-US>C</span><sub><span lang=EN-US>6</span></sub><span lang=EN-US>
chromium carbides and intermetallic phases (</span><span
lang=EN-US>), leading to sufficiently important Cr-depletion zones at grain
boundaries and then sensitize the material to IGC corrosion.  The assessment of the sensitivity to the
IGC, from the Strauss test or equivalent tests, requires cutting a sample off
the material that can be harmful to the integrity of structures in
service.  This assessment remains by its
aspect, qualitative and little sensitive to the low widths of Cr-depletion
profiles accompanying the beginning of precipitation. The EPR DL method
(Electrochemical Potentio-dynamic Reactivation double loop) establishes to be
as non-destructive and quantitative technique for the follow-up and the control
on site during the aging of steel 316 L. The criteria Ir/la &gt;1% and Qr/Qa
&gt;1%, of the evolution of the degree of sensitization to intergranular
corrosion, can be kept when the operative conditions are chosen well for the
detection of the fine precipitation responsible of impoverishment in elements
of alloy. This criterion is also valid for the detection of the desensitization
to consecutive repletion at prolongation of keeping in temperature.  </span></p>

