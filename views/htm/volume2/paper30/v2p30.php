    <h3>Conclusions</h3>
    <SCRIPT>comment(29)</SCRIPT><p>The local nature of corrosion of coated alloys necessitates the use of
    local electrochemical and chemical techniques toward understanding
    individual breakdown events.&nbsp; LEIM/S is capable of characterizing <i>in
    situ </i>the initiation of and changes in electrochemical activity of a
    single defect without disturbing blister development. LEIM/S of local
    breakdown sites has also confirmed previous observations of metastable
    behavior.</p>
    <SCRIPT>comment(30)</SCRIPT><p>LEIM has identified several different types of defects on coated
    AA2024-T3 with the two most prevalent examples being a red and a black
    blister.&nbsp; Through LEIM and LEIS the red blister was found to be more
    electrochemically active both in early and later stages of development than
    the black blister.&nbsp; CE analysis revealed that the underfilm solution of
    the red blisters contained&nbsp; Cu<sup>2+</sup>, Al<sup>3+</sup>, and Mg<sup>2+</sup>
    ions whereas the black blister only contained small amounts of Mg<sup>2+</sup>
    and Zn<sup>2+</sup> ions.&nbsp; LEIM showed that red blisters often
    repassivated during development and that this repassivation was found to
    correspond to an increase in local pH from an aggressive environment of 3-5
    to a more benign environment of 8-9.&nbsp; Red blisters caused a visibly
    severe amount of damage to the substrate as compared to black blisters.</p>
    <SCRIPT>comment(31)</SCRIPT><p>The metastable nature of defects on the coated panels was monitored by
    LEIM.&nbsp; Several possible explanations for the metastability, including
    changes in coating properties, accumulation of corrosion product, and
    variation in local pH were discussed.&nbsp; The addition of chromates to the
    bulk electrolyte caused an initial repassivation of a red blister.&nbsp;
    Whereas open circuit potential measurements remained steady after the
    addition of chromates, LEIM showed that repassivation was only temporary.</p>
    <SCRIPT>comment(32)</SCRIPT><p>Red blisters were observed to initiate in a given region and branch into
    secondary lobes.&nbsp; LEIM showed that the initiation sites were anodic to
    the secondary lobes, suggesting a possible cathodic head in growth.&nbsp;
    Local OCP measurements and EDS measurements of the composition of the
    affected substrate confirmed the differentiation of the red blisters into an
    active initiation region with corrosion taking place and relatively passive
    secondary lobes.</p>
    <SCRIPT>comment(33)</SCRIPT><p>LEIM revealed an initial increase in impedance of blisters in the
    earliest stages of development.&nbsp; Possible explanations, including
    nonuniform field distribution, water nucleation, and corrosion product
    accumulation were presented.&nbsp; Nonuniform field distributions were found
    to be an unlikely cause.&nbsp; Defects in such early, microscopic stages
    were located through LEIM of the substrate surface.</p>
