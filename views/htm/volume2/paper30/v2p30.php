    <h2>Characterization of the Electrochemical Events at Intrinsic Breakdown
    Sites on Organically Coated AA2024-T3</h2>
    <p align="left"><i>A.M. Mierisch and S.R. Taylor</i></p>
    <p align="left">Center for Electrochemical Science and Engineering,
    University of Virginia, Charlottesville, VA 22903</p>
<SCRIPT>notify();</SCRIPT><SCRIPT>comment(1)</SCRIPT>    <h3>Abstract</h3>
    <p>An understanding of the events leading to the local breakdown of
    organically coated alloys requires the use of local electrochemical and
    chemical methods.&nbsp; Intrinsic (i.e. naturally occurring) breakdown
    events on AA2024-T3 coated with a series of neat resins (vinyl,
    polyurethane, and epoxy) were examined using Local Electrochemical Impedance
    Mapping/Spectroscopy (LEIM/S) along with supportive techniques.&nbsp;
    Several revealing events and processes were identified. Several types of
    breakdown sites were identified that differed in both electrochemical and
    underfilm chemistry characteristics. The time evolution of these defects was
    observed as a rise in the local impedance in the first stages of
    observation. Metastable behavior defects have also been observed.&nbsp;
    Present effort is focussed on theoretical corroboration of the
    interpretation of these events.</p>
    <h3>Introduction</h3>
    <SCRIPT>comment(2)</SCRIPT><p>The initial sites of corrosion on organically coated alloy substrates are
    likely governed by either chemical heterogeneities within the organic
    coating [1], heterogeneities within the alloy (e.g. intermetallics) [2-4],
    or a juxtaposition of the two, assuming a physically uniform
    film.&nbsp;&nbsp; An understanding of the relative contributions from these
    processes to coating breakdown relies on the ability to obtain information
    on the local chemical and electrochemical processes.</p>
    <SCRIPT>comment(3)</SCRIPT><p>Traditionally, the electrochemical investigation of organic coating
    behavior has used global techniques to provide insight into the general
    performance of a coating [5-10].&nbsp; However, these measurements are
    averaged over the entire surface and do not provide information about
    individual sites of corrosion initiation.&nbsp; It is believed that
    information about these sites will help govern the development of better
    surface pre-treatment and coating chemistries.&nbsp; Thus, recent efforts
    have turned towards developing methods that can measure the important
    electrochemical events at these breakdown sites[11-16].</p>
    <SCRIPT>comment(4)</SCRIPT><p>This paper presents several observations of local electrochemical events
    associated with the breakdown of intrinsic defects (i.e. naturally
    occurring) on organically coated AA2024-T3 using Local Electrochemical
    Impedance Mapping (LEIM) and Spectroscopy (LEIM) in conjunction with other
    supportive methods.&nbsp; The literal translation of these data provides
    insight into the initiation and evolution processes of localized underfilm
    corrosion.</p>
    <h3><b>Experimental Methods</b></h3>
    <SCRIPT>comment(5)</SCRIPT><p>The&nbsp; LEIM/S system used in this investigation employed the
    five-electrode (split micro-reference electrode) configuration originally
    designed by Lillard, Isaacs, and Moran [14], and further developed for
    specific use on local defects in coated alloys [16] as described in detail
    by Mierisch [15].&nbsp; The probe uses a 5-electrode configuration with two
    teflon coated Ag/AgCl micro-reference electrodes, each 125mm in diameter, in
    addition to the standard working, counter and reference electrodes.&nbsp; In
    mapping, a single, small amplitude (ca. 15 mV)&nbsp; sine wave perturbation
    of optimal frequency (generally 700 Hz) is applied to a DC potential set at
    the global open circuit potential of the substrate.&nbsp; The local current
    density is measured by measuring the potential gradient in solution above
    the substrate between the two micro-reference electrodes and then converting
    this potential difference through Ohm�s law to a local current
    density.&nbsp; The local impedance (or admittance) is determined by
    comparing the global voltage to the local current density, and an LEI map is
    generated by moving the probe to discrete sites across the sample and
    plotting the local admittance as a function of x,y position. LEI
    spectroscopy operates by holding the probe over a particular x,y position
    and scanning the sine wave excitation frequency, generally between 10KHz and
    1Hz.&nbsp; In both cases the voltage signal from the split micro-reference
    electrode is amplified to minimize the required excitation signal and
    maintain a nondestructive character.</p>
    <SCRIPT>comment(6)</SCRIPT><p>All electrochemical measurements were performed on 1 mm thick AA2024-T3
    cut into 6.5x6.5 cm panels coated with a neat organic resin.&nbsp; One of
    several coating chemistries were applied depending on the particular
    variable to be studied.&nbsp; These coatings were (a) vinyl VYHH copolymer
    of polyvinyl chloride and polyvinyl acetate, (b) polyurethane coatings made
    with either a 100% polyether polyurethane or a polyurethane composed of 50%
    polyether and 50% polyester (both mixed with isocyanate, a dibutyl tin
    dilaurate catalyst and methyl ethyl ketone solvent), (c) a two-part
    polyester polyurethane comprised of Desmophen polyester polyol and desmodur
    aliphatic polyisocyanate, and (d) a two-part epoxy comprised of bisphenol A-epichlorohydrin-based
    epoxy resin solution with an Epi-cure fatty acid-polyethylenepolyamine based
    polyamide mixture curing agent.&nbsp; Butyl cellosolve, in varying amounts,
    was used as a solvent to adjust viscosity in coatings (c) and (d).</p>
    <SCRIPT>comment(7)</SCRIPT><p>All panels were cleaned prior to coating by the following procedure and
    then left to air dry: scrubbed with acetone, scrubbed with Alconox, cleaned
    ultrasonically in hexanes for at least 5 minutes, then rinsed with
    isopropanol.&nbsp; Ultrapure water was used for rinsing between steps.&nbsp;
    The appropriate coating was then spin cast onto the substrate producing a
    uniform dried film thickness of 5-20 microns depending on the
    experiment.&nbsp; Freshly coated samples were dried in a dessicator for at
    least 48 hours prior to use.&nbsp; A glass reservoir was affixed at its base
    perimeter to the coating surface with RTV silicone. Samples were exposed
    primarily to 0.6 M NaCl of ambient aeration, but other solutions including
    0.1 M NaCl and 0.1 M KCl were used as needed.</p>
    <h3><b>Results and Discussion</b></h3>
    <h4>Types of Defects</h4>
    <SCRIPT>comment(8)</SCRIPT><p>An important observation made in early investigations [15] was that
    several different types of defects occurred within a single panel. These
    defects types were differentiated initially by the color that they attained
    upon long-term exposure. Two such defects were classified as �red� and
    �black� blisters.&nbsp; LEIM was used on examples of such blisters in
    order to evaluate the electrochemical differences between them.&nbsp; Maps
    of a red and black blister are shown in Figure 1.&nbsp;
    <p><img src="{{ $htmURL }}v2p30f1.gif" width="576" height="215">
    <SCRIPT>comment(9)</SCRIPT><p><b>Figure 1</b>:&nbsp; LEIM plots showing the differences in
    electrochemical activity between black (left) and red (right)
    blisters.&nbsp; Note the higher electrochemical activity of the red
    blister.&nbsp; These AA2024-T3 panels were coated with vinyl VYHH to 10mm
    thickness and exposed to 0.6 NaCl. (<a href="../paper30/v2p30f1large.gif">larger image</a>)</p>
    <SCRIPT>comment(10)</SCRIPT><p>It should be noted that these types of blister (i.e. red and black) were
    observed on all samples when exposed to chloride solutions regardless of the
    coating chemistry, however differences in rate of formation, and extent may
    vary.&nbsp; It is clear from the figure that the red blister is
    electrochemically more active, while the black blister is relatively
    passive.&nbsp; After several weeks, the coating was removed from the
    substrate and the alloy surface at the blister sites examined by an optical
    microscope.&nbsp; As suspected from the LEIM results, the red blister had
    indeed incurred significant metal loss, whereas the black blister had caused
    little damage.&nbsp; An example of the pitting caused by a red blister is
    shown in an electron micrograph in Figure 2.&nbsp;</p>
    <p><img src="{{ $htmURL }}v2p30f2.jpg" width="248" height="205"></p>
    <SCRIPT>comment(11)</SCRIPT><p><b>Figure 2</b>:&nbsp; Electron micrograph of a pitted region of the
    substrate beneath the initiation site of a red blister.&nbsp; This substrate
    had previously been coated with a 10mm thick polyester polyurethane
    coating.&nbsp; The coating was removed for viewing in the electron
    microscope. (<a href="../paper30/v2p30f2large.gif">larger image</a>)</p>
    <SCRIPT>comment(12)</SCRIPT><p>It was hypothesized that LEIM/S could predict which initial defects might
    become the severe red blisters or the more innocuous black blisters by
    comparison of initial electrochemical activity to the eventual blister
    formed.&nbsp; LEIS was performed on defects which were identified through
    mapping in early, still microscopic stages.&nbsp; The results are shown in
    Figure 3. The defect which would later become a red blister was even then
    significantly more electrochemically active than the eventual black blister.</p>
    <p><img src="{{ $htmURL }}v2p30f3.gif" width="299" height="240"></p>
    <SCRIPT>comment(13)</SCRIPT><p><b>Figure 3</b>:&nbsp; LEIS shows that even in early stages, the red
    blister is more electrochemically active than the black blister.&nbsp; The
    smaller circle diameter on this Nyquist plot represents a lower pore
    resistance. (<a href="../paper30/v2p30f3large.gif">larger image</a>)</p>
    <SCRIPT>comment(14)</SCRIPT><p>In addition to differences in electrochemical activity, the blisters were
    also examined for chemical differences.&nbsp; In one experiment, small
    quantities of solution were drawn from several examples of each type of
    blister and the ionic content of the underfilm solution was analyzed using
    capillary electrophoresis (CE)[14]. Cu<sup>2+</sup>, Al<sup>3+</sup>, and Mg<sup>2+</sup>
    ions were found in the solution of the red blisters whereas only small
    amounts of Mg<sup>2+</sup> and Zn<sup>2+</sup> were found in the black
    blister solution.&nbsp; The cations present in the red blister solution
    suggest that active dealloying of the substrate or a particular phase within
    the substrate has taken place [2-4,17]. These results demonstrate that the
    electrochemical&nbsp; and chemical nature within very small (&lt;1 mm)
    blisters can be quantified and characterized.&nbsp;</p>
    <SCRIPT>comment(15)</SCRIPT><p>Differences in underfilm pH and potential also differentiate these
    blister types and substantiate the LEIM/S findings.&nbsp; In a similar
    experiment, local pH values of the underfilm solutions were obtained for
    several examples of each type of blister.&nbsp; This was done by inserting
    an Ir/IrO microelectrode into the blister.&nbsp; Prior to these tests, the
    electrochemical activity of each blister was mapped by LEIM.&nbsp; Some of
    the red blisters were found to be low in electrochemical activity, while
    most were found to be high in activity as expected.&nbsp; In fact, the
    blisters which were high in electrochemical activity were found to contain
    solution at pH values of 3-5, whereas the blisters with low electrochemical
    activity, presumably temporarily repassivated, contained solution of pH
    8-9.&nbsp; Local pH measurement provides valuable information about the
    underfilm solution, but it is also a destructive technique.&nbsp; Once the
    coating has been breached, the defect will no longer develop as it would
    have.&nbsp; However, this problem can be avoided through use of the
    nondestructive LEIM technique.</p>
    <h4>Time Evolution of Defect Sites</h4>
    The time evolution of electrochemical defect sites was studied in further
    detail.&nbsp; Defects were monitored over time using LEIM to detect changes
    in electrochemical activity.&nbsp; A representative time series of maps for
    a single defect is shown in Figure 4.
    <p><img src="{{ $htmURL }}v2p30f4.gif" width="576" height="462">
    <SCRIPT>comment(16)</SCRIPT><p><b>Figure 4</b>:&nbsp; LEIM of the evolution of a red blister.&nbsp; Note
    the fluctuation in electrochemical activity representative of metastability.&nbsp;
    At 52 hours, the open circuit potential dropped dramatically, suggesting a
    possible breach in the coating. (<a href="../paper30/v2p30f4large.gif">larger image</a>)</p>
    <SCRIPT>comment(17)</SCRIPT><p>Red blisters generally show metastable behavior, with periods of
    temporary repassivation or decreased activity occurring between periods of
    increasing electrochemical activity.&nbsp; Such metastable behavior has been
    suggested in the literature [18]but has not been specifically
    investigated.&nbsp; It has been suggested [19] that the biphasic nature
    parallels capacitance changes in the coating.&nbsp; Metastability could also
    be caused by accumulation of corrosion product periodically dispersed by the
    convection of hydrogen evolution.&nbsp; The former possibility will be
    investigated by comparing LEIM of a gold disk electrode with and without a
    coating.&nbsp; Reference standard gold disk electrodes have been created
    using Si/SiO wafers with embedded gold electrodes.&nbsp; These standards
    will be discussed in a future paper.</p>
    <SCRIPT>comment(18)</SCRIPT><p>In later stages, metastability is visually observable as growth
    spurts.&nbsp; The blisters experience rapid growth (visible growth within
    1-2 hours) followed by lengthy periods of no outward growth (12-24
    hours).&nbsp; In earlier stages, however, when these changes are not
    visually apparent, such a localized event can only be measured by local
    electrochemical methods.&nbsp; The observed changes in growth and in pH
    value of the solution render the possibility unlikely that metastability of
    the blisters might be due to changes in coating properties such as water
    uptake.&nbsp; However, preliminary studies have shown that a low (3-5) pH
    solution increases the rate of transfer of ions across the coating
    interface.&nbsp; It is possible that variations in pH affect the activity of
    the blisters and could contribute to metastability.</p>
    <SCRIPT>comment(19)</SCRIPT><p>Repassivation, or �healing� has been suggested to occur on chromate
    conversion coated (CCC) aluminum alloys and cited as an asset of these
    coatings [20].&nbsp; LEIM was used to study the effect of chromate added to
    solution on the activity of red blisters.&nbsp; In Figure 5, LEIM shows the
    change in electrochemical activity before and after chromate solution is
    added.</p>
    <p><img src="{{ $htmURL }}v2p30f5.gif" width="575" height="350"></p>
    <SCRIPT>comment(20)</SCRIPT><p><b>Figure 5</b>:&nbsp; LEIM of a red blister before and after a chromate
    solution was added to the bulk electrolyte.&nbsp; 1.2 M Na<sub>2</sub>CrO<sub>4</sub>
    was added to 0.6 M NaCl.&nbsp; After chromate is added, the admittance
    decreases, in agreement with a rise in open circuit potential.&nbsp; After
    24 hours, the admittance begins to rise, although open circuit potential
    alone does not indicate this change. (<a href="../paper30/v2p30f5large.gif">larger
    image</a>)
    <SCRIPT>comment(21)</SCRIPT><p>Once the chromate was added, the open circuit potential rose to a more
    noble value as expected, and LEIM showed a decreased electrochemical
    activity in the blister.&nbsp; LEIS also showed a decrease in
    electrochemical activity.&nbsp; However, while the open circuit potential
    remained relatively constant suggesting a continuing repassivation of the
    blister, LEIM and LEIS showed that, in fact, electrochemical activity began
    again to increase within the blister.&nbsp; Future studies will focus on
    metastability of defects on substrates coated first with CCC�s and then
    with organic coating.&nbsp; Although several investigations have studied CCC
    coated substrates [20-22], the effect of an organic coating on CCC coated
    substrates has not been investigated from a local electrochemistry
    standpoint.</p>
    <h4>Delineation of Anodic and Cathodic Regions</h4>
    <SCRIPT>comment(22)</SCRIPT><p>&nbsp;In addition to characterizing overall properties of individual
    blisters, LEIM was also used to distinguish between anodic and cathodic
    regions of red blisters.&nbsp; It was observed that blisters initiate at a
    given site, and then branch into one or more secondary lobes that grow away
    from the initiation site.&nbsp; LEIM showed that the initiation site is
    electrochemically active, while the secondary lobes are relatively
    inactive.&nbsp; An example of this is shown in Figure 6.</p>
    <p><img src="{{ $htmURL }}v2p30f6.gif" width="575" height="318"></p>
    <SCRIPT>comment(23)</SCRIPT><p><b>Figure 6</b>:&nbsp; An optical micrograph of a red blister with
    initiation site (lighter region to right) and secondary lobe (darker region
    to left).&nbsp; LEIM shows the higher activity in the initiation
    region.&nbsp; Local underfilm open circuit potentials, also shown, confirm
    the higher activity in the initiation region. (<a href="../paper30/v2p30f6large.gif">larger
    image</a>)
    <SCRIPT>comment(24)</SCRIPT><p>Energy dispersive spectroscopy (EDS) also revealed a higher amount of Cu
    and lower amount of Al and Mg in the general area of initiation (with Cu
    deposited just outside the initiation pit region).&nbsp; Local open circuit
    measurements, made with a Ag/AgCl microelectrode similar to the one used in
    pH measurements, showed a more anodic potential within the initiation region
    and a more noble potential in the secondary lobe.&nbsp; Although further
    studies are required to draw an absolute conclusion, it can be presumed that
    the initiation region is the anode and the secondary lobe/lobes the
    cathode.&nbsp; The contributing factors to the blister front advancement are
    presently under investigation.</p>
    <h4>Early Stages of Defect Formation</h4>
    <SCRIPT>comment(25)</SCRIPT><p>Whereas later stages of blister development can be characterized by
    compiling information obtained both from local electrochemical and chemical
    methods and from&nbsp; optical and global methods, the earliest stages of
    development and initiation of a blister require the use of local
    electrochemical methods.&nbsp; By stepping the LEI probe over the surface of
    a coated alloy panel, LEIM was able to reveal microscopic regions of defect
    initiation.&nbsp; During this early stage of development, it was found that
    the impedance had actually increased relative to the surrounding
    region.&nbsp; LEIM of an example of this is shown in Figure 7.&nbsp;</p>
    <p><img src="{{ $htmURL }}v2p30f7.gif" width="466" height="247"></p>
    <SCRIPT>comment(26)</SCRIPT><p><b>Figure 7:</b>&nbsp; An example of the initial decrease in admittance,
    or increase in impedance, in earliest stages of defects.&nbsp; This
    AA2024-T3 panel was coated with vinyl VYHH to 10 mm thickness and exposed to
    0.6 M NaCl solution.</p>
    <SCRIPT>comment(27)</SCRIPT><p>Several explanations for this increase in impedance have been
    proposed.&nbsp; Although it was suspected that nonuniform field
    distributions emanating from the defect may have given rise to the increased
    impedance, this explanation was found to be improbable.&nbsp; The expected
    field from an equipotential disk was derived and calculated for dimensions
    similar to those in the experiment.&nbsp; It was shown that the
    micro-reference electrodes would have to be very close to the surface
    compared to the radius of the disk (or size of the disk is large relative to
    the height of the micro-reference electrodes from the surface -unlikely in
    early stages).&nbsp; If the probe were indeed within this realm, a dip in
    admittance would originate from an increased base admittance value and
    substantial peaks from edge effects at the perimeter of the disk would be
    apparent.&nbsp;&nbsp; A representative plot of theoretical results that
    would result in an admittance dip is shown in Figure 8.&nbsp; The normal
    field over an equipotential disk is calculated for different heights above
    the surface relative to the radius of the disk.&nbsp; Actual admittance
    plots of defects did not display any of these features.&nbsp; Two additional
    explanations for the increased impedance are water nucleation under the
    coating and build-up of corrosion product at the initiation site [15].</p>
