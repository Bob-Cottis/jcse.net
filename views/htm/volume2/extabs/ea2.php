The use of a more concentrated solution of alkoxide seems to be positive for a good performance of the zirconia film. It is remarkable that the series 5 resulted the best, along with series 1, in promoting adhesion. In fact the detachment of the film after 7 days is not very different with series 3, made under the same conditions but with different concentration of alkoxide. Moreover the series 5 resulted even better than the other at longer time of analysis.
The microstructure of samples obtained from solution containing the acid appeared more suitable to adhere to the organic coating than the other ones.  In fact the stronger chelating action of the acetylacetone with respect the acid and its very difficult hydrolysis could account for the worst behaviour of those samples.
On the other hand, there was no surprise about the results with degreased and coated steel, showing the larger detachment of all tested. It is clear that the use of a pre-treatment is needed, but it is also clear that there is a big difference between the tested ones. The use of iron phosphate as a pre-treatment shows good results in short times of exposure (7 days), compared with the zirconia film in series 1, 5 and 2, but it is not a good option in long services evaluation because its behaviour is one among the worst. Since now, tricationic phosphate looks the best option .
With a few precautions, a ranking of the adhesion behaviour in Salt Spray Fog of our samples could be done. These results brought about the further study was restricted to samples of series 1 and 5.
<b>
  <h4>Swelling with N-methyl pyrrolidone (NMP)</h4>

</b>Van Ooij et al [6] have proposed the measure of the adhesion of the paint coating on the base of amounts of its swelling in N-methyl pyrrolidone (NMP). In fact the swelling produces stress, which  could detach the paint from the substrate. This test of adhesion of a film to the metal depends on the thickness of the film, the nature of the coating, and especially the pre-treatment on substrate surface. The results of this test are expressed as time of total detachment of the organic film in NMP. This parameter is named NMPRT (N-methyl pyrrolidone retention time). The test is based on the hypothesis that NMP acts on the organic coating, disregarding the substrate and pretreatment. 
Actually the presence of organic residuals in the zirconia films of this study, even if negligible, could invalidate the assumption, so that the detachment could be due both to the stresses induced by swelling and a chemical-physical interaction between NMP and the zirconia films. In fact the results of  NMP test on our samples didn&#146;t match perfectly with those obtained from the Salt Fog Chamber and showed before. As an example the series 5, which were the best-ranked on Salt Fog Chamber, showed a NMPRT only of 35 seconds. The poor adhesion of the primer on the zirconia films could account for the small NMPRT values recorded.
On the contrary, there was no total detachment of the coating on the samples pre-treated with phosphate, but after 2 hours we could appreciate the formation of blisters reaching a 4M and 7M (according to ASTM D 714), for the iron and tricationic phosphates, respectively.
Anyway the findings showed that the zirconia films, even if are not as good as tricationic phosphate, are better than the iron phosphate.
In order to explain these results, FTIR analyses were recorded on the zirconia film after the detachment. With respect the FTIR spectrum on zirconia pretreatment after deposition, we can appreciate some modifications. In particular the presence of a signal due to the C=O bond asserts the retainement of organic substances, pyrrolidone or a polyester residue (the assignment  couldn&#146;t be made doubtless). Some studies about the effect of the NMP on the zirconia thin film will be addressed.
<b>
  <h4>Pull-off test</h4>

</b>The tests carried out with a pull-off device (Sebastian IV) have not been useful in this research, because of the type of failure. The failure mode was decohesive in the topcoat, and we did not see the primer. As a result we just can say that the mechanical features of the coating are poor.
<b>
  <h4>Conclusions</h4>

</b>On the base of these results, it is easy to infer that thin amorphous zirconia films are better pre-treatment than a commercial iron phosphate, even if it is not so good as the tricationic one. Also, the thermal treatment on forming the zirconia film is important and a very strict control in the use of temperature and time of treatment is a condition in order to obtain the best performance of the ZrO<sub>2</sub> film.
