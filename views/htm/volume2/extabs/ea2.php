<h4>Introduction</h4>

  The most important properties of a protective coating are the adhesion to the substrate and the anticorrosive protection [1]. The last one should be obtained both as a barrier to aggressive chemical species or by using anticorrosive pigments like chromate or phosphate. Adhesion to the substrate has been object of some studies because it is supposed that if the protective system fail on adherence the total protection falls down [1, 2].
  <p>Chemical pre-treatments for improving adhesion properties of organic coatings on steel have been used for a long, and chromate and phosphate pre-treatments showed to be quite useful. However, by law, many countries have forbidden the use of toxic pre-treatments (chromate-like), in spite of their good anticorrosive properties. Because of that, industry is deeply concerned about the development of a chemical process able to substitute chromate pre-treatments.
  <p>The use of phosphate has shown good results on galvanised steel. So, it improves the adhesion of the paint film and the anticorrosive protection. But phosphates are not the best option in ecological terms. On steel it has been employed iron phosphate, zinc phosphate and tricationic one [3].
  <p>Other chromate-free pre-treatments have been developed and tested. The choice of the chemical conversion treatment must be based on several criteria: economical, ecological, technical, etc. Some of them have a chromate-like behaviour, but are not economically attractive. The application of zirconium oxide as a pre-treatment on steel appears a promising compromise. On the other hand, ZrO<sub>2</sub>-CeO<sub>2 </sub>films, put down on stainless steel by sol-gel techniques [4], resulted protective against dry and wet corrosion [5].
  <p>Sol-gel is a process to obtain films of metallic oxides in very mild conditions, but the reproducibility of thickness and quality of them depends on several parameters affecting the precursors solution. In this study it was evaluated if the use of two different complexing reagents and the control of the hydrolysis allows the formation of ZrO<sub>2</sub> films suitable as pretreatments promoting adhesion of organic coatings.
<b>
<h4>Experimental</h4>
</b>Low carbon steel was used as substrate. On 7.5cmx10cm degreased samples were deposited amorphous zirconia by dipping. After the deposition, the film underwent thermal treatments in oven at temperature between 150-250�C, in order to remove the most organic load. Repeating the deposition run, the thickness could be increased. The hydrolysis and therefore the formation of the film was made by reacting in controlled humidity chamber (70% RH) after each dipping, except in series 7 and 8 where this step was made using boiling water.
