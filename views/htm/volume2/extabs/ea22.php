<h4>Introduction</h4>
The use of anti-corrosion pigments in paints is becoming more and more restricted because of issues of toxicity. Of the traditional pigments only zinc phosphate is being used to any great extent. Any standard text on corrosion inhibitors[1] refers to a large number of organic compounds which are corrosion inhibitors, many of which have potential uses in organic coatings. However, in spite of the excellent studies on organic inhibitors in paints carried out by Braig and reported in several of these Conferences[2][3], the widespread use of organic corrosion inhibitors in conjunction with organic coatings has yet to make a major impact on the paint industry. This may be due to a natural conservatism and reticence on the part of the paint industry, or it may be due to problems associated with incorporation of an organic specie into an organic polymeric medium. 
A problem arises in that many functional groups in organic corrosion inhibitors, amines, amides, thiophenes, acids etc are also capable of reacting with fixed functional groups in many common binder systems such as epoxys and polyurethanes. The potential organic inhibitor may easily reacts with a functional group in a binder. The inhibitor is then rendered insoluble and is incapable of diffusing to the paint metal interface and making any positive contribution to anti-corrosion performance.
One way of solving this problem is by encapsulating the inhibitor molecule and using the encapsulant as an anti-corrosion pigment. The inhibitors is then slowly released into the paint and down to the metal surface. 
Encapsulating techniques are commonly used in other technologies, i.e. for the delivery of drugs, use of pesticides, provision of fragrances in the cosmetics industry, but in anti-corrosion, encapsulation is still a relatively new technology. The encapsulation method using sol-gel technology is a possible avenue.
Sol-gel has been defined by Brinker and Scherer[4] as 
&quot;the preparation of ceramic materials by preparation of a sol, gelation of a sol, and removal of the solvent. The sol may be produced from inorganic or organic precursors.&quot;
Although the name sol-gel is novel, the technology in not. Sodium silicate or water-glass has been used as a preservative for eggs for centuries and as a coating for plaster.  Ethyl silicate was synthesised and investigated by Wurtz in the last century. In the paint industry, sol gel methods have been used in both inorganic and organic zinc silicate systems. 
The incorporation of organic molecules into sol-gels has been referred to in [5] for use in optics, catalysis and sensors. However, it is believed that this is the first time sol-gel methods have been reported for the generation of an anti-corrosion pigment for paints.
In order to generate a ceramic material which had immediate compatibility with conventional paint technology, it was decided to work with a titanium based sol-gel, which on decomposition would yield a titania type powder. Paint formulators are used to handling rutile as a pigment and similar compounds were thought to have certain advantages. The organic molecule that was chosen to add to the sol-gel was a complex diamine. Previous studies[6] on corrosion testing by immersion of 3000 series aluminium in 3.5% NaCl solution containing the diamine, had shown that this diamine was indeed an excellent inhibitor for the corrosion of this aluminium alloy, even at levels of 0.01 ppm.
Experimentally, 0.1M of titanium isopropoxide (Aldrich) was mixed with 45 ml ethanol and added to a 1:1 mixture of ethanol/ammonia to give a pH of 9-10. On increasing the temperature to 50<sup>0</sup>C gelation begins at which point the diamine is added as a 20 w% solution in water while maintaining the temperature and adjusting the pH to 9-10.After adding the inhibitor, the mixture was cooled to room temperature and allowed to solidify for one week. At which point, the solid material was ground to 5 micron sized particles suitable for incorporation into a paint.
The initial concern was whether the diamine would be released from the titania and at what rate. Release rates were determined by taking 1g of the titania and stirring into 1L of 3.5% NaCl and extracting a small volume of solution from the flask and using UV Absorption, (Unicam UV Spectrophotometer, Model SP1700) to determine the diamine released. It was found that release was indeed possible, but that the rate of release was extremely rapid. To adjust the release rate, various additives were assessed and finally it was decided to use a natural polyester.
Linseed oil was added as a release moderator at 3, 5 and 7 w% at the stage after addition of the diamine and before cooling. It was found that the linseed oil had a profound effect on release of the diamine. 
Tests are currently underway to assess whether the diamine/sol-gel material is a successful corrosion inhibitive pigment in a paint for aluminium.
