<h4>Introduction</h4>
</dir>


  In order to obtain good corrosion protection and attractive appearance, painted steel sheets are used. Therefore, corrosion resistance after painting is an important consideration. As a method to evaluate the after-painting corrosion resistance, we tried electric potential measurement by the Kelvin method. In recent years, the Kelvin probe technique has been applied for the measurement of corrosion potential<sup>1),2),3)</sup>. This technique has been adapted for non-contact measurement of the corrosion. In the present study, to evaluate the applicability of the Kelvin method to under-film corrosion, we measured the electric potential of the filiform corrosion on acryl- painted steel sheet and investigated the corrosion mechanism.

<dir>

<h4>Experimental</h4>
</dir>


  The steel substrates were painted with clear acryl paint, and the dry film thickness was 5 micrometers. In order to induce filiform corrosion, knife scored specimens were set in a corrosive environment(5%NaCl solution spray and 40  80%R.H.).
  <p>Figure 1 shows the Kelvin scanning equipment. The substrate was part of an xy-stage, whose position was computer controlled. As the probe (vibrating reference electrode) an NiCr steel was used. The probe tip diameter was 150 micrometers. The z-position of the probe was also computer controlled. The vibrating reference electrode was calibrated versus the standard hydrogen electrode (SHE) measuring the electric potential difference to a Cu/Cu<sup>2+</sup> electrode. The measurement chamber was provided with gas inlet and outlet ports. During the analysis with the scanning Kelvin probe the humidity in the measurement chamber was kept over 90%.
  <p>In order to maintain a constant distance between the surface of specimen and the probe tip, surface profile measurement was made. After the profile measurement, an electric potential measurement was made of the same area. The tip-to-surface working distance was approximately 50 micrometers.

<dir>

<h4>Results and Discussion</h4>
</dir>


  Figure 2 shows the appearance of filiform corrosion after one week of exposure in the corrosive environment(5%NaCl solution spray and 40  80%R.H.). It is possible to observe the under-film corrosion, because the paint is clear. The line at the center is the knife score. It was observed that filiform corrosion grew in both directions from the score line. The filiform corrosion track is composed of two parts: the head and the tail. Blue areas are observable at the head, which is filled with a heavy FeCl<sub>2</sub> solution<sup>4)</sup>. The tail is filled with corrosion products.
  <p>The scanning Kelvin probe technique was applied to measure the electric potential of filiform corrosion inside the area surrounded by the white line in Figure 2. Figure 3 shows the profile and electric potential distribution of filiform corrosion: (a) is the profile, and (b) is the electric potential distribution. Contour lines are drawn at a pitch of one micrometer in Figure 3(a). Contour lines are drawn at a pitch of 30 mV in Figure 3(b). The comparison of Figure 3(a) and Figure 2 shows that they correspond well. Besides, comparing the profile in Figure 3(a) with the electric potential distribution in Figure 3(b), the electric potential on the head of filiform corrosion is less noble. This less noble area shows anodic site of the corrosion.

<dir>

<h4>Conclusions</h4>
</dir>


  Using the scanning Kelvin probe, the potential on the head of filiform corrosion was detected as less noble which showed anodic site. It is possible, with this technique, to measure under-film corrosion.

