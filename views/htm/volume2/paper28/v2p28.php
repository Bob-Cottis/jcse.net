<SCRIPT>notify();</SCRIPT><SCRIPT>comment(1)</SCRIPT>  <h3>Abstract</h3>
  <p>The paper describes the influence of environmental factors such as:
  ultraviolet radiation, thermal shocks, aggressive media (5% water solutions
  of: sulphuric acid, potassium hydroxide and sodium chloride) as well as
  weathering on the course of mechanical characteristics of an epoxide coating.
  It was stated that ageing processes that occur in the epoxide coating cause
  deterioration of its mechanical properties, such as: hardness, tensile
  strength as well as unit elongation. They also influence changes in mechanical
  characteristics obtained by means of dynamic mechanical thermal analyse, i.e.
  elongation and dynamic storage modulus E&#146;.</p>
  <b>
  <SCRIPT>comment(2)</SCRIPT><p>Keywords: </b>epoxide coating, ageing, mechanical properties</p>
  <h3>Introduction</h3>
  <SCRIPT>comment(3)</SCRIPT><p>Optimal choice of organic coatings that protect technical equipment &#150;
  during the design process &#150; requires a knowledge of wear processes of these
  coatings. Therefore, it is necessary to carry on model (accelerated) tests in
  order to predict the coating service life because multi-layer metal-organic
  coatings perform their protective role for a long time, even for fifty years.
  Unfortunately, there is not yet developed an universal accelerated test and it
  is not found the correlation between results of accelerated tests and
  weathering. Such situation results from a non-sufficient knowledge of
  destruction processes of an organic coating under the influence of ambient
  factors. The multicriterion investigations of physico-chemical properties of
  organic coatings aged under various conditions that are carried out by the
  author complete the knowledge from this field. The paper describes the effects
  of destruction of epoxide coatings aged with ultraviolet radiation, thermal
  shocks, aggressive media and weathering. The effect of these factors on the
  course of mechanical characteristics was estimated as well. The author uses
  the investigation results for prediction the organic coating service life.</p>
  <h3>Investigation Methods</h3>
  <b>
  <h4>Materials and Sample Preparation</h4>
  </b>
  <SCRIPT>comment(4)</SCRIPT><p>The model investigated coating was made of chemically resistant epoxide
  paint. The main components of this paint were: epoxy resin Epidian 119,
  pigments (iron oxide red), fillers (barium sulphate, microtalk) and diluent (ethyloglicol).
  Polyaminoamide was used as the curing agent. Its mass fraction (calculated
  stoichiometrically) amounted 30% of the whole composition. After addition of
  the curing agent the paint was stirred for half an hour and then, after two
  hours, the coating application started. Three-layer coatings were obtained by
  means of air operated spraying on the polyester sheeting as an apparent base.
  The layers were cured in two stages: at 20<sup>o</sup>C for 24 hours and at
  120<sup>o</sup>C for 0.5 hour. The samples were acclimatised before testing
  (according to the Polish Standard PN-86/C-81510) for 10 days at ambient
  temperature 20<sup>o</sup>C and at relative humidity (65�5)%.</p>
  <b>
