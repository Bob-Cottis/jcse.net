  <h3>Conclusions</h3>
  <b>
  <h4>Run of mechanical characteristics of the epoxide coating substantially
  depends on the ageing kind.</h4>
  </b>
  <SCRIPT>comment(30)</SCRIPT><p>Ageing of the epoxide coating causes deterioration of its mechanical
  properties, such as: hardness, static tensile strength, unit elongation. It
  also influences changes in mechanical characteristics obtained using dynamic
  mechanical thermal analyse, i.e. elongation and dynamic storage modulus E&#146;.</p>
  <SCRIPT>comment(31)</SCRIPT><p>Embrittlement of epoxide coatings increases with ageing time. It is also
  affected by the kind of ageing. The coating aged with ultraviolet radiation
  showed the highest embrittlement. It resulted from significant oxidation
  degree of the epoxide.</p>
  <SCRIPT>comment(32)</SCRIPT><p>As a result of ageing of the epoxide, pigment and filler particles lose
  coherence with it and are spalled from the coating structure. This results in
  an increase of the coating roughness. The coating aged with ultraviolet
  radiation showed the highest roughness.</p>
