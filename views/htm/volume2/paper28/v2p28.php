  <h3>Effect of Ultraviolet Radiation on Chemical Ageing of the Epoxide Coating</h3>
  <SCRIPT>comment(9)</SCRIPT><p>The changes occuring with time in the chemical structure of the epoxide
  coating superficial layer were studied in infrared spectroscopy
  investigations. It was found an increase of absorption of infrared radiation
  in the range 1800-1392&nbsp;cm<sup>-1</sup> what indicates ageing processes of
  the epoxide. The band 1800-1552&nbsp;cm<sup>-1</sup> included in this range
  represents oxidation products [1�3]. It contains the band that represents
  carbonyl group vibration. In case of the epoxide coatings aged with thermal
  shocks, weathering and with 5% water solutions of sodium chloride or potassium
  hydroxide it was stated an increase in carbonyl group intensity in the band
  1807-1700 cm<sup>-1</sup> &#150; with the maximum at 1740 cm<sup>-1</sup>.
  Coatings aged with ultraviolet radiation were characterised by the highest
  carbonyl group intensity (in the whole measured range) and the band
  representing these groups was 1807-1640&nbsp;cm<sup>-1</sup> &#150; with the
  maximum at 1717 cm<sup>-1</sup> [1]. The band of carbonyl groups was not found
  in the range of 1807-1640&nbsp;cm<sup>-1</sup> in case of unaged coatings and
  coatings aged with 5% water solution of sulphuric acid [1]. The increase of
  infrared absorption observed in the bands 1472-1424&nbsp;cm<sup>-1</sup> and
  1391-1344 cm<sup>-1</sup> corresponds to the increase of amount of CH<sub>3</sub>
  groups that are formed as a result of bond cleavage in epoxide. Increase of
  infrared absorption in the band 1422-1392 cm<sup>-1</sup> corresponds to the
  increase of amount of CH<sub>2</sub> groups (adjacent to carbonyl groups) what
  gives an evidence of epoxide oxidation progress [2]. The increase of intensity
  of the band having the maximum at 3400&nbsp;cm<sup>-1</sup> with ageing time
  was also found. This band represents valence vibration of hydroxyl group [1,
  3].</p>
  <SCRIPT>comment(10)</SCRIPT><p>It results from the total absorbance characteristics obtained with the use
  of infrared spectroscopic investigations that the ageing changes in the
  chemical structure of the superficial layers of the epoxide coating are
  non-homogenous. The most intensive ageing is due to ultraviolet radiation. The
  profilometric and microscopic investigations proves, however, a non-homogenous
  physical wear (cracking, pigment and filler spalling) of the epoxide coating
  surface under the influence of ambient factors. The results of microscopic
  investigations allow to state that number and size of the cracks depend on the
  ageing kind. For instance, cracks generated under the influence of 5% water
  solution of sulphuric acid were, in general, in the shape of rings with the
  diameter of 40 to 90 &mu;m
  m (for 1008 h ageing) situated around blisters. These cracks showed tendency
  to become larger with ageing time. However, cracks generated under the
  influence of other media were in the shape of bands 150 to 190 &mu;m long and 2.5 to 10 &mu;m wide. 
  In the case of ultraviolet
  radiation action, number and size of the cracks increased with ageing time.
  Moreover, these cracks were characterised by silver shines in the reflected
  light of an optical microscope what was observed in case of weathering too.
  Shape and shine of these cracks were similar to the &quot;silver cracks&quot;
  described by I.&nbsp;Narisava [4] but they were ten times wider.</p>
  <SCRIPT>comment(11)</SCRIPT><p>Substantial chemical and physical ageing effects of investigated epoxide
  coatings was caused by the action of 5% water solution of sulphuric acid as
  well as UV radiation what, in particular, can be seen analysing the courses of
  thermogravimetric characteristics. The greatest decreases of the decomposition
  start temperature as well as the most intensive thermal decompositions
  (Fig.&nbsp;1) were found for these ageing kinds in the whole ageing range (the
  measurements were done after 240, 360, 744 and 1008 hours).</p>
