  <font size="2">
  <p>This paper is most interesting. I only have a few minor comments and
  queries.</p>
  <p>I am curious about the hydrocarbon resin used to modify the epoxy.</p>
  <ul>
    <li>What is it?</li>
    <li>How much is added to the epoxy?</li>
    <li>Does it also have an effect on the rates of cathodic disbonding?</li>
  </ul>
  <p>In the paragraph near the end starting &quot;pigments in general...&quot; I
  would ask whether the authors have any evidence either experimental or from
  the literature to back up their claim that pigments effect the curing of the
  binder.</p>
  <p>In all the systems examined, the coatings contained significant quantities
  of filler. I wonder whether the authors have examined the specific effects of
  these fillers by carrying out cathodic disbonding tests on unfilled (clear)
  systems.</p>
