  <SCRIPT>notify();</SCRIPT><SCRIPT>comment(1)</SCRIPT><h3>Abstract</h3>
  <p>The effect of aluminium and glass barrier pigments on cathodic disbonding
  of epoxy coatings has been studied. Over a two years period, cathodic
  disbonding has been measured for nine model coatings with various
  concentrations of aluminium or glass pigments. The binder in the model
  coatings was either epoxy or epoxy mastic. The test specimens were exposed in
  natural seawater and polarised to -1050 mV SCE with zinc anodes. The pigments
  had a large effect on cathodic disbonding. Coatings without barrier pigments
  disbonded significantly during the test period. Coatings with 10% aluminium
  pigments or more did not show any sign of disbonding until the end of the
  test. Then some samples had about 3 mm disbonding around the damage in the
  coating. Coatings with 10% or more glass pigments had a very long delay time
  before disbonding started, but disbonded severely after that. Possible
  mechanisms of the effects are discussed.</p>
  <SCRIPT>comment(2)</SCRIPT><p><b>Keywords: </b>Organic coatings, cathodic disbonding, aluminium pigments,
  glass flake pigments.</p>
  <h3>Introduction</h3>
  <SCRIPT>comment(3)</SCRIPT><p>Cathodic disbonding is one of the most important mechanisms for loss of
  adhesion between coating and steel. The problem has lately received new
  attention due to the increased use of coatings in combination with cathodic
  protection of offshore oil production installations. Resistance against
  cathodic disbonding is an important property of the coating systems to be used
  on submerged structures. E.g. the NORSOK standard for development and
  operation of oil fields in the Norwegian sector use resistance against
  cathodic disbonding as a criterion for prequalification of coatings for this
  application [1].</p>
  <SCRIPT>comment(4)</SCRIPT><p>The purpose with this paper is to examine the effect of flake shaped glass-
  and aluminium pigments on cathodic disbonding. Model coatings with various
  contents of glass and aluminium pigments have been prepared and tested for
  cathodic disbonding. The conditions we have used are close to the conditions
  found on offshore installations in the North Sea. We have found that both
  glass and aluminium pigments improve the resistance of the coatings against
  cathodic disbonding. The effect was found with two different binders which
  indicate that this is a general effect, independent of the binder. We have
  outlined two possible mechanisms for the effect. The first possibility is
  improved barrier properties of the coating. Cathodic disbonding depends on
  transport of reactants through the coating. The barrier pigments may then
  impede the transport, which may slow down the cathodic disbonding. The other
  possibility we have looked at is buffering reactions with the pigments.
  Leidheiser <i>et al.</i> have suggested that the aluminium may reduce the pH
  at the coating/substrate interface and thereby decrease cathodic disbonding
  [2].</p>
  <SCRIPT>comment(5)</SCRIPT><p>Aluminium pigments have been widely used in engineering paints and
  protective coatings for many years. The aluminium pigments are shaped as
  flakes in different sizes. The pigments we have used are about 20 - 40 �m
  wide and 1 �m thick. This geometry, along with their metallic nature, is
  responsible for most of their properties in protective paints. The aluminium
  pigments improve the barrier properties of protective paints [3,4]. From a
  corrosion protection point of view, their ability to impede the transport of
  water and oxygen is the most important property. The pigments are impermeable
  to water and oxygen, and the diffusion is forced to go around the particles.
  This may increase the diffusion path significantly and thereby decrease the
  permeability through the paint [3]. In addition the pigments also act as
  barriers to radiation, as they reflect both IR, UV and visible light [3]. This
  increases the stability of the paint, and may also reduce heat radiation from
  the substrate. Aluminium pigments are commercially available as leafing or
  non-leafing grades. The leafing pigments float to the surface of the applied
  film, while the non-leafing pigments are evenly distributed in the film after
  application. The different behaviour is due to surface treatment of the
  pigments with different fatty acids.</p>
  <SCRIPT>comment(6)</SCRIPT><p>Glass flake pigments are also used in protective coatings, but not to the
  same extent as aluminium flake pigments. They are produced by breaking thin
  glass (about 5�m thick) into small flakes, about 100 - 400 �m wide. Their
  relatively large size restricts them to be used in very thick coatings only.
  The shape and impermeability to water and oxygen suggest that glass flakes
  will improve the barrier properties of the coating. Glass flake pigments have
  received little attention in the literature. Foster <i>et al</i>. have
  described some of the basic characteristics of glass flake filled coatings
  [5]. El-Sawy <i>et al.</i> have tested the effect of glass pigments in a
  scratch test and an environmental exposure test [6]. However, the glass
  pigments they used gave no positive effect on the performance of the coatings.</p>
  <SCRIPT>comment(7)</SCRIPT><p>The mechanism for cathodic disbonding is not known in every detail.
  However, it is widely accepted in the literature that high pH at the
  steel/coating interface is responsible for the disbonding [2,7,8]. The high pH
  is produced by the cathodic reaction on the steel surface under the coating.
  Structures in seawater protected by zinc- or aluminium anodes are polarised to
  between -800 and -1100 mV SCE. At these potentials the cathodic reaction can
  be both reduction of oxygen and hydrogen evolution:</p>
