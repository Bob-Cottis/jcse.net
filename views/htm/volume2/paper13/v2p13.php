  <SCRIPT>comment(42)</SCRIPT><p>Glass is also known to be unstable in highly alkaline solutions, but this
  requires very high pH, and will probably not affect the pH under the paint
  film significantly. The fact that the glass pigments also decreased the
  disbonding weigh against the active pigment theory. However, the two
  mechanisms may work in combination. Both aluminium and glass pigments may act
  as barriers. The aluminium pigments was more effective than the glass pigments
  in decreasing cathodic disbonding, which may be due to the buffer effect in
  addition to the barrier effect. Both the barrier mechanism and the buffer
  mechanism will be investigated in detail in Part 2.</p>
  <h3>Conclusions</h3>
  <SCRIPT>comment(43)</SCRIPT><p>Aluminium and glass barrier pigments decreased cathodic disbonding
  substantially for two different epoxy coatings. After exposure in natural
  seawater for two years, coatings with 10% aluminium pigments gave about 3 mm
  disbonding, while the coatings without the aluminium pigments disbonded about
  65 mm. Glass flake pigments were less effective than aluminium pigments in
  decreasing cathodic disbonding, but still they improved the performance of the
  coating significantly. The aluminium- and glass pigments act as barriers to
  diffusion of oxygen and water through the film. However, if transport of
  cations under the disbonded film is rate limiting, the barrier mechanism alone
  can not explain the reduced disbonding rate. For the aluminium pigments, a
  mechanism where the aluminium acts as a buffer that reduces the pH at the
  steel/coating interface is possible.</p>
