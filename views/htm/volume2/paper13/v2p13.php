  <h3>Results and Discussion</h3>
  <h4>Effect of aluminium pigments on cathodic disbonding</h4>
  <SCRIPT>comment(19)</SCRIPT><p>Figure 2 shows the disbonding distance in the long term test as a function
  of time, for the amide cured epoxy coatings with aluminium pigments. 15
  samples of each model paint were prepared, and cathodic disbonding was
  measured during a two years period. With increasing time intervals, 1-3
  samples was removed and analysed. The aluminium pigments were highly effective
  in improving the resistance of the coating towards cathodic disbonding. Epoxy
  10 and Epoxy 20 had not disbonded at all after 18 months of exposure. After
  two years Epoxy 10 had some disbonding, and about 3 mm of the coating around
  the holiday could be removed. Epoxy 5 disbonded about 20 mm and Epoxy 0
  disbonded about 65 mm in the same period of time.</p>
  <p><a href="../paper13/Knudsen%20part%201%20fig2.gif"><img src="{{ $htmURL }}Image304.gif" width="488" height="299"></a></p>
  <SCRIPT>comment(20)</SCRIPT><p><b><a name="_Ref457212386">Figure 2</a>. Cathodic disbonding of epoxy
  coating with various concentrations of aluminium pigments. Coatings applied on
  blast cleaned steel (Sa 21/2). Experimental conditions: Natural seawater,
  8-13�C, polarised with zinc anodes to -1050 mV SCE.<script>ifscreen('(Click for a larger image)')</script></b></p>
  <SCRIPT>comment(21)</SCRIPT><p>The aluminium not only affected the rate of disbonding, but also the degree
  to which the coating lost adhesion. For Epoxy 5, it was much harder to remove
  the disbonded coating, compared to Epoxy 0. This was also seen when we looked
  at the metal surface in a microscope after removing the disbonded coating.
  Under Epoxy 5 we found more &quot;islands&quot; where the coating still
  adhered to the substrate, compared to Epoxy 0, where very little of the binder
  was left on the steel surface.</p>
  <SCRIPT>comment(22)</SCRIPT><p>Figure 3 shows the amount of coating still found on the steel surface after
  removing the disbonded coating on samples of Epoxy 0 and Epoxy 5. The pictures
  were created and analysed in an electronic image analyser. The sample in
  Figure 2A was coated with Epoxy 5 and exposed for 18 months. The radius of the
  disbonded circle is about 32 mm. The quantitative analysis showed that
  remainings of the coating covered about 18% of the disbonded area. The sample
  in Figure 2B was coated with Epoxy 0 and exposed for one year. The radius of
  the disbonded circle is about 120 mm. For this coating only 4% of the
  disbonded area was covered with remainings of the binder.</p>
  <p><a name="_Ref408998562" href="../paper13/Knudsen%20part%201%20fig3.jpg"><img src="{{ $htmURL }}Image305.jpg" width="450" height="251"></a></p>
  <SCRIPT>comment(23)</SCRIPT><p><b><a name="_Ref457212468"></a>Figure 3. Pictures of the disbonded area
  after removing the disbonded coating taken with an electronic image analyser.
  The white spots are remainings of the binder. A: Epoxy 5 exposed for 18
  months, 32mm disbonded diameter. B: Epoxy 0 exposed for one year, 120 mm
  disbonded diameter.<script>ifscreen(' (Click for a larger image)')</script></b></p>
  <SCRIPT>comment(24)</SCRIPT><p>The coatings with high aluminium concentrations had little or no disbonding
  after two years in the long term test. To get quantitative information about
  how much the aluminium pigments decreased cathodic disbonding, we also tested
  the amide cured epoxy coatings in a short term test on ground steel. In
  general the rate of cathodic disbonding increases when the roughness of the
  substrate decreases [8]. Epoxy 10 and Epoxy 20 disbonded faster on ground
  steel. Figure 4 shows the rate of cathodic disbonding on ground steel as
  function of aluminium content. The disbonding rate for Epoxy 10 and Epoxy 20
  is about the same, but more than two decades slower than for Epoxy 0. The time
  dependence of the disbonding is different from the parabolic kinetics that
  usually is found, e.g. in Figure 2. This may indicate that some other
  mechanisms are involved, but we have not been able to investigate this.
  However, the results clearly demonstrate effect of the aluminium pigments.</p>
  <SCRIPT>comment(25)</SCRIPT><p>The surface roughness had a large effect on the disbonding rate. On ground
  steel substrate (R<sub>y</sub> = 5 �m) Epoxy 0 disbonded 25 mm in about one
  day. On blast cleaned steel (R<sub>y</sub> = 76 �m) it took about three
  months to get the same disbonded distance. Watts and Castle have also tested
  the effect of substrate roughness on the rate of cathodic disbonding [8]. They
  suggested that the effect of the substrate roughness was due to a change in
  the diffusion path length under the coating. As the substrate roughness
  increased the diffusion path length between the holiday and the disbonding
  front would increase. They found a reasonable correlation between the
  disbonding rate and the actual length of the surface profile. The effect may
  also be due to an increase in the contact area between the coating and the
  substrate, when the surface roughness increases [16]. When we measured
  cathodic disbonding on a substrate that had been abraded in one direction
  only, the disbonded distance was always larger along the abrasion lines. Jin<i>
  et al. </i>found the same [16]. This observation favours the mechanism
  suggested by Watts and Castle.</p>
  <p><a href="../paper13/Knudsen%20part%201%20fig%204.gif"><img src="{{ $htmURL }}Image306.gif" width="488" height="299"></a></p>
  <SCRIPT>comment(26)</SCRIPT><p><b><a name="_Ref457212493">Figure 4</a>. Cathodic disbonding of epoxy
  coating with various concentrations of aluminium pigments. Coatings applied on
  ground steel substrate. Experimental conditions: Artificial seawater, 25�C,
  -1050 mV SCE.<script>ifscreen(' (Click for a larger image)')</script></b></p>
  <SCRIPT>comment(27)</SCRIPT><p>The amide cured epoxy has low commercial interest due to the high content
  of solvents. The epoxy mastic binder was therefore included in the test to
  represent a more commercially interesting binder. The disbonding distances for
  the epoxy mastic coatings are given in Figure 5. Pre-treatment, application,
  curing and exposure were similar to the long term test of the amide cured
  epoxy. The unpigmented epoxy mastic gave about 8 mm disbonding in four months,
  but then the coating started to blister randomly all over the sample. After
  that, cathodic disbonding spread from the blisters, and it became impossible
  to measure the disbonded distance from the holiday. After about one year the
  whole sample had disbonded. We would like to emphasise that this is not a
  general problem with epoxy mastics. We have tested several commercial epoxy
  mastics which have perform much better than this.</p>
  <SCRIPT>comment(28)</SCRIPT><p>The blistering of Mastic 0 was independent of the initial coating holiday,
  and was even found on the back of the samples. Inside the blisters we found a
  highly alkaline liquid, which means that the blistering was caused by the
  cathodic reaction under the coating. The blistering was therefore also a kind
  of cathodic disbonding. It then seems that there are two different disbonding
  mechanisms, one mechanism associated with a mechanical damage in the coating,
  and another mechanism independent of any mechanical damage. This is in
  accordance with Type I and Type II failure described by Watts and Castle [26].
  Type I failure was associated with downward diffusion of the cation via
  conductive pathways in the film, corresponding to the random blistering of our
  coatings. Type II failure was associated with transport of the cation under
  the disbonded coating from a mechanical damage in the film. For the epoxy
  mastic coatings the aluminium pigments had a positive effect on both types of
  failure.</p>
  <p><a href="../paper13/Knudsen%20part%201%20fig5.gif"><img src="{{ $htmURL }}Image307.gif" width="488" height="299"></a></p>
  <SCRIPT>comment(29)</SCRIPT><p><b><a name="_Ref457212556">Figure 5</a>. Cathodic disbonding of epoxy
  mastic coating with various concentrations of aluminium and glass pigments.
  Coatings applied on blast cleaned steel (Sa 21/2). Natural seawater, 8-13�C,
  -1050 mV SCE.<script>ifscreen(' (Click for a larger image)')</script></b></p>
  <SCRIPT>comment(30)</SCRIPT><p>As for the amide cured epoxies, the epoxy mastics with 10% and 20%
  aluminium pigments (Mastic 10 and 20) had no cathodic disbonding at all after
  18 months exposure. No blistering or other signs of coating deterioration were
  observed either. Due to the low content of solvent, the concentration of
  aluminium in the dry film was lower than for the corresponding amide cured
  epoxies. Since the unpigmented coating performed so badly, the aluminium
  pigments had a tremendous effect on both random blistering and cathodic
  disbonding.</p>
  <SCRIPT>comment(31)</SCRIPT><p>In the amide cured epoxy coatings the pigment volume concentration (PVC)
  varied from 22% in Epoxy 0 to 31% in Epoxy 20 (Table 1). We could therefore
  suspect that the effect of the aluminium just was a general effect of the
  increased PVC. However, for the epoxy mastic coatings the PVC was 33% for all
  the five paints. The effect of the pigments on cathodic disbonding can
  therefore not just be a general effect of the PVC.</p>
  <h4>Effect of glass pigments on cathodic disbonding</h4>
  <SCRIPT>comment(32)</SCRIPT><p>Due to spark hazard, the classification companies have set a limit to the
  concentration of aluminium in marine paints to maximum 10% by weight in the
  dry film [27]. Glass flake pigments were therefore also tested with the epoxy
  mastic, as an alternative barrier pigment. The disbonding of these coatings is
  also shown in Figure 5. The glass flakes inhibited the disbonding compared to
  the unpigmented coating, but not as much as the aluminium pigments. After 4
  months some disbonding was found on Mastic 10G. For Mastic 20G disbonding was
  first found after a year of exposure. After 9 - 10 months disbonding started
  from random points all over Mastic 10G, and after one year it was impossible
  to measure the disbonded distance from the holiday. After 18 months about 70%
  of the coating had disbonded. For Mastic 20G some random initiation of
  disbonding was first found after 18 months.</p>
  <SCRIPT>comment(33)</SCRIPT><p>The random initiation of cathodic disbonding of Mastic 10G and Mastic 20G
  was probably due to opening of conductive pathways through the coating, as for
  the blistering of Mastic 0. The reason why Mastic 10G and Mastic 20G did not
  develop blisters may have been a reinforcing effect of the glass flake
  pigments. The random initiation of disbonding for the two films was therefore
  also a Type I failure, in the terminology by Watts and Castle . Since this
  type of failure was delayed by the glass flake pigments may, the pigments may
  have delayed the process of opening conductive pathways through the film.</p>
  <h4>Mechanism for the effect of the pigments</h4>
  <SCRIPT>comment(34)</SCRIPT><p>Figure 6, 7 and 8 show SEM micrographs of cross sections of Mastic 0,
  Mastic 10A and Mastic 10G. Immediately after application, the flake shaped
  aluminium and glass pigments mainly orient themselves parallel to the
  substrate. In Figure 7 and Figure 8 they therefore appear as needles aligned
  parallel with the substrate. The irregular shaped particles in all three films
  is the filler. The aluminium and glass flakes are assumed to improve the
  barrier properties of the coating by increasing the diffusion pathway through
  the coating [3]. The diffusing species (oxygen, water and ions) are forced to
  go around the impermeable particles, and this may increase the diffusion
  distance several times. The use of barrier pigments can therefore be seen as
  an alternative to increasing the film thickness. However, the aluminium and
  glass flakes may also increase the diffusion through the coating. If some
  pigment flakes are aligned perpendicular to the substrate they may introduce
  effective pathways for diffusion through the coating. This is extra risky with
  the glass pigments since they are so large that one pigment flake may
  penetrate the whole film. Coatings with such pigments must therefore always be
  applied thicker than the diameter of the largest pigments.</p>
