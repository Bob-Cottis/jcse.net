  <SCRIPT>comment(38)</SCRIPT><p>Pigments in general, and flake shaped pigments in particular, may also
  affect the curing of the binder. If the pigments impede diffusion, the curing
  agent may not be able to diffuse to the reactive sites on the binder chains
  around the pigment particles. This may then leave a partly cured resin around
  the pigment particles, which may serve as preferred routes for diffusion. If
  the resin does not adhere properly to the pigments small voids may be formed
  around the particles. This may also increase the diffusion through the
  coating.</p>
  <SCRIPT>comment(39)</SCRIPT><p>The initiation of cathodic disbonding from random points underneath the
  glass pigmented films may have been caused by the pigment flakes. They were
  wider than the film thickness, and some of them may have been aligned
  perpendicular to the substrate as described above. However, the films were
  applied in two coats, which should reduce the chance for pigments to penetrate
  the film. Since the unpigmented film performed so badly, it is more likely
  that the random initiation was due to the binder.</p>
  <SCRIPT>comment(40)</SCRIPT><p>The barrier effect of the glass and aluminium flake pigments is the most
  obvious explanation for their effect on cathodic disbonding. The effect of the
  barrier pigments can be compared to the effect of increasing the film
  thickness, which also has been shown to decrease cathodic disbonding
  [2,16,18]. The effect of the aluminium pigments can also involve another
  mechanism. Leidheiser <i>et al.</i> have suggested that the oxide on the
  surface of the pigments may act as a buffer [2]. The Pourbaix diagram for
  aluminium shows that aluminium corrodes at pH higher than about 9. The oxide
  on the surface dissolves in high pH environment and can not protect the metal.
  The reaction removes hydroxide from the electrolyte, and this may reduce the
  pH at the steel/coating interface:</p>
