  <SCRIPT>comment(8)</SCRIPT><p>Leidheiser have shown that above -1000 mV SCE the oxygen reduction
  dominates, while below this potential the hydrogen evolution dominates [9].
  However, in the cathodic areas under the coating the potential is probably
  well above -1000 mV SCE, due to the high resistance between the anode and the
  cathode. Leidheiser have therefore concluded that the oxygen reduction is the
  predominant reaction under the coating [9].</p>
  <SCRIPT>comment(9)</SCRIPT><p>Ritter <i>et al.</i> have measured the pH under the disbonded coating and
  found that it could be as high as 14 [10]. The exact consequence of the
  alkaline water film is not known, but three theories have received most
  attention. The three mechanisms are based on different locus of failure: in
  the iron oxide on the steel surface [2,10], in the coating [11,12] or at the
  interface [8,13].</p>
  <SCRIPT>comment(10)</SCRIPT><p>The transport of reactants for the cathodic reaction under the coating has
  also been subject to much research. Most paint films are permeable to both
  oxygen and water [14]. It is therefore reasonable to assume that these
  reactants mainly are transported through the film. Leidheiser <i>et al.</i>
  have also shown this experimentally [2]. For cations, the conclusions are less
  clear. Several researchers have concluded that the transport of cations goes
  laterally from the holiday under the disbonded coating [2,15]. The time
  dependence of the disbonding is an indication for this. The disbonded area is
  usually found to increase proportionally with time, i.e. the disbonded
  distance from the holiday in the coating is proportional with the square root
  of time [2,15-17]. As the disbonded area increases, the distance for transport
  of ions under the disbonded film to the disbonding front also increases.
  Therefore the resistance to the transport of cations increase and the rate of
  disbonding will decrease. However, there are also those who have found
  indications for transport of cations through the coating. Skar measured the
  disbonding of coatings with film thickness between 100 and 300 �m and found
  that the disbonding rate decreased linearly with film thickness [18]. He also
  found that the transport of cations was rate limiting. He therefore suggested
  that the transport of cations went through the disbonded film. Jin<i> et al. </i>[16],
  Leidheiser [19] and Parks <i>et&nbsp;al. </i>[20]<i> </i>have also concluded
  that cations might be transported through the coating when the specimen is
  under cathodic polarisation.</p>
  <SCRIPT>comment(11)</SCRIPT><p>Many researchers have studied the rate determining step in cathodic
  disbonding. Leidheiser, among others, have shown that the transport of oxygen
  through organic coatings is much lower than the transport of water [21].
  Oxygen may therefore for some coatings be rate limiting for cathodic
  disbonding. However, for most coatings researchers have found that the
  transport of cations are rate limiting. When cathodic disbonding is measured
  in different electrolytes (LiCl, NaCl, KCl, CsCl etc.), they have found that
  the rate of disbonding is linearly proportional to the mobility of the ions in
  water [2,15,18,22].</p>
  <h3>Experimental</h3>
  <SCRIPT>comment(12)</SCRIPT><p>We have used model coatings with two different binders: an amide cured
  epoxy and an amine cured epoxy modified with a hydrocarbon resin (epoxy
  mastic). The epoxy binder was pigmented with different concentrations of
  aluminium flake pigments. The epoxy mastic binder was pigmented with either
  aluminium flake pigments or glass flake pigments. The aluminium pigments we
  have used are about 20 - 40 �m wide and 1 �m thick. The glass flake pigments
  were up to 400 �m wide and 10 �m thick. Information about composition and
  pigmentation of the paints is given in Table 1 and 2.</p>
  <SCRIPT>comment(13)</SCRIPT><p>Cathodic disbonding for all nine model coatings was tested in a long term
  test. The substrate was 200 x 200 x 5 mm steel plates cut from mild steel (DIN
  17100). The composition of the steel was 0.10% C, 0.10% Si, 0.52% Mn, 0.14% P,
  0.014% S, 0.23% Cr and Fe to balance. They were blast cleaned with steel grit
  to Sa 21/2 [23] and medium roughness (R<sub>y</sub> = 76 � 7 �m) [24]. The
  paints were applied by airless spraying in two coats. The first coat cured for
  about 24 hours at room temperature before the second coat was applied. After
  that, the samples cured for 55 days at ambient temperature and humidity before
  testing. Coating thickness was measured with a magnetic gauge. The accuracy of
  the instrument is better than 10%. The dry film thickness is given in Table 1
  and 2. The samples were given a circular holiday in the middle, 6 mm in
  diameter, to start the cathodic disbonding process. They were exposed in
  continuously running natural seawater at 8-13�C. Galvanic coupling to zinc
  anodes gave cathodic polarisation to about -1050 mV SCE in the coating
  holiday. The experimental setup is shown in Figure 1A. Cathodic disbonding was
  measured destructively by gently lifting the disbonded paint film off the
  steel substrate with a scalpel. The diameter of the disbonded circle was
  measured in four directions and the disbonded distance was calculated from the
  average diameter.</p>
