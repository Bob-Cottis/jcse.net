<SCRIPT>notify();</script><SCRIPT>comment(1)</SCRIPT>  <h4>Abstract</h4>
  <p>Electro-polymerisation of phenol derivatives on zinc-coated steel in
  hydro-alcoholic media has been investigated.&nbsp; Thin, adherent poly-phenylene
  oxide (PPO) films form on the zinc surface without treatment (untreated zinc)
  by anodic oxidation of 2-allylphenol, provided the electrolytic solution
  contains a large amount of amine.&nbsp; On the other hand, under the same
  condition, they do not form on the zinc surface that is subjected to a
  passivation treatment in alkaline solution containing sulphide ions (S treated
  zinc) before the electrolysis.&nbsp; Control of passivation film formation on
  zinc surfaces is required to obtain good electro-polymerised films by anodic
  oxidation.</p>
<SCRIPT>comment(2)</SCRIPT>  <p><b>Keywords: </b>Electro-polymerisation, Poly-phenylene oxide film,
  Zinc-coated steel, Phenol derivative, Amine</p>
<SCRIPT>comment(3)</SCRIPT>  <h3>Introduction</h3>
  <p>Chromate materials have been used for painted zinc-coated steel in
  pre-treatments and/or undercoats to improve its corrosion resistance and paint
  adhesion.&nbsp; However, because of environmental concerns, use of harmful
  materials such as chromate is being controlled and banned.&nbsp; This has lead
  to vigorous research for finding alternatives to chromate for improving the
  product lifetime.</p>
<SCRIPT>comment(4)</SCRIPT>  <p>As one of the possible alternatives, the application of thin polymer films
  as pre-treatments has been examined [1].&nbsp; The relation of the required
  properties with the composition of the polymer film such as variety and
  density of functional groups is essential.&nbsp; However it has not been fully
  researched so far because it is difficult and time-consuming work to prepare
  polymers with various composition changes by conventional methods.&nbsp; From
  this viewpoint, electro-polymerisation can be a useful polymer producing
  process.</p>
<SCRIPT>comment(5)</SCRIPT>  <p>Electro-polymerisation is a polymer coating process in which polymerisation
  of monomeric materials and coating formation by the polymer produced is
  conducted at the same time on metal electrode surface electrochemically.&nbsp;
  One of the biggest benefits of this process is that a wide variety of chemical
  compositions can be obtained because, unlike polymers, monomeric materials are
  far easier to dissolve in usual common solvents and this leads to unique films
  unobtainable by the conventional processes.&nbsp; In addition, better
  reproducibility can be obtained because the reaction is electrically
  controlled.</p>
  <SCRIPT>comment(6)</SCRIPT><p>The application of thin electro-polymerised films on steel as adhesion
  enhancing pre-treatments has been examined [2].&nbsp; However, although zinc
  is one of the most useful materials, little work has been done so far because
  zinc is so active that it is difficult to obtain polymer films on it,
  especially by anodic oxidation [3,4].&nbsp; In this paper, we discuss the
  preparation of thin electro-polymerised films onto zinc-coated steel and also
  examine the effect of surface treatment on electrolysis, comparing the zinc
  without treatment (<i>untreated zinc</i>) and the zinc subjected to the
  sulphide passivation treatment (<i>S treated zinc</i>) that has been
  successfully used for electro-polymerisation of pyrrole [4].</p>
  <h3>Experimental</h3>
  <SCRIPT>comment(7)</SCRIPT><p>All chemicals were purchased from Aldrich and used without further
  purification.&nbsp; Electro-galvanised steel sheet (thickness: 1.0mm, coating
  amount: ca. 20g/m<sup>2</sup>) was used as the substrate.&nbsp; They were
  covered by a thick layer of epoxy resin, leaving a 10cm<sup>2</sup> electrode
  surface.&nbsp; The electrodes were degreased in acetone and then polarised
  cathodically in 0.1M NaOH aqueous solution at -1.6V vs. a saturated calomel
  electrode (SCE) for 10 minutes.&nbsp; For the S treated zinc, after immersion
  in 0.2M Na<sub>2</sub>S aqueous solution for 18 hours, electrodes were washed
  with water and then dried at room temperature.</p>
  <SCRIPT>comment(8)</SCRIPT><p>Electro-polymerisation was conducted in a 1/1 water/ethanol solution
  containing 0.25M 2-allylphenol and allylamine.&nbsp; Experimental conditions,
  including the electrode treatment and the composition of prepared electrolytic
  solution, are listed on Table 1.&nbsp; The pH of the solutions was adjusted by
  addition of sulphuric acid.&nbsp; The solutions with low amine concentration
  and those excluding 2-allylphenol were prepared for comparison.</p>
  <SCRIPT>comment(9)</SCRIPT><p>Electrolysis was conducted using cyclic voltammetry and potentiostatic
  methods after the electrodes were immersed in the electrolytic solutions for
  10-20 minutes.&nbsp; For cyclic voltammetry, the electrodes were polarised
  anodically from rest potential to 1.4V(SCE) at the speed of 20mV/second and
  then back to the start potential at the same speed.&nbsp; This procedure was
  repeated three times.&nbsp; For the potentiostatic method, the electrodes were
  polarised anodically at 1.0V(SCE) for 10 minutes.&nbsp; After the
  electrolysis, the electrodes were washed with water and ethanol, dried at room
  temperature and heated at 150�C for 30 minutes.</p>
  <SCRIPT>comment(10)</SCRIPT><p>The polymer films obtained on electrodes were analysed using Fourier
  transform infrared absorption spectrophotometry (FT-IR) and scanning electron
  microscopy (SEM).&nbsp; IR spectra of the polymer were obtained using
  Perkin-Elmer 2000 spectrophotometer by the single reflection method.&nbsp; The
  morphology of the films were observed using HITACHI-AMRAY scanning electron
  microscope.</p>
  <h3>Results and Discussion</h3>
  <h4>Cyclic voltammetry</h4>
  <SCRIPT>comment(11)</SCRIPT><p>Figure 1 shows cyclic voltammograms for the untreated zinc-coated steel in
  the solution with 2-allylphenol of pH11.0 (a), and the solution without
  2-allylphenol of pH11.0 (b).&nbsp; In both graphs, the rest potential is
  approximately �1.3V(SCE) and the zinc dissolution and the following
  passivation peak is seen in the first forward sweep.&nbsp; Comparing (a) with
  (b), the current increase from about 0.4V(SCE) in (a) corresponds to the
  oxidation of the 2-allylphenol and the increase is suppressed with cycles.</p>
  <SCRIPT>comment(12)</SCRIPT><p>In the same way, the current increase starts from 0.6-0.7V(SCE) for pH10.2
  and pH9.4 (Figure 1(c)), which means that the phenol oxidation potential goes
  down with increasing pH.&nbsp; However the current density in the phenol
  oxidation for pH10.2 and 9.4 is much bigger than that for pH11.0.&nbsp; This
  is contradictory to the case of steel and platinum substrates for which the
  current density increases with raising pH because of the increase of more
  reactive phenolate ions in the solution [5].&nbsp; The nature of the zinc
  oxide passivation film seems to affect the decrease of current density for pH
  11.0.&nbsp; Comparing the three zinc dissolution and passivation peaks, they
  become bigger and sharper with decreasing pH, whereas zinc hydroxide is most
  stable around pH9.4.&nbsp; It is presumed that zinc oxide passivation films
  become less stable in the pH9.4 and pH10.2 solutions because of the presence
  of more sulphate ions from pH adjustment, and that less stable passivation
  films allow more current flow to be available for the phenol oxidation through
  the oxide films.&nbsp; However, this requires further investigation.</p>
  <SCRIPT>comment(13)</SCRIPT><p>In the cyclic voltammograms for the S treated zinc-coated steel shown in
  Figure 2(a)-(c), rest potential is approximately �0.6V(SCE).&nbsp; This is
  far more positive than that of untreated zinc.&nbsp; Also, no zinc dissolution
  and passivation peak is seen in the first forward sweep.&nbsp; For the phenol
  oxidation, almost the same trend can be seen as untreated zinc, although the
  current density is far smaller.&nbsp; It seems that, by the sulphide
  passivation treatment, zinc passivation films become more stable due to the
  incorporation of zinc sulphide into the films and this restricts the current
  flow available for the phenol oxidation through the films.</p>
  <h4>Potentiostatic electrolysis</h4>
  <SCRIPT>comment(14)</SCRIPT><p>The current-time transients during anodic oxidation at 1.0V(SCE) over a
  period of 10 minutes are shown in Figure 3(a) (untreated zinc) and 3(b) (S
  treated zinc) and the result of the anodic polarisation including the
  appearance of electrode surface is summarised in Table 2.&nbsp; For untreated
  zinc, in all the solutions including 2-allylphenol and high(1.6M) or
  moderate(0.53M) amounts of allylamine (No.1, 2, 3 and 4), the current density
  gradually decreases with time, and after the electrolysis polymer films
  obtained were seen visually on the electrode surfaces and they show water
  repellent properties which are characteristic of the presence of poly-phenylene
  oxide coatings.&nbsp; In addition, these films have been identified as
  poly-(2-allyl)phenylene oxide by IR spectra shown in Figure 4 [6].</p>
  <SCRIPT>comment(15)</SCRIPT><p>On the other hand, for the solutions excluding the phenol (No.9, 10 and
  11), the current density falls during the first minute, then increases
  gradually, and after the electrolysis no polymer film was seen visually on the
  electrode surface.&nbsp; Also, for the low amine concentration solution
  (No.5), the overall current density is lower than that for higher amine
  concentration solution, but shows a fluctuation, and no polymer film was seen
  visually after the electrolysis.</p>
  <SCRIPT>comment(16)</SCRIPT><p>For S treated zinc, the trend is almost the same as untreated zinc although
  the current density is lower by about one order of magnitude than that for
  untreated zinc.&nbsp; However, on the zinc electrolysed in the solutions
  including 2-allylphenol and high amounts of allylamine (No.6, 7 and 8), no
  film was seen visually.&nbsp; In addition, for the pH11.0 solution (No.6)
  slight current increase is seen after 4 minutes, and for the pH9.4 solution a
  current fluctuation is seen.</p>
  <SCRIPT>comment(17)</SCRIPT><p>From these results obtained for untreated zinc and S treated zinc, it can
  be found again that the nature of the zinc passivation film affects the
  oxidation or polymerisation of the phenol, which causes the current
  suppression.&nbsp; It is presumed that the polymer films on the S treated zinc
  electrodes are too thin to be detected because the zinc passivation films are
  so stable that they restrict the current flow used for the phenol
  oxidation.&nbsp; From the fact that the thickness of the film formed in the
  solution No.2 is about 1mm (Photo 1), if current efficiency is similar in
  every solution, the film thickness obtained in the solution No.1 is presumed
  to be about 0.2mm and that in the solutions No.6 to 8 is much thinner, that is
  about 0.02-0.04mm.&nbsp; This thickness seems difficult to observe, especially
  on a rough zinc-coated steel surface.&nbsp; Also, the current-time transients
  for S treated zinc (No.6 to 8) and that for untreated zinc in the solution
  containing low concentrations of amine (No.5) show fluctuations presumably
  because the polymer films are so thin that breakdown and healing of the films
  occurs repeatedly [7].</p>
  <h4>Effect of amine</h4>
  <SCRIPT>comment(18)</SCRIPT><p>From the result of cyclic voltammetry and potentiostatic electrolysis, it
  is found that zinc oxide (sulphide) passivation films restrict the polymer
  film formation and the relevant polymer film can be obtained on the zinc
  surface where the zinc passivation film is not too stable.&nbsp; From the
  viewpoint of roll of amine, some kinds of amine compounds, especially
  allylamine, are reported to strongly absorb on metal surface, control the
  metal dissolution and contribute to adhesive film formation [5]. &nbsp;In this
  research, it was found that more amine was required to cover the rougher
  surface of unpolished zinc-coated steel electrode compared to polished steel.</p>
  <SCRIPT>comment(19)</SCRIPT><p>In addition, some kinds of amine compounds can suppress the oxide film
  formation and it has been reported that the film formation by electro-polymerisation
  can be accelerated by addition of suitable amines for the electrode metal
  [8].&nbsp; A large amount of amine may reduce the zinc oxide films to a
  thickness low enough to enable electro-polymerisation. &nbsp;It is presumed
  that surface of S treated zinc was covered with a film too stable to be
  disrupted by the amine.&nbsp; This restricted the electro-polymerisation of
  phenol, leading to very thin (or non-existent) polymer films.</p>
  <h4>Conclusion</h4>
  <SCRIPT>comment(20)</SCRIPT><p>To obtain thin electro-polymerised films on zinc as an adhesion enhancing
  pre-treatment, we have investigated the conditions under which phenol
  derivatives are electro-polymerised and have concluded that control of oxide
  passivation film formation on zinc is required for electro-polymerisation.&nbsp;
  Sulphide treated surfaces are not suitable for electro-polymerisation because
  the passivation film is so stable that it restricts polymer film
  formation.&nbsp; In addition, compared to steel and platinum, higher
  concentrations of amine are required for covering the rougher surface of zinc
  electrode and suppressing the passivation film formation.&nbsp; In the future,
  by application of the obtained thin film as pre-treatment on zinc, the effect
  of the film on adhesion and corrosion resistance will be investigated.</p>
