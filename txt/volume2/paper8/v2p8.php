  <h2>Electro-polymerisation of phenol derivatives on zinc-coated steel</h2>
  <p>Y. Yoshida, J. Marsh* and J. D. Scantlebury<br>
  <i>Corrosion and Protection Centre, UMIST, P.O.Box 88, Manchester M60 1QD, UK<br>
  </i><i>*CAPCIS LTD., CAPCIS House, 1 Echo street, Manchester M1 7DP, UK</i><i><br>
