<SCRIPT>notify();</SCRIPT><SCRIPT>comment(1)</SCRIPT><h3>Abstract</h3>
  <p>The use of Electrochemical Impedance Spectroscopy to make short-term
  predictions of the long-term performance of internally coated 2-piece food
  cans has been explored. In particular, the effects of thermal processing and
  of product on the electrochemical and corrosion performance of filled can
  variables have been investigated. It has been found necessary to use different
  parameters derived from Equivalent Circuit analysis to predict specific
  corrosion processes. Although the relative performance of different
  experimental variables can be predicted using comparatively short-term
  impedance tests, the power of the methodology was limited by the sensitivity
  of the data to the inherent variability between replicate samples.</p>
  <b>
  <SCRIPT>comment(2)</SCRIPT><p>Keywords: </b>impedance, polymer, coating, predictive, packaging, cans</p>
  <h3>Introduction</h3>
  <SCRIPT>comment(3)</SCRIPT><p>The metal packaging industry makes extensive use of organic lacquer
  coatings to provide internal protection against corrosion of the metallic
  substrates (tinplate, tin-free steel, and aluminium) by the contents of the
  can. Recent developments, such as the requirement to change from solvent-based
  to water-based internal coatings to satisfy environmental legislation and the
  constant pressure to reduce raw material costs have led to renewed focus on
  the performance of these coatings. Traditional methods of assessing coating
  performance involve long-term storage tests which are expensive to set up, and
  are, by their very nature, time-consuming. Much effort has been expended in
  trying to develop reliable predictive tests to allow the more rapid
  introduction of new coatings into commercial use whilst maintaining confidence
  in their long-term performance.</p>
  <SCRIPT>comment(4)</SCRIPT><p>Electrochemical Impedance Spectroscopy has, for a number of years, been
  regarded as the method of choice for investigating the behaviour of
  polymer-coated metals in contact with aqueous electrolyte systems. A number of
  papers have been published reporting its application to packaging systems
  [References 1-9 give a small selection of the published literature]. This
  paper describes some of the work carried out at Crown Cork &amp; Seal on the
  use of Electrochemical Impedance Spectroscopy (EIS) as a predictive test of
  coating performance, relating both some of the successes so far, and some of
  the challenges which remain.</p>
  <h3>General Experimental Considerations</h3>
  <SCRIPT>comment(5)</SCRIPT><p>A number of experimental configurations have been employed in this work on
  lacquered containers, but typically, a three electrode system has been used,
  with the container as working electrode, a platinized titanium gauze counter
  electrode and either an anodised silver wire or an M/10 calomel reference
  electrode. Packed cans are opened in the conventional manner immediately
  before testing, and the electrodes inserted through the opening. This,
  however, has the disadvantage of allowing oxygen ingress, so
  &quot;in-can&quot; electrodes have been developed, which are mounted in the
  can side wall prior to filling and processing. Graphite rod counter electrodes
  and silver wire reference electrodes are used in this case. However, this
  procedure can cause damage to areas of the internal coating adjacent to the
  electrode mounting points and inadequate sealing of the cut edge will result
  in additional initiation points for corrosion.</p>
  <SCRIPT>comment(6)</SCRIPT><p>All impedance measurements were carried out using a Solartron 1255
  Frequency Response Analyser with either a Solartron 1286 or 1287
  electrochemical interface. The equipment was computer controlled using
  in-house software, or more recently, ZPlot for Windows (Version 2.1, from
  Scribner Associates / Solartron). Equivalent circuit analysis was performed
  using EQUIVALENT CIRCUIT Version 3.96 or 3.99 (from Bernard A Boukamp,
  University of Twente), and more recently using ZView for Windows (Version
  2.1). The equivalent circuits found most appropriate were typically of the
  type illustrated in Figure 1, where n=0, 1, 2 or occasionally 3. Significantly
  better fits were obtained from equivalent circuit analysis if all capacitances
  in the equivalent circuits were treated as constant phase elements. In the
  case of the coating capacitance, the exponential factor was always close to
  unity, but significant deviations were often found for the exponential factor
  of the double layer capacitances. These are presumably caused by diffusion
  effects, but have so far not been successfully modelled using Warburg
  impedances.</p>
  <h3>Results and Discussion</h3>
  <SCRIPT>comment(7)</SCRIPT><p>Many food products are subjected to a thermal sterilisation treatment
  (processing) immediately after filling, with between 15 minutes and 1.5 hours
  at 121 �C in a steam retort being a typical process, depending on the
  product. This has obvious implications for the performance of the internal
  coating, with electrolyte being forced into pores in the coating and
  initiating corrosion reactions. The effects of different processing times has
  been studied on 2-piece drawn and wall ironed tinplate food cans, internally
  spray coated with a water-based epoxy-phenolic coating. The cans were filled
  with a synthetic vegetable medium (10 g/L sodium chloride, 3 g/L citric acid
  monohydrate, 13.3 g/L tri-sodium citrate dihydrate, adjusted to pH 5.5), and
  processing at 121 �C in a laboratory retort for various lengths of time.
  Representative Bode magnitude plots are shown in Figure 2. There is an obvious
  decrease in impedance response on bringing the retort up to pressure, which is
  accompanied by a decrease in pore resistance. There is, however, no
  significant increase in coating capacitance, (calculated as a ratio of the
  values obtained after and before processing to allow for different initial
  values) Figure 3, which is considered indicative of filling of pores with
  electrolyte. At processing times beyond about 45 minutes there is a further
  decrease in the impedance magnitude and pore resistance (Figure 4 shows the
  average of the pore resistance measured after processing), together with a
  shift in the phase angle minimum and the onset of a rise in the coating
  capacitance. This is thought to be caused by water being absorbed into the
  coating, reaching the metal substrate, and initiating delamination, and is
  substantiated by the onset of visible blistering at about this time.</p>
  <SCRIPT>comment(8)</SCRIPT><p>These marked changes on processing are perhaps to be expected but enable us
  to gain an indication of the susceptibility of different lacquers to barrier
  degradation on processing. For example, the then standard solvent-based
