  its developmental precursors (all lacquers were epoxy-phenolic based). Cans
  were filled with 1% sodium chloride, and impedance spectra run before and
  after processing at 121 �C for 1 hour. The pore resistance and lacquer
  capacitance ratio data obtained are given in Table 1.</p>
