  subsequently in 1% NaCl show that better performance should be obtained from
  the solvent-based lacquer, and this is indeed the case. When cans from a
  storage test run in conjunction with this exercise were opened after 2.5
  months, corrosion was observed only in the headspace (the area between the
  product fill level and the top of the can) and the water-based lacquer showed
  systematically more corrosion.</p>
  <SCRIPT>comment(17)</SCRIPT><p>At 2.5 months, the cans filled with blackcurrants show very little
  corrosion damage, and no systematic difference between the water and
  solvent-based variables. This is in line with the high values of pore
  resistance and comparatively small rises in lacquer capacitance observed when
  tests were performed &quot;in product&quot;. In addition, a high frequency
  feature was observed in the Nyquist plots, which is believed to be consistent
  with film formation over the whole can surface. This would explain why this
  product is rather less aggressive than would be expected given its low pH.</p>
  <SCRIPT>comment(18)</SCRIPT><p>The tests in mushy peas were complicated by the highly viscous, semi-solid
  nature of the product, and difficulty was experienced in removing the product
  from the cans, which may have resulted in some lacquer damage. However, the
  enormous increases in lacquer capacitances observed when re-tested in 1%
  sodium chloride indicate a high level of lacquer barrier degradation. After
  2.5 months storage, blushing (an opaque appearance caused by water uptake) was
  apparent on both water and solvent-based lacquers, in addition to severe
  headspace corrosion, which was more severe in the water-based variable. A
  small degree of sulphide staining was also present on the can base, to a
  similar extent for both variables.</p>
