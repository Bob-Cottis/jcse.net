  <SCRIPT>comment(20)</SCRIPT><p>One of the problems with this methodology is that opening cans prior to
  testing prevents the possibility of repeat measurements to monitor the
  longer-term behaviour. The approach of packing a large number of cans, and
  then opening a small sample of them for testing on each occasion (e.g. 1 day,
  1 week, 1 month, 3 months) is not ideal because of the inherent replicate
  sample variability [10]. To try to overcome these problems, in-can electrodes
  (as described in the Experimental Section) were fitted to cans prior to
  filling with chicken soup and processing. Again, 2-piece foodcans were used,
  with a water-based lacquer variable and solvent-based control. Impedance
  spectra were run after 1 day, 1 week and 1 month after filling (but not, on
