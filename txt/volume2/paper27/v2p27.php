  until opening 1 year after filling. Whilst no significant difference was
  predicted between the performance of the two lacquers (visual examination
  after 1 year indicated that the water-based lacquer actually performed
  slightly worse), it was possible to monitor the impedance response with time
  for individual cans, and predict comparatively good and bad performers.
  Figures 5 to 8 show the time dependence of the Bode plots of two cans coated
  internally with a solvent-based lacquer, filled with chicken soup. Can 1
  (Figures 5 &amp; 6) was a comparatively bad performer when examined on
  opening, whilst Can 3 (Figures 7 &amp; 8) was comparatively good. The major
  change appears to occur between 1 week and 1 month; the &quot;bad&quot; can
  shows a reduced low-frequency impedance magnitude, and the phase angle minimum
  at about 10Hz has disappeared. Examination of the time trends of the lacquer
  capacitance (Figure 9) shows that both cans display similar trends in lacquer
  capacitance, with Can 3 having a steeper initial fall and a steeper subsequent
  rise. It is not understood why the lacquer capacitance falls during the first
  few days of storage. Figure 10 shows the corresponding time dependence of the
  pore resistance, and it can be seen that Can 1 shows a greater decrease in
  pore resistance, particularly between 1 week and 1 month, as previously noted
  in the Bode plot.</p>
