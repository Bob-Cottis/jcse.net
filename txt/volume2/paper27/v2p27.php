  <SCRIPT>comment(23)</SCRIPT><p>Owing to the large number of samples in the experiment, it was not possible
  to use the in-can electrodes approach. Therefore the previously standard
  method of filling sufficient cans to enable batches of five to be tested at
  each time interval (initial, 1 day, 1 week, 1 month and 3 months after
  filling) was used. The initial tests were made on unprocessed cans, filled
  with 1% sodium chloride (the cans were allowed to equilibrate for two hours
  before testing). All other tests were made on samples taken from a batch which
  had been filled with chicken soup and processed for 90 minutes at 121 �C.
  Cans were opened and the reference and counter electrodes inserted immediately
  before running the impedance spectra. Figures 11 to 16 show the time
  dependence of the pore resistances and lacquer capacitances, grouped by
  lacquer type. The most striking observation is the degree of scatter within
  each variable, though in terms of pore resistance, the higher tin coating
  weight variables (H2, H4 and VC2) appear to be marginally better than their
  low coating weight counterparts with the same lacquer. As discussed
  previously, pore resistance and lacquer capacitance are indicators of the
  relative importance of different corrosion processes. Plotting one against the
  other should enable comparison of lacquers for both effects simultaneously,
  and, eventually, it should be possible to draw regions of &quot;acceptability
  and unacceptability&quot; on the graphs. Figures 17 and 18 show such graphs
  for the three variables with high tin coating weights at 1 day and 3 months.
  At first sight, they merely emphasise the experimental scatter, but
  differentiation becomes apparent in the 3 month&#146;s data, with variable H2
  shows higher pore resistance and H4 more susceptible to electrolyte uptake
  (higher lacquer capacitance ratios).</p>
  <SCRIPT>comment(24)</SCRIPT><p>Another approach is to examine the parameters directly related to the
  actual degree of corrosion, i.e. the charge transfer resistances [2]. Because
  typically two resistor / constant phase element combinations placed in series
  with each other have been used in the equivalent circuits, the two resistance
  values have been added to give an overall charge transfer resistance. Figure
  19 displays these data for the three variables with high tin coating weight.
  Some discrimination between variables is beginning to become apparent at 3
  months, but not before. A similar conclusion would be obtained by comparing
  the impedance magnitude at low frequency, because the pore resistances are
  small compared to the charge transfer resistances. The charge transfer data at
  1 month also reveal the superior performance of the high tin coating
  variables. This is shown in Figure 20 for lacquer A, the corresponding graphs
  for Lacquers B and C are not displayed, but are similar. The findings of these
  predictive tests have been borne out by the results of storage tests.</p>
  <h4>Conclusions</h4>
  <SCRIPT>comment(25)</SCRIPT><p>It has been demonstrated that: -</p>
  <ul>
    <li>Relative performance of different experimental lacquer variables may be
      predicted using comparatively short term impedance tests.<br>
    </li>
    <li>Different impedance parameters need to be used for the prediction of
      different modes of failure. Lacquer capacitance, pore resistance and
      charge transfer resistance are all useful as predictors derived from
      equivalent circuit analysis; the low frequency impedance magnitude can
      also be used.<br>
    </li>
    <li>It is more desirable to conduct impedance tests using the food product
      as electrolyte.<br>
    </li>
    <li>The most severe changes in impedance parameters occur as a result of the
      necessary thermal processing of the cans; subsequent changes are
      comparatively slow.</li>
  </ul>
  <SCRIPT>comment(26)</SCRIPT><p>However, the following problems have been encountered: -</p>
  <ul>
    <li>A high level of scatter within variables is typical, due to the inherent
      variability of commercial materials and processes. This may mask
      differences between variables.<br>
    </li>
    <li>The use of in-can electrodes to allow repeat measurements on a single
      can would be preferred, but experimental problems remain to be overcome<br>
    </li>
    <li>Differences in corrosion behaviour may not become apparent in the EIS
      parameters until some time after filling, which limits the predictive
      capability of the technique.</li>
  </ul>
