<SCRIPT>notify();</script><SCRIPT>comment(1)</SCRIPT><h3>Abstract</h3>
<p>Cyclic voltammetry, chronoamperometry and FTIR-ATR techniques have been
employed to investigate the phenol electropolymerization on stainless steel
electrodes in carbonate aqueous medium and .in solutions obtained by filtering
of Calcium Aluminate Cement and Ordinary Portland Cement slurries. The phenol
electropolymerization occurs on a passivated surface and leads to adherent and
stable polymeric film exhibiting a partial protection against corrosion. This
film maintains the aromatic character and contains ether-linked rings.</p>
<SCRIPT>comment(2)</SCRIPT><p><b>Keywords:</b> Stainless steel, phenol, coating, calcium aluminate cement,
electrochemical methods, reflection spectroscopy, corrosion.
<SCRIPT>comment(3)</SCRIPT><h3>1 Introduction.</h3>
<p>In recent years the convenience of substitution of Carbon Steel(CS) by
Stainless Steel(SS) as reinforcing material for concrete has been proposed in
various international instances. This change is required based on the superior
corrosion behaviour of SS in aggressive environments. However taking into
account the high cost of this material it seems an interesting task to enhance
as much as possible that behaviour. One of the alternatives for that purpose is
the application of organic coatings on the steel surface. Phenol and its
substituted derivatives can also be electropolymerized by oxidation in aqueous
and non-aqueous solutions giving phenol polymeric films [1-20]. In general,
these films are very thin, adherent and present low water mobility and low
permeability to different ionic and molecular species. The use of alkaline
solutions in the electropolymerization of phenol on stainless steel electrodes
can help to obtain stable passive layers that significantly allow the formation
of polymeric films on the electrode surface.</p>
<SCRIPT>comment(4)</SCRIPT><p>This paper responds to a double objective: first to study the formation and
further characterisation of a polymeric film onto SS electrodes deposited by
electrochemical oxidation of phenol (concentration 0.06 M) added to a water
solution of Sodium Carbonate 0.1 M. Secondly the behaviour of the coated
electrodes against corrosion has been studied as immersed in solutions obtained
by filtering of Calcium Aluminate Cement and Ordinary Portland Cement slurries
(pH values 11.4 and 12.6,respectively), including the influence of chloride ions
present in the medium.. The characteristics of the polymeric films formed have
been studied by cyclic voltammetry, Potentiostatic Current Transients and
ATR-FTIR techniques. Scanning Electron Microscopy (SEM) has been also used to
study the film morphology and Resistance Polarization Technique has been
employed for evaluation of instantaneous corrosion current density.</p>
<SCRIPT>comment(5)</SCRIPT><h3>2 Experimental</h3>
<p>The test solution was 0.1 M Na<sub>2</sub>CO<sub>3</sub> from Merck p.a. The
phenol solution was prepared from Merck p.a. reagents. The cement solution was
obtained by filtering out the solid components of a cement slurry prepared at
water/cement ratio=2.0. Water was obtained from a Millipore-Milli-Q System with
a resistivity near to 18.2 M&Omega; cm. All potentials are
referred to the reversible hydrogen electrode (RHE) immersed in the same test
solution.</p>
<SCRIPT>comment(6)</SCRIPT><p>The electrode material used in this work was stainless steel (SS) (chemical
composition in wt %: C &le; 0.050, Si&le;
0.750, Mn &le; 2.000, P 0.040, S 0.015, Cr 18-19, Ni
8.5-9). Cylindrical electrodes with 8 mm diameter, were employed.</p>
<SCRIPT>comment(7)</SCRIPT><p>The SS electrodes were degreased with acetone. Before each electrochemical
experiment, these were polished with alumina of 0.05 &mu;
m grade (Buehler) and after that, they were cleaned with ultrapure water in an
ultrasonic bath. To remove surface oxides, the electrodes were cathodically
polarized in the test solution at a potential of &#150;0.5 V for five minutes. For
electropolymerization process, the electrode treated as above was immersed in
carbonate+phenol solutions and the polarization programme was started.</p>
<SCRIPT>comment(8)</SCRIPT><p>The Resistance Polarization (Rp) measurement techniques, developed by Stern
et al [21, 22], have been used to evaluate instantaneous corrosion current
density (i<sub>corr</sub>). A polarization sweep from &#150;10 to 10 mV around the
corrosion potential (E<sub>corr</sub>) was applied to both steel electrodes at 1
mV s<sup>-1</sup>. i<sub>corr</sub> was calculated assuming values of B=26 mV
for corroding steel or 52 mV for passive steel in the Stern-Geary equation:</p>
<p align="CENTER">i<sub>corr</sub> = B/Rp</p>
<SCRIPT>comment(9)</SCRIPT><p>The Fourier Transform Infrared Attenuated Total Reflection (FTIR-ATR)
experiments were performed with a Nicolet Magna 550 Spectrometer equipped with a
DTGS detector and a 45� KRS-5 ATR crystal. Spectra was collected with a
resolution of 8 cm<sup>-1</sup>.</p>
<SCRIPT>comment(10)</SCRIPT><p>A Jeol JSM-840 Scanning Electron Microscope (SEM) was used to observe the
surface morphologies.</p>
<h3>3 Results and Discussion</h3>
<SCRIPT>comment(11)</SCRIPT><h4>3.1 Electrochemical Results</h4>
<p>Figure 1 shows the voltammetric response obtained with a carbon steel
electrode in 0.1 M Na<sub>2</sub>CO<sub>3</sub> solution. During the first
positive scan a region between &#150;0.2 and 0.6 V appears where the electrode
shows an active state in which its surface is oxidized. From this potential of
0.6 V the passivation of the electrode surface occurs. At about 1.6 V oxygen
evolution starts. The voltammetric profile changes with the number of sweeps in
this range of potentials and well-defined anodic and cathodic peaks appear.</p>
<SCRIPT>comment(12)</SCRIPT><p>Figure 2 shows the voltammetric response of carbon steel electrode in
presence of 6�10<sup>-2</sup> M phenol in the 0.1 M Na<sub>2</sub>CO<sub>3</sub>
solution. During the first positive scan up to 1.6 V the voltammetric profile is
roughly the same as the one obtained in Figure 1 (phenol free solution). From
this potential up a sharp oxidation peak is obtained with a maximum
approximately at 1.85V that can be associated to the oxidation of phenol. In the
following sweeps, the peak disappears and the phenol oxidation is practically
inhibited. Moreover, the evolution of the voltammetric profile with the number
of sweeps is different to that observed in Figure 1, as can be concluded from
the next observations:</p>
<ol type="i">
  <li>The anodic and cathodic peaks that appear between &#150;0.2 and 0.6 V in
    figure 1a associated to the oxidation of the stainless steel and the
    reduction of Fe(III) species are not observed in presence of phenol in the
    solution.</li>
  <li>The current density in the overall potential range diminishes.</li>
  <li>The oxygen evolution is shifted to more positive potentials (about 400
    mV).</li>
</ol>
<SCRIPT>comment(13)</SCRIPT><p>The oxidation of phenol on platinum electrodes forms a polymer film on the
electrode surface in the same aqueous carbonate solution [15]. Therefore, this
voltammetric behaviour obtained in Figure 2 in presence of phenol in solution
could be associated to the formation of a low permeable polymeric film on the
stainless steel surface.</p>
<SCRIPT>comment(14)</SCRIPT><p>To confirm the existence of a polymeric film on the electrode surface, the
electrode was potentiostatically maintained at 1.9 V for seven minutes in the
solution of phenol. The obtained electrode was then removed from the
electrochemical cell and thoroughly washed with ultrapure water. After that, it
was immersed in CAC filtering solutions free of phenol and cycled between &#150;0.5
and 1.4 V (Figure 3 and Figure 4). Theses Figures show the tenth cycle for both
bare and coated electrodes. It can be observed that the evolution of
voltammetric profile with the number of sweeps is slower when the polymeric film
exists on the electrode surface. Thus, the peak current associated to
dissolution of stainless steel electrode is smaller (about 40% less) for the
covered electrode than the one obtained for a clean stainless steel electrode.
Then, the polymer film obtained from phenol oxidation seems to have a protective
effect against the oxidation of the electrode surface. Similar results are
obtained with OPC filtering solutions as medium.</p>
<SCRIPT>comment(15)</SCRIPT><h4>3.2 Corrosion study</h4>
<p>The corrosion process in the presence and the absence of the protective layer
on the stainless steel electrodes has been studied from the instantaneous
corrosion current density (i<sub>corr</sub>).</p>
<SCRIPT>comment(16)</SCRIPT><p>For the corrosion tests, the polyphenol-coated samples (S=2.54 mm<sup>2</sup>)
were dipped in a CAC filtering solution. After 1 hour i<sub>corr</sub> was
measured by polarization resistance technique, Rp. For uncoated stainless steel
electrode (used as reference), the current density obtained in solution was: i<sub>corr</sub>(SS)=
0.9 &mu; A cm<sup>-2</sup>, while for the polyphenol
coated sample the coresponding value was: i<sub>corr</sub>(SS)= 0.2 &mu;
A cm<sup>-2</sup>. These values confirm the partial inhibiting effect of this
coating film in this particular medium.</p>
<SCRIPT>comment(17)</SCRIPT><h4>3.3 IR Spectroscopy</h4>
<p>Figure 5 shows the ATR-IR spectra of the film produced by electrooxidation of
6�10<sup>-2</sup> M phenol in carbonate medium for stainless steel electrodes.
For polymeric film formation, the electrode was submitted to one potential sweep
from &#150;0.5 to 1.9 V in this solution, and maintained at this final potential
for seven minutes.</p>
<SCRIPT>comment(18)</SCRIPT><p>In order to assign the IR bands obtained for the polymeric film of Figure 4,
the spectra of phenol monomer [23] and polymeric film obtained on Pt electrode
in carbonate medium [15] are used. The characteristic bands observed in Figure 5
are:</p>
<p>i) At 1400-1650 cm<sup>-1</sup> several bands appear associated to the
aromatic carbon-carbon stretching vibration [23].</p>
<p>ii) At 2800-3000 cm<sup>-1</sup> spectral region, several bands appear
associated to the aromatic C-H stretching vibration [23]</p>
<p>iii) A broad band at 3300 cm<sup>-1</sup> is also observed in both spectra
attributed to the O-H stretching vibration.[23]. A large decrease in the
intensity of this band is observed with respect to the phenol monomer spectrum.
This decrease can be associated to ether bond formation in the polymeric chain.</p>
<p>iv) At 900-1150 cm<sup>-1</sup> spectral region, a broad and unresolved band
associated to ether C-O symmetric and asymmetric stretching vibration (=C-O-C=
ring)[23] is observed in both spectra.</p>
<SCRIPT>comment(19)</SCRIPT><p>Therefore, from ATR-IR spectrum it can be concluded that the polymeric film
formed on stainless steel electrodes maintains the aromatic character and
contains ether-linked rings.</p>
<SCRIPT>comment(20)</SCRIPT><h4>3.4 SEM Results</h4>
<p>In order to obtain information about the surface morphology of the polymeric
films obtained on stainless steel electrodes, the Scanning Electron Microscopy
technique has been used. Figure 6 shows the SEM microphotograph of stainless
steel electrode coated with the polymeric film (left region) and bare surface
(right region). As it can be observed, the morphology of this film appears to be
scaly. This morphology is different to that obtained on platinum electrodes [15
]. A polymer with similar surface morphology is obtained on carbon steel
electrodes.</p>
<SCRIPT>comment(21)</SCRIPT><h3>4 Conclusions</h3>
<SCRIPT>comment(22)</SCRIPT><p>1. The electrooxidation of phenol in carbonate medium on stainless steel
electrodes causes the formation of a passivating film. This passivating film
avoids further phenol oxidation and partially inhibits the electrode metal
oxidation.</p>
<SCRIPT>comment(23)</SCRIPT><p>2. The IR spectra of the polymeric films show characteristic bands of
aromatic C-H stretching vibration and aromatic C=C stretching vibration. These
bands permit to propose that the polymeric films created in carbonate medium
maintain the aromatic character.</p>
<SCRIPT>comment(24)</SCRIPT><p>3. The intensity of the band associated to O-H stretching vibration decreases
in the IR spectrum. Moreover a band of ether =C-O-C= stretching vibration
appears. These results indicate that the polymeric film contains ether-linked
rings.</p>
<SCRIPT>comment(25)</SCRIPT><p>4. SEM results show the scaly looking surface morphology of such films.</p>
<SCRIPT>comment(26)</SCRIPT><p>5. The comparison of bare vs coated electrode voltammograms show that the
latter have current densities about lower at the oxidation peaks.</p>
<SCRIPT>comment(27)</SCRIPT><p>6. The corrosion rates in CAC media are lower for phenol polimer coated
stainless steel electrodes as compared to the bare surface electrodes.</p>
