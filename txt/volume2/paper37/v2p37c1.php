  <h4>General comment</h4>
  <p>The paper is interesting, novel and well worth publication as it stands.
  The experiment on the two coat systems to prove/disprove the barrier
  hypothesis is particularly pleasing.</p>
  <p>The measurement method for ionic conductivity and open circuit potential is
  not common. Summary of the principles of the method for obtaining coating
  resistivity would help the reader. Also resistivity data here are expressed as
  ohm cm, whereas most previous workers use ohm cm<sup>2</sup>. Some comment
  pointing out how the difference arises would be interesting.</p>
