<SCRIPT>comment(15)</SCRIPT><p>As previously
observed in the literature [8], the potential vs time curve exhibits an initial
drop in potential before a steady rise and levelling towards the potential of
steel (Figure 2).� The extent of the
initial part of the curve indicates the extent of the cathodic protection.</p>

<SCRIPT>comment(16)</SCRIPT><p>Initially the
curve shows an activation of the zinc while the potential moves to that of zinc
plate. This is followed by a stabilisation period as corrosion products begin
to form.� As the galvanic activity slows
the potential begins to move towards that of steel [8-14].� It should be said that this model concerns
zinc rich coatings but these zinc silicate shop primers have similar curves in
a shorter time scale. Table 3 shows a selection of coating systems and the time
to reach the protection potential of steel.</p>

<SCRIPT>comment(17)</SCRIPT><p>The behaviour of
the coating system in AMR is what would be expected. The potential vs time
curve shows that the trend of higher zinc content giving extended lifetime is
observed (Figure 3). The corrosion rate is accelerated at the extremes of pH
[15]. The zinc corrosion reaction takes part in a neutralisation of the pH
which moves from pH 4.5 to pH 6.5 within 24 to 30 hours. The potential vs time
curves appear to stabilise following a sharp movement to more noble values.</p>

