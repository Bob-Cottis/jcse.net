<h3>Conclusions</h3>

<SCRIPT>comment(36)</SCRIPT><p>The behaviour of
the coatings when immersed in 3.5%w/w sodium chloride solution has been shown
to be similar in character albeit in a smaller time frame to literature work.
Improvements in coating technology driven by the desire to increase ship
building time may have lead to current shop primers performing better when
welded and exposed than previous experience with low-level zinc coatings. It
must be remembered that these coatings were not designed for immersion in
aggressive solutions. </p>

<SCRIPT>comment(37)</SCRIPT><p>The use of AMR
shows that corrosion occurs faster initially before stabilising. As the
electrolyte is not refreshed the pH increases to neutral values to create a
less aggressive environment. The range of exposures to be studied will in
future include cyclic immersion and prohesion/spray to simulate rain pooling
and rainfall. Alongside potential vs time, the barrier development with time,
SEM observations with time and zinc content with time will be studied.</p>

<SCRIPT>comment(38)</SCRIPT><p>The SEM images have
shown the distribution, size, connectivity and clustering of zinc particles in
the coatings.� This establishes the type
and number of electronic and ionic conduction pathways in the coatings.
Localised corrosion of zinc particles has been observed which according to
Morcillo [21] indicates the mechanism of cathodic protection. If this is the
case then the extent of this protection in providing barrier properties is to
be investigated. </p>

