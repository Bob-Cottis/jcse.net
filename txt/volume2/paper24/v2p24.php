<SCRIPT>notify();</SCRIPT><SCRIPT>comment(1)</SCRIPT><h3>Abstract</h3>

<p>The use of Zinc
Ethyl Silicate shop primers and their desired properties have been explained.
Three primers with zinc contents of 46% by weight, 36% by weight and 18% by
weight have been studied. A comparison of change of potential with time when
immersed in 3.5%w/w sodium chloride solution has found the curves show similar characteristics
to literature work but in a shorter time period. Immersion in artificial
Manchester rainwater (AMR) has shown the potential initially moves toward that
of steel before stabilising when pH approaches neutrality and the solution
becomes less aggressive. The behaviour of the three coatings is much closer
with immersion in AMR but only the 18% coating has shown any visible sign of
steel corrosion and that is in 3.5%w/w sodium chloride solution. SEM images
show the size, distribution, connectivity and clustering of zinc within the
coatings. This allows for evidence of the protective action to be studied
later.</p>

<SCRIPT>comment(2)</SCRIPT><p><b>Keywords:� </b>Zinc
