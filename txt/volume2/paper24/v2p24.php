Immersion, Potential</p>

<h3>Introduction</h3>

<SCRIPT>comment(3)</SCRIPT><p>A variety of reasons
exist to ensure the success of anti-corrosive coatings within the ship building
industry. To commercial producers and users these range from economic and image
issues to the importance of safety. A comprehensive understanding of the
processes involved in the protective properties of these coatings is necessary.
This study is particularly concerned with the first available opportunity to
protect the steel, as the earlier protection can be applied the better the it
becomes in the later stages [1]. The initial coating applied to the steel is
known as the �shop primer� which utilises low levels of zinc metal giving
�weldability� to the primer.</p>

<SCRIPT>comment(4)</SCRIPT><p>Modern ships are
built using �blocks� and these blocks are formed from steel that has been
primed (Figure 1). The primer protects the steel while it is stored in the
marine atmosphere where the majority of shipyards are located. The primer also
forms part of the completed coating system. On delivery the steel plate that
forms the blocks can be covered in millscale. This is undesirable due to the
rapid way millscale delaminates when exposed to heat and thus removing any
paint applied over it. The millscale is removed by abrasive blasting which
gives the steel a blast profile of ~70mm [2]. This freshly cleaned surface wishes
to oxidise therefore the blasting and primer application are carried out on an
automated continuous line [3]. The primer should be compatible with cutting,
bending and welding providing corrosion protection by a thin coating. The
coatings should provide short term corrosion protection from the atmosphere in
marine environments.</p>

<SCRIPT>comment(5)</SCRIPT><p>Early shop
primers were poly vinyl butytral (PVB) types and iron oxide pigmented epoxy
paints. The shop primers used today are zinc silicate coatings with successive
generations having reduced zinc content. It is this content compared to the
level of success of corrosion protection stipulated by Evans &amp; Mayne [3,4]
that is not fully understood and the purpose of this study is to examine this
adventitious performance.</p>

