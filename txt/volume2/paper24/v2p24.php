<SCRIPT>comment(26)</SCRIPT><p>SEM observations
and analysis of the unexposed coatings indicate the arrangement of the zinc
particles and the situations of sites and types of corrosion. Arrangements
within the coatings would show pathways for both electronic and ionic
conduction. Ionic conduction pathways would take the form of cracks, binder and
voids. Electronic conduction pathways would take the form of particle to
particle to steel connectivity as illustrated by Morcillo [21].</p>

<SCRIPT>comment(27)</SCRIPT><p>Figures 5-8 show
zinc x-ray maps acquired using the SEM show the situations that the zinc finds
itself in over the four coatings. ZS63 (Figure 5) shows a relatively high
density of particles which coincides with zinc to zinc connectivity. This
higher density makes the gaps in the coating more apparent. The figure provides
a good example of the conduction pathways discussed above. It can be seen from
the ZS46 map (Figure 6) that the zinc is in clusters with an apparent
inter-cluster connectivity. The is also evidence of connectivity between the
clusters and the substrate. The connectivity is not as great as observed in
zinc rich coatings [21]. Figures 7 &amp; 8 show significantly less zinc and
connectivity for the ZS36 &amp; ZS18 coatings respectively than observed with
ZS63 &amp; ZS18.</p>

<SCRIPT>comment(28)</SCRIPT><p>Work carried out
by Morcillo [21] suggests that zinc in clusters which affords cathodic protection
will show localised attack whereas isolated zinc will be surrounded by a layer
of zinc corrosion product. These SEM images show that there is a certain degree
of zinc to zinc to steel contact that can give cathodic protection.� This can be partly observed with figures
9-11.� Figure 9 shows a SEM photograph
of a large particle in a ZS46 coating that has undergone localised attack.� The outer �ring� that can be observed contains
chlorine, as shown by elemental analysis. This is indicative of corrosion
product but is by no means an absolute method of determining the existence of
corrosion product. Visually, it is difficult other than in this case to
