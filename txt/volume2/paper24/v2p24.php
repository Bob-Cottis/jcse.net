<SCRIPT>comment(8)</SCRIPT><p>Three experimental coatings provided by <i>International Coatings Ltd </i>have been
used in this study. The coatings are based on commercially available products
that provide corrosion protection when used. The coatings are of the two-pack
type. The coatings were prepared using the manufacturers mix ratios and applied
to Q-panels (151mm � 101mm � 1.5mm) using a draw bar method. Table 1
identifies the coatings under test and their properties.</p>

<SCRIPT>comment(9)</SCRIPT><p>The coatings
become touch dry within 5-10 minutes and were allowed to cure in an atmosphere
of 20-25�C, 75-85%RH for 10 days. Assessment of cure was
carried out using a solvent resistance test. The pass mark was no loss of film
thickness.</p>

<SCRIPT>comment(10)</SCRIPT><p>Contacts were
attached to the panels, which were then edged and backed with beeswax:
colophony resin mixture (4:1). The panels were then immersed continuously in
3.5% weight/weight sodium chloride solution and artificial Manchester rainwater
solution [5]. Potential versus time was measured using a saturated calomel
electrode. SEM was carried out using an AMRAY 1810 operating at �20KeV in
secondary electron and backscatter electron detection. </p>

<h3>Results and Discussion</h3>

<SCRIPT>comment(11)</SCRIPT><p>In order to build
up an understanding of this type of coating it was decided to monitor potential
with respect to time by immersion in 3.5%w/w sodium chloride solution. This
solution was chosen to allow comparison with work reported in the literature.
It was also decided to monitor the potential at open circuit rather than
imposing a current because of the low levels of zinc would have a shorter
protection time [6].</p>

<SCRIPT>comment(12)</SCRIPT><p>The electrolyte
solution is particularly aggressive with regard to low-level zinc silicate shop
primers that were not designed for immersion in such solutions. Artificial
Manchester Rainwater (AMR) was selected to evaluate performance in acidic
conditions. Again, the coatings were not designed for immersion in such
solutions but nevertheless results will be useful in understanding coating
performance. The use of AMR fits in with a program of work that is explained
later in this paper.</p>

