<p align=left>An electrochemical
method has been used for the evaluation of anti-corrosive properties of coatings
on metals based on an under-film corrosion tester and using a current
interrupter technique.� The data
obtained from the under-film corrosion tester has been compared with data
obtained from electrochemical impedance spectroscopy. �It was found that where comparison was
possible a reasonable co-relation existed.�
The maximum value measurable in case of film resistance for the current
interrupter was 10<sup>10 </sup>&#937;cm<sup>
2</sup>and for electrochemical impedance spectroscopy 5X10<sup>8</sup>&#937;cm<sup>2
</sup>respectably. The current interrupter method gave more parameters than
electrochemical impedance spectroscopy and this makes this method more useful
not only for assessing anti-corrosive performance but also for the study of
corrosion mechanisms.</p>

