<h3 align=left>Introduction</h3>

<SCRIPT>comment(3)</SCRIPT><p align=left>Electrochemical methods
have long been studied for the quantitative analysis of degradation of coated
metals.[1]. A current interrupter technique has been developed for measuring
under-film corrosion which gives the same parameters as electrochemical
impedance spectroscopy.[2]� A comparison
of the data obtained from the current interrupter technique and electrochemical
impedance spectroscopy will be described. A comparison between these two
methods clearly indicates those parameters which relate to film performance.</p>

<h3 align=left>Experimental</h3>

<SCRIPT>comment(4)</SCRIPT><p align=left>A clear alkyd (La), a
lead cyanamide pigmented alkyd paint(La-P) and a lead cyanamide pigmented
epoxy(Ep-P) were used as the paint samples for evaluation.� Mild steel(JIS G 3141) was used as a
substrate. The substrate was coated with the paints after surface treatment by
degreasing with xylene, abrading by emery paper (#240) and degreasing again with
xylene.� The paints were then spray coated.� Dry film thicknesses of 20�m, 60�m, 100�m
and 150�m were obtained for each paint. Sodium chloride solution (3 wt%) was
used as a test solution. For impedance spectroscopy (EIS) the surface area of
8.55 cm<sup>2� </sup>of coated panels
was used as a working electrode.� A
platinum electrode and Ag/AgCl electrode were used as a counter electrode and a
reference electrode respectively.�
Measurements were carried out using a potentiostat (Tohogiken Model
2000), a frequency response analyzer (NF Model 5020) and PC(NEC PC-9801).� Frequencies were in the range of 20 kHz to
10 mHz and the applied potential was 9.0 mV(RMS). For the Current Interrupter
method (CI) the working electrode was same as that in the measurement of
impedance.� A platinum electrode and
Ag/AgCl electrode were used as a counter electrode and a reference electrode
respectively as well. Measurements were carried out using a current interrupter,
(Under-film Corrosion Tester developed by DNT and Hokuto Denko Co.) </p>

<h3 align=left>Results and discussion</h3>

<h4 align=left>Parameters obtained by
electrochemical measurements</h4>

<SCRIPT>comment(5)</SCRIPT><p align=left>Both electrochemical
impedance spectroscopy and current interrupter were expected to give parameters
of natural potential (E), film resistance(Rf), film capacitance(Cf), charge
transfer resistance (Rct) and double layer capacitance (Cdl).� These parameters were used to evaluate the protective
properties of the coated steel.� The
suffix 1 and 2 mean parameters for electrochemical impedance spectroscopy and
current interrupter method respectively.�
For example, Rf<sub>1 </sub>means film resistance obtained from electrochemical
impedance spectroscopy and E<sub>2</sub> means potential obtained from the current
interrupter method. </p>

