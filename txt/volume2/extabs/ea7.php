<h4>Introduction.</h4>
</dir>

  In the development of protection methods against the corrosion of carbon steel reinforcing bars embedded in concrete, one of the most promising and recent research lines is based in the development of new organic coatings on the steel surface.
  <p>This work deals with the electrochemical and against corrosion behaviour of carbon steel electrodes coated with a polymeric film. Such behaviour has been studied in solutions prepared by decantation and filtering of Calcium Aluminate Cement and Ordinary Portland Cement slurries. Polymeric coating of the steel surface is achieved by electrochemical oxidation of 0.06M phenol solved in a water solution of Sodium Carbonate 0.1M.
<b>
<p>Experimental
</b>
  <p>The carbon steel electrodes have been fabricated from standard bars regularly used in reinforced concrete.
  <p>Coating material has been previously characterised [1] by means of techniques such as Cyclic Voltammetry, Chronoamperometry and Fourier Transform Infrared (FTIR). Based on the voltammetries it has been found that films created using the above technique show low ionic  conductivity values, along with a deficient permeability to water molecules. On the other hand based on chronoamperommetry results a model have been developed which explains some of the main phases taking place along the electropolymerisation process.
  <p>For Reflection IR Spectroscopy steel rods previously polished in one of their faces as to produce a mirror-like surface were used. Spectroscopy results show that the synthesised film molecule keeps the aromatic character of the original monomer and evidence ether-type links in its structure.
  <p>Concerning the instant corrosion rates determined by Polarisation Resistance technique results indicate that the corrosion levels of polymer film coated electrodes are lower than those obtained for bare steel electrodes in the studied media, demonstrating the efficiency of such a coating as corrosion protection system.
