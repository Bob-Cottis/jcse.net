<p>
Figure 6 shows that the disbonding rate was low and almost the same for system 3 and 4. System 1 and 2 also had nearly the same disbonding rate, but much higher than system 3 and 4. This experiment clearly demonstrates the importance of applying the aluminium pigmented coat directly on the steel substrate. This also favours the buffer theory over the barrier theory. System 2 and 3 should have the same barrier properties, but the disbonding of system 3 is much slower than for system 2. In addition system 2 has only slightly less disbonding than system 1, which should have been a better barrier. The same is the case with system 3 compared to system 4. These results demonstrate that the positive effect of the aluminium pigments is linked to their presence at the steel/coating interface.</p>
<h4>Conclusions</h4>
Aluminium and glass barrier pigments decreased cathodic disbonding substantially for two different epoxy coatings. Glass flake pigments were less effective than aluminium pigments in decreasing cathodic disbonding, but still they improved the performance of the coating significantly. The aluminium pigments had little effect on cathodic disbonding unless the aluminium containing film was applied directly on the steel surface. The effect was therefore connected to their presence near the steel/coating interface. This indicates that the aluminium pigments had an additional effect on cathodic disbonding besides any barrier effects. It has been suggested that buffering reactions with the oxide on the surface of the pigments may decrease cathodic disbonding.
