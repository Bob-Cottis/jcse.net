<h4>Results and Discussion</h4>
Figure 2 shows the disbonded distance in the long term test as a function of time for the amide cured epoxy coatings with aluminium pigments. 15 samples of each model paint were prepared, and cathodic disbonding was measured during a two years period. With increasing time intervals, 1-3 samples was removed and analysed. The aluminium pigments were highly effective in improving the resistance of the coating towards cathodic disbonding. The coatings with 10 and 20% aluminium had not disbonded at all after 18 months of exposure. After two years the 10% aluminium coating had some disbonding, and about 3 mm of the coating around the holiday could be removed. The 5% aluminium coating had disbonded about 20 mm and the coating without aluminium about 65 mm in the same period of time. 
&nbsp; <span ><img src="../extabs/ea13.2.gif" v:shapes="_x0000_i1025" WIDTH="623" HEIGHT="383"></span> <b><a NAME="_Ref408998511">Figure 2</a>. Cathodic disbonding of epoxy coating with various concentrations of aluminium pigments.&nbsp;</b>
<p>
The amide cured epoxy has low commercial interest due to the high content of solvents. The epoxy mastic binder was therefore included in the test to represent a more commercially interesting binder. The disbonding distances for the epoxy mastic coatings are given in Figure 3. Pre-treatment, application, curing and exposure were similar to the long term test of the amide cured epoxy. The unpigmented epoxy mastic gave about 8 mm disbonding in four months, but then the coating started to blister randomly all over the sample. After that, cathodic disbonding spread from the blisters, and it became impossible to measure the disbonded distance from the holiday. After about one year the whole sample had disbonded. We would like to emphasise that this is not a general problem with epoxy mastics. We have tested several commercial epoxy mastics which have perform much better than this. 
The blistering of the epoxymastic without barrier pigments was independent of the initial coating holiday, and was even found on the back of the samples. Inside the blisters we found a highly alkaline liquid, which means that the blistering was caused by the cathodic reaction under the coating. The blistering was therefore also a kind of cathodic disbonding. It then seems that there are two different disbonding mechanisms, one mechanism associated with a mechanical damage in the coating, and another mechanism independent of any mechanical damage. This is in accordance with Type I and Type II failure described by Watts and Castle . Type I failure was associated with downward diffusion of the cation via conductive pathways in the film, corresponding to the random blistering of our coatings. Type II failure was associated with transport of the cation under the disbonded coating from a mechanical damage in the film. For the epoxy mastic coatings the aluminium pigments had a positive effect on both types of failure. 
&nbsp;</p>
