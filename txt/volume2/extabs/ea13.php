<h4>Introduction</h4>
The purpose with this paper is to examine the effect of flake shaped glass- and aluminium pigments on cathodic disbonding. Model coatings with various contents of glass and aluminium pigments have been prepared and tested for cathodic disbonding. We have found that both glass and aluminium pigments improve the resistance of the coatings against cathodic disbonding. The effect was found with two different binders, which indicate that this is a general effect, independent of the binder. We have outlined two possible mechanisms for the effect. The first possibility is that the flake shaped pigments improve the barrier properties of the coating. Cathodic disbonding depends on transport of reactants through the coating. The barrier pigments may then impede the transport, which may slow down the cathodic disbonding. The other possibility we have looked at is buffering reactions with the pigments. Leidheiser <i>et al.</i> have suggested that the aluminium may reduce the pH at the coating/substrate interface and thereby decrease cathodic disbonding .<center><table CELLSPACING="0" BORDER="0" CELLPADDING="7" WIDTH="564">
