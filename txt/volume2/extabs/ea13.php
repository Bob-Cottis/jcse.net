A barrier mechanism for the effect of aluminium pigments on cathodic disbonding implies that the pigments decrease the transport of at least one of the reactants, and that this reactant affects the rate of disbonding. The rate of cathodic disbonding should then correlate with the transport rate of this species. Figure 4 shows the transport properties and disbonding rate for the films as function of aluminium pigment concentration. The responses are normalised to the results for the epoxy coating without aluminium pigments.
<p ALIGN="JUSTIFY">&nbsp;</p>
<span ><img src="../extabs/ea13.4.gif" v:shapes="_x0000_i1025" WIDTH="623" HEIGHT="386"></span> <b>
<a NAME="_Ref408393295">Figure 4</a>. Cathodic disbonding and transport properties of the coatings as function of aluminium pigment concentration. The responses are normalised to the result for the coating without aluminium pigments.</b>
<p>&nbsp;Figure 4 shows that the decrease in water diffusivity, oxygen diffusivity and oxygen permeability was relatively small compared to the effect of aluminium on cathodic disbonding. The transport of cations through the film increased with the aluminium content, i.e. opposite to the effect on cathodic disbonding. Neither of the transport parameters correlates well with the disbonding rate. This indicates that the mechanism of the effect of the aluminium pigments on cathodic disbonding is something else than the barrier effect.
The aluminium pigments are covered with oxide. According to the supplier the pigments contain 1-3% aluminium oxide. Leidheiser <i>et al</i> . have suggested that the oxide on the pigments may act as a buffer that decreases the pH beneath the film. Hydroxide generated by the cathodic reaction at the steel/coating interface is generally accepted as being responsible for cathodic disbonding. If hydroxide is removed from the interface by buffering reactions from the film, disbonding may also decrease. According to the Pourbaix-diagram for aluminium , solid aluminium oxide is not stabile above pH 9-10 and will react with hydroxide (1). A simple experiment was performed to test the buffer theory. The epoxy coatings with 0 and 10% aluminium pigments were applied in two-coat films in the four possible combinations of the two paints. The application and coating thickness is illustrated in Figure 5. If there is a buffer effect from the aluminium pigments, it will be important to have the aluminium pigments in the first coat. Then only system 3 and 4 will show decreased cathodic disbonding. If the effect of the aluminium pigments on cathodic disbonding is a general barrier effect, then system 4 will perform best, system 2 and 3 will perform equally and system 1 will perform worst. The disbonding rates are given in Figure 6.
