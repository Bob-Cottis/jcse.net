<p ALIGN="JUSTIFY">As for the amide cured epoxies, the epoxy mastics with 10% and 20% aluminium pigments had no cathodic disbonding at all after 18 months exposure. No blistering or other signs of coating deterioration were observed either. Due to the low content of solvent, the concentration of aluminium in the dry film was lower than for the corresponding amide cured epoxies. Since the unpigmented coating performed so badly, the aluminium pigments had a tremendous effect on both random blistering and cathodic disbonding. </p>
<p ALIGN="JUSTIFY">Due to spark hazard, the classification companies have set a limit to the concentration of aluminium in marine paints to maximum 10% by weight in the dry film . Glass flake pigments were therefore also tested with the epoxy mastic, as an alternative barrier pigment. The disbonding of these coatings is also shown in Figure 3. The glass flakes inhibited the disbonding compared to the unpigmented coating, but not as much as the aluminium pigments. After 4 months some disbonding was found on the epoxymastic with 10% glass. For the 20% glass coating disbonding was first found after a year of exposure. After 9 &#150; 10 months disbonding started from random points all over the coating with 10% glass, and after one year it was impossible to measure the disbonded distance from the holiday. After 18 months about 70% of the coating had disbonded. For the 20% glass coating some random initiation of disbonding was first found after 18 months. </p>
<p align="left">
The random initiation of cathodic disbonding of the epoxymastics with 10 and 20% glass flake pigments was probably due to opening of conductive pathways through the coating, as for the blistering of the unpigmented epoxymastic. The reason why the coatings with glass flake pigments did not develop blisters may have been a reinforcing effect of the pigments. The random initiation of disbonding for the two films was therefore also a Type I failure, in the terminology by Watts and Castle . Since the glass flake pigments delayed this type of failure, the pigments may have delayed the process of opening conductive pathways through the film. 
