Aluminium pigments have been widely used in engineering paints and protective coatings for many years. The aluminium pigments are shaped as flakes in different sizes. The pigments we have used are about 20 - 40 µm wide and 1 µm thick. This geometry, along with their metallic nature, is responsible for most of their properties in protective paints. The aluminium pigments improve the barrier properties of protective paints .  
Glass flake pigments are also used in protective coatings, but not to the same extent as aluminium flake pigments. They are produced by breaking thin glass (about 5 µm thick) into small flakes, about 100 - 400 µm wide. Their relatively large size restricts them to be used in very thick coatings only. The shape and impermeability to water and oxygen suggest that glass flakes will improve the barrier properties of the coating.  


<h4>Experimental</h4>
We have used model coatings with two different binders: an amide cured epoxy and an amine cured epoxy modified with a hydrocarbon resin (epoxy mastic). The epoxy binder was pigmented with different concentrations of aluminium flake pigments. The epoxy mastic binder was pigmented with either aluminium flake pigments or glass flake pigments. The aluminium pigments we have used are about 20 - 40 µm wide and 1 µm thick.  The glass flake pigments were up to 400 µm wide and 10 µm thick. 
Cathodic disbonding for all nine model coatings was tested in a long term test. The substrate was 200 x 200 x 5 mm steel plates cut from mild steel. They were blast cleaned with steel grit to Sa 2½ and medium roughness (R<sub>y</sub> = 76 ± 7 µm). The paints were applied by airless spraying in two coats. The dry film thickness was between 250 and 350 µm. The samples were given a circular holiday in the middle, 6 mm in diameter, to start the cathodic disbonding process. They were exposed in continuously renewed natural seawater at 8-13°C. Galvanic coupling to zinc anodes gave cathodic polarisation to about -1050 mV SCE in the coating holiday. The experimental setup is shown in Figure 1.
