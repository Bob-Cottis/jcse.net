  <p><b>Keywords:</b> Electro-polymerisation, Poly-phenylene oxide film, Zinc-coated steel, Phenol derivative, Amine
&nbsp;</p>
<h4>1. Introduction</h4>
Chromate materials have been used for painted zinc-coated steel in pre-treatments and/or undercoats to improve its corrosion resistance and paint adhesion.  However, because of environmental concerns, use of harmful materials such as chromate is to be controlled and banned.  This has lead to vigorous research for finding alternatives to chromate for improving the product lifetime.
As one of the possible alternatives, the application of thin polymer films as pre-treatments has been examined [1].  The relation of the required properties with the composition of polymer film such as variety and density of functional groups is essential.  However it has not been fully researched so far because it is difficult and time-consuming work to prepare various polymers with the composition changed continuously by conventional methods.  From this viewpoint, electro-polymerisation can be a useful polymer producing process.
Electro-polymerisation is a unique polymer coating process in which polymerisation of monomeric materials and coating formation by the polymer produced is conducted at the same time on the metal surface electrochemically.  One of the biggest benefits of this process is that a wide variety of chemical compositions can be obtained because, unlike polymers, monomeric materials are far easier to dissolve in usual common solvents and this leads to unique films unobtainable by the conventional processes.
The application of thin electro-polymerised films on steel as adhesion enhancing pre-treatments has been examined [2].  However, although zinc is one of the most useful materials, little work has been done so far because zinc is so active that it is difficult to obtain polymer films on it, especially by anodic oxidation [3,4].  In this paper, we reported about preparation of thin electro-polymerised films onto zinc-coated steel and also examined the surface treatment before electro-polymerisation, comparing the zinc without treatment (untreated zinc) and the zinc subjected to passivation treatment in alkaline solution containing sulphide ions (S treated zinc) that has been successfully used for electro-polymerisation of aniline [4].
&nbsp;
<h4>2. Experimental</h4>
Electro-galvanised steel sheet (thickness 1.0mm, coating amount ca. 20g/m<sup>2</sup>) was used as the substrate.  They were covered by a thick layer of epoxy resin, leaving a 10cm<sup>2</sup>  electrode surface.  The samples were degreased in acetone and then polarised cathodically in 0.1M NaOH aqueous solution at -1.6V vs. a saturated calomel electrode (SCE) for 10 minutes.  All chemicals were purchased from Aldrich and used without further purification.  For the S treated zinc, after immersed in 0.2M Na<sub>2</sub>S aqueous solution for 18 hours, electrodes were washed with water and then dried at room temperature.
Electro-polymerisation was conducted in the 1/1 water/ethanol solution containing 0.25M 2-allylphenol and allylamine.  Experimental conditions, including the electrode treatment and the composition of prepared electrolytic solution, are listed on Table 1.  The pH of the solutions was adjusted by addition of sulphuric acid.  The solutions excluding 2-allylphenol were also prepared for comparison.  After immersed in the reaction solution for 10-20 minutes, electrodes were polarised anodically at 1.0V(SCE) for 10 minutes.  Then they were washed with water and ethanol, dried at room temperature and heated at 150<font FACE="Symbol">°</font>
C for 30 minutes.
For cyclic voltammetry, after immersed in the reaction solution for 10-20 minutes, electrodes were polarised anodically from rest potential to 1.4V(SCE) at the speed of 20mV/second and then back to the start potential at the same speed.  This procedure was repeated three times.
Infrared (IR) spectra of the polymer films on the substrate were obtained using Perkin-Elmer 2000 spectrophotometer by single reflection method.
&nbsp;
<h4>3. Results and Discussion</h4>
<h4>3-1. Formation of polymer films</h4>
The current-time transients during anodic oxidation at 1.0V(SCE) over a period of 10 minutes are shown in Figure 1(a) (untreated zinc) and 1(b) (S treated zinc) and the result of the anodic polarisation including the appearance of electrode surface is summarised in Table 2.
During the anodic polarisation current suppression occurs in all the solutions including 2-allylphenol and high amount of allylamine except for solution (5) in which slight current increase occurs after 4 minutes.  On the other hand, the current increases after about 1 minute in the solutions excluding the phenol.  This indicates that passivation by polymerisation or oxidation of the phenol occurs in the solutions including 2-allylphenol.  However polymer films have been visually observed only on untreated zinc electrodes No.1, 2 and 3 and no film can be seen on S treated zinc electrodes which show current suppression.  In addition, the films on electrodes No.1, 2 and 3 have been identified as poly-(2-allyl)phenylene oxide by IR spectra shown in Figure 2 [5].
It is presumed that the polymer films on the S treated zinc electrodes are too thin to be detected because the current amount used for oxidation is much less than that for the untreated zinc electrodes.  From the fact of that the thickness of the film formed on electrode 2 is about 1<font FACE="Symbol">m</font>
m (Photo 1), if current efficiency is almost same in every electrode, the film thickness of the electrode No.1 is presumed to be about 0.1<font FACE="Symbol">m</font>
m and that of the electrode No.5 to 7 is 50 times thinner, that is about 0.02<font FACE="Symbol">m</font>
m.  This thickness seems difficult to observe, especially on rough zinc-coated steel surface and also this is the reason why current-time transient is slightly fluctuating (No.7) or increasing (No.5).  From the same reason, the electrodes No.4 and 8 show fluctuating current transients because the polymer passivation films are so thin that breakdown and healing of the films occurs repeatedly [6].
<h4>3-2. Cyclic voltammetry</h4>
Figure 3 shows cyclic voltammograms for the untreated zinc-coated steel in the solutions with 2-allylphenol of pH11.0 (a), and the solutions without 2-allylphenol of pH11.0 (b).  Comparing (a) with (b), current increase starting from about 0.4V(SCE) in (a) corresponds to the oxidation of phenol and the increase is suppressed with cycles.  In the same way, the current increase starts from 0.6V(SCE) for pH10.2 and 0.7V(SCE) for pH9.4 (Figure 3(c)), which means that phenol oxidation potential goes down with increasing pH.  However the current density in phenol oxidation for pH10.2 and 9.4 is much bigger than that for pH11.0.  This is contradictory to the case of steel substrate for which the current density increases with raising pH because of the increase of more reactive phenolate ions in the solution [7].  The conditions of zinc oxide passivation films seem to affect the decrease of current density for pH 11.0, but this needs further investigation.
The same trend can be seen also in the cyclic voltammograms for the S treated zinc electrodes, although the current density is far smaller than the case of untreated zinc (Figure 4, the solutions with 2-allylphenol of pH11.0 (a), and the solutions without 2-allylphenol of pH11.0 (b)).  Additionally, in the case of S treated zinc the cyclic voltammogram is complicated for pH9.4 (Figure 4(c)), which presumably related to lack of stability of the S treated zinc in the solutions at this pH.
<h4>3-3. Effect of amine</h4>
Some kinds of amine compounds, especially allylamine, are reported to strongly absorb on metal electrodes and contribute to adhesive film formation [7].  In this research, it was found that a large amount of amine was required to cover the rougher surface of unpolished zinc-coated steel electrode compared to polished steel.  In addition, some kinds of amine compounds have the function by which the surface oxide film formation can be suppressed, and it has been reported that the film formation by electro-polymerisation can be accelerated by addition of suitable amines for the electrode metal [8].  A large amount of amine may reduce the zinc oxide films to a thickness low enough to enable electro-polymerisation.  It is presumed that surface of S treated zinc was covered with a film too stable to be disrupted by the amine.  This restricted the electro-polymerisation of phenol, leading to very thin (or non-existent) polymer films.
&nbsp;
<h4>4. Conclusion</h4>
To obtain thin electro-polymerised films on zinc as an adhesion enhancing pre-treatment, we have investigated the conditions under which phenol derivatives are electro-polymerised and have concluded that control of oxide passivation film formation on zinc is required for electro-polymerisation.  Sulphide treated surfaces are not suitable for electro-polymerisation because the passivation film is so stable that it restricts polymer film formation.  In addition, high concentrations of amine are required for covering the rougher surface of zinc electrode and suppressing the passivation film formation.  In the future, by application of the obtained thin film as pre-treatment on zinc, the effect of the film on adhesion and corrosion resistance will be investigated.
