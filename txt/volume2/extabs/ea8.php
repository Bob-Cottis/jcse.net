<h2>Electro-polymerisation of phenol derivatives on zinc-coated steel</h2>
<b>Y. Yoshida, J. Marsh<font FACE="Symbol">*</font>
 and J. D. Scantlebury</b><br>
<i>Corrosion and Protection Centre, UMIST, P.O.Box 88, Manchester M60 1QD, UK <font FACE="Symbol">*</font>
CAPCIS LTD., Capcis House, Echo street, Manchester M1 7DP, UK<br>
