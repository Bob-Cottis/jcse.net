  It is noted that these pHs are less than those attainable by the cation hydroxides at saturation.  The rate-controlling step in the delamination process of corroding non-chromated FBE-coated panels is therefore the passage of the hydrated cations through the holiday and along the coating/substrate interface to the cathodic site.  The cations are being transported to the cathode such that charge balance may be achieved.
  <p>From the radial delamination / square root of time graphs for the FBE-panels it may be seen that there is a delay time before delamination begins to occur, as found by other researchers including Leidheiser and co-workers<sup>(158,160)</sup> and Sharman, Sykes and Handyside<sup>(159)</sup>. Delay time appears to be independent of both hydrated cation mobility and solution pH (Figures 10 and 11).
  <p>Leidheiser, Wang and Igetoft<sup>(158)</sup> linked delay time to ion ingress into the coating, and Wang <sup>(173)</sup> to water diffusion through it.  Examination of the FBE-panels exposed to different solutions at open circuit reveals that there is no obvious relationship between the delay times of the FBE-coated panels and cation transport through the coating.
  <p>The differing delay times of the corroding systems cannot be explained in terms of an interfacial attack process from the holiday at the coating / substrate interface, either.  If this was the important mechanism in control of delay time, then delay times should again be very similar because all the samples started to corrode after approximately the same period of immersion.  Delay time must therefore be related to a transport process through the FBE-coating.  There is no evidence that it is related to cationic transport, so this delay time is probably a function of either water or oxygen transport.  Mayne<sup>(82)</sup> has shown that oxygen transport through a coating is a relatively rapid process, and Cottis<sup>(228)</sup> has stated that a typical oxygen permeability of an epoxy resin is 5.2 x 10<sup>-12</sup> mole.m<sup>-1</sup>s<sup>-1</sup>atm<sup>-1</sup>, so it is unlikely that this factor could account for the length of delay time recorded in these conditions (typically 3-4 days<sup>½</sup>).  Oxygen transport as a factor in delay time would also not explain the differing delay times in the systems investigated.  It is therefore likely that the delay time of the corroding system at open circuit is related to water transport through the coating.
  <p>Investigation of FBE-panels in contact with the test solutions where no corrosion took place reveals that delay time cannot be related to bulk solution pH or cationic transport.  Examination of their delay times does however reveal that they are of the same order (between 3.1 and 5.2 days<sup>½</sup>).  It is therefore possible that delay times of these near-passive systems are related to transport through the coating.  With no extensive electrochemical reactions taking place, it is unlikely that cationic or oxygen transport through the coating will have a significant effect on time before initiation of coating delamination.  It is possible however that water transport is the important factor.
  <p>Another interesting trend that may be derived from the delay time data is that corroding systems tend to have lower delay times than those displaying passive properties.  Parks and Leidheiser<sup>(100)</sup> have shown how ionic migration through an organic coating increases in rate when the substrate is placed under polarisation at &#150;0.80V, and they have referred to other work conducted by themselves showing a similar trend for water.  A similar process may be occurring with those systems undergoing corrosion, with the polarised substrate causing an increase in water flux and hence increasing the rate of water transport through the coating, therefore shortening the delay time of the system.
  <p>Prior to the measured propagation period of the delamination process, it is possible that delamination was occurring at a rate that was sufficiently small that it was not visibly discernible on inspection of the clear FBE-coated panels, the rate being limited by the high degree of bonding between the FBE-coating and its substrate in the interfacial and interphasial region.  When the water diffusing through the coating reached the coating / substrate interphasial region, it will have disrupted the interactions between the two materials, as Funke and other workers have shown<sup>(116)</sup>.  With this disruption, delamination may then be able to take place at an accelerated rate.  This delay time &#145;initiation&#146; mechanism would explain the traces of disbonding sometimes found prior to the extrapolated delay time by other authors such as Sharman, Sykes and Handyside<sup>(159)</sup>, and also their finding that delay time was only marginally influenced by coating thickness, the more important factor being uptake of water in the region around the defect.
  <p>The possibility is therefore raised that in both active and passive systems, delay time is controlled primarily by water diffusion, which is a function of the potential gradient across the coating, and secondarily by the nature of the coating / substrate interface.  In these conditions, increasing thickness of coating would be expected to result in increasing delay time, as has been found by Wang and Leidheiser<sup>(160)</sup> and Sharman, Sykes and Handyside<sup>(159)</sup>.
  <p>Visual studies of passive and corroding delaminating FBE-coated systems at open circuit reveals an area of bright untarnished substrate at the disbonding front.  A closer examination of these regions by SEM reveals that they are the same in morphology as the substrate prior to delamination (Figures 12 and 13). As such it is unlikely that delamination of the coating is directly caused by any anodic reactions taking place.  It is therefore likely that FBE coating delamination will be caused by a variable that causes degradation of the steel substrate at the disbondment front.  No FBE visible to SEM is left on the delaminated substrate surface.
  <p>The FBE-resin is already intimately mixed with its hardener in its powder form prior to spraying onto the heated workpiece (i.e., rebar or panel), and its gel time occurs in a matter at seconds<sup>(215)</sup>.  There is therefore no time for a separation of resin and hardener to take place, as has been suggested for solvent-based paint systems<sup>(106)</sup>.  The FBE coating should therefore effectively be the same in chemical and structural composition from the outer limit of the polymer / substrate interphasial region to its external surface.  In this situation, if the mechanism of adhesion loss was coating degradation and cohesive failure, then it would be equally likely to occur at any depth of the FBE coating on the passive specimens at open circuit.  Since failure always appeared to occur at the coating / substrate interface, this mechanism was clearly not the cause of disbondment.
<b>
<h4>Conclusions</h4>
</b>The following conclusions may be drawn from this investigation into delamination of FBE-coatings in a simulated pore solution environment:
  <p>1) Delay time before initiation of observable delamination processes may be a function of water penetration through the coating to the interfacial or interphasial coating/substrate region.
  <p>2) Delamination of FBE coatings from steel substrates is predominantly caused by hydroxyl ions.
  <p>3) Rate of FBE delamination is controlled by transport processes from a pore in the coating and along the delaminated coating/substrate interface to the disbondment front.
  <p>4) The locus of failure of coating adhesion is in the interfacial or interphasial coating/substrate region.
  <p>5) The rate of FBE delamination in near-passive conditions is controlled by hydroxyl ion migration from the bulk external solution to the coating/substrate disbondment front.
  <p>6) The rate of FBE delamination in the condition of underfilm corrosion is controlled by hydrated cation movement to the cathode site.
