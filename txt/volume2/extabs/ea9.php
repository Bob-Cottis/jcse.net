</b><h4>Discussion</h4>
</dir>


  A linear relationship is derived when radial disbondment of the FBE-coating on both panels and rebars is plotted against the square root of time of immersion of the specimen.  This relationship is found for corroding and passive systems.  The parabolic nature of delamination shows that rate of FBE disbondment is greatest upon its initiation and subsequently decreases with time until a theoretical cessation of disbondment occurs after an infinite time period.  More importantly from a mechanistic viewpoint, this relationship indicates that the path of the factor that is the rate-determining step in the speed of the delamination reaction is along the coating / substrate interface from the coating holiday. This relationship has also been found for coated systems corroding at open circuit by Leidheiser and co-workers<sup>(100,155,158)</sup>, Sharman, Sykes and Handyside<sup>(159)</sup>, and Nguyen and co-workers<sup>(163-167)</sup>, and systems under cathodic polarisation by Kendig, Addison and Jeanjaquet<sup>(132)</sup>.  If transport of this delamination parameter was through the coating, then a linear relationship between radial delamination and time of immersion would be encountered.
  <p>Kendig<sup>(132)</sup> has described this parabolic relationship mathematically as:
<i>
<p>d = d<sub>0</sub> + K<sub>d</sub>t<sup>1/2
</i></sup>
  <p>where d  = radial delamination from the flaw in the coating.
  <p>d<sub>0</sub> = a constant relating to the delay time of the system.
  <p>K<sub>d</sub> = delamination rate constant.
  <p>t<sup>½</sup> = square root of time of immersion.
  <p>There is considerable evidence that this part of the mechanistic delamination process occurs at open circuit potentials in corroding systems by workers such as Parks and Leidheiser<sup>(100)</sup>, Leidheiser, Wang and Igetoft<sup>(158)</sup>, and Sharman, Sykes and Handyside<sup>(159)</sup>.  Referral to Table 2 informs us that delamination rates in corroding systems tend to be greater than those in passive.  It is therefore likely that the rate-determining step in the delamination process of corroding and passive systems will be different.
  <p>The FBE-panels which were exposed to passivating solutions at open circuit underwent delamination.  The only electrochemical reactions which took place on their substrates in these conditions would have been the extremely small passive current derived from the formation and dissolution of the substrate surface oxide film.  It is therefore unlikely that the cause of delamination was an electrochemical process.
  <p>When the delamination rate constants of the FBE-panels are plotted on a logarithmic scale against the respective pHs of the passivating test solutions that they were exposed to, a linear relationship is derived (Figure 6).  Concentration of hydroxyl ions in solution is therefore directly proportional to the rate of FBE coating delamination, and the rate-controlling step in the delamination process is the passage of hydroxyl ions along the coating/substrate interface.  This information indicates that it is these hydroxyl ions which are directly responsible for coating delamination.  Kendig, Addison and Jeanjaquet<sup>(132)</sup> contend that hydroxide ions cause coating delamination by lowering electrolyte surface tension, thereby catalysing wetting of the steel and accelerating coating displacement by electrolyte penetration.  They support this claim with data showing a linear decrease in the surface tension of 0.5M NaCl on mild steel with pH in the range of 8-11 (Figure 7).  Kendig's supposition may be supported by the linear delamination rate constant/pH fit generated by the flat panel data, providing the linear relationship between pH and surface tension is continued at a higher pH region.  It is noted from his graph that it is based on a small number of data points, and that a logarithmic fit could just as easily be made through them, making his argument for surface tension being an important delamination parameter more tenuous.  It is equally possible that the linear pH / radial delamination rate constant relationship is related to the increasingly alkaline environments affecting acid-base interactions between coating and substrate, and hence polymer adsorption and adhesion, as suggested by Fowkes and Mostafa<sup>(138)</sup>.
  <p>Extrapolation of the pH / K<sub>d</sub> graph to the point of zero delamination rate constant correlates with a bulk solution pH of approximately 13.0.  This value will be associated with the minimum pH required to cause disbondment in the FBE / substrate system.  At pHs below 13.0 there should therefore be no delamination.  It has been shown that there was no delamination of coating from a non-chromated FBE-panel after 6 months of exposure to a pH 12.0 solution.  These observations suggest that the disbondment mechanism is unlikely to be either the chemical disruption of the polymer / metal oxide bonds by the presence of polar water molecules or through creation of a hydrodynamical stress on the bonds, as suggested by Funke<sup>(116)</sup>.  If the disbondment mechanism were related to hydrodynamical stress on the bonds, then increasing solution osmotic pressure would be expected to result in a decrease in delamination rate.  It has been shown that delamination will occur at pHs greater than approximately 13.0; the proposed oxide dissolution delamination mechanism proposed by Ritter and his co-workers<sup>(130,144-146)</sup> cannot therefore be the cause of this delamination because it will not be thermodynamically viable at a pH of 13.2<sup>(17)</sup>.  That the relationship between pH and K<sub>d</sub> is linear is of interest, since this means that K<sub>d</sub> is proportional to the logarithm of hydroxyl ion concentration in solution.  The delamination mechanism is therefore not a simple one; hydroxyl ions are not involved in a {1 hydroxyl ion : 1 coating/substrate adhesive bond disrupted} relationship.  It is therefore unlikely that delamination of the FBE coating ion is caused by its saponification.
  <p>Hydrated cation mobility is plotted versus delamination rate constant for corroding  FBE-panels in test solutions with chloride in Figure 8.  A trend is found showing that increasing hydrated cation mobility in the test solution results in an increased rate of coating delamination, an observation that is in agreement with the hydrated cation diffusion-limited delamination mechanisms at open circuit for specific coating systems postulated by Leidheiser and his co-workers<sup>(155,158,100)</sup>, Sharman, Sykes and Handyside<sup>(159)</sup>, and Nguyen and his co-workers<sup>(163-167)</sup>.
  <p>It may noted from the derived linear relationship of Figure 8 that no delamination should take place if the cations present in solution have an absolute ionic mobility of or below approximately 1.6 x 10<sup>8</sup>m<sup>2</sup>s<sup>-1</sup>V<sup>-1</sup>.  An explanation for this observation may be made by consideration of the important factors responsible for maintenance of a high pH at the disbondment front.  In the corroding underfilm system, the pH at the disbondment front will be controlled by the rate at which cations are transported to the cathode site and their maximum attainable pH, <i>and</i> the rate at which hydroxyl ions are transported away from it.  From the pH / delamination rate constant data recorded for non-corroding non-chromated FBE-Panel systems it may be seen that below a given pH, delamination will not take place.  It may be postulated from Figure 8 that this pH is also that which would be generated at the delamination front by a cation of absolute mobility 1.6 x 10<sup>8</sup>m<sup>2</sup>s<sup>-1</sup>V<sup>-1</sup>.  At cationic mobilities at or below this value the flux of hydroxyl ions away from the disbonding front would be too great for a sufficiently high pH to be generated to cause delamination.
  <p>When the delamination rate constants created with the relevant cations in the corrosion-inducing solutions are plotted on the best-line fit generated by delamination rates of FBE-coated panels in solutions of different pH (Figure 6), the corresponding pHs at the delamination front may be estimated (Figure 9).  These pHs are based on the assumption that pH at the delamination front of passive panels is essentially the same as that of the bulk solution, and that delamination of an FBE coating from a corroding substrate is predominantly due to hydroxyl ions.  The estimated pHs are shown in Table 3 below:

