<h4>Introduction</h4>
</dir>


  As part of a study of epoxy-coated rebar steel in concrete, work was also carried out at open circuit, using a clear epoxy powder and in various alkali environments. Variations in specific cation, pH and chloride ion concentration were examined. The mechanisms of adhesion loss is investigated.

<dir>

<h4>Experimental</h4>
</dir>


  Cold-rolled mild steel Q-Panels, 1 mm thick, were degreased by the process of washing in detergent solution, rinsing well in de-ionised water and then spraying with ethanol.
  <p>Pigment-free epoxy resin used was supplied by Akzo Nobel Powder Coatings. The panels were heated in an oven, such that when they were removed and the epoxy powder sprayed onto them they would be in the temperature range 235-240<sup>o</sup>C, a requirement of the coating for its curing.. A Gema Volstatic manual fluidised bed powder system and PG1 powder gun was used.  Sufficient powder was used to achieve a cured coating thickness of 100<font FACE="Symbol">m</font>
m. Cooled powder coating thickness was at all times within the range 100 ± 10 µm.  Panels with holidays in the powder coating were not used, the holidays being detected by using an oscilloscope to pass a sine wave across the coating.  A 3.0 mm diameter holiday was introduced into the epoxy coating by means of a flat-head drill bit such that the Q-panel steel was exposed.
  <p>Test cells were then created by adhering a perspex tube of 5 cm diameter and 6 cm nominal height to the epoxy surface of the mild steel panel, such that the artificial holiday was at its centre.  Adhesion was achieved by the sparing use of Silastic 732 RTV silicone sealant.
  <p>The test solutions used are shown in Table 1. All testing was made at open circuit. A minimum of three panels were used for each exposure condition.  The pH of the solutions was measured daily and adjusted as necessary to maintain the correct activity. Delamination rate (apparent by a lighter halo of delaminated coating) and half-cell potential measurements were made.

<dir>

<h4>Results and Discussion </h4>
</dir>


  Typical potential/time responses of FBE-coated panels with a 3mm holiday exposed to the different electrolytes are shown in Figure 1.
  <p>All the FBE-coated panels in solution with no chloride have potentials indicating their passivity or near-passivity.  Recorded panel potential becomes more base as solution pH is increased.  Their visual appearance also remained essentially unchanged.  The exposed steel at the 3mm holiday remained bright and untarnished.  No corrosion products were visible.  Delamination of coating from substrate could be followed by measuring the expansion of a circle of lighter or occasionally darker-coloured FBE-coating from the artificial holiday (Figure 2).
  <p>The effect of the addition of 0.6M chloride to the solutions that non-chromated FBE-coated panels were exposed to is shown in Figure 3.  The potentials of all the panels became more base, in the region of &#150;400 to &#150;550mV.  The exposed substrate at the artificial holiday tended to remain bright and untarnished.  Blue deposits began to appear underneath the coating adjacent to the holiday. At the edges of the artificial holidays adjacent to the blue areas under the coating, red deposits sometimes appeared.  These observations strongly suggest that corrosion was occurring.  The presence of a region of bright visibly uncorroded substrate adjacent to and at the delamination front indicates that delamination of the FBE coating from its substrate was not being caused by anodic undermining.  The presence of a cathode region at the coating/substrate disbondment front has been widely reported previously by many authors, and is often an implicit and sometimes explicitly stated part of their postulated coating delamination mechanism.  These authors include Wiggle, Smith and Petrocelli<sup>(125)</sup>, Funke<sup>(121)</sup>, Schwenk<sup>(168)</sup>, Leidheiser<sup>(129)</sup>, Watts and Castle<sup>(131)</sup>, Holubka, deVries and Dickie<sup>(141)</sup>, Ritter and Rodriguez<sup>(146)</sup>, Nguyen, Hubbard and McFadden<sup>(163)</sup>, Skar and Steinsmo<sup>(162)</sup>, and Fürbeth and Stratmann<sup>(149)</sup>.  From potential / time data it may be seen that corrosion of the FBE-coated test specimens at open circuit initiates soon after they are placed in the test solutions containing chloride ions.  The corrosion process does not take place on the exposed mild steel substrate of the holiday, but is seen to develop at its edge.  A tafel plot of substrate potential / current response in pore solution can be seen to be virtually identical in comparison with pore solution with chloride (Figure 4).  Only when the bond between the mild steel test specimen and its encapsulating epoxy araldite resin is poor does evidence of crevice corrosion appear.  It is therefore the belief of the author that corrosion initiates in a crevice between the FBE-coating and mild steel substrate, caused by the passive current density on the steel.
  <p>The delamination data derived for each exposure condition was averaged. These values are plotted in Figure 5, where the average radial delamination of the panel is plotted against the square root of the time of its exposure to the test solution.  A linear relationship is found when the delamination data is plotted using these axes.
  <p>It is also noted that on extrapolation of these plots to the x axis there is a period of time before delamination of the FBE coating from its substrate begins to take place (delay time).  The calculated delay time along with the equations of the lines of the plotted data is displayed in Table 2.  No data for panels exposed to pH 12.0 KOH solution is plotted because no delamination could be found after 6 months testing.

