<h3>Abstract</h3>

<p>A clear commercial epoxy-powder lacquer, has been applied by electrostatic

spray onto mild steel panels and cured at 235&deg;C at a film thickness of 100

&mu;m. Coatings were tested in alkaline environments of mainly pH 13.2.

Assessment was by exposure at open circuit where delamination rates were

measured as a function of time. Variations in cation and cation concentration

and the presence or absence of chloride were studied. Delamination mechanisms

have been proposed.</p>

<SCRIPT>comment(2)</SCRIPT><p><b>Keywords: </b>Epoxy lacquers, power coatings, mild steel, alkali environments, adhesion,</p>

  <h3>Introduction</h3>

  <SCRIPT>comment(3)</SCRIPT><p>This paper is part of a study of epoxy-coated rebar steel in concrete. In

  continuation of previous work [1], investigations were made at open circuit,

  using a clear epoxy powder on mild steel panels in various alkali

  environments. Variations in specific cation, pH and presence of chloride were

  examined. The mechanisms of adhesion loss are investigated.</p>

