  <h3>Experimental</h3>

  </b>

  <SCRIPT>comment(4)</SCRIPT><p>Cold-rolled mild steel Q-Panels, 1 mm thick, were degreased by washing in

  detergent solution, rinsing well in de-ionised water and then spraying with

  ethanol. Pigment-free epoxy powder was supplied by Akzo Nobel Powder Coatings.

  The panels were heated in an oven, such that when they were removed and the

  epoxy powder sprayed onto them they would be in the temperature range 235-240<sup>o</sup>C,

  a requirement of the coating for its curing.. A Gema Volstatic manual

  fluidised bed powder system and PG1 powder gun was used to achieve a powder

  coating thickness within the range 100 � 10 �m. Panels with obvious defects

  in the powder coating were not used. A 3.0mm diameter holiday was introduced

  into the epoxy coating by means of a flat-head drill bit such that the Q-panel

  steel was exposed. As an alternative to the 3mm holiday, a pinhole was

  introduced to some specimens.</p>

  <SCRIPT>comment(5)</SCRIPT><p>Test cells were then created by adhering a perspex tube of 5 cm diameter

  and 6 cm nominal height to the epoxy-coated panel, such that the artificial

  holiday was at its centre. Adhesion was achieved with the sparing use of

  silicone sealant.</p>

