  <SCRIPT>comment(32)</SCRIPT><p>These pHs are less than those attainable by the cation hydroxides at

  saturation. The rate-controlling step in the delamination process of corroding

  FBE-coated panels is therefore the passage of the hydrated cations through the

  holiday and along the coating/substrate interface to the cathodic site. The

  cations are being transported to the cathode such that charge balance may be

  achieved.</p>

  <SCRIPT>comment(33)</SCRIPT><p>It may noted from the derived linear relationship of Figure 9 that no

  delamination should take place if the cations present in solution have an

  absolute ionic mobility of or below approximately 1.6 x 10<sup>8</sup>m<sup>2</sup>s<sup>-1</sup>V<sup>-1</sup>.

  An explanation for this observation may be made by consideration of the

  important factors responsible for maintenance of a high pH at the disbondment

  front. In the corroding underfilm system, the pH at the disbondment front will

  be controlled by the rate at which cations are transported to the cathode site

  and their maximum attainable pH, <i>and</i> the rate at which hydroxyl ions

  are transported away from it. From the pH / delamination rate constant data

  recorded for non-corroding non-chromated FBE-Panel systems it may be seen that

  below a given pH, delamination will not take place. It may be postulated from

  Figure 8 that this pH is also that which would be generated at the

  delamination front by a cation of absolute mobility 1.6 x 10<sup>8</sup>m<sup>2</sup>s<sup>-1</sup>V<sup>-1</sup>.

  At cationic mobilities at or below this value the flux of hydroxyl ions away

  from the disbonding front would be too great for generation of a sufficiently

  high pH to cause delamination.</p>

  <SCRIPT>comment(34)</SCRIPT><p>From SEM analysis it has been seen that disbondment does not leave visible

  coating on the steel panel substrate (Figures 4 and 5). The FBE-resin is

  already intimately mixed with its hardener in its powder form prior to

  spraying onto the heated panel, and its gel time occurs in a matter of seconds

  [24]. There is therefore no time for a separation of resin and hardener to

  take place, as has been suggested for solvent-based paint systems [25]. The

  FBE coating should therefore approximate the same chemical and structural

  composition from the outer limit of the polymer / substrate interphasial

  region to its external surface. In this situation, if the mechanism of

  adhesion loss was coating degradation and cohesive failure, then it would be

  equally likely to occur at any depth of the FBE coating on the passive

  specimens at open circuit. Failure always appeared to occur at or near the

  coating / substrate interface, i.e. in the interfacial or interphasial region,

  so these mechanisms were not the cause of disbondment.</p>

