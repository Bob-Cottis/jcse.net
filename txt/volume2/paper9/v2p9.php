  <SCRIPT>comment(26)</SCRIPT><p>Extrapolation of the pH / K<sub>d</sub> graph to the point of zero

  delamination rate constant correlates with a bulk solution pH of approximately

  13.0. This value will be associated with the minimum pH required to cause

  disbondment in the FBE / substrate system. At pHs below 13.0 there should

  therefore be no delamination. It has been shown that there was no delamination

  of coating from an FBE-panel after 6 months of exposure to a pH 12.0 solution.

  These observations suggest that the disbondment mechanism is unlikely to be

  either the chemical disruption of the polymer / metal oxide bonds by the

  presence of polar water molecules or through creation of a hydrodynamical

  stress on the bonds, as suggested by Funke [16]. If the disbondment mechanism

  were related to hydrodynamical stress on the bonds, then increasing solution

  osmotic pressure would be expected to result in a decrease in delamination

  rate. It has been shown that delamination will occur at pHs greater than

  approximately 13.0; the proposed oxide dissolution delamination mechanism

  proposed by Ritter and his co-workers [6,17-19] is therefore unlikely to be

  the cause of this delamination because it will not be thermodynamically viable

  at a pH of 13.2 [20]. That the relationship between pH and K<sub>d</sub> is

  linear is of interest, since this means that K<sub>d</sub> is proportional to

  the logarithm of hydroxyl ion concentration in solution. The delamination

  mechanism is therefore not a simple one; hydroxyl ions are not involved in a

  {1 hydroxyl ion : 1 coating/substrate adhesive bond disrupted} relationship.

  It is therefore unlikely that delamination of the FBE coating ion is caused by

  its saponification.</p>

