  <SCRIPT>comment(19)</SCRIPT><p>This parabolic delamination shows that rate of FBE disbondment is greatest

  upon its initiation, decreasing with time until a theoretical cessation of

  disbondment at infinity. More importantly from a mechanistic viewpoint, this

  relationship indicates that the rate-determining process is along the coating

  / substrate interface from the coating holiday. This relationship has also

  been found for coated systems corroding at open circuit by other workers

  [10,11,14]. If transport of this delamination parameter was through the

  coating, then a linear relationship between radial delamination and time of

  immersion would be encountered.</p>

