  <SCRIPT>comment(16)</SCRIPT><p>The presence of a cathode region at the coating/substrate disbondment front

  has been widely reported previously by many authors [3-12], and is often an

  implicit and sometimes explicitly stated part of their postulated coating

  delamination mechanism. Potential/time data shows that corrosion of the FBE-coated

  panels initiates soon after they are placed in the test solutions containing

  chloride ions. The corrosion process does not take place on the exposed mild

  steel substrate of the holiday, but is seen to develop at its edge. A Tafel

  plot of substrate potential/current response in pore solution can be seen to

  be virtually identical in comparison with pore solution with chloride (Figure

  6). Only when the bond between the mild steel test specimen and its

  encapsulating epoxy araldite resin is poor does evidence of crevice corrosion

  appear. It is suggested that corrosion initiates in a crevice between the FBE-coating

  and mild steel substrate, caused by the passive current density on the steel.</p>

