  <SCRIPT>comment(38)</SCRIPT><p>Leidheiser, Wang and Igetoft [21] linked delay time to ion ingress into the

  coating, and Wang [27] to water diffusion through it. The differing delay

  times of the corroding systems cannot be explained in terms of an interfacial

  attack process from the holiday at the coating / substrate interface. If this

  was the important determinant, then delay times should be very similar because

  the samples exposed to chloride started to corrode after approximately the

  same period of immersion. Delay time may therefore be related to a transport

  process through the FBE-coating. There is no evidence that it is related to

  cationic transport, so this delay time is probably a function of either water

  or oxygen transport. Mayne [28] has shown that oxygen transport through a

  coating is a relatively rapid process, and Cottis [29] has stated that a

  typical oxygen permeability of an epoxy resin is 5.2 x 10<sup>-12</sup> mole.m<sup>-1</sup>s<sup>-1</sup>atm<sup>-1</sup>,

  so it is unlikely that this factor could account for the length of delay time

  recorded in these conditions (typically 3-4 days<sup>�</sup>). It is

  therefore possible that the delay time of the corroding system at open circuit

  is related to water transport through the coating.</p>

  <h3>Conclusions</h3>

  <SCRIPT>comment(39)</SCRIPT><p>The following conclusions may be drawn from this investigation into

  delamination of FBE-coatings in alkaline environments:</p>

  <ul>

    <li>Delay time before initiation of observable delamination processes may be

      a function of water penetration through the coating to the interfacial or

      interphasial coating/substrate region.</li>

    <li>Delamination of FBE coatings from steel substrates is predominantly

      caused by hydroxyl ions.</li>

    <li>Rate of FBE delamination is controlled by transport processes from a

      pore in the coating and along the delaminated coating/substrate interface

      to the disbondment front.</li>

    <li>The locus of failure of coating adhesion is in the interfacial or

      interphasial coating/substrate region.</li>

    <li>The rate of FBE delamination in near-passive conditions is controlled by

      hydroxyl ion migration from the bulk external solution to the

      coating/substrate disbondment front.</li>

    <li>The rate of FBE delamination in the condition of underfilm corrosion is

      controlled by hydrated cation movement to the cathode site.</li>

  </ul>

