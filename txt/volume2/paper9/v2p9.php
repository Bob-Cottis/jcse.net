  <SCRIPT>comment(22)</SCRIPT><p>The FBE-panels which were exposed to passivating solutions at open circuit

  underwent delamination. The only electrochemical reactions which took place on

  their substrates in these conditions would have been the small passive current

  derived from the formation and dissolution of the substrate surface oxide

  film. It is therefore unlikely that the cause of delamination was an

  electrochemical process.</p>

  <SCRIPT>comment(23)</SCRIPT><p>When the delamination rate constants of the FBE-panels are plotted on a log<sub>10</sub>

  scale against the respective pHs of the passivating test solutions that they

  were exposed to, a linear relationship is derived (Figure 8). Concentration of

  hydroxyl ions in solution is therefore directly proportional to the rate of

  FBE coating delamination, and the rate-controlling step in the delamination

  process is the passage of hydroxyl ions along the coating/substrate interface.

  This information indicates that it is these hydroxyl ions which are directly

  responsible for coating delamination. It is possible that this relationship is

  due to the increasingly alkaline environments affecting acid-base interactions

  between coating and substrate, and hence polymer adsorption and adhesion, as

  suggested by Fowkes and Mostafa [15].</p>

