<h2>A Sol-gel derived anti-corrosion pigment</h2>

<p>Authors:
J.D.Scantlebury and Dezhu Xiu</p>

<p><i>Corrosion and Protection Centre, UMIST, PO Box 88,
Manchester, M60 1QD, UK<br>
e-mail address: <SCRIPT>mailto2('Scantlebury','manchester.ac.uk');</SCRIPT></i></p>

<SCRIPT>notify();</SCRIPT><SCRIPT>comment(1)</SCRIPT><H3>Abstract</H3>
<SCRIPT>comment(2)</SCRIPT><p><b>Keywords: </b> anti-corrosion pigment, corrosion inhibitor,
amine. Sol-gel, titania</p>

<h3>Introduction</h3>

<SCRIPT>comment(3)</SCRIPT><p>The use of anti-corrosion
pigments in paints is becoming more and more restricted because of issues of
toxicity. Of the traditional pigments only zinc phosphate is being used to any
great extent. Any standard text on corrosion inhibitors[1] refers to a large
number of organic compounds which are corrosion inhibitors, many of which have
potential uses in organic coatings. However, in spite of the excellent studies
on organic inhibitors in paints carried out by Braig and reported in several of
these Conferences[2][3], the widespread use of organic corrosion inhibitors in
conjunction with organic coatings has yet to make a major impact on the paint
industry. This may be due to a natural conservatism and reticence on the part
of the paint industry, or it may be due to problems associated with
incorporation of an organic specie into an organic polymeric medium. </p>

<SCRIPT>comment(4)</SCRIPT><p>A problem arises in
that many functional groups in organic corrosion inhibitors, amines, amides,
thiophenes, acids etc are also capable of reacting with fixed functional groups
in many common binder systems such as epoxys and polyurethanes. The potential
organic inhibitor may easily react with a functional group in a binder. The
inhibitor is then rendered insoluble and is incapable of diffusing to the paint
metal interface and making any positive contribution to anti-corrosion
performance.</p>

<SCRIPT>comment(5)</SCRIPT><p>One way of solving
this problem is by encapsulating the inhibitor molecule and using the
encapsulant as an anti-corrosion pigment. The inhibitor is then slowly released
into the paint and down to the metal surface. </p>

<SCRIPT>comment(6)</SCRIPT><p>Encapsulating
techniques are commonly used in other technologies, i.e. for the delivery of
drugs, use of pesticides, provision of fragrances in the cosmetics industry,
but in anti-corrosion, encapsulation is still a relatively new technology. The
encapsulation method using sol-gel technology is a possible avenue.</p>

<SCRIPT>comment(7)</SCRIPT><p>Sol-gel has been
defined by Brinker and Scherer[4] as </p>

<p>�the preparation of ceramic materials by preparation of a sol, gelation
of a sol, and removal of the solvent. The sol may be produced from inorganic or
organic precursors.�</p>

<SCRIPT>comment(8)</SCRIPT><p>Although the name
sol-gel is relatively novel, the technology in not. Sodium silicate or
water-glass has been used as a preservative for eggs for centuries and as a
coating for plaster.� Ethyl silicate was
synthesised and investigated by Wurtz in the last century. In the paint
industry, sol-gel methods have been used in both inorganic and organic zinc
silicate systems. </p>

<SCRIPT>comment(9)</SCRIPT><p>The incorporation of
organic molecules into sol-gels has been referred to in [5] for use in optics,
catalysis and sensors. However, it is believed that this is the first time
sol-gel methods have been reported for the generation of an anti-corrosion
pigment for paints.</p>

<SCRIPT>comment(10)</SCRIPT><p>In this study, three
alkoxide precursors have been studied based on silicon, titanium and aluminium
chemistry; tetraethyl orthosilicate (TEOS), titanium isopropoxide and aluminium
isopropoxide. The corrosion inhibitor which was investigated was a diamine
(NN). Previous studies[6] on corrosion testing by immersion of 3000 series
aluminium in 3.5% NaCl solution containing the diamine, had shown that this
diamine was indeed an excellent inhibitor for the corrosion of this aluminium
alloy, even at levels of 0.01 ppm. Specific details of the formula of this
inhibitor are unable to be released because of possible commercial interests.</p>

<SCRIPT>comment(11)</SCRIPT><p>Four questions were
addressed in this study.</p>

<p>�&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
What are the
miscibility characteristics of the ternary systems, alkoxide, water, ethanol?</p>

<p>�&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
How does the
decomposition of the alkoxide vary with pH and temperature?</p>

<p>�&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
How can
encapsulation of the corrosion inhibitor be optimised in terms of release rate?</p>

<p>�&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
Can we identify a
specific system which can be prepared and used in a typical paint formulation?</p>

<h3>Experimental</h3>

<SCRIPT>comment(12)</SCRIPT><p>Tetraethylorthosilicate,
titanium and aluminium isopropoxide were obtained from Aldrich chemicals. </p>

<h4>Miscibility characteristics</h4>

<SCRIPT>comment(13)</SCRIPT><p>At room temperature,
each alkoxide was added to a glass test tube containing ethanol. After
dissolution, water is added. After shaking by hand for five minutes, the
solution was observed. The composition was deemed immiscible when the solution
turned colloidal. In this way a series of miscibility diagrams were produced
for the three systems and are shown in Figures 1 to 3.</p>

<h4>Alkoxide decomposition</h4>

<SCRIPT>comment(14)</SCRIPT><p>On mixing an alkoxide
with water, decomposition by hydrolysis takes place instantly. To optimise the
process, the influence of pH and temperature was studied. The test procedure
was as follows</p>

<SCRIPT>comment(15)</SCRIPT><p>0.01mol of the alkoxides was placed in a 50 ml glass test tube and 1ml
of water and 10 mls of ethanol was added. Two drops of 0.1N HCl and/or ammonium
hydroxide (30%) were added for pH adjustment. The pH was varied between 2 and
13 in unit increments and the temperature between 20 and 60<sup>0</sup>C in 10<sup>0</sup>
increments, 48 variables in all. That a gel is formed is judged by observing
the solution which on gelation becomes static when gently shaken.</p>

<SCRIPT>comment(16)</SCRIPT><p>Figures 4, 5 and 6 are
the data from these experiments.</p>

<h4>Encapsulation of corrosion
inhibitor</h4>

<SCRIPT>comment(17)</SCRIPT><p>The encapsulation of
the corrosion inhibitor is accomplished in three stages.</p>

<SCRIPT>comment(18)</SCRIPT><p>����� Stage 1; Pre hydrolysis</p>

<p>����� 0.1M
of the alkoxide was mixed with 45 ml of ethanol until dissolution and then a
1:1 mixture of ethanol/ammonia was added to give a pH of 9-10. On increasing
the temperature to 50<sup>0</sup>C the solution gradually thickens indicating
the onset of gelation.</p>

<SCRIPT>comment(19)</SCRIPT><p>����� Stage 2; Addition of corrosion inhibitor</p>

<p>A known quantity of the NN inhibitor was dissolved in 25ml of water and
added slowly with stirring to the alkoxide solution at 50<sup>0</sup>C to give
30, 40 and 50% weight of the total material. For example, 5.3g of the NN will
produce 40% in the titanium isopropoxide system. The pH is maintained at 9-10
by adding the ethanol/ammonia mixture, again with stirring.</p>

<SCRIPT>comment(20)</SCRIPT><p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Stage 3; addition of modifier.</p>

<p>On addition of the inhibitor, the solution thickened. A further 10ml
ethanol was added to reduce the solution viscosity. A known quantity of raw
linseed oil was added to the mixture and stirring continued for a further 5
minutes. Typically 5.3g of linseed oil gives a 7% solution in the titanium
system.</p>

<h4>Preparation of the pigment
particles</h4>

<SCRIPT>comment(21)</SCRIPT><p>The mixture was allowed to cool (about one week) after
which time it had become totally solid. The preparation of the solid particles
was accomplished manually using an agate pestle and mortar to give a particle
size of not greater than 5&#956;m.</p>

<h4>Release Rate of inhibitor</h4>

<SCRIPT>comment(22)</SCRIPT><p>1 litre of 3.5% NaCl
was contained in an Erlenmeyer flask. 1 gram of the pigment particles was
added. A brown colour appeared in the solution, indicating release of NN from
the sol gel. The change in NN concentration in solution was followed using UV
absorption, UNICAM UV Spectrophotometer, SP1700. Release rates as a function of
linseed oil concentration, and NN concentration over a 30 day period are shown
for the three alkoxides in Figures 7-15.</p>

<h3>Discussion</h3>

<SCRIPT>comment(23)</SCRIPT><p>Figures 1-3 are the
miscibility diagrams for the silicon, titanium and aluminium alkoxide systems.
Zone A is where a homogeneous mixture exists at room temperature. B is where
the system is immiscible. It is clear from the three diagrams that the titanium
system has the largest miscibility region. Figures 4-6 indicate the effects of
pH and temperature on the gel time for the three systems studied. The rates of
gelation are clearly pH and temperature dependent. Compared with neutral pH,
the rate of gelation is greatly increased as the pH is moved either in an
alkali or acid direction. The rate of gelation at neutral pH conditions and at
room temperature, is fastest for the titanium based system. Figures 7-15
clearly demonstrate that the inhibitor is released over a period of 30d from
the sol-gel encapsulated pigment, that addition of small amounts of linseed oil
does effect the rate of release, especially in the titanium system and that
again with the titanium system, low levels of release can be achieved. From all
these data, it was decided to work with a titanium based sol-gel, which on
decomposition would yield a titania type powder. Paint formulators are used to
handling rutile as a pigment and similar compounds were thought to have certain
advantages. The optimum concentration chosen on the basis of the data generated
in this paper was 7% linseed oil and a 40% NN concentration.</p>

<SCRIPT>comment(24)</SCRIPT><p>Tests are currently
underway to assess whether the diamine/titanium based sol-gel pigment modified
with linseed oil is a successful anticorrosion additive in a paint for aluminium.</p>

