<h3><B>4. 
Discussion</B> </h3></DIV>
<DIV 
align=left>
<script>comment();</script><p>Generally 
it can be expected that the process of metal dusting of iron aluminium alloys 
includes surface oxidation and the onset of metal dusting via carburisation 
reactions at defects of an oxide layer. </P></DIV>
<DIV 
align=left>
<script>comment();</script><p>As 
mentioned above, the aim of the present work is to investigate metal dusting 
processes and not the effect of pre-oxidation on the resistance against metal 
dusting. </P></DIV>
<DIV 
align=left>
<script>comment();</script><p>Regarding           
Al<SUB>2</SUB>O<SUB>3</SUB> formation the following information is relevant for           
an understanding of metal dusting experiments of Fe-Al alloys in carburising and           
oxidising CO-H<SUB>2</SUB>-H<SUB>2</SUB>O atmospheres. Although aluminium oxide           
was not detected by XRD analysis due to very small volume fractions its presence           
can be assumed because the oxygen partial pressure in the           
CO-H<SUB>2</SUB>-H<SUB>2</SUB>O gas mixtures is significantly higher than the           
value necessary for alumina formation. For example the equilibrium partial           
pressure of oxygen of the 50%CO-49.3%H<SUB>2</SUB>-0.7%H<SUB>2</SUB>O gas           
mixture is 5.05x10<SUP>-32</SUP> bar at 500 °C and 3.77x10<SUP>-25</SUP> bar at           
700 °C. However, the oxygen partial pressure required for           
Al<SUB>2</SUB>O<SUB>3</SUB> formation is only 2.77x10<SUP>-65</SUP> bar at 500           
°C and 8.99x10<SUP>-50</SUP> bar at 700 °C. As it is shown for chromia forming           
alloys [#ref<I>18-20</I>] the formation of an oxide layer generally retards the           
carburisation process. Metal dusting attack occurs preferentially at defects of           
the oxide layer with subsequent pit formation (see Figs. 3a, 4a, and 10). At           
present, conclusions concerning details of the pit formation during metal           
dusting of iron aluminium alloys can not be drawn. Also the influence of size           
and distribution of defect in the oxide layer on the metal dusting kinetics is           
not yet understood. From the TGA results shown in Fig. 9 it can be deduced that           
an increase of the aluminium content leads to a retardation of the onset of           
metal dusting. </P></DIV>          
<DIV 
align=left>
<script>comment();</script><p>The 
metal dusting attack is differently characterised as pitting (Figs. 2b, 3a, 4a, 
and 10) or general metal wastage (see Figs. 2a, 11a and 11b). Concerning this 
simple distinction it should be mentioned that local attack with many fast 
growing pits and general metal wastage might be the same. </P></DIV>
<DIV 
align=left>
<script>comment();</script><p>Some           
results of the present work confirm the assumption that the alumina formation           
depends on temperature, gas composition and aluminium content. At 500, 600 and           
650 °C no oxides were detected in this work. However, the light optical           
microscopic observation of a thin oxide layer (Fig. 2d) on the Fe-26Al sample           
carburised at 700 °C indicates that an increase of the temperature enhances the           
formation of aluminium oxide. For this sample no metal dusting was detected (see           
also the TGA curves in Figs. 1 and 9c). Thus it can be concluded that the           
presence of the fairly dense oxide layer formed at a relatively high temperature           
of 700 °C leads to a significant protection against metal dusting. However,           
decreasing the aluminium content to 15 at. % leads to a serious attack (Fig.           
9c). </P></DIV>
<DIV 
align=left>
<script>comment();</script><p>At           
temperatures below 700°C no significant protection against metal dusting of           
Fe-Al alloys is observed. Thus it can be concluded that at relatively low           
temperatures there is no distinctive formation of protecting           
Al<SUB>2</SUB>O<SUB>3</SUB> layers. This result confirms the reports that with           
increasing temperature the formation of a dense aluminium oxide layer is           
favoured [#ref<I>23</I>]. The formation of <font face="Times New Roman">α</font>-Al<SUB>2</SUB>O<SUB>3</SUB> is           
optimal for materials protection because of its very dense crystal structure.           
However, α-Al<SUB>2</SUB>O<SUB>3</SUB> formation occurs at temperatures around           
1000 °C or higher [#ref<I>24</I>]. Therefore, a pre-treatment for oxide layer           
formation is required in order to protect iron aluminium alloys from metal           
dusting. The aspect of protective scales will be treated in future. </P></DIV>          
<DIV 
align=left>
<script>comment();</script><p>At           
those locations where carburisation can take place the formation of carbon           
profiles with decreasing carbon content from the surface to the matrix can be           
assumed. Supersaturation of the matrix with carbon will lead to carbide           
formation. Thermodynamic calculations showed that for a binary Fe-15Al alloy the           
formation of the <font face="Times New Roman">κ</font>-phase Fe<SUB>3</SUB>AlC<SUB>x</SUB> and for increased carbon           
contents additional cementite Fe<SUB>3</SUB>C precipitation can be expected           
during carburisation at 650 °C [#ref<I>4</I>]. In this work, κ-phase with Perovskite structure was detected by XRD on Fe-26Al samples carburised at 500           
and 600 °C (Fig. 6). Plate- or needle-like Fe<SUB>3</SUB>AlC<SUB>x</SUB>           
precipitates are directly observed in the matrix close to the surface of a           
carburised Fe-15Al alloy (see Fig. 11). Cross-section analysis by light optical           
and scanning electron microscopy does not reveal the presence of cementite in           
the matrix, but with XRD analysis of the coke removed from this sample cementite           
was detected (Fig. 7). The experimental observation of cementite present in the           
coke leads to the assumption that there are similarities between metal dusting           
of iron and of iron aluminium. The mechanism of metal dusting of iron aluminium           
alloys will be studied in more detail in the future. </P></DIV>          
<DIV 
align=left>
<script>comment();</script><p>The           
TGA curves in Fig. 1 show that for Fe-26Al alloys the metal dusting rate reaches           
its maximum at about 600°C. This result is very similar to that of pure iron           
reported by Chun et al. [#ref<I>25</I>]. In their report the rate of metal           
dusting has its maximum at 575°C. This finding supports the assumption of           
similarities between metal dusting of Fe and of Fe-Al. </P></DIV>          
<DIV 
align=left>
<h3><B>5. 
Conclusions</B> </h3></DIV>
<DIV 
align=left>
<script>comment();</script><p>In           
this work metal dusting of Fe-15Al, Fe-26Al, and Fe-40Al alloys was investigated           
in CO-H<SUB>2</SUB>-H<SUB>2</SUB>O gas mixtures at temperatures from 500 to 700           
°C. Generally it can be expected that the main processes of metal dusting of           
Fe-Al alloys are oxide formation and carburisation at defects of the oxide at           
the surface. The effect of pre-oxidation was not investigated. </P></DIV>          
<DIV 
align=left>
<script>comment();</script><p>Thermogravimetric 
analysis (TGA) reveals that the onset time of metal dusting depends on 
temperature and alloy composition for a given gas composition. The metal dusting 
attack is characterised as pitting or general metal wastage. </P></DIV>
<DIV 
align=left>
<script>comment();</script><p>The           
investigation of the influence of temperature on metal dusting on Fe-26Al           
reveals a maximum carburisation rate at about 600 °C. With increasing           
temperature and with increasing Al content the formation of an           
Al<SUB>2</SUB>O<SUB>3</SUB> layer is favoured. </P></DIV>          
<DIV 
align=left>
<script>comment();</script><p>In           
the case of a Fe-26Al alloy carburised at 700 °C an Al<SUB>2</SUB>O<SUB>3</SUB>           
layer was detected. For this sample metal dusting was prevented from starting           
for at least 72 h. Cementite Fe<SUB>3</SUB>C and also the κ-phase were detected           
by X-ray diffraction (XRD) and scanning electron microscopy (SEM). The role of           
carbides in the decomposition process still remains unclear. The coke on the           
surface of the carburised samples was determined to consist mainly of fine           
filaments with iron-containing particles at both pitted and non pitted areas.           
</P></DIV>
