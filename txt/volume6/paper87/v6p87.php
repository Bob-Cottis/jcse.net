<h3><B>Abstract</B> 
</h3></DIV>
<DIV 
align=left>
<script>comment();</script><p>Iron 
aluminium alloys are reasonably resistant against high temperature oxidation and 
sulphidation. There is only little information available about carburisation and 
metal dusting of iron aluminium alloys. In this work metal dusting of Fe-Al 
alloys was investigated in CO-H<SUB>2</SUB>-H<SUB>2</SUB>O gas mixtures at 
different temperatures. </P></DIV>
<DIV 
align=left>
<script>comment();</script><p>The           
effect of temperature on metal dusting was investigated on Fe-26Al alloys           
(compositions are given in at. %). By thermogravimetric analysis it was found           
that the carburisation rate reaches its maximum at about 600 °C. With increasing           
temperature and with increasing aluminium content from 15 to 40 at. % the           
tendency of the formation of a protective aluminium oxide layer on Fe-Al alloys           
becomes stronger. An alumina (Al<SUB>2</SUB>O<SUB>3</SUB>) layer was observed at           
the surface of a Fe-26Al sample carburised at 700 °C. In this case no metal           
dusting was observed for at least 72 h. By X-ray diffraction analysis (XRD)           
cementite (Fe<SUB>3</SUB>C) was detected in the coke. The 
<font face="Times New Roman">κ</font>-phase           
Fe<SUB>3</SUB>AlC<SUB>x</SUB> was identified as a precipitate in the matrix. The           
graphite on the surface of the carburised samples was determined to consist           
mainly of fine filaments with iron-containing particles. </P></DIV>          
<DIV 
align=left>
<script>comment();</script><p><B>Keywords: 
</B>Iron aluminides, metal dusting, carburisation, oxide layer, cementite,           
κ-phase. </P></DIV>
<DIV 
align=left>
<h3>1. Introduction 
</h3></DIV>
<DIV 
align=left>
<script>comment();</script><p>Iron           
aluminium based alloys are generally regarded as promising for materials for           
high-temperature applications. High temperature corrosion studies of Kl<font face="Times New Roman">ö</font>wer           
[#ref<I>1</I>] and Stott et al. [#ref<I>2</I>] showed that iron aluminides           
exhibit a reasonable corrosion resistance to high temperature oxidation and           
sulphidation due to the formation of a dense protective alumina           
(Al<SUB>2</SUB>O<SUB>3</SUB>) scale. However, there is only little information           
about carburisation and metal dusting of iron aluminium alloys [#ref<I>1</I>,           
#ref<I>3-4</I>]. </P></DIV>
<DIV 
align=left>
<script>comment();</script><p>Metal           
dusting is a high temperature corrosion process which attacks iron, low and high           
alloy steels and Co- or Ni-based alloys in strongly carburising gas atmospheres           
with carbon activities a<SUB>C</SUB> &gt;&gt; 1 at temperatures between 400 and           
1000 °C [#ref<I>5</I>]. Metal dusting of iron and steels at various conditions           
such as temperature and carbon activity has been studied by [#ref6-17] using           
CO-H<SUB>2</SUB>-H<SUB>2</SUB>O and CH<SUB>4</SUB>-H<SUB>2</SUB> gas mixtures.           
</P></DIV>
<DIV 
align=left>
<script>comment();</script><p>The           
metal dusting of iron and low alloyed steels during exposure to           
CO-H<SUB>2</SUB>-H<SUB>2</SUB>O mixtures with carbon activities a<SUB>C           
</SUB>&gt; a<SUB>C</SUB>(Fe/Fe<SUB>3</SUB>C) has been explained by the following           
reaction mechanism [#ref<I>6-10</I>]: (i) rapid supersaturation of the metal           
phase with dissolved carbon by transfer from the gas phase, (ii) growth of a           
cementite layer at the surface, (iii) graphite deposition, (iv) subsequent           
cementite decomposition Fe<SUB>3</SUB>C <font face="Times New Roman">→</font> 3 Fe + C into graphite and fine metal           
particles, which (v) act as catalysts for coke deposition. Catalytic coke           
deposition can be observed in thermogravimetric experiments by a drastic           
acceleration of the mass gain kinetics. </P></DIV>          
<DIV 
align=left>
<script>comment();</script><p>The 
coke layer contains graphite and finely dispersed iron particles [#ref<I>10</I>] 
or carbide particles [#ref<I>13-14</I>, #ref<I>17</I>]. </P></DIV>
<DIV 
align=left>
<script>comment();</script><p>Protection 
of steels against metal dusting using chromium oxide 
(Cr<SUB>2</SUB>O<SUB>3</SUB>) layers was tested by [#ref<I>18-20</I>]. 
Chromium-containing steels are to a large extent resistant against metal 
dusting. This holds for the surface areas which are covered with 
Cr<SUB>2</SUB>O<SUB>3</SUB>, while at defects of the chromia layers local metal 
dusting was observed. </P></DIV>
<DIV 
align=left>
<script>comment();</script><p>Alumina           
(Al<SUB>2</SUB>O<SUB>3</SUB>) forming iron aluminium alloys are promising           
high-temperature materials for application in the petrochemical industry. Here           
the gaseous environments can have very low oxygen partial pressures. Thus only a           
very thermodynamically stable oxide such as Al<SUB>2</SUB>O<SUB>3</SUB> can be           
considered to form a protective scale. First experiments on the metal dusting           
behaviour of Fe<SUB>3</SUB>Al were performed by <I>Grabke</I> et al.           
[#ref<I>3</I>]. The constitution of the Fe-Al-C was investigated by <I>Palm</I>           
and <I>Inden</I> [#ref<I>21</I>]. A ternary carbide           
Fe<SUB>3</SUB>AlC<SUB>x</SUB> (κ-phase) exists in this system which might play           
an important role as suggested by <I>Grabke</I> [#ref<I>3</I>], but his first           
experiments revealed only the presence of cementite after metal dusting           
treatment of Fe<SUB>3</SUB>Al alloys. The mechanisms of metal dusting of iron           
aluminium alloys are still not understood. The resistance of Fe-Al-Cr alloys           
against metal dusting was studied by <I>Klöwer</I> [#ref<I>1</I>]. The           
investigated alloys showed good resistance against corrosive attack in           
CH<SUB>4</SUB>-H<SUB>2</SUB> gas mixtures at 850, 1000 and 1100 °C. </P></DIV>          
<DIV 
align=left>
<script>comment();</script><p>In 
this work investigations on metal dusting of binary <FONT color=black>iron 
aluminium alloys are presented. </FONT>Generally it can be expected that the 
process of metal dusting of iron aluminium alloys includes surface oxidation and 
carburisation reactions at defects of an oxide layer. The aim of the present 
work is to investigate metal dusting processes, which may occur at such defects 
or at the whole surface. The effect of pre-oxidation is not investigated. 
</P></DIV>
<DIV 
align=left>
<h3><B>2. 
</B><B>Experimental</B> </h3></DIV>
<DIV 
align=left>
<script>comment();</script><p><FONT 
color=black>The iron aluminium alloys were processed by vacuum induction 
melting. The melts were cast into cylindrical copper moulds (32 mm in diameter 
by 186 mm in length). The as-cast alloys were then cut by spark-erosion to 
obtain disks with d = 1 mm in thickness. Quarter segments of the disks were used 
for the metal dust</FONT><FONT color=black>ing experiments. Before starting the 
experiment t</FONT>he samples were ground (1000 grit). Thermogravimetric           
analysis (TGA) was carried out by measuring the mass gain versus time using a 
microbalance with an accuracy of 1<font face="Times New Roman">μ</font>g. The samples were fixed at the mass balance           
by quartz filaments. At the end of the treatment the quartz filaments holding           
the sample were broken so that the sample fell on a quartz plate in the cold           
region of the TGA apparatus. </P></DIV>          
<DIV 
align=left>
<script>comment();</script><p>Gas           
mixtures of CO-H<SUB>2</SUB>-H<SUB>2</SUB>O were used for the metal dusting           
treatment. The water vapour pressure was established by passing H<SUB>2</SUB>           
through a mixture of oxalic acid and its dihydrate at a certain temperature           
[#ref<I>22</I>]. The following two gas mixtures were used:           
50%CO-49.3%H<SUB>2</SUB>-0.7%H<SUB>2</SUB>O (a<SUB>C</SUB> (500 °C) = 1720; a<SUB>C</SUB> (600 
°C) = 153; a<SUB>C</SUB> (650 °C) = 55; a<SUB>C</SUB> (700           
°C) = 22) and 15%CO-84.3%H<SUB>2</SUB>-0.7%H<SUB>2</SUB>O (a<SUB>C</SUB> = (650           
°C) = 28). The gas velocities were controlled by capillary flow meters. The flow           
rate of the gas mixtures was fixed at 2 ml/s and the total pressure of the gas           
mixture was 1 bar. The diameter of the reaction tube is 27 mm. </P></DIV>          
<DIV 
align=left>
<script>comment();</script><p>The           
phases on the carburised samples were determined by X-ray diffraction (XRD)           
using Co-K<SUB><font face="Times New Roman">α</font></SUB> radiation. The carburised samples were coated with nickel           
and then analysed by means of metallographic cross-sections. Surface analysis of           
the carburised samples was performed by scanning electron microscopy (SEM).           
</P></DIV>
