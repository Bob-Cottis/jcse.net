<h4>Microhardness characterization</h4> 
<script>comment();</script> 
<p>As surface finish was discounted as the reason for the observed increase in passive current density with hydrogen treatment, it was decided to explore the possibility of changes in surface compositions, which may have caused the observed differences. Compositional changes was studied by microhardness measurements. As compositional changes result in changes in mechanical behaviour, microhardness measurements can indirectly indicate surface compositional changes. A major advantage of microhardness measurements is that a large sample area can be sampled so as to obtain statistically meaningful results for comparison purposes. Further, the instrumentation involved is not sophisticated and data collection times are relatively short.</p> 
<script>comment();</script> 
<p>The microhardness of the bulky carbides and grains were measured in the as-received alloy as well as all the hydrogen treated samples. Hardness measurement of needle-shaped carbides was not possible because proper indentations could not be made on these small sized carbides. Moreover, they dissolved fairly early into the matrix during high temperature hydrogen treatment. A 50-gram load was applied for the measurement of hardness as this provided optimum size of the indentation. If the size of the indentation is too small, then the measurement error of the diagonal will be high. On the other hand, if a higher load is applied in order to make the indentation, the indentation mark could overlap some nearby phase. Microhardness of the bulk carbide and matrix has shown in the table 7 and 8, as a function of hydrogen treatment properties. </p> 
