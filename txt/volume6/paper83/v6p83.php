<script>comment();</script> 
<p>The ZCP, E<sub>pp1</sub>, and E<sub>pp2</sub> values of the hydrogen treated samples polarized in 0.25 M H<sub>2</sub>SO<sub>4</sub> (Table 4) were similar to the values of as-received samples polarized in 0.05 M to 0.50 M H<sub>2</sub>SO<sub>4</sub> (Table 2). However, E<sub>cp</sub> decreased whereas E<sub>tp</sub> values showed a gradual increase with increase in hydrogen treatment time and temperature. Therefore, the passive ranges in the hydrogen treated Fe-16.2Al-0.54C samples increased with increasing treatment temperature and time. The i<sub>pass</sub> also increased significantly with increasing treatment temperature and time. The increases in the passive range and i<sub>pass</sub> must be related to the nature of the passive film that forms on the surface during polarization. In order to understand this, the effect of surface state on polarization behaviour was first determined.</p> 
<h4><b>Effect of surface roughness</b></h4> 
<script>comment();</script> 
<p>One possible reason for the change in passive film nature could be the surface condition of the hydrogen-treated samples. It is well known that passive films formed on smooth polished surfaces are superior compared to those on rough surfaces [15]. Microstructural study of the hydrogen treated samples indicated surface roughening with increasing treatment time and temperature [14].</p> 
<script>comment();</script> 
<p>�In order to understand the effect of surface roughness, potentiodynamic polarization experiments were carried out on the as-received samples for a mirror polished surface (after cloth polishing using 1 mm alumina powder) and for a rough surface (after wheel grinding). Figure 4 shows the potentiodynamic polarization curves for both these surface finishes in 0.25 M H<sub>2</sub>SO<sub>4</sub> solution. The kinetic (i.e., i<sub>corr</sub>, Tafel slopes b<sub>c</sub> and b<sub>a</sub>) and the passivation (ZCP, E<sub>pp1</sub>, E<sub>pp2</sub>, E<sub>cp</sub>, E<sub>b</sub>, E<sub>b</sub>- E<sub>cp</sub>, i<sub>crit1</sub>, i<sub>crit2</sub>, i<sub>pass</sub> and <sub> <img
src="paper_conf_files/image006.gif" width="40" height="36"></sub>) parameters were determined from the potentiodynamic polarization curves and are tabulated in Tables 5 and 6, respectively. Interestingly, the passivation ranges of the samples with different degree of surface roughness did not differ significantly. The passive current density apparently decreased with increasing roughness. Therefore, the increase in the passivation ranges and i<sub>pass</sub> values of hydrogen treated samples with increasing treatment temperature and time can not be attributed to the surface roughness induced by the high temperature hydrogen treatment.</p> 
