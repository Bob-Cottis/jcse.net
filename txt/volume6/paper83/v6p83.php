<p>The lower matrix and carbide hardness with increasing temperature and time of hydrogen treatment verified surface compositional changes. As passivation of surfaces in electrolytic solutions is sensitive to surface composition, the change in passive current density with hydrogen treatment can be concluded to result from compositional changes on surfaces that occurred during the high temperature hydrogen treatment.</p> 
<h3><b>&nbsp;4. Conclusions</b></h3> 
<script>comment();</script> 
<p>The salient conclusions of the present study were:</p> 
<script>comment();</script> 
<p>1.&nbsp;Electrochemical polarization studies were conducted on a carbon-alloyed iron aluminide of composition Fe-16.2Al-0.54C (in wt %) both before and after high temperature hydrogen treatments. </p> 
<script>comment();</script> 
<p>2.&nbsp; The as-received alloy, as well as the hydrogen treated samples, exhibited active-passive behaviour in 0.25 M H<sub>2</sub>SO<sub>4 </sub>acidic solution. A second anodic peak was observed in the polarization curves. </p> 
<script>comment();</script> 
<p>3.&nbsp; The potentiodynamic polarization behaviour of as-received alloy was studied in sulfuric acid of different molarities (0.05 to 0.50 M). The corrosion rate and exchange current density for hydrogen evolution reaction increased systematically with increasing molarity of H<sub>2</sub>SO<sub>4</sub>, while the Tafel slopes were similar. The increased corrosion rate has been attributed to the increasing hydrogen ion concentration in the solution and to increasing exchange current density. The critical current density for the second anodic peak increased with molarity due to the larger amount of hydrogen available for oxidation in solutions of higher molarities. The passive current density also increased with increasing molarity. </p> 
<script>comment();</script> 
<p>4.&nbsp; The corrosion rates of the hydrogen-treated sample were similar in 0.50 M H<sub>2</sub>SO<sub>4</sub>. The second anodic peak increased with increase in treatment temperature and time. This has been attributed to the evolution of hydrogen on the surface during the initial cathodic run of the potentiodynamic polarization scan. The hydrogen dissolved in the material during the high temperature hydrogen treatment did not affect the second anodic peak.</p> 
<script>comment();</script> 
<p>5.&nbsp;&nbsp;The increase in the passivation ranges and passive current densities of the hydrogen treated samples with increasing treatment temperature and time could not be attributed to the surface roughness induced by the high temperature hydrogen treatment. This has been related to the surface compositional changes that occurred during high temperature hydrogen treatment, which was confirmed by surface microhardness measurements. </p> 
