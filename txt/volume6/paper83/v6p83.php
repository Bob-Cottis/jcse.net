<h3>Abstract</h3> 
<script>comment();</script> 
<p>The effect of high temperature heat treatment on the electrochemical behaviour of a carbon-alloyed iron aluminide of composition Fe-16.2Al-0.54C (in wt %) was studied by potentiodynamic polarization studies. The electrochemical behaviour of the as-received alloy was characterized in sulphuric acid of different molarities. Well-polished samples were exposed to hydrogen at 1 atmospheric pressure at 700<sup>o</sup>C and 900<sup>o</sup>C for different times. The effect of high temperature hydrogen treatment on the electrochemical behaviour of the alloy was addressed. The high temperature treatment did not affect the second anodic peak observed in the polarization experiments. The increase in passive range and passive current densities of the hydrogen treated samples, with increasing treatment temperature and time, was attributed to surface compositional changes, which was confirmed by microhardness studies.</p> 
<script>comment();</script> 
<p><b>Keywords</b>: Iron aluminides, hydrogen treatment, electrochemical behaviour, microhardness</p> 
<h3>1. Introduction</h3> 
<script>comment();</script> 
<p>There is an increasing interest in the development of high temperature materials based on intermetallic phases. Intermetallic alloys based on iron aluminides, centred around the stoichiometric compositions Fe<sub>3</sub>Al and FeAl, are being considered for high temperature structural applications. Specific advantages of iron aluminides include excellent sulphidation resistance, very good oxidation resistance, lower density (5400-6700 kg/m<sup>3</sup>) which is 30% lower than that of the commercially available high temperature materials, good wear resistance, good cavitation erosion resistance and potential lower cost [1]. Although these alloys exhibit poor room temperature ductility and low fracture toughness, significant improvements have been achieved by alloying additions and process control [1]. The available literature generally addresses iron aluminide compositions with very low (&lt;0.01 wt %) carbon contents because it was reported that carbon reduced ductility [2]. Recently, Baligdad <i>et al </i>reported that addition of carbon in the range of 0.14 to 1.1 wt % significantly increased the strength and hardness of iron aluminides, which was attributed to solid solution strengthening by interstitial carbon in the matrix, to the precipitation of perovskite-based Fe<sub>3</sub>AlC<sub>0.5 </sub>phase and the formation of a duplex Fe<sub>3</sub>Al-Fe<sub>3</sub>AlC<sub>0.5</sub> structure [3-5]. The moderate ductilities exhibited by these alloys have been related to irreversible trapping of hydrogen at bulky carbide-matrix interfaces [6]. An added advantage in these iron aluminides of high carbon content is their excellent machinability, which is due to the presence of uniform carbide precipitates that facilitate the formation of small, uniform-sized chips during machining [4]. One of the key factors in increasing the maximum use temperature is enhanced resistance to high temperature atmospheres. The Fe-Al alloys with higher aluminium and carbon contents are more resistant to oxidation and decarburization [7]. A better oxidation resistance appears to lead to resistance to decarburization in these alloys [8]. The hot corrosion resistance of the carbon-alloyed iron aluminides was superior compared to the base iron aluminide (i.e. without any carbon addition), which has been attributed to the presence of carbides that hinder the diffusion of damaging species down grain boundaries [9, 10]. </p> 
<script>comment();</script> 
<p>�It was reported recently that the experimental potentiodynamic polarization curves of several carbon-alloyed iron aluminide alloys (Fe-15.6Al-0.05C, Fe-15.6 Al-0.14C, Fe-15.6Al-0.5C-Fe-15.6Al-1C, in wt %) exhibited a typical second anodic peak [11]. Frangini <i>et al.</i> [12] did not, however, report the existence of second anodic peak in a hot rolled iron aluminide alloy containing 24.4 wt % Al. The appearance of the second anodic peak in acid solution has been proposed to be due to the reoxidation of absorbed hydrogen atoms on the surface [13]. It was, therefore, decided to understand this phenomenon by controlled exposure of a carbon-alloyed iron aluminide to hydrogen at high temperature for several different times. The aim of the present study is to characterize the electrochemical behaviour of a carbon-alloyed iron aluminide after high temperature hydrogen treatment. </p> 
<h3>2. Experimental</h3> 
<script>comment();</script> 
<p>An iron aluminide ingot of composition Fe-16.2Al-0.54C (wt %), which corresponds to Fe-28.1Al-2.1C in at %, was prepared by air induction melting and chill cast into a cast iron mould. Commercial purity aluminium and mild steel scrap were used as raw materials. Grinding was used to clean the surface of the iron charge. After melting, the slag product was skimmed off. Aluminium pieces were then added to the molten iron bath. The melt was held at 1620<sup>o</sup> C for a very short time (2 minutes) to prevent aluminium losses and then normalized to room temperature. The ingot was then tested for its soundness by radiography. The ingot (55 mm diameter, 360 mm long) was machined to 50 mm diameter. They were refined in an electro-slag remelting (ESR) furnace of 350 kVA capacity. A commercial prefused flux based on CaF<sub>2</sub> was used. The flux was preheated and held at 850<sup>o</sup>C for 2 hours before use in order to remove moisture. The iron aluminide electrode was remelted under this flux cover and cast into 76 mm ingot in a water cooled steel mould. At the end of the process, the power supply was gradually reduced to impose a condition of hot topping. The ESR ingots were also radiographed to check their soundness. The ingots were then forged to a reduction ratio of 70% and subsequently hot rolled down to strips of 3 mm thickness. The alloy exhibited recrystallized grains after thermomechanical processing. Rectangular specimens (of approximate size 8mm�5mm�3mm) were cut from the alloy strips using a diamond cutter (ISOMET, Buehler). All the surfaces of the specimens were mechanically polished in fine cloth using 0.5m alumina powder and then degreased using acetone before each experiment.</p> 
<script>comment();</script> 
<p>The apparatus for hydrogen attack studies consisted of a tube furnace, a hydrogen gas cylinder, an argon gas cylinder, a gas pressure controller and a gas train. A vertical tube furnace of 150 mm length was employed. Temperature profiling of the furnace showed that a constant temperature zone of about 25 to 30 mm was obtained in the central region of the furnace. The temperature of the furnace was controlled within �2K and measured by a Pt/Pt-10%Rh thermocouple. A mullite tube (45 mm inner diameter and 460 mm length) acted as the reaction chamber. The mullite tube was fitted with silicone rubber stopper at the top to provide airtight fitting cover. The gas inlet tube and outlet tubes were inserted through the top of the reaction chamber through the silicone rubber stopper. All these tubes were glass fitted with rubber tubing and sealed with sealant. Teflon tapes were tightly wound on the locations where the glass tubes were fit with the rubber tubes. The specimen was placed inside a quartz crucible with three holes at the bottom to allow easy passage of gas. This crucible was then hung from the top of the furnace using a platinum wire into the reaction zone of the chamber. The experiments were carried out isothermally. Once the furnace attained the test temperature, sufficient time (15 to 20 minutes) was allowed for temperature to stabilize. The furnace was then first flooded with argon gas for 10 minutes to remove all entrapped air and then again hydrogen gas was purged in the reaction chamber to remove the argon gas and to maintain the reaction environment condition inside the furnace before introduction of the sample. A gas train was utilized to monitor the flow rates of the gas and purify it from probable impurities present. Pure gas was passed through a bubbler and capillary flow meter. The gas was then passed through an Ascarite (sodium hydroxide coated silica) column to remove carbon dioxide. It was then passed through anhydrous calcium chloride and Drierite (CaSO<sub>4</sub>) columns successively before introduction into the reaction chamber. Drierite possesses low equilibrium residual water vapour pressure and therefore, it was used after the anhydrous calcium chloride column in the gas train for efficient removal of moisture. The outlet gas was passed out of the furnace through a bubbler to ensure that the flow of gas was being maintained through the system.</p> 
<script>comment();</script> 
<p>Hydrogen treatment was performed under five different conditions changing the treatment temperature and time (at 700<sup>o</sup>C for 10 h and 48 h, and at 900<sup>o</sup>C for 144 h, 336 h, 720 h) at one atmospheric hydrogen pressure. The experiment at 900<sup>o</sup>C for 144h was repeated for duplicate testing. The samples, after hydrogen treatment, were slightly polished to remove a thin oxide layer that had formed on high temperature exposure and utilized for electrochemical studies. </p> 
<script>comment();</script> 
<p>Electrochemical polarization studies were conducted using a Perkin Elmer potentiostat (Model 263A) and a flat cell. A silver/silver chloride (SSC) electrode in saturated KCl was used as the reference electrode and the counter electrode was a platinum grid. All the potentials reported in the paper are with respect to SSC electrode (+197 mV vs standard hydrogen electrode). Two sets of experiments were conducted. In the first set, the as-received sample that had not been treated with hydrogen was tested at different molarities of H<sub>2</sub>SO<sub>4</sub> from 0.05 M to 0.50 M to study the effect of acid concentration. In the second set, experiments were carried out in a solution of 0.25 M sulphuric acid on the samples after exposure to hydrogen. The potentiodynamic polarization experiments were carried out in the potential range of �700 mV to +1600 mV for all the samples. The experiments were started immediately after immersion of the alloy in the electrolyte. All the polarization experiments were carried out at a scan rate of 0.5 mV/s. </p> 
