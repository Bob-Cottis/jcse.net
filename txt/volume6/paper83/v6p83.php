<p>The ZCP, E<sub>cp</sub> and E<sub>tp</sub> were almost similar for different molarities of solutions. However, i<sub>pass</sub> generally increased with increasing molarity of H<sub>2</sub>SO<sub>4</sub>. The value of i<sub>pass</sub> in case of 0.10 M H<sub>2</sub>SO<sub>4</sub> was slightly higher than that in case of 0.15 M H<sub>2</sub>SO<sub>4</sub>, the reason for which could not be understood. All experiments were duplicated and the results were highly reproducible. The increasing passive current density with decreasing pH has been recognized [15] and the results of the present study are in conformity with literature. In order to obtain some insights on the dominant reduction reaction in the cathodic region of the polarization diagram, the ZCP was compared with the reversible potential for hydrogen evolution reaction. Table 1 indicates that the ZCP values of the alloy were in the range between -522 to �566 mV vs SSC. The equilibrium reversible potential for the hydrogen evolution reaction was calculated from the Nernst equation, and this was slightly noble compared to the observed ZCP values. Therefore, it can be concluded that hydrogen evolution was the dominant cathodic reaction. Analysis of the transpassive region was also similarly undertaken. The transpassive potential (E<sub>tp</sub>) of the samples were compared with the equilibrium reversible potential for the oxygen evolution reaction. As the estimated reversible potentials were moderately active compared to the transpassive potential (E<sub>tp</sub>), the transpassive region was concluded to be due to oxygen evolution.</p> 
<script>comment();</script> 
<p>The polarization diagrams (Fig. 2) showed an extra anodic peak in addition to the first peak, in conformity with literature [11]. The first anodic peak was observed in the range between �238 mV to �393 mV for different molarities of H<sub>2</sub>SO<sub>4</sub> solution. A second anodic peak was observed in the range of �45 mV to �92 mV before reaching the broad passive zone of constant passive current density. It is important to note that the second anodic peak was observed for all the molarities of the solution used. The primary passivation potentials (E<sub>pp</sub>) and critical current densities (i<sub>crit</sub>) were calculated for both the peaks and tabulated in Table 2. The subscript 1 denotes the active peak and the subscript 2 denotes the noble peak (i.e. the second anodic peak). The E<sub>pp1</sub> and E<sub>pp2</sub> did not vary significantly in different molarity solutions. Although the values of i<sub>crit1</sub> exhibited an increase with molarities for some cases (for example, when 0.20 M H<sub>2</sub>SO<sub>4</sub> was used as the electrolyte instead of 0.15 M H<sub>2</sub>SO<sub>4</sub>), a general trend of increasing i<sub>crit1</sub> with increasing molarity was not observed. On the other hand, the i<sub>crit2</sub> values increased with increasing molarity. As can be noticed in the polarization diagrams (Fig. 2), a larger amount of hydrogen was reduced (indicated by larger cathodic current densities at any fixed potential active to ZCP ) when the specimen was first polarized in the cathodic region (during the potentiodynamic polarization experimental scan) with increasing H<sup>+</sup> concentration in electrolyte. Therefore, the increasing i<sub>crit2</sub> for the second anodic peak can be explained by the larger amount of hydrogen available for oxidation in solutions of higher normalities.</p> 
<script>comment();</script> 
<p>The exchange current density for the hydrogen evolution reaction (<sub><img
src="paper_conf_files/image006.gif" align="absmiddle" width="40" height="36"></sub>) was also estimated by extending the cathodic Tafel slope and intersecting it with the parallel line to log (i) axis at the equilibrium reversible potential for the reaction. It is evident from Table 2 that the exchange current densities increased with increasing molarity of H<sub>2</sub>SO<sub>4</sub>. Therefore, this could be another plausible reason for the observed increase in corrosion rate with increasing H<sup>+</sup> concentration, especially since the Tafel b<sub>c</sub> slope was not affected by molarities. The estimated exchange current densities were comparable to those obtained on other carbon-alloyed iron aluminides [11]. Moreover, the exchange current densities were an order of magnitude higher than that on iron (2.0 x 10<sup>-3</sup> mA/cm<sup>2</sup> in 0.25 M H<sub>2</sub>SO<sub>4</sub>) [11] and by several orders of magnitude higher than that on Al (7.9 x 10<sup>-7</sup> mA/cm<sup>2</sup> in 0.25 M H<sub>2</sub>SO<sub>4</sub>) [11]. The enhanced hydrogen reaction kinetics in carbon alloyed iron aluminides must be related to the presence of carbides in the microstructure. This is indirectly indicated by higher exchange current densities reported for a Fe-15.6Al-1.0C alloy (50.1 x 10<sup>-2</sup> mA/cm<sup>2</sup> in 0.25 M H<sub>2</sub>SO<sub>4 </sub>[11]).</p> 
<h4><b>Effect of hydrogen treatment</b></h4> 
<script>comment();</script> 
