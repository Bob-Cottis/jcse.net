<h3>Discussion</h3>

<script>comment();</script><p>The deposition of titanium onto coupons of typical nuclear reactor materials
and its incorporation into the oxide layers demonstrates that the perceived
immobility of titanium ions in reactor coolants is not valid.&nbsp; It appears
that when fresh titanium metal is exposed to simulated reactor coolant and
operating conditions, its solubility is significant enough such that aqueous
titanium may be transported to surfaces away from the source.&nbsp; The
solubility of titanium in reactor coolants has been examined before in other
research programs at UNB <A href="#_edn7" title="">[7]</A>.&nbsp; In those experiments, powdered samples
of rutile (TiO<sub>2</sub>) and ilmenite
(FeTiO<sub>3</sub>) were heated in an autoclave containing simulated reactor
coolants (pH adjusted with lithium) and samples were drawn off, concentrated on
ion exchange beds, eluted with concentrated nitric acid and the titanium
content measured through ICP-MS. Although there was considerable scatter in the
data, titanium was measured at the 0.1 � 0.5 ppb level, depending on pH.</p>

<script>comment();</script><p>For verification of aqueous titanium in the autoclave used for the present
experiments, an additional test was run with the same chemistry and conditions
given in Table 1, only with a sample line inserted in place of the
coupons.&nbsp; Fresh titanium turnings were placed in the stainless steel
basket and the loop was operated for a period of one week before samples were
taken.&nbsp; The samples were digested in the same manner described above and
titanium was measured to be between 0.1 � 0.2 ppb.</p>

<script>comment();</script><p>The highest concentration of titanium measured with GDS on the ferrous
materials appears to be between the inner and outer oxide layer.&nbsp; This
would be consistent with an initial large release of titanium from the freshly
machined surfaces, which would quickly become passivated
as the TiO<sub>2</sub> film built up.&nbsp; Additional titanium would be
incorporated on the surfaces of the reactor materials as exposure time
progressed through dissolution of the TiO<sub>2</sub> surface films on the
source material, albeit at a much slower rate.&nbsp; The titanium deposited on
the nickel alloys appears to be completely on the outer surface of the oxide,
presumably because it is incorporated into the sparse scattering of small crystallites
that cannot be distinguished as a separate layer.</p>

<script>comment();</script><p>The presence of the rounded ilmenite crystals on
all the coupons suggests that, although titanium may be incorporated to some
extent into the spinel oxides formed on the coupons,
particularly in the early stages of oxide formation, much of the titanium
transported to the surface precipitates as a separate phase.&nbsp; Also of
interest is the manganese content in the ilmenite
crystals � it is present at about one percent by element as measured by
EDX.&nbsp; This is further evidence that these crystals are truly ilmenite, not rutile producing a
signal adulterated from the substrate, since in minerals containing ilmenite it is frequently seen that manganese will displace
some of the ferrous ions from the lattice structure producing ore rich in
manganese <a name="_ednref8"></a><a href="#_edn8" title="">[8]</a>.</p>

<h3>Conclusions</h3>

<script>comment();</script><p>It has been demonstrated that, when titanium is applied as a construction
material in corrosion test loops used to simulate the primary heat transport
system of nuclear reactors, there is a measurable transport of aqueous titanium
species from the construction material to test surfaces within the
system.&nbsp; The titanium content in oxides formed on typical reactor
materials tended to concentrate near the interface between the inner and outer
layers, which is consistent with an initial high dissolution rate of a fresh
titanium surface that would significantly diminish as its own oxide film
developed.&nbsp; In such instances, the titanium transported to the surfaces of
the reactor materials will precipitate as a separate phase, ilmenite.</p>

<script>comment();</script><p>These results point to the possibility of using titanium or titanium oxides
as an inhibitor for corrosion in the primary heat transport system of nuclear
reactors or in similar coolant systems.&nbsp; Corrosion protection may result
from the incorporation of&nbsp; titanium into native
oxide layers, probably by making them less soluble in aqueous environments.</p>

