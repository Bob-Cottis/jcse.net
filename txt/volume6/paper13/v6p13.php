<script>comment();</script><p>Four materials of interest were fabricated into rectangular coupons (20 x 18
mm) that were between 1 mm and 2 mm thick.&nbsp; The four materials were A106-B
carbon steel, 304L stainless steel, Alloy-600 and Alloy-800.&nbsp; The
available stock of these materials was all in the form of tubing, except for
the carbon steel, so the tubing was sectioned and rolled flat to fabricate the
coupons.&nbsp; Prior to insertion into the loop for exposure, all the coupons
were polished to a 600 grit finish, degreased with acetone and weighed.</p>

<script>comment();</script><p>For this test, duplicate coupons of each material were hung from a sample
tree using stainless steel wire and placed in the third titanium autoclave
(Ti-3).&nbsp; Fresh titanium turnings were put into a stainless steel basket
that was suspended from the autoclave head (Figure 2).&nbsp; The turnings were
used to ensure that the coolant in the autoclave would be saturated in titanium
at the test conditions.</p>

<script>comment();</script><p align=center>
<img border=0
