<script>comment();</script><p>The temperature in the third titanium autoclave was maintained around 290<sup>o</sup>C
for the duration of the experiment; this is significantly less than the 325<sup>o</sup>C
normally seen at the outlet of a PWR reactor core and was due to limited
heating capacity available in the third autoclave at the time of the
test.&nbsp; </p>

<h3>Results</h3>

<script>comment();</script><p>Following the 1100-hour exposure time, the heating in the loop was shut down
and the system was allowed to cool.&nbsp; Once workable temperatures were
obtained, the pump was shut off, and the coupons were removed from the
autoclave, dried with nitrogen and weighed.&nbsp; Table 2 presents the weight
loss/gain data and apparent corrosion rate for each coupon exposed.&nbsp; The
values are low, as expected for this environment, but cannot be used for
accurate indications of corrosion rate without oxide stripping.</p>

<script>comment();</script><p>As described above, one set of coupons was packaged and shipped to the
Department of Quantum Engineering and Systems Science at the <st1:place>
<st1:PlaceType><st1:place><st1:PlaceType>University</st1:PlaceType></st1:PlaceType>
 of <st1:PlaceName><st1:PlaceName>Tokyo</st1:PlaceName></st1:place></st1:PlaceName></st1:place>
for analysis by GDS and XPS, whilst the remaining coupons were examined at UNB
by SEM and EDX analysis.&nbsp; Typical SEM micrographs for each material are
shown in Figures 3 � 6.&nbsp; As expected, the oxide layers formed on the
materials appeared quite different under the given chemistry conditions.&nbsp;
The carbon steel coupon shows a thick outer oxide layer composed of octahedral
crystals, approximately 1 &micro;m in size, typical of magnetite (Fe<sub>3</sub>O<sub>4</sub>).&nbsp;
The stainless steel coupon shows a more-or-less exposed inner oxide layer with
a covering of octahedral crystals, again typical for magnetite, nickel ferrite
(NiFe<sub>2</sub>O<sub>4</sub>) or a nickel-substituted magnetite (Ni<sub>x</sub>Fe<sub>3&minus;x</sub>O<sub>4</sub>).&nbsp;
The Alloy-600 and Alloy-800 surfaces were virtually bare of large oxide
crystals with the grooves from the polishing performed before the experiment
still visible. </p>

