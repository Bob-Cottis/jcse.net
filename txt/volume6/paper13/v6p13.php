<SCRIPT>notify();</SCRIPT><h3>Abstract</h3>

<script>comment();</script><p>Titanium, in components such as autoclaves and tubing, constitutes a
significant fraction of the construction material used in many experimental
high-temperature water loops and autoclaves.&nbsp; At the <st1:place>
<st1:PlaceType>University</st1:PlaceType>
of <st1:PlaceName>New Brunswick</st1:PlaceName></st1:place>, such a system has
been used for corrosion studies of reactor materials under pressurized water
reactor (PWR) conditions (0-1000 ppm boron, 0.5-2.5 ppm lithium and hydrogen saturated at STP).&nbsp; A
stainless steel section of the loop is used to condition coolant to reactor
inlet conditions. The coolant is further heated to reactor outlet temperatures
in the titanium sections, allowing minimal addition of corrosion products to
the coolant and in this respect simulating the reactor core.&nbsp; However, the
incorporation of titanium into the oxides of the test materials has indicated a
significant solubility of titanium under these conditions and led to the
suggestion that titanium dosing of coolant systems may be used for corrosion
protection.</p>

<script>comment();</script><p>To examine the titanium transport properties, coupons fabricated of A106-B
carbon steel, 316L stainless steel, Alloy-600 and Alloy-800 were placed in a
titanium autoclave and exposed to simulated PWR coolant (600 ppm boron, 2.5 ppm lithium) at
290<sup>o</sup>C for a period of 1100 hours.&nbsp; A basket filled with
titanium turnings placed upstream of the coupons in the autoclave ensured
coolant saturation in titanium.&nbsp; After exposure, the coupons were examined
with several surface analysis techniques.&nbsp; Results showed significant
titanium incorporation in the oxide layers of all materials tested.&nbsp; The
oxide layers were generally duplex � precipitated crystals overlaying a compact
inner layer.&nbsp; The titanium tended to concentrate at the middle of the
oxide between the two layers.&nbsp; Since the inner layer grows inwards towards
the metal and the outer layer grows outwards towards the coolant, the
disposition of the titanium suggests that it was incorporated in the oxide
mostly at the start of the exposure.</p>

<script>comment();</script><p><b>Keywords:</b>&nbsp; High Temperature Corrosion, Reactor Materials,
Titanium, Magnetite, Nickel Ferrite, Ilmenite.</p>

<h3>Introduction</h3>

<script>comment();</script><p>Modern pressurized water reactors (PWRs) operate
under strict regimes with regard to primary system chemistry.&nbsp; Optimization of coolant chemistry conditions leads to
improved corrosion resistance of the primary system materials and reduces the
crud circulating through the system.&nbsp; Activity transport is therefore
reduced since coolant borne crud and dissolved corrosion products may become
activated while traversing the reactor core and subsequently deposit on
out-of-core components such as the steam generators.</p>

<script>comment();</script><p>Much progress has been made in recent years to reduce the material transport
throughout the primary circuit.&nbsp; The coordinated chemistry program of the
1980s and 1990s <a name="_ednref1"></a><a href="#_edn1" title="">[1]</a> and its modifications, in conjunction
with removing, as much as possible, cobalt-containing materials such as Stellite significantly reduced the collective dose to plant
workers <a name="_ednref2"></a><a href="#_edn2" title="">[2]</a>.&nbsp; Other programs, such as dosing
the primary circuit with zinc, have also been shown to reduce the out-of-core
radiation fields by displacing the cobalt from deposited oxides such that it
can subsequently be removed via the purification system <a name="_ednref3"></a><a
href="#_edn3" title="">[3]</a>.</p>

<script>comment();</script><p>Recently, experiments have been ongoing at UNB to study the morphology of
oxides formed on 316 SS under primary circuit conditions, where the material
was subjected to a high velocity jet.&nbsp; The results of this work were put
into a model describing the material transport through the primary heat
transport system <a name="_ednref4"></a><a href="#_edn4" title="">[4]</a>.&nbsp; The results, however, were
somewhat difficult to interpret due to interference of titanium by the
surface-grown oxide films.&nbsp; Significant portions of the loop used for this
work were constructed of titanium, which was apparently transported though a
dissolution-precipitation mechanism to the surfaces of the test coupons.</p>

<script>comment();</script><p>The incorporation of titanium into corrosion films is not a new phenomenon,
in fact, titanium has been proposed as an oxide modifying agent to suppress
IGSCC (Intergrandular Stress Corrosion Cracking) and
boiler tube denting on the secondary side of steam generators [<a
name="_ednref5"></a><a href="#_edn5" title="">[5]</a>
- <a name="_ednref6"></a><a href="#_edn6" title="">[6]</a>.&nbsp; Titanium dioxide (TiO<sub>2</sub>)
in the anatase form has also been proposed as an
additive to CANDU primary coolant to mitigate the accelerated corrosion of the
outlet feeder pipes by a flow-assisted corrosion mechanism <a name="_ednref7"></a><a
name="_Ref40623337"></a><a
href="#_edn7" title="">[7]</a>.&nbsp;
However, titanium autoclaves and components have long been used for corrosion
testing equipment at high temperatures and pressures because it was thought to
be �inert� under most conditions so it would not affect the test results.&nbsp;
Evidence is mounting that this is not the case for the conditions encountered
in the primary heat transport system of nuclear reactors. </p>

<script>comment();</script><p>The experiments undertaken here were initiated to examine the pick-up of
titanium into the oxide layers of typical reactor materials under PWR primary
system conditions.&nbsp; A collaborative program was established between UNB
and Dr. Ishigure, then of the <st1:place><st1:PlaceType><st1:place>
<st1:PlaceType>University</st1:PlaceType></st1:PlaceType>
 of <st1:PlaceName><st1:PlaceName>Tokyo</st1:PlaceName></st1:place></st1:PlaceName></st1:place>,
currently at the Saitama Institute of Technology in Saitama, and Mr. Ono of the
<st1:place><st1:PlaceType><st1:place><st1:PlaceType>Institute</st1:PlaceType></st1:PlaceType>
 of <st1:PlaceName><st1:PlaceName>Research</st1:PlaceName></st1:place></st1:PlaceName></st1:place>
and Innovation in <st1:City><st1:place><st1:City><st1:place>Tokyo</st1:place></st1:City></st1:place></st1:City>.</p>

<h3>Experimental</h3>

<script>comment();</script><p>PWR primary system conditions were simulated in the high-temperature loop
shown schematically in Figure 1.&nbsp; Simulated primary system coolant is
circulated via the positive displacement pump through an interchanger and heater
and passed into a two-litre autoclave.&nbsp; All components in the system to
this point are Type 316 SS.&nbsp; The coolant is then passed through the three
one-litre titanium autoclaves through the valve arrays (all components in this
section are Grade 2 titanium) and is returned to the reservoir via the
interchanger, cooler and back-pressure regulating valve (BPRV). The system also
has full flow purification via an ion exchange column equilibrated to the
desired chemistry condition. Typically, experiments consisted of heating the
coolant to reactor inlet temperatures (260<sup>o</sup>C � 285<sup>o</sup>C) in
the stainless steel section and increasing to reactor outlet temperatures (300<sup>o</sup>C
� 315<sup>o</sup>C) in the titanium section, thus simulating the heat pick-up
in a virtually ferrous-free reactor core.</p>

<script>comment();</script><p align=center>
