<h3>Abstract</h3>

<script>comment();</script><p>The effect of partial oxygen pressure and a
native oxide on the structure, morphology and chemical constitution of the
initial oxide layer grown on a <i>M</i>CrAlY bond
coat was investigated. To this end, a state-of-the-art Ni-19Co-18Cr-21Al-0.2Y
(at.%) bond coat, as prepared by electron beam physical vapour deposition, was
oxidised in a UHV reaction chamber, with or without its native oxide, at 1373 K
and a partial oxygen pressure of 0.1 <st1:State><st1:place>Pa.</st1:place></st1:State> The structure,
morphology and chemical constitution of the initially grown oxide layer, as
well as the oxidation-induced changes within the coating, were characterised
using a broad range of analytical techniques. Special attention was paid to
differences in the distribution of Y and the presence of any non-protective
oxide phases within the initially grown oxide layer. The results were compared
with a corresponding oxidation treatment at atmospheric conditions in a
conventional tube furnace. It is shown that the exclusive formation of a
continuous, closed &#945;-Al<sub>2</sub>O<sub>3</sub> layer with a strong
Y-enrichment at its outer surface is promoted at low partial oxygen pressure
and in the absence of a native oxide. </p>

<p><b>Keywords: </b>MCrAlY bond coat, pre-treatment,
oxidation, annealing, Y segregation and distribution.</p>

<h3><a name="_Ref36272804">Introduction</a></h3>

<script>comment();</script><p>In modern high-temperature (&gt; 1273 K)
coating systems, <i>M</i>CrAlY (<i>M</i> = Ni, Co)
alloys are used as a bond coat between a ceramic thermal barrier topcoat (TBC)
and the Ni-base superalloy component, such as a blade
or vane in a gas turbine engine [1,2]. The superalloy is designed to provide the mechanical strength
of the load-bearing components at high operating temperatures. The TBC, in
conjunction with component cooling, serves to reduce the component-surface
temperature up to about 150 K [3], thereby allowing a higher operating
temperature, which results in an enhanced efficiency of energy conversion. </p>

<script>comment();</script><p>The susceptibility to cracking and spalling of the coating system in service mainly relies on
the capability of the <i>M</i>CrAlY bond coat to
produce and maintain a protective (i.e. stable, adherent, slow-growing)
&#945;-Al<sub>2</sub>O<sub>3</sub> layer, since the TBC is highly
oxygen-transparent. In practice, the <i>M</i>CrAlY
bond coat is already oxidised upon deposition of the TBC in the fabrication
process. Although the structure, morphology and chemical composition of the
resulting, initial oxide layer are crucial for the subsequent formation and
continued growth of a protective and adherent &#945;-Al<sub>2</sub>O<sub>3</sub>
layer on the bond coat during service, these aspects are nearly always poorly
defined and controlled.</p>

<script>comment();</script><p>Therefore, the effect of different, <i>controlled</i>
oxidation or annealing treatments of a <i>M</i>CrAlY
coating on the structure, morphology and chemical composition of the resulting,
initially grown oxide layer, was investigated. To this end, a state-of-the-art
dual-phase Ni-19Co-18Cr-21Al-0.2Y (at.%) coating, as prepared by Electron-Beam
Physical Vapour Deposition (EB-PVD), was annealed or oxidised in a UHV reaction
chamber at a temperature of 1373 K in pure argon or oxygen gas at a pressure of
0.1 Pa, respectively. To study the effect of the presence of a native oxide
film on the coating surface, the treatments were repeated for the
sputtered-clean coating surface. The results for the controlled oxidations
executed in the UHV reaction chamber at a low partial oxygen pressure (<i>p</i>O<sub>2</sub>)
were compared with a corresponding oxidation treatment at atmospheric
conditions in a conventional tube furnace. </p>

<script>comment();</script><p>The microstructure, oxide phase
constitution and chemical composition of the grown oxide layers were
investigated using X-ray Photoelectron Spectroscopy (XPS), Auger Electron
Spectroscopy (AES), Transmission Electron Microscopy (TEM), Secondary Ion Mass
Spectrometry (SIMS) and X-ray Diffractometry (XRD).
The oxide-layer thickness, morphology and architecture were analysed with Light
Optical Microscopy (LOM) and Scanning Electron Microscopy (SEM) in combination
with X-ray microanalysis. Special attention was paid to differences in the
distribution of Y and the presence of any non-protective oxide phases within
the initially grown oxide layer.</p>

<h3>Experimental</h3>

<h4><a name="_Ref35929764">Material</a></h4>

<script>comment();</script><p>A 2 mm thick dual-phase NiCoCrAlY
coating was produced by EB-PVD deposition of an as-cast Ni-20Co-20Cr-22Al-0.2Y
(at.%) alloy. After the deposition process, the
coating was densified by shot-peening
and subsequently stress-relieved by annealing for 2 h at 1353 K in a vacuum
oven (i.e. system pressure ~ 0.1 Pa). The resulting coating has a very low
porosity and possesses a fine-grained, columnar structure, consisting of a
brittle Al-rich/Cr-poor &#946;-phase (bcc) and a more ductile Al-poor/Cr-rich &#947;-phase (fcc). The typical width and length
of the columnar grains is about 1 &#956;m and 50 &#956;m,
respectively.� The nominal composition of
the coating, as determined by EPMA (see Sec. 2.4), was Ni-19Co-18Cr-21Al-0.2Y (at.%).� The corresponding compositions of the &#947;- and &#946;-phase were
Ni-20Co-26Cr-11Al (at.%) and Ni-20Co-8Cr-33Al (at.%),
respectively. The concentration of Y within the &#947; and &#946;-phase
was below the detection limit of the EPMA (i.e. &lt; 85 ppm).
The Y was homogeneously distributed as YNi-rich intermetallic precipitates at the &#947;/&#946;-phase boundaries
within the coating.</p>

<h4>Sample Preparation</h4>

<script>comment();</script><p>Disc-shaped specimens (diameter 10 mm and
thickness 1 mm) were cut using spark-erosion. Prior to oxidation, the specimen
surface was prepared by grinding, lapping and polishing, successively.
Polishing was performed with a paste of 3 �m, then 1 �m and finally 0.25 �m
diamond grains. Thereafter, the surface was fine-polished, using a chemical
resistant cloth with a suspension of 0.04 �m silica in distilled water. After
each preparation step the specimens were thoroughly cleaned ultrasonically with
isopropanol and dried by blowing with pure compressed
nitrogen gas.</p>

<script>comment();</script><p>For the preparation of the oxide/coating
cross-sections, first a copper or gold layer was deposited on the grown oxide
layer by evaporation or sputtering, respectively. Then, a nickel layer was
electroplated on top of the copper layer using a <st1:place>Watts</st1:place> bath. Next, the specimen
was cut perpendicular to its surface with a diamond saw. Finally, the
oxide/coating cross-sections were ground and polished as described above. </p>

<script>comment();</script><p>For the preparation of the TEM sample, a 1
mm thick glass plate was glued on the Ni plated side of the specimen (see
above) prior to the cutting. Both sides of the cross section were mechanically
ground and polished until the slide had attained a thickness of about 15 mm. Then, the slide was put onto a Cu slot grid. Finally, both sides
of the cross-section were ion milled (GATAN Precision Ion Polishing System,
type 691) using a 4 keVAr<sup>+</sup>
beam at an angle of 3-5� with
respect to the surface until a hole was formed. Then, at the edge of the hole,
electron transparent areas were present for TEM analysis of the oxide/coating
cross-section.</p>

<h4>Oxidation and Annealing</h4>

<script>comment();</script><p>The low <i>p</i>O<sub>2</sub> oxidation
experiments, as well as the annealing treatments, were performed at 1373 K in a
UHV reaction chamber (base pressure &lt; 10<sup>-7</sup> Pa) coupled to an
instrument for XPS analysis (base pressure &lt; 10<sup>-8</sup> Pa).� In some cases, the native oxide film (a few
nm thick), consisting of Ni, Al and Cr oxides with adventitious C on top, was
removed prior to oxidation or annealing by sputtering for 1 hour with a focused
4 keVAr<sup>+</sup> beam
scanning the surface area. Next, the specimens were heated within 5 minutes to
1373 K in pure argon gas (99.998 vol.%Ar) at 0.1 <st1:State><st1:place>Pa.</st1:place></st1:State> Then, the coating was either annealed in pure argon gas (99.998 vol.%) at 0.1 Pa or oxidised by exposure to pure oxygen gas
(99.998 vol.%) at 0.1 <st1:State><st1:place>Pa.</st1:place></st1:State></p>

<script>comment();</script><p>The high <i>p</i>O<sub>2</sub> oxidation
experiment was performed at 1373 K (or 1273 K) in a horizontal alumina tube
furnace (Lenton PTF 16/75/610; tube inner diameter 75
mm). A gas mixture of argon (99.998 vol.%Ar) and 20 vol.% oxygen gas (99.998 vol.% O<sub>2</sub>) was passed through the furnace, at a total pressure
of 10<sup>5</sup> Pa (i.e. <i>p</i>O<sub>2</sub> = 2&times;10<sup>4</sup> Pa), with a controlled flow rate of 750 ml/min.
Before mixing, both gasses were passed through a moisture filter and a charcoal
filter. The specimens were placed in an annealed alumina crucible and
transported to the hot zone of the furnace, which is at the oxidising
temperature of 1373 K. </p>

<h4><a name="_Ref35929260">Analysis and Data Evaluation</a></h4>

<script>comment();</script><p>The coating composition (see Sec. 2.1) was determined with EPMA using a JEOL JXA 8900R
WD/ED combined microanalyzer, equipped with five
wavelength-dispersive spectrometers (WDS) (see [4] for details).</p>

<script>comment();</script><p>The chemical constitution at the oxidised
or annealed coating surface was determined with XPS. Spectra were recorded with
a PHI 5400 ESCA instrument, equipped with a spherical capacitor analyser, set
at constant analyser pass energy of 71.55 eV, and
with a step size of 0.125 eV, using unmonochromatised Al or Mg X-ray radiation. The emitted
electrons were detected at an angle of 45� with respect to the specimen surface.
</p>

<script>comment();</script><p>XRD was employed to determine the
crystalline phases present in the grown oxide layers. The diffraction
experiments were performed with a Bruker AXS D5005 &#952;-&#952;diffractometer
using monochromatic Cu K<sub>&#945;</sub> radiation and applying sample rotation. The samples were analysed
in the 2&#952;
range 20-80� with a step size of 0.050�. For phase identification the JCPDS
database was used (<st1:place><st1:PlaceName>JCPDS-International</st1:PlaceName><st1:PlaceType>Center</st1:PlaceType></st1:place> for Diffraction Data, <st1:place>
<st1:City>Swarthemore</st1:City>, <st1:State>PA</st1:State></st1:place>).</p>

<script>comment();</script><p>In some cases, the element distribution as
a function of depth below the oxide surface was determined by SIMS or AES depth
profiling. For the AES analysis, the Ni LMM, Cr LMM, Co LMM, Al KLL, Y MNN, O
KLL and C KLL peaks were recorded with a PHI 4300 scanning Auger electron
microscope using a primary electron beam of 5 keV and
1 �A, and employing a sputter interval of 1 minute (4 keVAr<sup>+</sup> beam; 3x3 mm<sup>2</sup> raster; with Zalar rotation). To resolve the concentration-depth
profiles of the elements in the oxide layer and the coating, first the
intensities of the corresponding oxidic and metallic
components in the measured spectra were resolved as a function of depth below
the surface by Factor Analysis (FA) of the background corrected, normalised
spectra [5]. Next, the corresponding concentration-depth profiles were obtained
by multiplying the resolved metallic and oxidic
intensities of each element with their sensitivity factor and normalising to
100 at.%.� </p>

<script>comment();</script><p>For the SIMS sputter-depth profiling, a 2 keVAr<sup>+</sup> beam in spot
mode was focused on the oxidised surface and the emitted Al, Ni, Co, Cr and Y
secondary ions were analysed using a quadrupole mass
spectrometer (Balzers QMA 430) equipped with a
three-lens ion optics systems and a secondary electron multiplier (Balzers SEV 217). To reduce charging of the oxide layer
during the SIMS analysis, a 20 nm thick gold layer was sputter-deposited on the
oxidised surface, and any oxide layer present at the backside of the sample was
removed by grinding. To visualize the distribution of the concerned elements as
a function of depth below the oxidised surface, each recorded
element intensity was multiplied with an arbitrary sensitivity factor,
and subsequently normalised.</p>

<script>comment();</script><p>LOM and SEM analysis of the prepared
oxide/coating cross-sections was applied to determine the morphology and
architecture of the grown oxide layers. SEM analysis was performed with a JEOL
JSM 6500F, equipped with an Energy-Dispersive Spectrometer (EDS) for
quantitative composition analysis.
The spectra acquisition and processing was performed with a ThermoNoran
Vantage system.</p>

<script>comment();</script><p>The TEM analysis was performed with a
Philips CM30T electron microscope, operated at an accelerating voltage of 300
kV. EDS analysis with a spot size in the range of 10 -
100 nm was applied to determine the composition at specific positions within
the oxide layer and the underlying coating.</p>

<h3>Results and Discussion</h3>

<h4>Oxidation-Induced Changes within Coating</h4>

<script>comment();</script><p>Light optical and backscattered electron
micrographs of cross-sections of the dual-phase Ni-19Co-18Cr-21Al-0.2Y (at.%) EB-PVD coating after three different oxidation
treatments for 1 h at 1373 K are shown in Figs. <a href="#Fig1">1</a> and <a href="#Fig2">2</a>,
respectively.</span> Clearly, a much thicker oxide layer has formed after
oxidation for 1 hour in a conventional furnace at a relatively high <i>p</i>O<sub>2</sub>
of 2</span>&times;10<sup>4</sup>
Pa than for the oxidations in a UHV reaction chamber at a low <i>p</i>O<sub>2</sub>
of 0.1 <st1:State><st1:place>Pa.</st1:place></st1:State> Consequently, a stronger Al depletion has occurred (within the same
period) for the oxidation at high <i>p</i>O<sub>2</sub>, leading to the
transformation of the &#946;-phase
into the Al-poor &#947;-phase
in the region contiguous to the oxide/coating interface (further denoted as &#947;*-zone; cf. Fig. <a href="#Fig1">1a</a>).</span></p>

<script>comment();</script><p>For the oxidations at low <i>p</i>O<sub>2</sub>,
the &#947;*-zone is absent. Instead, for
the oxidation of the coating with native oxide, the original dual-phase &#947;+&#946; structure
of the coating is still preserved up to the oxide/coating interface (Fig. <a
href="#fig1">1b</a>), whereas for the oxidation of the sputtered-clean coating,
a single-phase </span>&#946;
region (i.e. Al-rich) has formed in the region contiguous to the oxide/coating
interface (further denoted as &#946;*-zone; see Fig. <a href="#fig1">1c</a> and Sec. 3.2).</span></p>

