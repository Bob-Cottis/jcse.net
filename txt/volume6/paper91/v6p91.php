<script>comment();</script><p>As follows from the above discussion, the
(micro)structure, morphology, constitution and chemical composition of the
initial, thermally grown oxide layer on a <i>M</i>CrAlY
bond coat is governed by its oxidation conditions (e.g. <i>T</i> and <i>p</i>O<sub>2</sub>),
as well as by its microstructure (e.g. Y-depleted &#946;*-zone) and surface
condition (e.g. native oxide). It was shown that the distribution of Y within
the coating, as well as within the developing oxide layer, is strongly
dependent upon the partial oxygen pressure and the presence of a native oxide
on the coating surface at the onset of oxidation. In general, it can be
concluded that the rate of formation of a protective &#945;-Al<sub>2</sub>O<sub>3</sub>
layer, with an Y-enrichment at its very surface, is
enhanced for the oxidation of a <i>M</i>CrAlY coating
at low <i>p</i>O<sub>2</sub> and
in the absence of a native oxide layer. The influence
of the Y-distribution within the initially grown oxide layer on the long-term
performance of the entire coating system (i.e. superalloy
substrate | bond coat | TBC; see Sec. 1) in service is still unknown. It is expected that a
well-defined pre-treatment, consisting of controlled annealing and subsequent
oxidation (prior to deposition of a TBC in the fabrication process), can have a
beneficial effect on the durability of the coating system during service. This
will be investigated by the present authors in the near future.</p>

<h3>Conclusions</h3>

<script>comment();</script><p>For the oxidation of a <i>M</i>CrAlY
coating at 1373 K, the exclusive formation of a continuous, closed &#945;-Al<sub>2</sub>O<sub>3</sub>
layer with a strong Y-enrichment at its outer surface is promoted at low <i>p</i>O<sub>2</sub>
and in the absence of a native oxide. </p>

<script>comment();</script><p>A lower <i>p</i>O<sub>2</sub> reduces the activity of
oxygen at the oxide/coating interface during the initial, fast oxidation stage,
thereby promoting the coalescence of &#945;-Al<sub>2</sub>O<sub>3</sub>
crystallites into a closed &#945;-Al<sub>2</sub>O<sub>3</sub> layer.
Consequently, the duration of the initial, very fast oxidation stage and its
associated Al depletion from the coating, as well as the formation of
less-protective (spinel) oxides of Cr, Ni and Co, are
strongly suppressed. </p>

<script>comment();</script><p>In the absence of a native oxide and at low <i>p</i>O<sub>2</sub>,
the initial segregation of Y to the coating surface is promoted, resulting in a
strong Y-enrichment at the developing oxide-layer surface and a limitation of
the subsequent formation of large pegs of Y<sub>3</sub>Al<sub>5</sub>O<sub>12</sub>
(and some AlYO<sub>3</sub>) at the oxide/coating interface. </p>

<script>comment();</script><p>Upon the high-temperature oxidation of a<i>MCrAlY</i>
coating, internal oxides of Y<sub>3</sub>Al<sub>5</sub>O<sub>12</sub> and AlYO<sub>3</sub>
are grown at the &#947;/&#946; phase boundaries by internal oxidation of YNi-rich
intermetallic precipitates. </p>

<script>comment();</script><p>Heating and subsequent annealing of a <i>M</i>CrAlY coating at 1373 K in UHV results in the fast
formation of a Y-depleted, single-phase &#946;-zone in the region contiguous to the oxide/coating interface due to
extensive Cr evaporation and strong Y segregation. The initial Y segregation
and/or Cr-evaporation-induced roughening of the coating surface may promote the
heterogeneous nucleation of &#945;-Al<sub>2</sub>O<sub>3</sub>, thereby promoting the coalescence of
the initial &#945;-Al<sub>2</sub>O<sub>3</sub>
crystallites into a closed &#945;-Al<sub>2</sub>O<sub>3</sub> layer.</p>

