<script>comment();</script><p>It is concluded that, for the
high-temperature oxidation of a <i>M</i>CrAlY
coating, the oxide-layer growth kinetics and its associated Al depletion from
the coating are suppressed at low <i>p</i>O<sub>2</sub>. As demonstrated in
Ref. [4] for the high-temperature oxidation of &#947;-NiCrAl, a lower <i>p</i>O<sub>2</sub>
reduces the activity of oxygen at the oxide/alloy interface during the initial,
fast oxidation stage, thereby promoting the coalescence of initially grown &#945;-Al<sub>2</sub>O<sub>3</sub>
crystallites into a continuous closed &#945;-Al<sub>2</sub>O<sub>3</sub> layer. Consequently, the duration of
the initial, very fast oxidation stage and the associated Al depletion from the
coating are suppressed. The presence of a native oxide layer on the coating may
result in an additional, small decrease of the activity of oxygen at the native
oxide/coating interface at the onset of oxidation.</p>

<h4><a name="_Ref36288330">Annealing-Induced Changes within
Coating</a></h4>

<script>comment();</script><p>For the sputtered-clean coating, no native
oxide resides on the coating surface at the start of the oxidation in the UHV
chamber. Due to the absence of the native oxide, extensive Cr evaporation
occurs upon heating in UHV and subsequent oxidation at low <i>p</i>O<sub>2</sub>.
As evidenced by LOM and SEM analysis of the coating after sputter-cleaning and
subsequent annealing in the UHV chamber for 1 h at 1373 K (see Fig. <a
href="#Fig3">3</a>), the Cr evaporation results in the transformation of the </span>&#947;-phase into the Al-rich &#946;-phase in the region
contiguous to the oxide/coating interface (further denoted as &#946;*-zone), and a
roughening of the parent coating surface. A corresponding annealing treatment
of the coating <i>with</i> native oxide showed that the formation of the &#946;*-zone upon heating is
suppressed in the presence of a native oxide.</p>

