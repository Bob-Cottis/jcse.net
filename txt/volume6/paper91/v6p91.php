(and some AlYO<sub>3</sub>) at the oxide/coating interface is suppressed.</p>

<h4>Internal Oxidation of Y</h4>

<script>comment();</script><p>For all three oxidation treatments,
internal oxidation of the YNi-rich intermetallic precipitates, which are concentrated at
original &#947;/&#946; phase boundaries in the
coating, has occurred through inward diffusion of oxygen along original &#947;/&#946; phase boundaries (Fig. <a
href="#Fig1">1</a>). XRD analysis of the oxidised specimens in combination with
EDS analysis of their cross-sections in the SEM, show that these internal
oxides are predominantly constituted of Y<sub>3</sub>Al<sub>5</sub>O<sub>12</sub>
with some AlYO<sub>3</sub>. The internal oxidation of the YNi-rich
precipitates in the coating results from the higher inward flux of oxygen along
original </span>&#947;/&#946; phase boundaries as compared to the outward flux of Y along these
boundaries (as determined by the rate of dissolution and subsequent
segregation). </p>

<script>comment();</script><p>For the oxidations at low <i>p</i>O<sub>2</sub>
in a UHV chamber, SEM analysis of the oxide/coating cross-sections often
reveals paths of Y<sub>3</sub>Al<sub>5</sub>O<sub>12</sub> (and some AlYO<sub>3</sub>)
running from the oxide/coating interface through the developing &#945;-Al<sub>2</sub>O<sub>3</sub>layer
to an outer surface, which is mainly constituted of Y<sub>2</sub>O<sub>3</sub>
(cf. Figs. <a href="#fig2">2b</a>
and <a href="#Fig2">c</a>).</span> These features are confined to positions
above original </span>&#947;/&#946; phase boundaries, where dissolution and subsequent segregation of Y
to the coating surface, as well as internal oxidation of YNi-rich
intermetallic precipitates, has occurred. These YAl-oxide paths are much less pronounced for the oxidation
of the <i>sputtered-clean</i> coating at <i>low</i> <i>p</i>O<sub>2</sub>
(which initially forms an Y-depleted &#946;*-zone) and were not observed
for the oxidation of the coating <i>with</i> native oxide at <i>high</i> <i>p</i>O<sub>2</sub>
(for which initial Y segregation is hindered). The presence of these YAl-oxide paths within the grown oxide layer (for the
oxidations at low <i>p</i>O<sub>2</sub>) indicate that dissolution and
segregation of Y along original &#947;/&#946; phase boundaries to the oxide/coating interface, followed by
outward diffusion of Y along oxide grain boundaries to the oxide surface, has
occurred during the oxidation. The outward diffusion of Y through the oxide is
driven by the oxygen potential gradient across the developing oxide layer [11,
12]. However, an additional driving force must be responsible for the
dissolution and segregation of Y from the interior of the coating to the
oxide/coating interface, since this process also takes place in the absence of
an oxygen potential gradient (i.e. upon annealing in UHV; see Sec. 3.2). In the absence of an oxygen
potential gradient, the dissolution and segregation of Y to an alloy surface is
generally attributed to the high lattice strain energy associated with the size
mismatch of the relatively large Y atoms within the alloy [10, 11]. The outward
diffusion of Y along alloy and oxide grain boundaries hinders the outward
transport of other, less reactive cations along these
grain boundaries, and inhibits the formation and growth of voids at the
oxide/coating interface [11, 12].</p>

<h4>Distribution of Non-Protective Oxide Phases</h4>

<script>comment();</script><p>After
continued oxidation (i.e. after 4 h) of the sputtered-clean coating at low <i>p</i>O<sub>2</sub>,
the resulting oxide layer is constituted of &#945;-Al<sub>2</sub>O<sub>3</sub>with the initially segregated and subsequently oxidised
Y located at its surface (see Fig. <a href="#fig4">4</a>); the </span>&#946;*-zone has already
partially transformed back into the &#947;-phase due to the oxidation-induced Al depletion. This
contrasts with the oxidation of the coating <i>with</i> native oxide at
high <i>p</i>O<sub>2</sub>, for which a doubled-layered oxide structure has
evolved (see Figs. <a href="#fig5">5a</a> and <a href="#Fig6">6</a>).</span> As revealed by XRD, SEM, TEM,
XPS analysis and SIMS depth profiling (see Figs. <a href="#fig5">5</a> and <a
href="#Fig6">6</a>), the top oxide layer (i.e. the dark layer in Fig. <a
href="#fig5">5a</a>) has a relatively fine-grained, equiaxed
structure and is constituted of (spinel) oxides of
Al, Cr, Ni and Co with a strong Al-enrichment at its very surface. The bottom
oxide layer (i.e. the bright layer in Fig. <a href="#fig5">5a</a>) has a
denser, coarse-grained, columnar structure and consists of only </span>&#945;-Al<sub>2</sub>O<sub>3</sub>
with small amounts of Cr and Y. The strong cathodoluminescence of the &#945;-Al<sub>2</sub>O<sub>3</sub> bottom
layer, as observed with the secondary electron detector (Fig. <a
href="#fig5">5a</a>), indicates that the columnar</span> &#945;-Al<sub>2</sub>O<sub>3</sub>
grains are doped with Y (and Cr). The interface between the top and bottom
oxide layer is relatively sharp, whereas the interface between the &#945;-Al<sub>2</sub>O<sub>3</sub>
bottom layer and the coating is irregular due to the inward growth of &#945;-Al<sub>2</sub>O<sub>3</sub>
and the formation of large YAl-oxide pegs at the
oxide/coating interface by internal oxidation along original &#947;/&#946; boundaries. This
complies with the general view that the top oxide layer is grown mainly by the
outward diffusion of metal cations, whereas the
bottom oxide layer is grown predominantly by the inward diffusion of oxygen
anions through the &#945;-Al<sub>2</sub>O<sub>3</sub> layer.</p>

