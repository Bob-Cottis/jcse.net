<script>comment();</script><p>The formation of a Y-depleted &#946;*-zone, in combination
with a strong Y-enrichment at the outer oxide surface, is suppressed by the
presence of a native oxide on the coating at the onset of the oxidation.
Instead, segregated Y becomes concentrated as large pegs of Y<sub>3</sub>Al<sub>5</sub>O<sub>12</sub>
[7] (and some AlYO<sub>3</sub> [8]), extending from the outer surface along &#947;/&#946; phase boundaries into
the &#947;+&#946; interface region of the
coating (cf. Fig. <a href="#fig2">2b</a>). Consequently,
the developing </span>&#945;-Al<sub>2</sub>O<sub>3</sub>
[9] layer is less compact (i.e. less dense) and less uniform of thickness than
for the sputtered-clean coating (cf. compare Fig. <a href="#fig1">1b</a> and <a href="#fig1">c</a>).</span> </span></p>

<script>comment();</script><p>The observed differences in oxide-layer
morphology and constitution, for the oxidation of the coating with or without a
native oxide at low <i>p</i>O<sub>2</sub>, are attributed to the differences in
the extent of Y segregation at the onset of the oxidation, and the resulting
distribution of Y within the initially grown oxide layer. For example, the
presence of initially segregated Y at the coating surface, as well as
Cr-evaporation-induced roughening of the coating surface, may result in a
higher density of heterogeneous nucleation sites for the formation of &#945;-Al<sub>2</sub>O<sub>3</sub>
[10, 11]. Consequently, the coalescence of the initial &#945;-Al<sub>2</sub>O<sub>3</sub>
crystallites into a closed &#945;-Al<sub>2</sub>O<sub>3</sub> layer is promoted for the oxidation of
the sputtered-clean coating (cf. compare Fig. <a href="#fig1">1b</a> and <a href="#fig1">c</a>).</span> </span></p>

<script>comment();</script><p>For the oxidation of the coating with
native oxide at <i>high</i> <i>p</i>O<sub>2</sub>, no Y is detected at the oxide
surface (Fig. <a href="#fig5">5c</a>)
or in the oxide subsurface region (Fig. <a href="#fig5">5d</a>),
indicating that virtually no Y segregation has occurred at the onset of the
oxidation. Instead, subsequently segregated Y becomes concentrated as large
pegs of Y<sub>3</sub>Al<sub>5</sub>O<sub>12</sub> (and some AlYO<sub>3</sub>),
extending from the oxide/coating interface (i.e. below the initially grown
oxide layer) along original </span>&#947;/&#946; phase boundaries into the &#947;*-region of the coating (cf. Fig. <a href="#fig5">5</a>). </span></p>

