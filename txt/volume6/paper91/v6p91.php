<h4>Initial Segregation and Subsequent Distribution of Y</h4>

<script>comment();</script><p>As evidenced by e.g. Figs. 1c and 2c, the YNi-rich intermetallic
precipitates in the Cr-evaporation-induced &#946;*-zone, have dissolved upon the oxidation of the sputtered-clean
coating at low <i>p</i>O<sub>2</sub>. XPS and SEM analysis of
the annealed coating (Fig. <a href="#Fig3">3c</a>) showed that the Y has
segregated from the </span>&#946;*-zone
along the original &#947;/&#946; phase boundaries to the
coating surface. As a result, upon subsequent oxidation, the internal oxidation
zone of YAl-oxides (see below) is concentrated below
the Y-depleted &#946;*-zone
(Fig. <a href="#Fig1">1c</a>) and the developing oxide layer is
covered with a noticeable thin layer of Y<sub>2</sub>O<sub>3</sub> [6] (see
Figs. <a href="#fig2">2c</a> and <a
href="#fig4">4</a>).</span> </span></p>

