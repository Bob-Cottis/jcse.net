<script>comment();</script><p>Huang et al. [<SCRIPT>crossref(3);</SCRIPT>] found that hot-dipping Ebrite steel (~ Fe-26.3 wt%
Cr) in Ni<sup>2+</sup>- and Ni<sup>+2</sup>/Y<sup>3+</sup> nitrate solutions
before oxidation reduces the contact resistance of pre-oxidized samples against
