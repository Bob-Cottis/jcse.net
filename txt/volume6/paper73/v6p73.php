the NiO coating reacts with the Cr<sub>2</sub>O<sub>3</sub> scale to form the
spinel NiCr<sub>2</sub>O<sub>4</sub>, which has been reported to be a good
electrical conductor (s = 2.70 S cm<sup>-1
</sup>at 1273K, porosity = 38%, doped with 0.66 at% Ce [<SCRIPT>crossref(9);</SCRIPT>]).</p>

