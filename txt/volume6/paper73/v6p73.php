coating�. However, adherence of Cr<sub>2</sub>O<sub>3</sub> and the alloy was
poor and oxide spallation was visible.</p>

<script>comment();</script><p>Under the working condition of the cathode (typically 1123 K and air
with about 3% H<sub>2</sub>O) Cr<sub>2</sub>O<sub>3</sub>disintegrates by evaporationof CrO<sub>3</sub> and CrO<sub>2</sub>(OH)<sub>2</sub>
[<SCRIPT>crossref(4);</SCRIPT>]. Such evaporation is detrimental for the electrical performance of the SOFC [<SCRIPT>crossref(4);</SCRIPT>] and a suitable coating is sought to reduce/ prevent this
evaporation. </p>

<script>comment();</script><p>In the present paper, the oxidation of Fe-22Cr at 1173K is investigated
for as pre-treated, Ni- and NiO coated conditions. The results are discussed in
relation to the Ni-Cr-O, the Cr-Fe-O and the Fe-Ni-O phase diagrams. The
isotherms at 1173K (figure 1) and 1323K were calculated with Thermo-Calc
