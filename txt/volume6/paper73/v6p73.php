<h3>Experimental</h3>

<h4>Reaction between NiO and Cr<sub>2</sub>O<sub>3</sub> powder</h4>

<script>comment();</script><p>In order to investigate the possible reaction between NiO and Cr<sub>2</sub>O<sub>3</sub>,
mixtures of Cr<sub>2</sub>O<sub>3</sub> and NiO powders were prepared with the
following Ni: Cr cation ratios: 5:1, 2:1, 1:1, 1:2, 1:5 (the crosses in the
Ni-Cr-O isotherm represents these mixtures). They were heat-treated twice at
1173 or 1323K in air (heating rate 100 K/h) for 60 hours followed by quenching
in air. The second heat-treatment was performed in order to ensure that
equilibrium was reached. After each heat-treatment, all mixtures were
characterised with XRD using Si as a reference.</p>

<script>comment();</script><p>High-temperature XRD was preformed on the mixture with the cation ratio
Ni:Cr 1:2. </p>

<h4>Pre-treatment of Fe-22Cr</h4>

<script>comment();</script><p>A 0.2 mm thick Fe-22Cr foil, containing 21.63 wt% Cr, 0.31 wt% Si, 0.27
wt% Mn, 0.19 wt% Ni and 0.011wt% Ce as main alloying elements was used in the
present work. The Fe-22Cr foil was etched in an aqueous solution (75 ml H<sub>2</sub>O)
of conc. HF (5 ml) and conc. HNO<sub>3</sub> (15 ml) in an ultrasonic bath in
order to remove the native oxide layer.</p>

<h4>Deposition of the Ni-coating</h4>

<p>The Ni-coating was electrodeposited by a commercial process yielding a
thickness of approximately 5�m.</p>

<h4>Deposition of the NiO-coating </h4>

<script>comment();</script><p>NiO coatings were deposited by spray-painting and plasma spraying. </p>

<script>comment();</script><p>In the case of spray-painting, an ethanol-based slurry of NiO particles
(particle size: 0.1-20 �m, average 5.35 �m) was sprayed onto pre-treated
Fe-22Cr samples at room temperature, using air as the carrier gas. The
thickness of the as-sprayed layer was about 15 �m. The samples were annealed
for 2 hours in air at 1123, 1223, 1323 and 1423K; the heating- and cooling rates
were 100K/h. Samples annealed at 1323K were used in the oxidation measurements.
</p>

<script>comment();</script><p>Prior to plasma spraying the etched Fe-22Cr samples were sandblasted
with Al<sub>2</sub>O<sub>3</sub> in order to ensure good adherence of the
coating to the substrate. The NiO powder was mixed with 2 wt% SiO<sub>2</sub>
to improve the flow-ability of the powder during spraying. Oxygen was used as
carrier gas. The thickness of the coating was 15-20 �m and it contained some
porosity. </p>

<h4>High temperature oxidation</h4>

<script>comment();</script><p>High temperature oxidation experiments were performed on as pre-treated,
NiO-coated and Ni-coated samples at 1173K (�7K) in air containing 1 % H<sub>2</sub>O. The heating
time was ca. 2 hours and the cooling time was ca. 6 hours. The oxidation time
(not including the heating- and cooling time) was varied between 0 and 504
hours.</p>

<h4>Microstructure characterization</h4>

<script>comment();</script><p>For phase identification X-ray diffractograms were measured for selected
samples using a STOE diffractometer equipped with Cu-Ka radiation
(XRD). On the spray-painted samples, diffractograms were also recorded after
removing the upper part of the NiO particle layer by grinding. </p>

<script>comment();</script><p>Samples for cross-sectional investigation were mounted in resin, ground
and polished (last step 1 �m diamond). Microstructure investigations were
performed using a JEOL JSM-840 scanning electron microscope (SEM) in
back-scatter electron (BSE) mode. Local chemical analysis was performed using
Noran energy dispersive X-ray spectroscopy (EDS). </p>

<h3>RESULTS</h3>

<h4>Reaction between NiO and Cr<sub>2</sub>O<sub>3</sub> powder</h4>

<script>comment();</script><p>Upon reaction between NiO and Cr<sub>2</sub>O<sub>3</sub> powders a tetragonal
distorted spinel with the lattice parameters a = 0.584 nm and c = 0.843 nm
developed. It has the cation ratio Ni:Cr = 1:2, i.e. would be consistent with
NiCr<sub>2</sub>O<sub>4</sub>. With respect to this stoichiometry, the Ni-rich
mixtures consisted of NiO and NiCr<sub>2</sub>O<sub>4</sub>, whilst the Cr-rich
mixture consisted of Cr<sub>2</sub>O<sub>3</sub> and NiCr<sub>2</sub>O<sub>4</sub>.
High temperature XRD showed, that the tetragonally distorted spinel transformed
instantaneously to a cubic spinel with a = 0.831 nm at ca. 315K.</p>

<h4>The structure of the spray-painted NiO coating</h4>

<script>comment();</script><p>Cross-sections of NiO coated samples annealed at 1323 and 1423K are
shown in figure 2. At the temperatures investigated, a Cr<sub>2</sub>O<sub>3</sub>
layer developed at the metal surface; the thickness of the layer increased with
increasing annealing temperature. The NiO powder sintered during the 2 hours
annealing; the NiO particle-layer became denser the higher the temperature.
Dark regions consisting of silica (determinated by EDS-analyses) appeared in
the substrate below the coating of the samples baked at 1323K (in some areas)
and 1423K. Atomic Force Microscopy on similarly treated samples showed an
increase in height across the dark regions. This strongly indicates that the
dark regions consist entirely of silica, as silica has a higher hardness than
the matrix.</p>

<script>comment();</script><p>In the samples annealed at 1223, 1323 and 1423K a spinel phase with a
lattice parameter between 0.83518 (�0.00025) and 0.83558 (�0.00020) nm was identified by XRD. EDS-line scans
across the NiO - Cr<sub>2</sub>O<sub>3</sub> interface gave no indication of
the presence of a mixed oxide phase in any of the samples. </p>

