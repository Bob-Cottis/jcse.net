<h3>Abstract</h3>

<script>comment();</script><p>The effect of Ni and NiO coatings on Fe-22Cr during oxidation at 1173K
in 1% H<sub>2</sub>O was examined with respect to scale microstructure for
oxidation times between 0 and 504 hours. Upon oxidation of the as pre-treated
Fe-22Cr, Cr<sub>2</sub>O<sub>3</sub>&nbsp;and a spinel developed. Oxidation and
inter-diffusion between the Ni coating and Fe-22Cr occurred simultaneously. The
scale consisted of NiO, a Fe-Ni spinel and Cr<sub>2</sub>O<sub>3</sub>. </p>

<script>comment();</script><p>For the NiO coated alloy, a thin spinel layer developed between the NiO
coating and the Cr<sub>2</sub>O<sub>3</sub> scale. The microstructures of the
scales are discussed with respect to calculated isotherms in the Fe-Cr-O,
Cr-Ni-O and Ni-Fe-O phase diagrams and inter-diffusion profiles between Ni and
Fe-22Cr. </p>

<h4>Keywords: Oxidation, microstructure,
Cr-Ni-Fe-O isotherms, inter-diffusion</h4>

<h3>Introduction</h3>

<script>comment();</script><p>In Solid Oxide Fuel Cell (SOFC) stacks the interconnect separates the
fuel on the anode side from the oxidant at the cathode side and it serves to
transport the electrical current between the individual cells and to the
external circuit. High-temperature oxidation resistant Fe-Cr alloys are
candidates for interconnect materials, due to the combination of a slow oxide
layer growth rate and a reasonable (but still low) conductivity of the Cr<sub>2</sub>O<sub>3</sub>
scale [<SCRIPT>crossref(1);</SCRIPT>]. However, the contribution of the growing chromia scale to the
total resistance of the SOFC stack gets significant during the operation time
of the stack. Therefore, the growth rate of the scale has to be reduced by
coating and, simultaneously the conductivity of the scale has to be
enhanced.� </p>

<script>comment();</script><p>The conductivity of Cr<sub>2</sub>O<sub>3</sub> can be enhanced by
doping it with a divalent cation, e.g. Ni<sup>2+</sup>, because this increases
the hole-concentration of the intrinsic conductor, cf. equation 1. The effect
is most pronounced in oxidizing atmospheres [<SCRIPT>crossref(2);</SCRIPT>].</p>

