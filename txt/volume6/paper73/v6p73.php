<h3>Discussion</h3>

<h4>The NiO and Cr<sub>2</sub>O<sub>3</sub> powders</h4>

<script>comment();</script><p>Thermodynamic calculations predict the development of the NiCr<sub>2</sub>O<sub>4</sub>
spinel upon reaction of NiO and Cr<sub>2</sub>O<sub>3</sub> at 1173 K (cf.
figure 1) and 1323 K in air (at 1323 K the spinel forms at p(O<sub>2</sub>) =
10<sup>-12 </sup>bar). NiCr<sub>2</sub>O<sub>4</sub> did indeed form by
reaction between NiO and Cr<sub>2</sub>O<sub>3</sub> powder at both
temperatures.� The self-diffusion
coefficients of Ni<sup>2+</sup> and Cr<sup>3+</sup> [<SCRIPT>crossref(10);</SCRIPT>] in NiCr<sub>2</sub>O<sub>4</sub>
are of the same order of magnitude, whereas the oxygen self-diffusion
coefficient is approximately one order of magnitude smaller [<SCRIPT>crossref(11);</SCRIPT>]. This
indicates that NiCr<sub>2</sub>O<sub>4</sub> forms by counter-diffusion of the
relatively small cations over the interstices of the relatively rigid (fully
occupied) O<sup>2-</sup> lattice.</p>

<h4>Oxidation of as pre-treated samples</h4>

<script>comment();</script><p>During oxidation of the as pre-treated samples, Cr<sub>2</sub>O<sub>3</sub>
and a spinel phase formed, as detected by XRD. The presence of FeCr<sub>2</sub>O<sub>4</sub>
could not be confirmed by EDS. According to the Cr- Fe-O phase diagram at
1173K, figure 1, only corundum is thermodynamically stable for an oxygen
partial pressure corresponing to that of air. Following the vertical dashed
line in the Cr- Fe-O in the direction of decreasing p(O<sub>2</sub>), a
multi-layered scale and several zones of internal oxidation are expected
between the corundum layer and the substrate. Neither of these appeared in the
cross-section (Figure
3). Selective oxidation of Cr is favoured at chromium
contents above 14 wt %; selective oxidation of Cr stabilizes the chromia-based
scale at the expense of the faster growing spinel [<SCRIPT>crossref(12);</SCRIPT>].</p>

<script>comment();</script><p>During oxidation of FeCr alloys a layer containing a large proportion of
iron oxides forms in the beginning of the oxidation process, because the iron
oxides grow faster than Cr<sub>2</sub>O<sub>3</sub>[<SCRIPT>crossref(13);</SCRIPT>].� The identified spinel
might be a Fe-Cr spinel formed by reaction between initially form iron oxides
and Cr<sub>2</sub>O<sub>3</sub>. However, thermodynamics predicts that
initially grown iron oxides are incorporated in Cr<sub>2</sub>O<sub>3</sub>,
because Fe<sub>2</sub>O<sub>3</sub> and Cr<sub>2</sub>O<sub>3</sub> form a
continuous series of solid solutions cf. figure 1.� </p>

<script>comment();</script><p>The alloy contains a small amount of Mn. The formation of MnCr<sub>2</sub>O<sub>4</sub>
spinel on the top of Cr<sub>2</sub>O<sub>3</sub> has been observed during the
oxidation of high-alloy steel with a low Mn content (0.56-1.58 wt %) [<SCRIPT>crossref(14);</SCRIPT>]
(0.31 wt %) [<SCRIPT>crossref(15);</SCRIPT>] in air and has been explained by very fast diffusion of Mn
cations through Cr<sub>2</sub>O<sub>3</sub> [<SCRIPT>crossref(16);</SCRIPT>]. The lattice parameter of
the identified spinel a = 0.84425 (�0.00032) and 0.84649 (�0.00025) nm nm is close to that of MnCr<sub>2</sub>O<sub>4</sub>
(0.8413 nm). </p>

<h4>Oxidation of the Ni-coated samples</h4>

<script>comment();</script><p>Upon the oxidation of the Ni-coated samples, two competing processes
occur: Inter-diffusion between the Ni-coating and the Fe-22Cr alloy and
oxidation of the Ni-coating.</p>

<script>comment();</script><p>Inter-diffusion between Fe-22Cr (bcc, 100 �m) and Ni (fcc, 5 �m) was
investigated with DICTRA [<SCRIPT>crossref(17);</SCRIPT>] by calculation of the concentration profiles
of Fe, Cr and Ni after diffusion at 1173K for 1 hour and 10 hours in an inert
atmosphere (cf. figure 8). The calculation shows that the thickness of the fcc
phase increases with annealing time. The Cr-concentration drops sharply across
the bcc � fcc interface, whilst the Ni and Fe concentration change more
gradually. </p>

<script>comment();</script><p>The porosities at and close to the NiO � spinel interface might have
appeared as a consequence of the inter-diffusion between the Ni-coating and the
Fe-22Cr substrate. In the case, that the inward Ni-flux differs from the sum of
the out-ward Fe and Cr fluxes, there will be a net flux of vacancies which
might coalesce as Kirkendall porosities. This may have occurred close to the
Ni/Fe-22Cr interface (alternatively or additionally, these voids could be the
result of outward diffusion of Ni cations on oxidation, see below).</p>

<script>comment();</script><p><img
src="image032.jpg" width="324" height="285"><img
src="image034.jpg" width="203" height="184"></span></p>

<script>comment();</script><p><a name="_Ref40713944">Figure </a>8: Left: Concentration
profiles across the Fe-22Cr (bcc, 100�m) � Ni (fcc, 5�m) interface after 1 hour
and 10 hours at 1173K as calculated with DICTRA [<SCRIPT>crossref(17);</SCRIPT>]. The dashed lines
indicate the position of the interface between bcc and fcc. Only Fe, Cr and Ni
are included in the calculation. Right: The Fe-Ni-Cr phase diagram at 1173K as
calculated using Thermo-Calc [<SCRIPT>crossref(5);</SCRIPT>]</p>

<script>comment();</script><p>The experiments show that Ni has diffused throughout the Fe-22Cr foil
for oxidation times longer than 72 hours, resulting in a Ni bulk content of
about 2 wt %. According to the Fe-Cr-Ni phase diagram at 1173K, figure 8, the
maximum solubility of Ni in Fe-21.63 wt % Cr is 2.13 wt %, indicating that the
ferritic substrate has been saturated with Ni. At this stage, the Ni content in
the alloy at a depth of 10 �m is so high (ca. 20 wt %) that the outer part of
the alloy is austenitic. The fcc phase is expected to grow with oxidation time
as Cr is consumed during the growth of Cr<sub>2</sub>O<sub>3</sub> and less Fe
and Ni are needed to stabilize the fcc phase. </p>

<script>comment();</script><p>The concentration profiles in figure 8, left, also show that the coating
is richer in Fe than in Cr. This result of the calculations would be consistent
with the observation (from EDS-line scans) that the spinel formed in-between
the NiO and the Cr<sub>2</sub>O<sub>3</sub> layer and the spinel formed close
to the surface is richer in Fe (and Ni) than in Cr. </p>

<p>At temperatures below 1373K, NiO grows predominantly by outward Ni
diffusion via short circuit paths, particularly grain boundaries [<SCRIPT>crossref(18);</SCRIPT>]. For
Ni with a purity less than 99.99%, voids are formed at the oxide-metal
interface by accumulation of Ni vacancies. Compressive stresses in combination
with the voids lead to local detachment of the scale and shear stresses in the
detached parts initiate cracks through the NiO layer [<SCRIPT>crossref(19);</SCRIPT>]. O<sub>2</sub>
penetrates the cracks and reacts with Ni below the detached oxide and forms a
fine-grained NiO. The cracks may heal as the outward Ni diffusion is not
interrupted causing an internal volume increase and thereby growth stresses,
which initiate new cracks. The resulting NiO scale has a duplex structure with
an outer columnar and an inner equiaxed grained scale [<SCRIPT>crossref(19);</SCRIPT>][<SCRIPT>crossref(20);</SCRIPT>]. </p>

<script>comment();</script><p>From figure 4 it is evident that the NiO formed upon oxidation of the
Ni-coated Fe-22Cr contains voids at and close to the NiO - spinel interface
(alternatively or additionally, these voids could be associated with the diffusion
of Ni into the substrate, see above). </p>

<script>comment();</script><p>Furthermore the NiO grains appear columnar. Cracks may have formed
during the NiO growth. As a consequence of inter-diffusion between the
Ni-coating and the substrate, the O<sub>2</sub> penetration may have resulted
in the formation of the Fe-Ni rich spinel, instead of an inner equiaxed grained
NiO layer. This is supported by thermodynamic calculations, which predict the
formation of both the spinel and NiO for 0.23 &lt; n(Ni)/((n(Ni)+n(Fe)) &lt; 0.85, cf. the Ni-Fe-O isotherm in figure 1.
Furthermore, the appearance the Fe-Ni spinel along grain boundaries in NiO (cf.
figure 4) indicates that also Fe diffuses along short circuits.</p>

<script>comment();</script><p>Cr<sub>2</sub>O<sub>3</sub> forms in-between the alloy and the spinel
because it is thermodynamically the most stable oxide in the Fe-Cr-Ni-O system
(cf. figure 1). The Cr<sub>2</sub>O<sub>3</sub> layer appears thicker and more
porous in areas, where the NiO has spalled off. Spallation may have occurred
during oxidation due to growth stresses or during cooling due to thermal
stresses. In both cases, the cracks have most likely propagated along the NiO -
spinel interface due to the presence of voids at this location. This is
supported by the occurrence of thin light oxide layer on top of the Cr<sub>2</sub>O<sub>3</sub>
layer, where NiO is missing. The enhanced growth of chromia indicates that
spallation has occurred during oxidation. After spallation, the newly created
surface is exposed to the ambient atmosphere and thereby a higher chemical
potential of oxygen. The increase in the chemical potential leads to a steeper
gradient in the oxygen potential and thus faster growth. </p>

<h4>The NiO-coated samples</h4>

<script>comment();</script><p>The formation of the Cr<sub>2</sub>O<sub>3</sub> layer below the NiO
spray-painted/ plasma sprayed coating during annealing/ oxidation is in
accordance with the Ni-Cr-O phase diagram (figure 1), which shows that Cr<sub>2</sub>O<sub>3</sub>
is thermodynamically more stable than NiO and NiCr<sub>2</sub>O<sub>4</sub>. </p>

<script>comment();</script><p>The formation of NiCr<sub>2</sub>O<sub>4</sub> by cation inter-diffusion
between the formed Cr<sub>2</sub>O<sub>3</sub> and the NiO coating (both
spray-painted and plasma-sprayed) was expected during annealing/ oxidation. Although
a spinel did form during annealing of the spray-painted samples, it was not
NiCr<sub>2</sub>O<sub>4</sub> as it would transforms instantaneously to a
tetragonal phase during cooling. Substituting a small amount of the Cr or Ni
with Fe might result in a spinel, which transforms to a distorted spinel at a
temperature below room temperature. Fe may be present at this location as a
large proportion of iron oxide forms in the initial stage of oxidation of FeCr
alloys due to faster growth rate of iron oxides than of Cr<sub>2</sub>O<sub>3</sub>
[<SCRIPT>crossref(13);</SCRIPT>]. Alternatively, the spinel might be a Ni-Fe spinel formed by reaction
between the initially formed iron oxides and the NiO coating. The lattice
parameter, a = 0.83518 (�0.00025) and
0.83558 (�0.00020) nm
of the identified spinel is close to that of NiFe<sub>2</sub>O<sub>4</sub> (a =
0.8339 nm). </p>

<script>comment();</script><p>A similar spinel might have formed in-between the NiO plasma coating and
the Cr<sub>2</sub>O<sub>3</sub> layer as the EDS-line scans indicates the
presence of Fe in this interface.</p>

<h4>The role of Si</h4>

<script>comment();</script><p>Silica develops below Cr<sub>2</sub>O<sub>3</sub> because it is
thermodynamically more stable than Cr<sub>2</sub>O<sub>3</sub> (SiO<sub>2</sub>
is placed below Cr<sub>2</sub>O<sub>3</sub> in a Ellingham/ Richardson diagram)
[<SCRIPT>crossref(13);</SCRIPT>]. Silica can be present as a more or less continuous thin layer
in-between the Cr<sub>2</sub>O<sub>3</sub> layer and the substrate (after
oxidation of the as pre-treated Fe-22Cr, cf. figure 3), and as particles in the alloy (after oxidation
of the Ni-coated sample and after annealing of the NiO spray-painted sample
at 1423K, cf.
figure 2). A combination of both appearances, i.e. silica films and silica
particles, was observed in different areas of the NiO spray-painted sample
after annealing at 1323K. </p>

<script>comment();</script><p>The
different appearances of silica could be explained as follows. The appearance
of silica as a thin layer at the scale/substrate interface reflects the higher
thermodynamic stability of this oxide as compared to chromia. The nucleation of
silica in the substrate is expected to occur at the location where vacancies,
necessary to maintain the outward diffusive fluxes of Fe and Cr, are
annihilated. For the case that Kirkendall porosity develops in the substrate,
silicon is anticipated to segregate at the internal surfaces thus created and
is later oxidised to silica. This occurs particularly when a high outward flux
of Cr (and Fe) is maintained, i.e. at a relatively high temperature where CrO<sub>3</sub>
and /or CrO<sub>2</sub>(OH)<sub>2</sub> evaporate.</p>

<h3>Conclusion</h3>

<script>comment();</script><p>Cr<sub>2</sub>O<sub>3</sub> forms closest to the alloy upon oxidation of
coated and as pre-treated samples, as this is the thermodynamically most stable
oxide in the Cr-Fe-Ni-O system. Silica forms as an interfacial film between the
alloy and Cr<sub>2</sub>O<sub>3</sub> or as particles in the alloy. The silica
is situated below Cr<sub>2</sub>O<sub>3</sub> because it is thermodynamically
more stabile than Cr<sub>2</sub>O<sub>3</sub>. </p>

<script>comment();</script><p>A spinel is formed upon oxidation of the as pre-treated Fe-22Cr. The
spinel was proposed to be MnCr<sub>2</sub>O<sub>4</sub> due to the fast
diffusion of Mn cations through Cr<sub>2</sub>O<sub>3</sub>. </p>

<script>comment();</script><p>Oxidation and inter-diffusion between the Ni-coating and Fe-22Cr
occurred competitively. NiO, Fe-Ni-spinel and Cr<sub>2</sub>O<sub>3</sub>
develop during oxidation. The outer part of the alloy becomes austenitic due to
the inter-diffusion, after ca. 72 hours of exposure the Fe-22Cr is saturated in
Ni. Continued depletion of Cr due to oxidation most likely results in growth of
the austenitic region.</p>

<script>comment();</script><p>A thin spinel layer developed between the NiO coating and the Cr<sub>2</sub>O<sub>3</sub>
scale. It was proposed to be a Fe-Ni spinel (or a Fe-Ni-Cr spinel) formed by
inter-diffusion between initially formed iron oxide and the NiO coating (and
the chromia scale). </p>

<script>comment();</script><p>At 1173K and 1323K, NiCr<sub>2</sub>O<sub>4</sub> forms by
inter-diffusion between NiO and Cr<sub>2</sub>O<sub>3</sub> powder. NiCr<sub>2</sub>O<sub>4</sub>
is tetragonal a room temperature but transform instantaneously to a cubic
spinel at about 315K.</p>

