<h2><b>The
Passivation Characteristics of 54Cu-11Ni-29Zn-5Mn-1Fe alloy in Synthetic
Seawater</b></h2>

<p>A.P. Patil and R.H. Tupkary<br>
<i>Department of Metallurgical Engineering, V.R. College of Engineering, Nagpur,
440011 (India)</i></p>
<SCRIPT src="/jsinc/AuthNote.js"></SCRIPT>

<SCRIPT>notify();</SCRIPT><SCRIPT>comment(1)</SCRIPT><h3 align=left>Abstract</h3>

<p>Copper alloys are generally more resistant
than copper in seawater mainly because of protective nature of the oxide film.
In the present investigations an attempt has been made to study the corrosion
behaviour of 54Cu-11Ni-29Zn-5Mn-1Fe alloy in synthetic seawater by weight loss
method and polarization studies. Corroded specimens were weighted to estimate
the thickness of the film before the post-exposure cleaning in the weight loss
tests. It is found that rate of change of weight loss decreases and film
thickness increases with time. By polarization studies it is confirmed that
54Cu-11Ni-29Zn-5Mn-1Fe alloy has passivating tendency and that its corrosion
rate decreases with exposure duration. It is also confirmed that the drop in
corrosion rate is due to formation of protective film and that the film
polarizes the cathodic and anodic reactions. </p>

