<h3>1. Introduction</h3>

<SCRIPT>comment(1)</SCRIPT><p>Copper alloys are generally more resistant
than copper to seawater corrosion, mainly because of relatively protective
oxide film formed on the surface of the alloy [1]. A film of Cu<sub>2</sub>O forms
on surface when copper and its alloys are exposed to seawater [2]. The Cu<sub>2</sub>O
is a p-type semiconductor [3]. The cation sublattice of Cu<sub>2</sub>O has few
vacancies, whereas oxygen sublattice is almost completely filled [3]. Cation
vacancies are associated with electron holes. These vacant cation sites allow
cation movement and associated electron holes allow electron migration. Thus,
Cu<sub>2</sub>O exhibits cationic and electronic conductivity. When copper is
suitably alloyed, the alloying elements enter the film and enhance corrosion
resistance [1]. This is because when a bivalent or trivalent cation enters into
Cu<sub>2</sub>O lattice then its ionic and electronic conductivity is decreased
[3]. The cations may enter the Cu<sub>2</sub>O film either in vacant sites or
by substitution. This improves the corrosion resistance of the alloy as in case
of 90-10 or 70-30 Cu-Ni alloys [3]. It has been found that better corrosion
behaviour of 54Cu-11Ni-29Zn-5Mn-1Fe alloy as compared to 89Cu-10Ni-1Fe alloy is
due to incorporation of Ni, Zn, Fe and Mn in oxide film [4]. Corrosion process
is associated with formation of film and its growth. Once the film forms on the
surface, rate of further corrosion is then controlled by nature of the film.
The nature of film is characterised by its ionic and electronic conductivity.
Potentiodynamic polarization studies may throw light on the ionic and
electronic conductivity of the film that forms on the surface during the
process of natural corrosion vis-a-vis during anodic polarization. Therefore,
an attempt has been made to investigate effect of exposure duration on the
corrosion rate, film growth and polarization behaviour of the test alloy. </p>

<h3 align=left><span style="font-weight: 400"><b>2.
Experimental</b></span></h3>

<SCRIPT>comment(1)</SCRIPT><p>Actual composition of the test alloy was
Cu-54.05%, Ni-10.99%, Zn-29.2%, Mn-4.81%, Fe-0.95%. The preparation of the test
alloy has been presented elsewhere [4]. </p>

<SCRIPT>comment(1)</SCRIPT><p><b>Weight Loss:</b> Specimens of
10mmx25mmx1.2mm size were cut and prepared by abrading on a series of emery
paper (1/0, 2/0, 3/0 and 4/0) for the weight loss tests. Specimens were
degreased in 5% NaOH solution and then washed with water just before testing.
Synthetic seawater (SSW) solution of pH 8.2 was prepared from L.R. grade
chemicals as per ASTM standard D1141-75 (re-approved 1980) without heavy metal
ions. The pH of SSW was 8.2 and temperature was 27�C. Test duration was 15
days, but to study effect of exposure duration the specimens were taken out at
planned intervals of 0-3, 0-6, 0-9, 0-12 and 0-15 days. After removal specimens
were washed in running tap water, then rinsed in methanol, then air-dried and weighed.
After weighing the corrosion products were removed by scrubbing with bristle
brush and scourer powder.� The samples were then washed in distilled water,
rinsed in methanol and air dried before weighing again. This practice enabled
to get not only the weight loss but also the weight of corrosion product
adherent to the surface. Considering that bulk of this corrosion product is Cu<sub>2</sub>O,
thickness of the film was estimated. Bulk density of Cu<sub>2</sub>O was
assumed to be 6.1 g.cm<sup>-2 </sup>[2]. </p>

<SCRIPT>comment(1)</SCRIPT><p><b>Polarization:</b> The specimen for
polarization studies was prepared by soldering a piece of insulated copper wire
at one flat surface of 10x10x1.8mm specimen. It was then mounted in cold
setting resin in such a way that soldered joint was completely embedded and
other flat surface was open. The open surface was prepared by abrading on
series of emery papers (1/0, 2/0, 3/0, 4/0 and 5/0). Polished specimens were
then washed with soap just before setting up of the cell. These specimens were
allowed to reach to stable open circuit potential for almost 30-35 minutes,
before carrying out polarisation test. One set of specimens was prepared in the
same way and then exposed to synthetic seawater for 15 days to grow a film on
it. At the end of 15 days exposure the specimens were removed from the solution
and immediately subjected to polarization studies. Such specimens would be
referred as �specimens with film�.</p>

<SCRIPT>comment(1)</SCRIPT><p>Anodic and cathodic polarization studies
were carried out with a three-electrode system with platinum mesh and saturated
calomel electrode using �Sycopel� make computer-controlled potentiostat
(AUTOSTAT 252). The cathodic polarization was started from �560mV(SHE),
continued in noble direction and stopped when current became positive. The
solution was stirred with a magnetic stirrer to minimize effects of
concentration polarization. The anodic polarization was started at �60mV(SHE),
continued in the noble direction and stopped after +500mV(SHE). The solution
was stagnant in polarization studies. The scan rate in cathodic polarization
was 0.166mV/s and that in anodic polarization was 0.08mV/s. </p>

<h3 align=left><b>3. Result and Discussion</b></h3>

<SCRIPT>comment(1)</SCRIPT><p>Figure 1 shows how the corrosion rate and
film thickness varies with exposure duration. It is evident from Fig. 1 that
the rate of corrosion and rate of film growth is high initially and tapers down
with passage of time. The corrosion rate is 8.3 mdd at the end of 3<sup>rd</sup>
day, which decreases to 3.8 mdd at the end of 15 days. Simultaneously, the
thickness of the film increases from 0.2m
to 0.6m. The drop in the instantaneous
rate of corrosion with increasing thickness over the exposure period indicates
that the film is passive and protective to considerable extent. The corrosion
rate of 3.8 mdd at the end of 15 days by itself indicates good corrosion
resistance. The thickness of the film after 15 days exposure is 0.6 mm, but the graph has yet not reached a
plateau. It means that for longer exposure time the film may grow thicker, may
become more protective and decrease the rate of corrosion substantially
further.</p>

<SCRIPT>comment(1)</SCRIPT><p>Figure 2 and 3 respectively show cathodic
and anodic polarization plots of the specimen in polished condition (curve-1)
and that with the film (curve-2). Comparison of anodic and cathodic Tafel
slopes in Fig. 2 and 3 indicates that the corrosion of this alloy in synthetic
seawater is under cathodic control. Figure 2 shows that i<sub>corr</sub> of
specimen with film is lower than that of polished specimen. It means that the
film that formed on the alloy surface after exposure to SSW for 15 days is
polarizing the cathodic reaction. It is pertinent to note that at pH = 8.2
oxygen ionisation is the only possible cathodic reaction. This is because
hydrogen reduction will require a potential lower than �484mV(SHE) (E=
-0.059pH). Actual potential at which hydrogen evolution can take place will
depend upon the overvoltage for hydrogen evolution reaction. If hydrogen
reduction takes place in cathodic polarization test then it would contribute to
the cathodic current. It is found in Fig.2 that the current density did not
increase at potential lower than �484mV(SHE). It means that the hydrogen
reduction is occurring neither in cathodic polarization test nor in weight loss
test. Therefore, oxygen ionisation reaction is the only cathodic reaction that
is taking place in this system. </p>

<SCRIPT>comment(1)</SCRIPT><p>It is also evident from Fig. 2 that the E<sub>corr</sub>
value in curve-2 is lower than that in curve 1. The drop in E<sub>corr</sub>
value together with drop in corrosion rate signifies the polarization of
cathodic reaction. The curve 2 in Fig. 2 shows that current density at
potential �560mV(SHE) is about 40mA/cm<sup>2</sup>,
which increases to about 90mA/cm<sup>2</sup>
at potential of �475mV(SHE). This indicates that initially the film is able to
polarize the cathodic reaction to large extent, but probably due to reduction
of some component of the film there is current contribution upto �500mV(SHE).
It is also seen that the specimen with preformed film exhibits lower i<sub>corr</sub>
than the one without it. The lower current density of the specimen with the
film in Fig. 2 (curve-2) indicates that the film is protective in nature. It
implies that the protection accorded by the film formed on the specimen in 15
days exposure to synthetic seawater is much less then the film formed on alloy
in just 30 minutes. This finding is in agreement with the findings in the
weight loss measurements that the corrosion rate decreases as the film
thickness increases. It is easy to understand as Cu<sub>2</sub>O is a p-type
semiconductor and when any bi-valent or tri-valent ion like Ni<sup>2+</sup>, Fe<sup>2+</sup>,
Mn<sup>2+</sup>, Fe<sup>3+</sup> or Mn<sup>3+</sup> enters the lattice of Cu<sub>2</sub>O
in place of Cu<sup>+</sup>, then it increases the positive charge. To maintain
charge neutrality one or more electron hole will be annihilated. This will
increase the electronic resistance of the Cu<sub>2</sub>O containing film [2].
Therefore, the findings of cathodic polarization shown in Fig. 2 present an
electrochemical evidence of the behaviour observed in weight loss studies that
corrosion rate decreases with time.</p>

<SCRIPT>comment(1)</SCRIPT><p>It is evident from curve�1 in Figure 3
that the alloy undergoes rapid active dissolution upto 180 mV(SHE) and then the
passivity builds up. In the passive region the current density increases slowly
with increasing potential upto 450mV(SHE). After 450mV(SHE) the current density
increases rather rapidly and film is observed to breakdown and therefore this
value represents the breakdown potential (E<sub>b</sub>). Looking at the fact
that i<sub>crit</sub> is high (16mA/cm<sup>2</sup>) and the difference between i<sub>crit</sub>
and i<sub>passivation</sub> is not substantial, this passivity is termed as
pseudo- passivity.� Nevertheless, Fig. 3 exhibits the passivation tendency of
the alloy. </p>

<SCRIPT>comment(1)</SCRIPT><p>Figure 3 also indicates that the
specimen with the film (curve-2) has lower current density upto 180mV(SHE) as
compared to that of freshly polished specimen (curve-1). It implies that the
preformed film polarizes the anodic reaction also. This is because the ionic resistivity
of the film increases with increasing thickness of the film. It is also evident
that i<sub>crit</sub>, i<sub>passivation </sub>and E<sub>pp </sub>are higher,
and E<sub>b</sub> values are lower in curve-1 than those exhibited in curve-2.
Relatively lower E<sub>b</sub> and higher i<sub>crit</sub> and i<sub>passivation
</sub>in case of specimen with film is due to the incorporation of Cl� in the
Cu<sub>2</sub>O lattice. The Cl� ions are incorporated in the lattice by
replacing O<sup>2</sup>� in the lattice. If one O<sup>2</sup>� ion is replaced
with one Cl� ion in the Cu<sub>2</sub>O lattice then it will result in loss of
one negative charge from the lattice and to maintain charge neutrality one Cu<sup>+</sup>
ion will pass into solution [3]. This will increase the cation vacancy of the
Cu<sub>2</sub>O lattice and since ionic conduction is via cation vacancy
exchange, it will increase the ionic conductivity [3]. Thomas and Tiller [5] investigated
the anodic polarization behaviour of copper in 0.01 M and 0.1 M NaCl after 1 h
and 24h immersion. They found that the effect of chloride on the cuprous oxide
film on copper is time dependent and that after longer times of interaction
with chloride ions, the protective character of the oxide deteriorates. It also
likely that during 15 days exposure in weight loss test some Cl� ions were
incorporated in the film, but adverse effects of Cl� incorporation were
countered by beneficial effects of film growth. </p>

<h3 align=left><b>4. Conclusion</b></h3>

<SCRIPT>comment(1)</SCRIPT><p>A film of corrosion product builds up on
the 54Cu-11Ni-29Zn-5Mn-1Fe alloy when
exposed to synthetic seawater. The film thickness increases rapidly initially
and subsequently rate of its growth tapers down. The rate of corrosion drops
considerably with exposure duration.� Therefore it can be concluded that film
is passive and protective. The anodic polarization behaviour indicates and
confirms the passivation tendency of the alloy The polarization plots indicate
that the corrosion of this copper alloy in synthetic solution is under cathodic
control. Findings of polarization tests confirm that the film formed on the
specimen exposed to SSW polarizes the cathodic and anodic reactions. Therefore
it can be concluded that corrosion rate of the test alloy decreases with
exposure duration due to ability of film to polarize cathodic as well as anodic
reactions.</p>

