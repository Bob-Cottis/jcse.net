<h2>Deposition of cerium contained conversion film on LC4 alloy with square wave pulse method</h2>

<p>ZHANG Wei, LI Jiuqing, WU Yinshun and ZHANG Pei<br>
<i>Corrosion and Protection Center, School of Material Science &amp; Engineering,
P.O.Box 621, USTB, Beijing, 100083, China. <script>mailto2('zhwresults','sina.com.cn')</script></i></p>

<SCRIPT src="/jsinc/AuthNote.js"></SCRIPT>

<SCRIPT>notify();</SCRIPT><SCRIPT>comment(1)</SCRIPT><h3><b>Abstract</b></h3>

<p> An electrochemical technique named as SWPT2 was determined to
deposit cerium contained conversion film on LC4 aluminum alloy using square
wave pulse in CeCl<sub>3</sub> solution with an oxidant. Energy dispersive
spectroscopy (EDS) and Scanning Electron Microscopy (SEM) were adopted to study
the composition and the morphology of the film. The polarization curves of the
specimens treated with SWPT2 technique measured in 3.5%(wt) NaCl solution
revealed that the film thus formed inhibited both the anodic and the cathodic
process of the corrosion of the specimen. And the immersion test in 3.5%(wt)
NaCl solution also indicated that the corrosion resistance of the SWPT2 treated
specimen was better than that of the untreated and equivalent to or better than
that of the traditionally electrochemically treated specimens.</p>

<SCRIPT>comment(2)</SCRIPT><p><b>Keywords</b>: aluminum alloy, corrosion, rare
earth metal, electrochemical method/technique, square wave pulse</p>

<h3>Introduction</h3>

<SCRIPT>comment(3)</SCRIPT><p>Chromate conversion coatings have been
widely used for corrosion protection of aluminum alloys because of its
convenience to obtain. But the toxicity of chromium compound has led to much
research to find out a replacement to form corrosion resistant coatings on
aluminum alloys. Since the mid of the 1980s Hinton et al found cerium salt
could inhibit the corrosion of aluminum alloys, much work have been carried out
on using cerium salt in the treatment of aluminum alloys [<script>crossref(1)</script>-<script>crossref(13)</script>].
Usually these methods can be divided into three main categories: (1) immersion
in inorganic solution, (2) electrochemical treatment in inorganic solution, (3)
electrochemical treatment in organic solution. The first kind of treatment
usually needs a very long period or complicated steps if good corrosion
resistance is wanted. Electrochemical methods are more time saving. But the
quality of the film obtained is not satisfactory. One of the reasons is thought
to be the negative effect of hydrogen evolution during the precipitation of
cerium hydroxide [<script>crossref(5)</script>]. However, the use of square wave pulse to
deposit cerium contained film on aluminum alloys hasn�t been reported yet.
Since the method using square wave pulse permits hydrogen to be let out during
the interruption of the applied current, the precipitation of cerium on the
surface of the alloy may less be affected. Thus better quality of the film
obtained can be expected. The authors carried out some research on an
electrochemical method using square wave pulse to deposit corrosion resistant
film containing cerium on LC4 alloy. And the corrosion resistance of the film
obtained was studied.</p>

<h3>Experiments</h3>

<SCRIPT>comment(4)</SCRIPT><p>The test material comprised a Al-Zn-Mg-Cu
alloy which was rolled and undergone CS heat treatment, of composition:
Cu:1.4~2.0, Mg:1.8~2.8, Zn:5.0~7.0, Ti,Cr:0.10~0.5, Al: balance. Specimens of
40mm�60mm were cut from the plate and abraded to a 800 grit finish on silicon
carbide paper. The specimens were washed with deionized water after grease
removal with acetone and dried, then put into desiccator for use. </p>

<SCRIPT>comment(5)</SCRIPT><p>Some of the specimens were treated in 1g/L CeCl<sub>3</sub> solution
with square wave pulse at room temperature. After orthogonal experiments, the
parameters were determined as: the voltage was �0.5V, the duty ratio was 3/1
and the frequency was 750Hz. This technique was named as SWPT1 (Square Wave
Pulse Technique 1).</p>

<SCRIPT>comment(6)</SCRIPT><p>The other specimens were treated in CeCl<sub>3 </sub>+KMnO<sub>4</sub>
solution with square wave pulse at room temperature. Also the parameters were
determined after orthogonal experiments: the high and low level was 0V and �1.5V,
respectively. The frequency was chosen 150Hz and the duty ratio was 2/1. The
concentration of CeCl<sub>3 </sub>andKMnO<sub>4</sub> was 10g/L and
2.5g/L, respectively. The pH value of the solution was adjusted to 2.45 with
1mol/L HCl solution. This technique was named as SWPT2 (Square Wave Pulse
Technique 2).&nbsp;</p>

<SCRIPT>comment(7)</SCRIPT><p>In order to make comparison on the
corrosion resistance the previously reported film forming methods was adopted
to deposit film on the LC4 specimens. According to the conventional technique,
at room temperature, the specimens were cathodically electrolyzed in 1g/L CeCl<sub>3</sub>
solution with a constant current for 40 min and 1.5 hours, and the
corresponding current density was -0.5A/m<sup>2</sup> and -2.8 A/m<sup>2</sup>,
respectively. A PS-1 potentiostat was adopted to apply the current and a piece
of Pt sheet of 10 cm<sup>2</sup> worked as the counter electrode.</p>

<SCRIPT>comment(8)</SCRIPT><p>After the treated specimens were washed in
ultrasonic washer with deionized water, the morphology of the specimens
was observed under LEO1450 scanning electron microscope and the
composition of the film was studied with energy dispersive spectroscopy. </p>

<SCRIPT>comment(9)</SCRIPT><p>The polarization curves of the SWPT2 and SWPT1 treated specimen were measured in 3.5% (wt) NaCl solution. PS-168
electrochemical measuring system controlled by a computer was used to obtain
the polarization curves and the scanning rate was 40mV/min. The starting
potential was about 200 mV negative to the free corrosion potential E<sub>corr</sub>.
The exposed area of the specimen was 1cm<sup>2</sup>. A saturated calomel
electrode and a piece of Pt sheet with an area of 10 cm<sup>2</sup> worked as
the reference electrode and counter electrode respectively.</p>

<SCRIPT>comment(10)</SCRIPT><p>In
addition, the polarization resistances of
differently treated specimens were measured with a three-electrode system. The
system was composed of three LC4 specimens treated with same technique. The
electrolyte was 3.5% (wt) NaCl solution. And the exposed area of each specimen
was 1 cm<sup>2</sup>. Before starting measuring the resistance, the system was
firstly kept untouched for 1 hour in order to reach a stable condition. Then a
linear polarization within E<sub>corr</sub>-20mV~E<sub>corr</sub>+20mV was
carried out in the electrolyte. The scanning rate was 10mV/min.</p>

<SCRIPT>comment(11)</SCRIPT><p>Finally, the corrosion resistances of
differently treated specimens were examined with immersion test in 3.5%(wt)
NaCl solution.</p>

<h3 align=left>Results and Discussion</h3>

<SCRIPT>comment(12)</SCRIPT><p>The film formed with SWPT1 technique was
light grey dotted with little dark grey spots. The film was smooth and even
observed with naked eyes. The SEM photographs of the film were shown in Fig.1.
According to area EDS analysis, there were three kinds of elements existing in
the film: Al (80.6% (wt)), Zn (16.36%(wt)) and Cu (3.04). However, cerium was
not detected by EDS in the film.</p>

<SCRIPT>comment(13)</SCRIPT><p>The film obtained with SWPT2 technique was
gold yellow and even observed with naked eyes. Its morphology was shown in
Fig.2 and its composition was listed out in Table1. According to Fig.2, it was
found that the film evenly covered the surface of the specimen. Table1
revealed that area analysis detected the existence of cerium in the film. From
Fig.2, it could be seen there was some white floc scattering on the surface of
the specimen. After analysis of the floc with EDS, it was found some cerium
existed in it. And also some cerium existed in the particles as marked in
Fig.2. The composition of the particle was shown in Table2. So it can be seen
that cerium deposited with Mn and alloy elements such as Cu and Zn when SWPT2 technique was adopted. </p>

<SCRIPT>comment(14)</SCRIPT><p>The morphology of the film formed with the
conventionally electrolyzing method mentioned above was shown in Fig.3 (a) and
(b). From Fig.3 it could be seen that the film was not so even as that shown in
Fig.2. </p>

<SCRIPT>comment(15)</SCRIPT><p>The polarization curves of SWPT1 and SWPT2 treated specimens measured in 3.5% NaCl solution were shown in Fig.4. It was
found that the corrosion current density of SWPT2 treated specimen was a bit
smaller than that of the naked specimen in the anodic branch. While the anodic
branch of the polarization curve of the specimen treated with SWPT1 technique was
at the right side of that of the naked specimen. But both the cathodic branches
of the polarization curves of the specimens treated with SWPT2 and SWPT1 technique
were at the left side of the naked speicmen�s cathodic branch. The free
corrosion potentials of these two treated specimen were almost 100mV(SCE)
positive to that of the naked specimen. Moreover, the free corrosion potential
of SWPT2 treated specimen was about 40mV positive than that of SWPT1 treated
specimen, which indicated that SWPT2 treated specimen was more stable than SWPT1
treated specimen in chloride contained solution.</p>

<SCRIPT>comment(16)</SCRIPT><p>The polarization resistances of differently
treated specimen measured in 3.5% NaCl solution were listed out in Table3. It
was found that the polarization resistance of SWPT1 and SWPT2 treated specimens
were higher than that of the naked specimen. But the polarization resistance of SWPT2
treated specimen was higher than that of SWPT1 treated specimen and almost 10
times of that of the naked specimen. So better corrosion resistance of SWPT2 treated specimen could be expected in chloride contained solutions compared
with SWPT1 treated specimen. Thus specimens treated with SWPT2 technique were
immersed in 3.5% NaCl solution for examination of its resistance to uniform
corrosion and pitting. Along with SWPT2 treated specimens, the specimens treated
with conventional method mentioned above also underwent the immersion test in
3.5%NaCl solutions. The results were shown in Table4. It could be seen that the
corrosion resistance of SWPT2 treated specimen was much better than that of the
specimen cathodically electrolyzed with small current density for short time
and equivalent to that of the specimen cathodically electrolyzed with greater
current density for longer time.</p>

<SCRIPT>comment(17)</SCRIPT><p>It was interesting to find out that without
oxidant of KMnO<sub>4</sub> in the CeCl<sub>3</sub> solution, cerium couldn�t
deposit on the specimen using square wave pulse. According to previous study <sup>[5]</sup>.
The reason might be that besides O<sub>2</sub> and H<sup>+</sup> could be
reduced at the cathodic sites, MnO<sub>4</sub><sup>-</sup> would also be
reduced to MnO<sub>2</sub> at the cathodic sites if KMnO<sub>4</sub> was added
into the solution (as shown in equation 1), which would lead to the increase of
local pH value. That was good for cerium to deposit in the form of cerium
hydroxide. This was proved by the EDS results showing that cerium precipitated
where Mn appeared in high amount. On the other hand, the interruption between
pulses reduces the effects of hydrogen evolution to some extent, which could
improve the structure quality of the film. This was indicated by the SEM
photographs shown in Fig.2 and Fig.3.</p>

<p align=center>2
MnO<sub>4</sub><sup>- </sup>+ 4 H<sub>2</sub>O + 6 e = 2 MnO<sub>2 </sub>+ 8 OH<sup>-</sup>(equation 1)</p>

<SCRIPT>comment(18)</SCRIPT><p>While electrolyzed in cerium salt solution
without oxidant with square wave pulse, the specimen was at a negative
potential for a very short time, which was too short for cerium to precipitate.
That was the reason why cerium was not detected in the film obtained with SWPT1 technique. So it could be reasoned that the oxidant not only helped cerium to
precipitate at the cathodic sites, but also increase the precipitation speed of cerium.</p>

<h3>Conclusions</h3>

<ul>
  <SCRIPT>comment(19)</SCRIPT><li>Two techniques named as SWPT1 and SWPT2 using square wave pulse were adopted to deposit film on the surface of LC4 alloy. Both
films were mainly composed of the alloy elements. While the film formed with SWPT2
    technique contained cerium.</li>
  <SCRIPT>comment(20)</SCRIPT><li>The corrosion resistances of LC4 alloy
treated with these two kinds of techniques were improved in chloride contained
solution. Results showed that SWPT2 technique could obtain more corrosion
resistant film than SWPT1 technique. The film formed with SWPT2 technique inhibited
both the anodic and cathodic progress of corrosion, while the film deposited
with SWPT1 technique only inhibit the cathodic process.</li>
  <SCRIPT>comment(21)</SCRIPT><li>Compared with the conventional
electrochemical treatment, SWPT2 technique could obtain the film having the
equivalent corrosion resistance or better corrosion resistance.</li>
  <SCRIPT>comment(22)</SCRIPT><li>Cerium could precipitate faster in 1g/L
CeCl<sub>3</sub> solution with the oxidant of KMnO<sub>4</sub> than in the
solution without the oxidant.</li>
</ul>
