<h3><b>Introduction
</b></h3>

  <SCRIPT>comment(2)</SCRIPT><p>The susceptibility of iron
against corrosion makes it necessary to be used in the form of stainless steel. The corrosion
resistance of steels is provided by a very thin oxide layer (passive film)
formed upon them. Although this layer readily dissociates in various media, it
quickly renews itself. The addition of elements such as Cr, Ni and Mo increases the corrosion resistance
of steels to a great extent. </p>

<SCRIPT>comment(3)</SCRIPT><p>Tomashov et al [<script>crossref(1)</script>]
observed that the elements such as Mo, Si, V and Re prevented the formation of
pitting corrosion in steels. Lizlovs and Bond [<script>crossref(2)</script>] proved that the
addition of Mo into high purity
stainless steel decreased their critical current densities. Rockel [<script>crossref(3)</script>] on
the other hand examined the effect of addition of Mo to Fe18-Cr steel and
reported that the Mo had a
significant effect in the anodic dissolution region but the effect of Mo in the
transpassivation region was not so big. Sugimoto and Sawada [<script>crossref(4)</script>] investigated the effect of
alloying elements in steels. They reported that there was no pitting corrosion
after the addition of Mo till the transpassive dissolution started and the
corrosion potential shifted to more anodic direction. The Mo content of the
passive film showed a linear increase with the Mo content of the steel. Lu et
al. [<script>crossref(5)</script>] investigated the behaviour of steel in a medium containing MoO<sub>4</sub><sup>2-</sup>
and found that Mo present in the passive layer was both in 6+ (as [MoO(OH)<sub>2</sub>]<sup>2-</sup>
and MoO<sub>4</sub><sup>2-</sup>) and 4+ (as MoO<sub>2</sub>) oxidation states. It was proposed that MoO<sub>4</sub><sup>2-</sup> increased the corrosion
resistance of the passive film by two-pole action or rectification[<script>crossref(5)</script>]. </p>

<SCRIPT>comment(4)</SCRIPT><p>It was observed that the
formation of pitting is largely prevented in solutions containing 50 �100 mM
