<SCRIPT>comment(22)</SCRIPT><p>From Table 3 and Fig.3, when concentration of molybdate is 100 mg/L, the 
inhibitive effect is equivalent with molybdate or BTA used alone. But when 
concentration of molybdate is 200 mg/L, we can find that there is obvious change 
in inhibitive efficiency. Especially in the test that concentration ratio of 
molybdate and BTA 2:1, the inhibitive efficiency reaches 81.7%. There is 
obvious synergistic effect between molybdate and BTA, which indicates that it 
is beneficial for molybdate to use with BTA.</p>

