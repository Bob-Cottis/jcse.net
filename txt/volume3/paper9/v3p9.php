<SCRIPT>comment(25)</SCRIPT><h4>Impedance Test of compatibility 
test</h4>

<p>Fig. 4 gives Bode phase spectrograph of the three tests for which the
concentration of molybdate is 200mg/L. When concentration of BTA is 100mg/L,
the phase angle peak reaches 63.0deg or so. And the corresponding frequency of
phase angle peak is in the range 10~100Hz, which is higher than test of
molybdate used as only inhibitor in solution. Therefore, impedance test can
prove that there is obvious synergistic effect between molybdate and BTA.</p>

<SCRIPT>comment(26)</SCRIPT><h3>Conclusions</h3>

<ol>
<SCRIPT>comment(27)</SCRIPT>  <li>Molybdate can be 
inhibitive to copper material in deionized water; molybdate used with BTA can 
play superior role in inhibitive property to BTA used lonely as copper 
inhibitor;</li>
<SCRIPT>comment(28)</SCRIPT>  <li>The possible 
mechanism lies in that molybdate attends forming passivation material, which is 
steady and anti-corrosion. So, molybdate is useful and potential for industry 
usage.</li>
</ol>

