<SCRIPT>notify();</SCRIPT><SCRIPT>comment(1)</SCRIPT><h3>Abstract</h3>
<p>Inhibition effect of molybdate inhibitors in tap water has been studied
in this article, according to electrochemistry test. It is pointed out that
compatibility with molybdate can playcomparable
effect as benzotriazole. </p>

<SCRIPT>comment(2)</SCRIPT><p><b>Key Words:</b> Electrochemistry, Molybdate, benzotriazole (BTA)</p>

<SCRIPT>comment(3)</SCRIPT><h3>Introduction</h3>

<p>BTA was ever popular to copper material in industry application. Now, it
is seen that molybdate has gradually come into taking place of BTA, for BTA is
difficult to dissolve[<script>crossref(1)</script>].</p>

<SCRIPT>comment(4)</SCRIPT><p>At present, it is very necessary to study molybdate as copper
inhibitor[<script>crossref(2)</script>]. This article mainly discussed inhibitive effect of molybdate and
made comparison between molybdate and traditional copper inhibitor (BTA) by
electrochemistry polarization and impedance test. </p>

<SCRIPT>comment(5)</SCRIPT><h3>Experimental</h3>

<p>All tests were carried out with PARC M283 potentiostat, 5210 type locked
amplifier made in EG&amp;G USA. The volume of electrochemistry test cell is 1L,
which is in accordance with ASTM standard. Electrode system are composed of
three parts---saturated calomel electrode as reference, carbon stick as aided
electrode (two stick placed symmetrically in order to diffuse electric current
evenly), copper as work electrode (area is 0.785 cm<sup>2</sup>).</p>

<SCRIPT>comment(6)</SCRIPT><p>The measurements set the potential scanning rate at 1mV/sec for    
potentiodynamic tests. AC impedance measurements were carried out using a 5mV    
peak-to-peak sinusoidal modulation from 10mHz to 100KHz. A two-channel    
dynamic-signal analyser was used to obtain the impedance. All experiment were    
performed at room temperature (25�C) in deionized water (DD&lt;0.2&micro;s/cm), and    
immersion time in tap water before test is all set 2h for getting steady open    
circuit potential - E<sub>oc</sub>, which is useful and referenced in    
comparison of electrochemistry behaviour in measurement. </p>    

<SCRIPT>comment(7)</SCRIPT><h3>Results and Discussion</h3> 

<p>In order to express clearly in figure and table, we use M, B to represent
molybdate and BTA respectively. </p>

<SCRIPT>comment(8)</SCRIPT><h4>Polarization test of Molybdate</h4> 

<SCRIPT>comment(9)</SCRIPT><p>Molybdate is an anodic inhibitor, and its oxidation is inferior to
chromate. Passivation may occur quickly if oxygen is enough, and a high
dosage of molybdate is needed if insufficient oxygen is available. Therefore, compatibility with chemicals
is the best way to develop efficient inhibitors.</p>

