<h3>4.
Conclusion</h3>

<SCRIPT>comment(26)</SCRIPT><p>(1) The
structure of RE-Ni-W-B-SiC composite coating is affected by boron content and
heat treatment temperature. The coating as-deposited is in a thermodynamical
nonequilibrium state, and its structure is oversaturated solid solution when
boron content is low. With a rise in boron content the coating structure
transits gradually to amorphous state, and changes to equilibrium crystalline
state after heat treatment. At about 400&deg;C boride phase is precipitated.</p>

<SCRIPT>comment(27)</SCRIPT><p>(2) The
hardness of as-deposited RE-Ni-W-B-SiC coating is 600~800Hv, and it can reach
1400~1650 Hv after heat treatment (400&deg;C x 1h). The high hardness is attributed
to the combination strengthening of solid solution hardening and dispersion
hardening.</p>

<SCRIPT>comment(28)</SCRIPT><p>(3) The
corrosion resistance of RE-Ni-W-B-SiC coating is lower than that of Ni-W-P-SiC
composite coating, but higher than that of 1Cr18Ni9Ti stainless steel.</p>

