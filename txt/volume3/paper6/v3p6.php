<SCRIPT>notify();</SCRIPT><SCRIPT>comment(1)</SCRIPT><h3>Abstract</h3>

<p>A composite coating, containing (wt%) 1.35RE, 5.46W, 4.82B, 15SiC and Ni can be
electrodeposited on the matrix by use of appropriate bath composition and
process parameters. The addition of RE can enhance the hardness and wear
resistance of the coating remarkably. The hardness and wear resistance of the
coating increase with a rise in heat treatment temperature, and the hardness
reaches the optimum value at 400&deg;C, while the coating has the best wear
resistance at 500&deg;C. The wearing tests show that the wear resistance of the
coating is 4~5 times as high as that of hard chromium, and the corrosion tests
indicate that the corrosion resistance of the coating is superior to that of
1Cr18Ni9Ti stainless steel in various corrosive media, except for nitric acid.</p>

<SCRIPT>comment(2)</SCRIPT><p><b>Keywords: electrodeposition, composite coating, wear resistance, corrosion
resistance.</b></p>

<h3>1. Introduction</h3>

<SCRIPT>comment(3)</SCRIPT><p>Composite
materials are important new materials used today. Several materials with
different properties can be combined to form a new type of composite materials
to meet various requirements. Composite electrodeposition is a new
surface-strengthening process by which some inorganic or organic particles can
codeposit with metal or alloy to form composite coatings with many special
properties, such as wear resistance, high temperature oxidation resistance, corrosion
resistance and electrical contact function[1~6].</p>

<SCRIPT>comment(4)</SCRIPT><p>The
existing electrodeposited composite coatings are mostly based on nickel or
copper, e.g. Ni-SiC or Cu-SiC coatings. The wear resistance of these coatings is
higher than that of pure nickel or copper[7],but their corrosion resistance is
relatively poor. Another kind of composite coatings is obtained by electroless
plating ,e.g. Ni-P-SiC or Ni-B-SiC coatings[8~9].The hardness of these coatings
can be enhanced to 1100~1200Hv,and their wear resistance improved after
appropriate heat treatment, but the coating thickness is usually only 50~70um
and their wear resistance also poor. After many tests, a new composite coating
with higher hardness and better wear resistance is developed. The hardness of
this electrodeposited composite coating is 600~900Hv as-deposited, and can be
increased to 1400~1700Hv after heat treatment (400&deg;C x 1h).This process has
been applied to treat the parts of cigarette and textile machines.</p>

