<h3>3.
Results and discussion</h3>

<h4>3.1
Effect of Heat Treatment Temperature on Coating Hardness and Wear-resistance</h4>

<SCRIPT>comment(11)</SCRIPT><p>The
relationship between the hardness of composite coating and the heat treatment
temperature is shown in Fig.1 . The coating hardness increases with a rise of
temperature and reaches the peak value at 400&deg;C . After that, the hardness
decreases as temperature increases. This is because that when the coating being
heated, the coating structure changes, and becomes oversaturated with respect to
nickel solid solution and the distortion in the crystal lattice gets serious so
that the coating hardness increases. Since a large number of Ni3B particles
appear and precipitation hardening occurs, the coating hardness reaches peak
value. With continuously increasing heat treated temperature, crystalline particles of nickel solid solution grow up and Ni3B particles� aggregate and coarsen and the composite
coating softens. It can be seen that the additions of RE increase the coating
hardness because RE can facilitate the codeposition of SiC particles and Ni-W-B
alloy and enhance the content of SiC particles in the coating. Furthermore,
Fig.1
also indicates that the coating microhardness increases with a rise in boron
content, the hardness of the plated coating is 650~850Hv and that after heat
treatment can reach 1400~1650Hv.</p>

<SCRIPT>comment(12)</SCRIPT><p>As
shown in Fig.2, the abrasion rate of the composite coating without heat
treatment is the highest and decreases linearly with a rise in heat treatment
temperature until it reaches the lowest value at 500&deg;C. Although the coating
hardness is the highest at 400&deg;C , but at this temperature, the abrasion rate
is relatively large because the coating contains a lot of hydrogen, which makes
the coating internal stress high, and more brittle so that some cracks form in
the coating, and the coating drop out partially under the alternate stress
during abrasion test. When heated, the coating liberates the hydrogen, and the
stress and brittleness of the coating decrease. At 500&deg;C ,the coating hardness
is relatively high and the brittleness lower, so the wear resistance is the
best. At higher temperature, the crystal grains of nickel solid solution grow up
and Ni3B particles coarsen, so that the coating softens and its abrasion rate
increases. Moreover, Fig.2 shows that the coating wear resistance increases with
a rise in B content, and the additions of RE improve the wear resistance
remarkably.</p>

