that of FeO concentration of melts. The moving rate of slag film and reaction
between the melts and refractories in ingredients of molten slag film will be
speeded up when the temperature is increased. On the other hand, increasing
molten bath temperature would speed up the mass diffusion in slag film, molten
melts and deteriorate layer of refractory. The reduction rate of FeO in melts
would also be increased and the stirring of the molten bath by the heat motion
and the gaseous CO produced in the reduction reaction would be fierce as the
temperature increasing.</p>

