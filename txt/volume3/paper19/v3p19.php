<h4>Influence of the Iron Oxides on Corrosion Rate</h4>

<SCRIPT>comment(1)</SCRIPT><p>The corrosion behavior and the
mechanism for several kinds refractory in the melts of smelting reduction
technology have been investigated in detail. FeO concentration and activity are
the important factors that affect the corrosion behavior of refractories
[<script>crossref(2)</script>,<script>crossref(4)</script>,<script>crossref(6)</script>,<script>crossref(9)</script>]. A comparison test of study the relationship between FeO
concentration and corrosion rate is shown in figure 1. </p>

<p align=center><img border=0 width=680 height=524
src="v3p19_files/image004.gif"></p>

<SCRIPT>comment(1)</SCRIPT><p align="center"><b>Figure 1�� Test
results of the influence of FeO content in melts on the corrosion rate of the
refractories, test temperature is 1773k at the rotary immersion</b></p>

<SCRIPT>comment(1)</SCRIPT><p>The corrosion rate of refractory
is increased almost as a linear relationship with the FeO content in the
smelting reduction melts. The corrosion resistance of the refractories is
evident decreased as the FeO of the melts increasing. As the concentration
increasing in the melts, the oxidization rate of carbon graphite contained in
the refractory is increased. The graphite carbon contained in refractory will be
as a reduction agent and reduce FeO of the melts and produce metal Fe and
gaseous CO. The pores and cracks will be formed in reaction layer of
refractory. This effect will improve the kinetics of the corrosion process and
the rate of melts penetration refractory. The corrosion resistance of AZ
refractory is higher than that of AC refractory because ZrO<sub>2</sub>
exhibits a good anti-oxidization behavior. </p>

