<SCRIPT>notify();</SCRIPT><SCRIPT>comment(1)</SCRIPT><h3>Abstract</h3>

<p>The corrosion resistance and microstructure
of Al<sub>2</sub>O<sub>3</sub>-C-ZrO<sub>2</sub> refractories in smelting
reduction melts were studied and evaluated by the quasi-station immersion and
rotary immersion test. The corrosion rate of the Al<sub>2</sub>O<sub>3</sub>-C-
ZrO<sub>2</sub> refractories is decreased with the addition of the graphite
carbon and ZrO<sub>2</sub>. The test results showed that the ZrO<sub>2 </sub>containing
bricks had much better corrosion resistance than the ZrO<sub>2</sub>-free
bricks. The ZrO<sub>2 </sub>addition improved the oxidization resistance of the
graphite carbon of the refractory and decreased the interaction rate between
the melts and the refractory. The corrosion mechanism of Al<sub>2</sub>O<sub>3</sub>-C-ZrO<sub>2</sub>
refractories is the interaction between melts and refractory and the
dissolution of the refractory constituents into the melts. </p>

<p><b>Key Words</b>: Al<sub>2</sub>O<sub>3</sub>-C-
ZrO<sub>2</sub> refractory, Smelting reduction, Corrosion
mechanism, Microstructure</p>

<h3>Introduction</h3>

<SCRIPT>comment(1)</SCRIPT><p>The corrosion resistance of the
refractories to the smelting reduction melts is one of the imperative problems
for the development of smelting reduction [<script>crossref(1)</script>-<script>crossref(3)</script>]. The composition and
oxidization behavior of the smelting reduction melts with iron bath are much
more complicated than that of the blast furnace technology [<script>crossref(4)</script>-<script>crossref(7)</script>]. The
properties of the melts are changed dynamically and constantly in the melting
process. The chemical properties of the melts depend greatly on the activity of
FeO and the reduction rate of iron oxides in the molten bath [<script>crossref(8)</script>-<script>crossref(9)</script>]. Due to the
high concentration FeO slag and high reduction rate of iron oxides in the
melts, the corrosion behavior and corrosion mechanism of the refractories in
smelting reduction melts are complicated in development of the smelting
reduction technology. Al<sub>2</sub>O<sub>3</sub>-C-ZrO<sub>2 </sub>refractories
have been widely used in blast furnace, rotary furnace, electric-resistance
furnace[<script>crossref(10)</script>-<script>crossref(14)</script>]. It is imperative to investigate the corrosion mechanism of
different kind refractories in the melts of smelting reduction with iron bath,
control the corrosion reaction and decrease the corrosion rate of refractories
efficiently for the development of smelting reduction technology. This
investigation focused on studying the physical properties, corrosion behavior,
corrosion rate, and examining the microstructure change for the Al<sub>2</sub>O<sub>3</sub>-C
-ZrO<sub>2</sub> based refractories.</p>

