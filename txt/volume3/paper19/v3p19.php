<h3>Conclusion</h3>

<SCRIPT>comment(1)</SCRIPT><p>The corrosion rate of the Al<sub>2</sub>O<sub>3</sub>-C-ZrO<sub>2</sub>
refractories in the melts of smelting reduction with iron bath is decreased
with the addition of graphite carbon and ZrO<sub>2</sub> in the refractories.
The principal constituents of the Al<sub>2</sub>O<sub>3</sub>-C refractory are
composed of Corundum, Mullite, a-Al<sub>2</sub>O<sub>3, </sub>andgraphite.
During the corrosion test, the interaction between melts and the refractory was
occurred and the new compounds of FeO.SiO<sub>2,</sub> FeO.3CaO, 2CaO.SiO<sub>2</sub>
and CaO.SiO<sub>2</sub> were formed in the deteriorate layer. The graphite
carbon has been oxidized over 90% and porosity is increased greatly in the
deteriorate layer of refractory. </p>

