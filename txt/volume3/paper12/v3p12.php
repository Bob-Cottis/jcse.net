<h2><span lang=EN-AU>Underneath Pitting Corrosion in Bottom
of Crude Storage Tanks and Its Mitigation</span></h2>

<p class=MsoNormal><span lang=EN-AU style='font-size:9.0pt;'>&nbsp;</span></p>

<p class=MsoNormal><b><span lang=EN-AU
style='font-size:8.0pt;font-family:Arial;'>S.S.Gurjar , </span></b></p>

<p class=MsoNormal><b><span lang=EN-AU
style='font-size:8.0pt;font-family:Arial;'>Pipelines Division, Indian Oil Corporation Limited ,</span></b></p>

<p class=MsoNormal><b><span lang=EN-AU
style='font-size:8.0pt;font-family:Arial;'>Vadinar, Gujrat state , India.������������� </span></b></p>

<p class=MsoNormal style='text-align:justify'><b><span lang=EN-AU style='font-size:12.0pt;'>&nbsp;</span></b></p>

<p class=MsoNormal style='text-align:justify'><b><span lang=EN-AU style='font-size:12.0pt;'>Abstract</span></b></p>

<p class=MsoNormal style='text-align:justify'><span lang=EN-AU
style='font-size:12.0pt;'>Underneath pitting corrosion
is localised corrosion in bottom plates from soil side, this is most critical
corrosion problem in tanks and it leads to bottom failure. Corrosion rate
depends on the soil characteristics, moisture content in the padding and extent
of water ingress between padding &amp; bottom plates. Cause of pitting
corrosion and embedded stone particle assisted crevice corrosion based on field
&amp; laboratory inspections and alternatives to mitigate the underneath
corrosion effectively in future, are included in this paper broadly.</span></p>

<p class=MsoNormal style='text-align:justify'><span lang=EN-AU
style='font-size:12.0pt;'>&nbsp;</span></p>

<p class=MsoNormal style='text-align:justify'><b><span lang=EN-AU style='font-size:13.0pt;'>INTRODUCTION:</span></b></p>

<p class=MsoNormal style='text-align:justify;'><span
lang=EN-AU style='font-size:12.0pt;'>&nbsp;</span></p>

<p class=MsoNormal style='text-align:justify'><span lang=EN-AU
style='font-size:11.0pt;'>The pitting corrosion in the
tank bottom mild steel plates is commonly observed. During the internal
inspection of tank bottom, it is very difficult to ascertain the condition of
plates toward the soil side. To evaluate the extent of underneath corrosion,
coupon plate method or ultrasonic inspection by making grids and using
ultrasonic thickness meter or scanning the bottom plates by using ultrasonic
floor scanner is employed. The latter method is very precise, reliable and less
time consuming. The pitting corrosion is extremely localised in nature in which
penetration into the metal occurs either from chemical or galvanic action in
the form of cavities and pits over the surface of metal. In pitting corrosion,
attack penetrates at much faster rate in a localised area as compare to general
corrosion. This leads to development of holes and failure of tank bottom.</span></p>

<p class=MsoNormal style='text-align:justify'><span lang=EN-AU
style='font-size:11.0pt;'>�</span></p>

<p class=MsoNormal style='text-align:justify'><span lang=EN-AU
style='font-size:13.0pt;'>Cause of Underneath Pitting
Corrosion:</span></p>

<p class=MsoNormal style='text-align:justify'><b><span lang=EN-AU style='font-size:13.0pt;'>&nbsp;</span></b></p>

<p class=MsoNormal style='text-align:justify'><span lang=EN-AU
style='font-size:11.0pt;'>The degree and mechanism of
corrosion in tank bottom depends on the chemical composition and corrosive
nature of petroleum product stored. Crude oil and different petroleum products
taken in huge storage tanks are normally stored over bottom water (sea water or
brine) layer due to some operational constraints. Sea water / brine which comes
along with petroleum products during unloading of tankers into storage tanks,
this brine can not be drained totally from the storage tanks because during
water draining through the water draw off nozzles, petroleum products comes
along with the brine. This layer of brine present in the storage tanks for the
long time attacks the bottom plates from the product side. The brine travels to
metal substrate through the cracks / pinholes/ holidays present in the existing
bottom coating. This type of brine attacks results in the highly aggressive localized
corrosion and after few years this, bottom plates may punctured locally. If
brine contains dissolved acidic impurities like H<sub>2</sub>S, CO<sub>2 </sub>etc.
then the corrosion attacks will be more over the substrate.� Though these punctures/ holes, brine goes
under the bottom plates and it attacks the plates from the soil side severely.
In the most of the storage tanks, it is practically observed that corrosion
from the underside is also localized because sea water can only�� stay in the bulged portion of bottom plates
&amp; it can not travels through the intact portion of bottom plate with the
bottom padding.</span></p>

<p class=MsoNormal style='text-align:justify'><span lang=EN-AU
style='font-size:11.0pt;'>&nbsp;</span></p>

<p class=MsoNormal style='text-align:justify'><span lang=EN-AU
style='font-size:11.0pt;'>�Most of the huge storage tank owner in the world is commonly used
the concave type of bottom design which are slopping from centre to
circumference / shell. In this type of design, sea water/ brine layer height
increases towards the circumference. Hence, most of the corrosion attacks are
envisaged in the portion from middle section in-between of sketch plates.
During the construction/ repair of the bottom, the rain water/ moisture may got
ingressed in between the bottom bituminous carpet and bottom plates. Normally,
hand tool cleaning followed by painting with one coat of high built bituminuous
paint is employed for the painting of bottom plates from the soil side. This
painting system is not enough corrosion resistant to brine and during the
corrosion attack of ingressed���
water�� under bottom plates, this
paint coating is unable to resist and hence localized corrosion takes place.</span></p>

<p class=MsoNormal style='text-align:justify'><span lang=EN-AU
style='font-size:11.0pt;'>&nbsp;</span></p>

<p class=MsoNormal style='text-align:justify'><span lang=EN-AU
style='font-size:11.0pt;'>Differential cell formation
plays a major role in pitting corrosion, when one portion of the surface is
exposed to an aerated liquid in respect to another portion which is in contact
with less aerated liquid exposure. Latter portion will corrode &amp; it will be
act as anode with respect to former because of formation of concentration cell.
</span></p>

<p class=MsoNormal style='text-align:justify'><span lang=EN-AU>&nbsp;</span></p>

<p class=MsoNormal style='text-align:justify'><span lang=EN-AU
style='font-size:11.0pt;'>During field testing of
retrieved bottom plates of 22 years old crude storage tank� at Vadinar tank farm, similar case of
corrosion is observed . The mild steel plates are generally laid over the black
bituminous carpet consisting a homogeneous mixture of black bitumen, fine river
sand and stone aggregates. The mild steel plates are painted with high build
bituminous paint towards the soil side. At the time of construction, plates are
laid over hot bituminous carpet to reduce the completion period of project. The
stone particles got embedded in the bituminous paint coating of mild steel
plates during laying, pressing, fitting and welding itself as shown in the
figure_1. The rain water got ingressed in under the bottom plates during
construction or� </span></p>

<p class=MsoNormal style='text-align:justify'><span lang=EN-AU
style='font-size:11.0pt;'>&nbsp;</span></p>

<p class=MsoNormal style='text-align:justify'><span lang=EN-AU
style='font-size:11.0pt;'>The corrosion due to saline
water will be more aggressive if the metal substrate has embedded sulphur
inclusions as manganese sulphide. Manganese sulphide is more active to
chlorides present in the saline water as compare to mild steel. The pit
interior of� retrieved bottom plates
from� a 22 years old crude storage tank
were examined using Energy Dispersive Analysis of X-ray (EDAX) attachment� to Scanning Electron Microscope. </span></p>

