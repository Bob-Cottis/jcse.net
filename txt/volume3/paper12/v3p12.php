<p class=MsoNormal style='text-align:justify'><span lang=EN-AU
style='font-size:12.0pt;'>Methods to Mitigate
Underneath Pitting Corrosion:</span></p>

<p class=MsoNormal style='text-align:justify'><b><span lang=EN-AU style='font-size:11.0pt;'>&nbsp;</span></b></p>

<p class=MsoNormal style='text-align:justify'><b><span lang=EN-AU style='font-size:11.0pt;'>i)
Avoid ingress of water:</span></b></p>

<p class=MsoNormal style='margin-left:9.0pt;text-align:justify;text-indent:
-9.0pt'><b><span lang=EN-AU
style='font-size:11.0pt;'>�� </span></b><span lang=EN-AU style='font-size:11.0pt;'>Care should be taken to avoid rainwater ingress under tank bottom
plates during the construction or repair of bottom. At the time of bottom
replacement, site activities should be carried out in such a way that ingress
of water can be avoided. </span></p>

<p class=MsoNormal style='margin-left:9.0pt;text-align:justify;text-indent:
-9.0pt'><b><span lang=EN-AU
style='font-size:11.0pt;'>&nbsp;</span></b></p>

<p class=MsoNormal style='margin-left:9.0pt;text-align:justify;text-indent:
-9.0pt'><b><span lang=EN-AU
style='font-size:11.0pt;'>ii) Improved Bituminous
Carpet:</span></b></p>

<p class=MsoNormal style='margin-left:9.0pt;text-align:justify;text-indent:
-9.0pt'><span lang=EN-AU style='font-size:11.0pt;'>�� To reduce the formation of differential cell
due to embedded stone particles, it is important to note that upper layer of
bituminous carpet should be mixture of fine river sand &amp; black bitumen. The
plates should be laid after proper drying of bituminous carpet. The amount of
bitumen in the mixture of upper layer should be more than the parent carpet. </span></p>

<p class=MsoNormal style='text-align:justify'><b><span lang=EN-AU style='font-size:12.0pt;'>&nbsp;</span></b></p>

<p class=MsoNormal style='text-align:justify'><b><span lang=EN-AU style='font-size:12.0pt;'>iii)
Improved Bottom Underside Paint System:</span></b></p>

<p class=MsoNormal style='margin-left:9.0pt;text-align:justify;text-indent:
-9.0pt'><span lang=EN-AU style='font-size:12.0pt;'>�� </span><span lang=EN-AU style='font-size:
11.0pt;'>To reduce pitting due to soil corrosivity, it
is strongly recommended to use the better surface finish prior to painting,
(e.g. sand blasting SA 2<sup>1</sup>/<sub>2</sub>), followed by application
high performance corrosion resistant paint, the selection of paint system may
vary &amp; dependent on the value of soil corrosivity.</span></p>

<p class=MsoNormal style='margin-left:9.0pt;text-align:justify;text-indent:
-9.0pt'><span lang=EN-AU style='font-size:11.0pt;'>&nbsp;</span></p>

<p class=MsoNormal style='margin-left:9.0pt;text-align:justify;text-indent:
-9.0pt'><b><span lang=EN-AU
style='font-size:12.0pt;'>iv) Double Bottom</span></b><b><span lang=EN-AU style='font-size:12.0pt;'>Design:</span></b></p>

<p class=MsoNormal style='margin-left:9.0pt;text-align:justify;text-indent:
-9.0pt'><b><span lang=EN-AU
style='font-size:12.0pt;'>&nbsp;</span></b></p>

<p class=MsoNormal style='margin-left:9.0pt;text-align:justify;text-indent:
-9.0pt'><span lang=EN-AU style='font-size:11.0pt;'>�� Whole tank replacement (if required based on
internal inspection), then it should be better to opt the double bottom design.
In this, new tank bottom is laid over the old tank. Old tank bottom will act as
a shield against in future service.</span></p>

<p class=MsoNormal style='margin-left:9.0pt;text-align:justify;text-indent:
-9.0pt'><span lang=EN-AU style='font-size:11.0pt;'>&nbsp;</span></p>

<p class=MsoNormal style='margin-left:9.0pt;text-align:justify;text-indent:
-9.0pt'><b><span lang=EN-AU
style='font-size:12.0pt;'>v) Bottom Cathodic
Protection:</span></b></p>

<p class=MsoNormal style='margin-left:9.0pt;text-align:justify;text-indent:
-9.0pt'><span lang=EN-AU style='font-size:11.0pt;'>�� Using suitably designed cathodic protection
system; underside pitting corrosion can be eliminated. The CP system can be
installed at the time of construction or during the major repair of tank
bottom. </span><b><span lang=EN-AU
