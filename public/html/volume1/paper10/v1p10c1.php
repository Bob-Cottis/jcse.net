<blockquote>
  <h2>Volume 1, Paper 10</h2>
  <i>Submitted 20 June 2000</i>
  <hr>
  <h2>Comments on 'The Preliminary Study for the Interpretation of
  Electrochemical Noise with Continuous Wavelet Transform' by Ding Hong-Bo et
  al.</h2>
  <p>Yong-Jun Tan<br>
  <i>School of Materials Engineering Nanyang Technological University, Singapore</i></p>
  <p>The use of noise signatures as a means of detecting localized corrosion has
  evoked much controversy during recent years. Attempts to use the Continuous
  Wavelet Transform (CWT) as an alternative method of analyzing electrochemical
  noise (ECN) is certainly commendable and worthwhile. However, I feel that some
  clarifications are needed in order to improve this paper.</p>
  <p>1. The authors concluded that 'The CWT is a promising method for the data
  analysis of ECN signal.' since 'we can draw information from the
  non-stationary signal because of its time-frequency trade-offs.' They also
  commented that Wavelets 'have advantages over traditional Fourier methods in
  analyzing physical situations where the signal contains discontinuities and
  sharp spikes.' However, the paper provided little evidence to support these
  claims. I am interested in learning more details about this. So far we have
  experienced difficulties in drawing conclusive and extra information from CWT
  for localized corrosion detection [e.g. Reference: Tan, Y. J., Lowe, A.,
  Kinsella, B. and Bailey, S. 1998, 'Analysis of electrochemical noise using
  Fourier transform, maximum entropy and wavelet methods', Proc. ACA Corrosion
  &amp; Prevention 98, Hobart, Australia].</p>
  <p>2. The ECN shown in Figure 2 appears to me to be common and is obtainable
  from almost all relatively stable corroding systems. Based on a
  self-similarity structure in the time-frequency phase plane (Figure 3), the
  authors concluded that corrosion process has a chaotic nature. However, as we
  know, corrosion system is often relatively unstable and ECN often contains DC
  trends and sharp spikes. In these cases, this self-similarity would be lost.
  Is this corrosion process still 'chaotic' from the point of view of CWT
  analysis?</p>
  <p>3. What are your evidences supporting the conclusion 'the process of metal
  corrosion has a chaotic characteristic and determinacy.' Please explain
  'behind the randomness, there are inner rules and determinacy.'</p>
  <p>I understand that this work is at its preliminary stage and more work is
  planned, as indicated by the authors. However, I believe that it would be very
  helpful if the authors address the above questions.</p>
