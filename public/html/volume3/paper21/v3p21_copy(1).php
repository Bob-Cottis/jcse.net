<h3 align=left><b>Conclusion</b></h3>

<SCRIPT>comment(36)</SCRIPT><p>The corrosion of mild steel in HCl
solution without inhibitor increases with increase in acid concentration and
temperature. The inhibitors used in this investigation (AMMPTA, AMMPTC and TGA)
inhibit the acid corrosion of mild steel to various degrees. The order of
inhibition efficiency (%) is AMMPTA &gt; AMMPTC &gt; TGA. On the basis of� activation energy, AMMPTA and its precursor
obey the mechanism of physical adsorption. The inhibition efficiency increases
with increasing inhibitor concentration and decreasing temperature.</p>

