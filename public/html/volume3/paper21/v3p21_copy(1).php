efficiencies are recorded for all the inhibitors� signifying that the compounds are all physically adsorbed on the
mild steel coupons for the inhibition to be effective. The average activation
energy values of the inhibitors reported as 73.9 kJmol<sup>-1</sup> for AMMPTA,
61.5 kJmol<sup>-1</sup> for AMMPTC and 46.6 kJmol <sup>-1</sup> for TGA confirm
the assertion that the inhibition of the corrosion of mild steel is by physical
adsorption mechanism. This observation is in good agreement with several
investigators [3,5 ]. These reports state that the heat of chemical adsorption
should be greater than 80 kJ mol<sup>-1</sup> and less than this value
signifies a physical adsorption mechanism. Also, for a chemical adsorption
mechanism, inhibition efficiency increases with increase in temperature [12 ],
whereas increase in inhibition efficiency with decrease in temperature is
suggestive of a physical adsorption mechanism (tables 1-3 and fig. 3). Since we
have proposed physical adsorption mechanism for the present inhibitors, a multi
