<SCRIPT>comment(22)</SCRIPT>  <p>Dissolved salts have an important effect on film breakdown in aluminium
  alloys and they are known to concentrate in localised corrosion cells, yet the
  increase in filament growth resulting from deliberate chloride contamination
  was surprisingly small. This could be related to the way that the filiform
  corrosion test was performed in that a high concentration of chloride was also
  deposited on the surface of the scribe during exposure to HCl vapour in the
  initiation stage. It was shown that increasing the initiation time caused
  longer relative filament lengths due to the higher concentration of salt
  deposited.&nbsp;</p>
<SCRIPT>comment(23)</SCRIPT>  <p><u>Effect of surface polishing </u>It is known that filaments tend to
  follow the surface irregularities introduced by the rolling process (typically
  5 &mu;m deep ) and that there is little development
  of filaments in the transverse direction [6]. For this reason, it was expected
  that the extent of filiform corrosion would be affected by polishing the
  surface with different grades of silicon carbide papers. Fig 8 shows the
  relative filament lengths for non-chromated panels that had been polished with
  320 grit (45 &mu;m), 800 grit (22 &mu;m) and 4000 grit (5 &mu;m) papers. The finer
  polishing had a beneficial effect in reducing the filament length and these
  results appear to support the view that filiform corrosion is enhanced by
  surface topography.</p>
