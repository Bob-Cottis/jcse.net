<SCRIPT>comment(31)</SCRIPT>  <p>It appears that the excellent performance of the electropolished surface
  could be attributed in part to the reduction in surface roughness. A further
  consideration is that each stage in polishing not only reduces the
  irregularities but it also involves the removal of more of the surface. The
  electropolished panels, which were the least susceptible to filiform
  corrosion, had more material removed during preparation. In a similar way,
  sequential mechanical polishing (45 &mu;m - 22 &mu;m - 5 &mu;m), 
  that was shown in Fig 8 to reduce the
  relative filament lengths, also required removing successively more material.
  However, the mechanical and electropolishing treatments removed approximately
  100 &mu;m and 200 &mu;m,
  respectively. This was considerably greater than the depth of the original
  surface layer and it is clear that all of the polished surfaces would have
  been free of high temperature oxide films, impressed rolling lubricant and
  other sources of contamination arising from processing. It is considered that
  it was the removal of the surface layer and the resulting freedom from
  contamination that produced the greatest contribution to the improvement in
  filiform corrosion resistance but there were additional benefits in
  controlling filiform corrosion by polishing to reduce the surface
  irregularities. The effects of all the cleaning and pretreatments are
  summarised in Table 4.</p>
