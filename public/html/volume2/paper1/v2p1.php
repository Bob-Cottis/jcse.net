<SCRIPT>comment(8)</SCRIPT>  <p><u>Filiform corrosion testing</u> The painted panels were tested using the
  standard filiform method BS X 32: 1991. A scribe 2mm wide and 0.15 mm deep was
  produced in the longitudinal and transverse directions. Filiform corrosion was
  initiated by exposure to HCl vapour for 1 hour and the panels were then tested
  at 42&deg;C and 82% RH for 1000 hours. The extent of filiform corrosion was
  quantitatively assessed by measuring the lengths and areas of the filaments
  that developed on each scribe using a Leica 500QMC image analyser.</p>
<SCRIPT>comment(9)</SCRIPT>  <h3>Results and Discussion</h3>
  The appearance of the filiform corrosion that developed on the non-chromated
  panels is shown in Fig 1. In each case, the filaments grew more rapidly on the
  transverse scribe and propagated in the direction of the irregularities on the
  rolled surface. After long exposure times the filaments slowed down and
  eventually stopped growing as shown in Fig 2. This behaviour suggests that the
  filaments develop by a mechanism that involves oxygen diffusion along the
  length of the filament to a cathode region just behind the growing anodic
  head. This process becomes more difficult as the length increases and as the
  corrosion product dries out.&nbsp;
