<SCRIPT>comment(12)</SCRIPT>  <p>It was found that the most useful measure of the extent of filiform
  corrosion was the total length of the filaments that developed on the 70 mm
  long scribe during the 1000 hours exposure. In each case, these total lengths
  were expressed as Relative Filament Lengths (%) by comparing them with those
  that formed on 3003A when tested in the painted mill finish condition, without
  any form of pretreatment. The filament lengths were consistent between the two
  replicate panels, being within 15% of each other in all cases and within 10%
  on panels that had been chromated.&nbsp;</p>
