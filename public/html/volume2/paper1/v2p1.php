<SCRIPT>comment(33)</SCRIPT>  <h3>Conclusions</h3>
  <ol>
<SCRIPT>comment(34)</SCRIPT>    <li>The filiform corrosion susceptibility of aluminium sheet was shown to be
      more closely related to the condition of the original surface layer than
      to the microstructure of the substrate. This was due to the presence on
      the surface of high temperature oxides, rolling lubricants and other
      contaminants from processing.&nbsp;</li>
<SCRIPT>comment(35)</SCRIPT>    <li>Solvent cleaning of the aluminium prior to painting could reduce some of
      the subsequent development of corrosion filaments but those treatments
      that removed the original mill finished surface layer were found to be
      more effective.&nbsp;</li>
<SCRIPT>comment(36)</SCRIPT>    <li>The use of appropriate alkaline cleaning, together with mechanical or
      electrochemical polishing, was able to control filiform corrosion to a
      similar extent to a chromate conversion coating.&nbsp;</li>
<SCRIPT>comment(37)</SCRIPT>    <li>Electropolishing the surface gave the greatest reduction in filament
      development. This was attributed both to the efficient removal of the
      surface layer and to the production of a microscopically flat surface,
      free from the polishing irregularities that promote filiform corrosion.</li>
  </ol>
