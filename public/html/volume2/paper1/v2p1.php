<SCRIPT>comment(25)</SCRIPT>  <p>Although the corrosion filaments (typically 70 &mu;m 
  diameter) are larger than the surface irregularities [6,9], it is likely
  that the occluded cell at the head of the filament can propagate more easily
  along the irregularities and finer polishing would have the effect of reducing
  these pre-existing corrosion paths. This point was examined further by
  electropolishing the surface to remove all grinding and rolling marks and the
  results of these tests are discussed later. An alternative explanation for the
  effect of surface topography on the mill finished panels is that paint
  adhesion may be poorer at the bottom of the rolling marks. This could be the
  result of inadequate cleaning, particularly where rolling lubricant has been
  incorporated into the surface.&nbsp;</p>
<SCRIPT>comment(26)</SCRIPT>  <p><u>Effect of polishing direction </u>A further possible explanation for
  filiform corrosion following the rolling direction more easily than the
  transverse direction is because it is affected by the elongated microstructure
  of the substrate rather than by the surface topography. (The aspect ratios of
  the grains in the three materials are shown in Table 3.) This possibility was
  examined by testing panels in which different areas had been polished in
  either the longitudinal or the transverse directions. It was shown that the
  filaments followed the direction of the polishing marks rather than that of
  the elongated grains and the relative filament lengths were the same in each
  case. The results indicate that the directional filiform growth is caused by
  characteristics of the surface layer rather than by the properties of the
  substrate microstructure [10].<br>
