<SCRIPT>notify();</SCRIPT><SCRIPT>comment(1)</SCRIPT><h3>Abstract</h3>
  Filiform corrosion tests to BS X 32: 1991 were performed on 3XXX series
  aluminium alloys to study the effectiveness of various cleaning methods and
  chromating treatments and the influence of grain structure, surface topography
  and chloride contamination. The filiform susceptibility was shown to be more
  closely related to the surface condition than to the substrate microstructure
  and those treatments that removed the original mill finished surface layer
  were found to be the most effective. The behaviour of the chromate treated
  panels was only slightly affected by the thickness of the conversion coatings
  and similar resistance could be achieved by an alternative treatment in an
  appropriate alkaline cleaner together with mechanical or electrochemical
  polishing. The greatest reduction in filament growth was achieved by
  electropolishing, due both to the efficient removal of contamination and the
  freedom from surface irregularities that enhance filament growth in the
  rolling direction.&nbsp;
