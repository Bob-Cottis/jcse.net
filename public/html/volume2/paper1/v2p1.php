<SCRIPT>comment(17)</SCRIPT>  <p><u>Chromating time </u>In each case, longer chromating times slightly
  reduced the extent of filiform corrosion as shown in Fig 6. This is assumed to
  result from the accompanying increase in the thicknesses of the chromate
  conversion coatings with treatment time. The chromating times of 45, 90 and
  180s corresponded to coating thicknesses of approximately 60, 120 and 230 nm,
  respectively. These treatment times were considerably longer than those used
  commercially, where catalysts are added to the bath and where the time
  required to produce a 60 nm coating is typically in the order of 5 seconds.
  Even the thicker coatings were nodular in appearance and the discontinuities
  are sites at which corrosion initiation will occur. It is believed that the
  effectiveness of chromating is due, in part, to the improved paint adhesion.
  &nbsp;</p>
