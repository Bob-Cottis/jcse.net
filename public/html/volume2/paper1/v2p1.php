  microstructure, contamination, electropolish</p>
<SCRIPT>comment(3)</SCRIPT>  <h3>Introduction</h3>
  Filiform corrosion is a localised form of attack that can occur beneath paint
  films and leads to the appearance of thread-like filaments. It is usually
  considered to result from surface contamination and will only propagate in
  humid conditions [1]. The process initiates when water reaches the contaminant
  and hydrolysis of these salts results in lowering of the local pH. Filiform
  corrosion is an example of a differential aeration cell. Oxygen diffuses
  towards the tip of the filament and is reduced at cathodic sites [2]. This
  causes a rise in the pH in these regions causing corrosion products to be
  precipitated at the sides of the filament and stifling further growth. The
  effect is that corrosion continues only at the anodic tip and the filament
  develops in this direction alone. A further factor contributing to the problem
  is that the high pH is believed to be damaging to paint films, promoting
  cathodic disbondment. In addition, the voluminous corrosion products may cause
  stresses which lift the coating and assist propagation [3]. The nature of the
  chemical pretreatment used prior to painting has a very important influence on
  the subsequent occurrence of filiform corrosion [4,5]. Chromate containing
  pretreatments have been widely used commercially, although alternatives are
  being actively sought on health and environmental grounds. A considerable
  range of susceptibility can be displayed between different alloys [6]. It has
  been unclear whether this range of behaviour is caused by differences in the
  alloy compositions or by their particular microstructures or surface condition
  [7]. In the work described in this paper the individual factors that influence
  an alloyï¿½s susceptibility to filiform corrosion have been studied in detail.
  In particular, the effects of various cleaning methods and chromating
  treatments have been investigated, together with the influence of surface
  contamination, grain structure and mechanical and electrochemical polishing.
