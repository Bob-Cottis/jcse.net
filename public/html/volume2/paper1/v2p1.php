<SCRIPT>comment(19)</SCRIPT>  <p>Chromating is usually followed by a chromium VI rinse and this gave a
  further small reduction in the filament lengths. It is probable that some
  residual Cr VI remains on the surface after the rinse and that this acts as a
  corrosion inhibitor, although it was not possible to detect its presence in
  this study. Further reduction of chromium during rinsing would improve the
  properties of the conversion coating but this reaction is believed to be slow
  and the use of the chromium VI rinse on non-chromated panels only reduced the
  filament lengths by 10%.&nbsp;</p>
