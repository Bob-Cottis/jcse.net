<h4>Introduction</h4>
Coatings or linings with organic and inorganic compounds are a widespread method to provide corrosion protection in order to improve the durability of engineering structures. The corrosion protection properties are determined by complex mechanisms including factors such as the coating formulation, the transport of corrosive species, gradual defect formation which promotes the progressive adhesion loss and attack of the underlying metal.
In the development of coatings it is therefore necessary to optimize the permeability of the coating for water and corrosive species. Water in organic coatings, becomes the major cause of swelling, loss of adhesion, deterioration of the mechanical properties and start of the corrosion process. Modern electrochemical techniques, such as electrochemical impedance spectroscopy, has been used for the investigation of the protective coating properties on metals [1]. In the present work, changes of coating properties have been monitored employing electrochemical impedance spectroscopy to evaluate the protective behaviour of the final coating system containing an alkyd-type-top coating and either sprayed zinc or zinc rich paint (ZRP) based on different organic binders such as epoxy, alkyd or ethyl-silicate. The whole impedance spectrum of each coating system, in the frequency range 0.01 Hz <font FACE="Symbol">�</font>
