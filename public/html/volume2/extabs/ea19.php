Permeation of water in the coating is considered the first stage of deterioration of the total system. Water uptake can be monitored through changes in the top-paint film capacitance values (C<sub>top</sub>) with
immersion time. The permeation of water in the top film determines an increase in C<sub>top</sub>. according to the relation of Brasher and Kinsbury[2]. In this work, the diffusion coefficient of water in the top paint was determined by a non-linear least squared fit (NLLS) of the C<sub>top</sub> vs. t curve[3].
Dynamic system analysis employing small signal perturbation allows the determination of specific parameters of the system, which characterize the gradual deterioration of the final paint scheme with increasing immersion time in sea water.</p>
