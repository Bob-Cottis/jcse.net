</b><h4>Introduction.</h4>
With the introduction of double hulled designs for the carriage of crude oil and oil related products, the area of the ballast tanks to be coated has increased considerably.  Other factors such as reduced steel plate thickness, higher operating temperatures and new steel types have all combined to challenge the performance of the ballast coatings.  With major refurbishment of double hulled vessels likely to be prohibitively expensive, &quot;getting it right&quot; at new construction is essential.
This paper presents a means to achieve a cost effective anti-corrosion strategy for ballast tanks, based on selecting the optimal combination of coatings and sacrificial anodes.
Data on coating performance has been obtained from both the laboratory and from vessels in service, using several novel experimental techniques.  These results have been combined with information from coating and anode manufacturers on material costs, together with figures from shipyards, to provide a cost benefit analysis of a series of  different options over a 25 year projected service life time.
