  <h4>Cathodic disbondment test</h4>

The samples were prepared with 2 weight % concentrations of the pigments. A single holiday of 250 microns was made in the coating of each panel using  a laser ( Quanty Ray-Laser, Model GCR-150-20 NBI YAG ). Each sample was coated with a 3:1 mixture of beeswax and colophony resin rounds the edges leaving a central area of 36<b> </b>cm<sup>2</sup> unmasked.  The sample, a saturated calomel reference<b> </b>electrode with a luggin agar gel bridge  and a platinum counter electrode were dipped in 1 litre 3%w/w NaCl solution.  The working electrode, counter electrode and reference electrode were connected to the terminals of a potentiostat, Thomson model Ministat 253 and maintained at -1000 mV (SCE) The disbonded area was calculated by weighing tracing paper of an area equal to the separated layer of coating.

  <h4>Accelerated testing</h4>

Two accelerated atmospheric corrosion tests were performed.
Firstly a 1000h 5% NaCl continuous salt spray test according to ASTM: B117-95. Both scribed and unscribed samples were tested. Various degradation features were selected and assigned a number between 1 and 6 based on visual observation where the higher the number indicates the more corrosion. Specific features include; corrosion product at the edge, corrosion products at the scribe, blistering size and extent, black spots not at scribe and edge. Five samples were tested for each situation and the numbers were added. Thus the number for best is 6 and worst is 30.
Secondly, a 2000h wet dry cyclic test was performed using Artificial Manchester Rain Water. Because of the benign nature of the environment only scribed samples were tested. Cabinet conditions were 2h at 25<sup>0</sup>C with spray followed by 2h at 35<sup>0</sup>C without spray. Visual assessment was carried out as previously described except no blistering took place and was therefore not considered.<dir>

<h4>Results and Discussion</h4>
</dir>

  Figures 1, 2 and 3 show the rates of disbonding over 15 days for the five systems. Clearly the greatest disbonding occurs with the 0% protonated PANI, the least disbonding with the strontium chromate. Further, the strontium chromate system is the only system to exhibit a delay time before disbonding. The rest of the samples are not too dissimilar. The lack of alkali resistance of the 0% protonated PANI [5], is the probable explanation of the poor performance of this system. The resistance of the chromate system may be explained by a variety of mechanisms including enhanced adhesion[6], resistance to cathodic reduction[7], improved cross linking of the adjacent coating[8].
