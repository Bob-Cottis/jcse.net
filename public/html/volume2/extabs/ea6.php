<p><b>Keywords: Poly-aniline, Powder epoxy coatings, Strontium chromate, Corrosion prevention, Cathodic disbonding, Accelerated testing<dir>

</b><h4>Introduction</h4>
</dir>

  Previous work by the same authors has looked at dispersion of protonated polyaniline, PANI, (42% and 50% protonated[1],[2]) in a solvent borne epoxy system. This produced severe flocculation in the final film and further study on these systems was abandoned. When attempting to use powder techniques it was found that the problem of flocculation disappeared and this paper reports data on the powder coating system. The purpose of this research is to examine the corrosion resistance of the different states of PANI in comparison with strontium chromate in a powder epoxy coating, under three conditions of exposure; namely full immersion in 3%w/w NaCl solution at &#150;1000 mV(SCE), hot salt spray testing (ASTM B117-95) and a wet-dry cyclic test employing artificial Manchester rain water[3].

<dir>

<h4>Preparation of Powder Epoxy Paint</h4>
</dir>

  Different states of PANI(0%, 42% and 50% protonated) were synthesised based on the oxidation reaction using ammonium persulphate[4]. The powder epoxy was produced according to the following process:
  <p>All materials including epoxy resin, hardener, flowing agent and pigment (different states of polyaniline, strontium chromate) were premixed in 2%w/w concentration, 5 min 1800 rpm, in order to prepare for the next stage of extrusion which is a homogenous blend of ingredients.
  <p>Mixed materials were extruded so that the pigment blends and disperses more efficiently.<b> </b>The extruded polymer was chipped using a  Cumberland Granulater and granulated and ground to a fine powder by a mini-kek pin disk mill.
  <p>The final epoxy powder paint was sieved obtain small particles less than 75 microns in size.
  <p>The powder coatings were applied to a thickness of 22<font FACE="Symbol">±</font>
2 microns using an electrostatic spray gun. The curing schedule used for all powder coated panels was 15 minutes at 180<font FACE="Symbol">°</font>
C.
