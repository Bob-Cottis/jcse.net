<h4 ALIGN="left"><b>Conclusions</h4>
</b>

<ol>

<li>Run of mechanical characteristics of the epoxide coating substantially depends on the ageing kind.</li>
<li>Ageing of the epoxide coating causes deterioration of its mechanical properties, such as: hardness, static tensile strength, unit elongation. It also influences changes in mechanical characteristics obtained by means of dynamic mechanical thermal analyse, i.e. elongation and dynamic storage modulus E&#146;.</li>
<li>Embrittlement of epoxide coatings increases with ageing time. It is also affected by the kind of ageing. The coating aged with ultraviolet radiation showed the highest embrittlement. It resulted from significant oxidation degree of the epoxide.</li>
<li>As a result of ageing of the epoxide, pigment and filler particles lose coherence with it and are spalled from the coating structure. This results in an increase of the coating roughness. The coating aged with ultraviolet radiation showed the highest roughness.</li></ol>

