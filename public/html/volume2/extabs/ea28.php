<h2>Influence of Ageing on Mechanical Properties of Epoxide Coating</h2>
<p>Danuta Kotnarowska<br>
<i>Radom Technical University, Al. Chrobrego 45, 26-600 Radom. Poland<br>
<SCRIPT>mailto2('Kotnard','kiux.man.radom.pl');</SCRIPT></p>
</i><b><p>Keywords: epoxide coating, ageing, mechanical properties</p>
</b><h4>Introduction</h4>
<p>Optimal choice of organic coatings that protect technical equipment &#150; during the design process &#150; requires a knowledge of wear processes of these coatings. Therefore, it is necessary to do model (accelerated) tests in order to predict the coating service life because multi-layer metal-organic coatings perform their protective role for a long time, even for fifty years. Unfortunately, there is not yet developed an universal accelerated test and it is not found the correlation between results of accelerated tests and weathering. Such situation results from a non-sufficient knowledge of destruction processes of an organic coating under the influence of ambient factors. The multicriterion investigations of physico-chemical properties of organic coatings aged under various conditions that are carried out by the author complete the knowledge from this field. The paper describes the effect of destruction of epoxide coatings aged with ultraviolet radiation, thermal shocks, aggressive media and weathering. The effect of these factors on the course of mechanical characteristics was estimated as well. The author uses the investigation results for prediction the organic coating service life.</p>
<h4>Investigation Methods</h4>
<p>The model investigated coating was made of chemical resistant epoxide paint. Destruction kinetics of the epoxide coating aged with 5% water solutions of sulphuric acid, potassium hydroxide and sodium chloride as well as ultraviolet radiation, thermal shocks and weathering was investigated. </p>
<p>Estimation of wear processes of the aged epoxide coatings was done with the help of:</p>

<p>1) infrared spectroscopic investigations,<br>
2) thermogravimetric investigations,<br>
3) microscopic investigations,<br>
4) profilometric investigations,<br>
5) investigations of statical strength,<br>
6) investigations of dynamical strength<br>
7) hardness investigations.</p>

<h4>Effect of Ultraviolet Radiation on Chemical Ageing of the Epoxide Coating</h4>
<p>The changes occuring with time in the chemical structure of the epoxide coating superficial layer were studied in infrared spectroscopy investigations. It was found an increase of absorption of infrared radiation in the range 1800-1392&nbsp;cm<sup>-1</sup> what indicates ageing processes of the epoxide. The band 1800-1552&nbsp;cm<sup>-1</sup> included in this range represents oxidation products [1�3]. It contains the band that represents carbonyl group vibration. In case of the epoxide coatings aged with thermal shocks, weathering and with 5% water solutions of sodium chloride or potassium hydroxide it was stated an increase in carbonyl group intensity in the band 1807-1700 cm<sup>-1</sup> &#150; with the maximum at 1740 cm<sup>-1</sup>. Coatings aged with ultraviolet radiation were characterised  by the highest carbonyl group intensity (in the whole measured range) and the band representing these groups was 1807-1640&nbsp;cm<sup>-1</sup> &#150; with the maximum at 1717 cm<sup>-1</sup> [1]. The band of carbonyl groups was not found in the range of 1807-1640&nbsp;cm<sup>-1</sup> in case of unaged coatings and coatings aged with 5% water solution of sulphuric acid [1]. </p>
<p>It results from the total absorbance  characteristics obtained with the use of infrared spectroscopic investigations that the ageing changes in the chemical structure of the superficial layers of the epoxide coating are non-homogenous. The most intensive ageing is due to ultraviolet radiation. The profilometric and microscopic investigations proves, however, a non-homogenous physical wear (cracking, pigment and filler spalling) of the epoxide coating surface under the influence of ambient factors. The results of microscopic investigations allow to state that number and size of the cracks depend on the ageing kind. For instance, cracks generated under the influence of 5% water solution of sulphuric acid were in the shape of rings with the diameter of 40<font FACE="Symbol">�</font>
90&nbsp;�m (for 1008 h ageing) tending to become larger with ageing time. However, cracks generated under the influence of other media were in the shape of bands 150<font FACE="Symbol">�</font>
190 �m long and 2.5<font FACE="Symbol">�</font>
10 �m wide. In the case of ultraviolet radiation action, number and size of the cracks increased with ageing time. Moreover, these cracks were characterised by silver shines in the reflected light of an optical microscope what was observed in case of weathering. Shape and shine of these cracks were similar to the &quot;silver cracks&quot; described by I.&nbsp;Narisava [4] but they were ten times wider. Substantial chemical and physical ageing of investigated epoxide coatings was caused by the action of 5% water solution of sulphuric acid what, in particular, can be seen analysing the courses of thermogravimetric characteristics. One of the greatest decreases of the decomposition start temperature as well as one of the most intensive thermal decomposition were found in the whole ageing range (the measurements were done after 240, 360, 744 and 1008 hours).</p>
<p>Essential ageing changes, in case of action of 5% sulphuric acid water solution, were confirmed in the investigations by the means of dynamic mechanical thermal analyse (DMTA). A substantial increase (by 30%) of the glass transition temperature was found for 1008 hour ageing. A decrease of the glass transition temperature &#150; on the average by 10-15% - was found for the other ageing kinds.</p>
<p>Action of 5% sulphuric acid water solution caused an increase of waviness depth from 1.9&nbsp;�m (for the unaged coating) to 63&nbsp;�m (for the coating aged for 1008 hours) [1]. The greatest changes of the coating roughness, estimated with
arithmetic mean roughness (R<sub>a</sub>) and mean paek-to-valley height (R<sub>z</sub>) were caused by the action of ultraviolet radiation).This was connected with pigment and filler spalling from the coating structure as a result of the loss of coherence with the epoxide. This kind of ageing contributes to the greatest decrease of the coating hardness (Fig. 1). </p>
