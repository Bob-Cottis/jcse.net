<p>Keywords: </p>
</b><h4>Introduction</h4>
Short term testing of materials is always required and therefore new test methods are still under development. Corrosion is a severe problem and coatings, specially organic coatings are widely used. Up to date materials give a guaranteed protection period of more than 5 years and so the performance of such materials are quite good. Nevertheless it is necessary to develop new or modified coating materials due to new legal situation or customer requirements. 
Our new short term test for coatings using a vapor deposited metal film on a glass plate as a sensor allows to determine the performance of coating materials. 
Furthermore this new test can also be applied in the case of corrosion studies. A corrosive environment attacks the metal film and causes its dissolution, whereby the required time until the dissolution gives a measuring value for the corrosiveness of the media. 

<h4>Experimental Setup</h4>
<img SRC="../extabs/Image149.gif" WIDTH="382" HEIGHT="295">
<p><b>Fig. 1: Schematic diagram of the experimental setup.</b></p>
<p>
The mirror like vapor deposited metal film reflects the laser beam and the reflected intensity is measured with a light detector. This metal film can be overcoated with the coating material and after curing immersed into a test solution which is fitted in the laser reflection photometer. This photometer measures the reflectivity of the metal film from the back (Fig. 1). The decrease in reflectivity is connected with the corrosion of the metal film under the coating due to permeation and diffusion of the corrosive media to the substrate. 
If the bare metal film is exposed to a corrosive media the dissolution of the film is directly linked to the resistance of the metal against the investigated media.
Therefore this test can also be used as a short term test for inhibitors.
So the period, until the reflectivity decreases to a certain value, is representative for the persistence of the coating against the corrosive media and depends on the chemical nature of the binder and the type and amount of pigments. 
To increase the aggressiveness of the media organic solvents or salts can be used additionally.</p>
<h4>Results And Discussion</h4>
<font SIZE="2"><img SRC="../extabs/ea35.2.gif" WIDTH="594" HEIGHT="420"></font>
<p><b>Fig. 2: Determination of the durability of three different coatings
against ln HCl</b></p>
<p>
The reproducibility of the method is demonstrated in Fig. 2. Several curves of three different coating materials are plotted together in the same diagram. As metal iron was used and the thickness of the deposited metal film was 50nm. The metallic side was sprayed with three coating materials and the coated samples were immersed into a one molar hydrochloric acid. It can be seen that for every coating the curves are falling together, giving three groups of curves corresponding to the distinct performance of three different coating materials (on a logarithmic time scale). The absolute value at the beginning and at the end of the measurement are unimportant and can be chosen. Important is the time when the change takes place. 
In Fig. 2 iron was chosen as metal, but this choice limits the aggressive media because only acids and more precisely hydrochloric acid, like in our experiments, can cause a significant decay of the iron film. In alkaline media iron stays passive and no corrosion occurs. 
As a further metal Aluminum can be deposited, which corrodes in acids and in alkaline media. The use of an alkaline media offers the advantage that the alkaline hydrolysis of the polymers can be studied easily.
In principle, this simple technique could be extended to every metal which is of interest and under every condition, even at high pressure and high temperatures, only the measuring cell, in which the experiment has to be carried out, must be adapted to the operating conditions.</p>
<h4>Conclusion</h4>
LAREPH (laser reflection photometry) seems to be a new promising technique to determine the performance of coatings. LAREPH can be helpful for the evaluation of the chemical resistance of coatings and floorings if transparent mixtures of organic solvents with aggressive agents are used.
The use of LAREPH can be extended to many applications in material science.<br>
