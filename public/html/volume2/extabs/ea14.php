<h2>Silane Coatings for Replacement of Phosphate/Chromate Pretreatments of Automotive Metal Sheets</h2>
W.J van Ooij<sup>*</sup> and Guru Prasad Sundararajan<b><br>
*</b><i>Department of Materials Science and Engineering University of Cincinnati Cincinnati, OH 45221-0012  USA </i><script>mailto2('wvanooij','uceng.uc.edu')</script></i>
<b>Keywords:</b> <b>Silanes, E-coat, Hot-salt soak test, EIS, NMP, A-1170, A-1289<dir>

</b><h4>Introduction</h4>
</dir>

  The environmentally toxic properties of Phosphate and Chromate pretreatments on automotive metals have been widely documented for many years recently. Although some alternatives have been proposed [1,2], they have not yet been universally acceptable or cost-effective without loss of performance. In our laboratory we have developed a environmentally friendly as well as cost-effective method of replacing the conventional pretreatments by the use of novel organofunctional silanes on automotive alloys which includes electro galvanized steel (EGS), hot-dipped galvanized steel (HDG), cold-rolled steel (CRS) and aluminum alloys (Al 6061  &amp; Al 6111). The typical silanes are bis-organofunctional silanes viz.  (A-1170) bis-(trimethoxysilylpropyl)amine and (A-1289)bis-(triethoxysilylpropyl)tetra sulfide. Earlier [3] we had reported the performance of organofunctional and non-functional silanes on metals as a pretreatment for painting and demonstrated that the silane with the optimum performance is not the same for all the metals. However, as a part of the ongoing work on automotive steels, we have now successfully reduced the choice of silanes to the above two silanes. It is shown that if either of the two silanes (A-1170/A-1289) alone or their mixtures, if properly applied, the silane treatment can outperform the currently used phosphating (with chromate final rinse) on automotive metal sheets. After active hydrolysis with water A-1170 obtains the structure: (HO)<sub> 3  </sub> - Si &#150; (CH<sub>2</sub>)<sub> 3 </sub>&#150;NH - (CH<sub>2</sub>)<sub> 3 </sub>&#150; Si- (OH)<sub> 3</sub>. A-1170 is supplied as a methoxy ester while A-1289 is supplied as an ethoxy ester. Similarly A-1289 has the following structure after hydrolysis: (HO)<sub> 3  </sub> - Si &#150; (CH<sub>2</sub>)<sub> 3 </sub>&#150;S<sub> 4</sub> - (CH<sub>2</sub>)<sub> 3 </sub>&#150; Si- (OH)<sub> 3</sub> 
  <p>A-1170 being an amino silane could be completely hydrolyzed in water. However, the stable pH range (where condensation of silanols) do not occur was found to be between 4-9.5. A-1289 on the other hand required at least 50-vol% of solvent (preferably ethanol) and had a pH stability range of 3.5- 9.5 [4]. However, we have earlier shown [5] that amino silane hydrolysis at an acid pH protonates the group and hence unfavorable especially on zinc surface as the zinc oxide is stable within a pH of 6 &#150;12. So A-1170 was deposited at a pH of 8.5 on EGS, acetic acid being used for controlling the pH. Whilst A-1289 could be hydrolyzed without addition of acid at a natural pH of 4.5
  <p>The silanes after application were in-place cured or preferentially baked at 100<sup>o</sup>C for 5 minutes and then electro coated with the standard automotive E-coat ED-5000. The performances of the silanes were then reported for the EIS, GM 9540P accelerated cyclic corrosion test and NMP (N- Methyl pyrrolidinone) swelling test [6]. In order to screen the silanes for performance and to reduce the 1000 hr Salt Spray test program, we have introduced a new test called the Hot-Salt Soak test (HSS). This involves the immersion of  e-coated panels with two parallel scribes (scribed deep into the base metal) about 10 cm long into a 3% NaCl solution for 5 days at 55<sup> o</sup>C. The panels are then washed with DI water and a tape-pull off using an adhesive tape is done. The average creep from the scribe gives the extent of adhesion of the e-coat with the metal. (Fig.1). HSS test has been found to be very useful for demonstrating the effect of adhesion of functional silanes with the e-coat.  The silane films were also characterized by RA-IR.

<dir>

<h4>Experimental</h4>
</dir>

  It has been earlier reported that the properties of the silane films on metals depends upon the metal cleaning procedure, concentration of the silane, pH of application and post-treatment [2-7]. A 2% vol. A-1170 was hydrolyzed at pH=8.5 for about a day and and a 5 vol.% of A-1289 were hydrolyzed in 50-45 ethanol-water mixture for about 4 days for complete hydrolysis [8]. All the automotive metals were degreased ultrasonically using acetone, hexane and then alkaline cleaned with alkaline cleaner for 5-10 minutes. They were then washed thoroughly with DI water until complete wettability was attained and then blown-dried. They were then dipped into the corresponding silane solution for at least 2 seconds and then in-place cured. A-1170 coated silane films were preferentially baked at 100C for 5 minutes. The silane coated films were then cathodically electro coated with the paint ED-5000 (from PPG Inc.). The painted panels were then screened using HSS test. EIS in 3% NaCl provided the resistance of the e-coat with the silane, while the panels were also subjected to the GM Scab creep cyclic corrosion test which was performed by exposing them in a humidity chamber to conditions at 60C and 85% relative humidity followed by salt immersion, drying and exposure in a cycle for 4 weeks. The scribe creep was measured after a tape-pull off using a standard adhesive tape.

<dir>

<h4>Results and Discussion</h4>
<p><i>Hot-Salt Soak Test and GM Scab Creep Test</i></p>
</dir>

  While either of the above two silanes deposited on EGS, HDG and Al 6061 could provide adhesion and corrosion performance on par with the phosphated system, for CRS it was found that the mixture of A-1170 &amp; A-1289 (2% prehydrolyzed conc. each, 9:1 mix ratio, at pH= 8), provided results comparable to the zinc phosphated CRS The Hot-Salt Soak test clearly shows the effect of pH on automotive substrates, specially on zinc surfaces. (Table.1). While, A-1170 deposited between 8-9 showed the best performance, those silanes deposited below pH=8 showed considerable delamination of the paint.  Similarly the concentration of the silane was also critical as far as the e-coatability is concerned. Concentrations of A-1170 higher than 2% provided inferior performance. This correlated with the scribe creep from GM test where in the A-1170 deposited at a pH= 5, showed lower performance. The requirement of an optimum concentration (2% in case of A-1170) is also seen in the GM test results (Table. 2)

<dir>

<p><i>EIS of E-Coated Steels</i></p>
</dir>

  EIS has been shown to be a very scientific method to study the corrosion performance of polymer-coated metals in recent years. However, from the data (Table 3) shown, it can be inferred that the quality of the paint can mask the performance of pretreatment. Hence in this case EIS testing at pH = 6 was not useful for studying the effect of pretreatment on metals. Also we can see that even an alkaline cleaned panel with e-coat performs well in neutral 3% NaCl. Also we can see that the correlation between EIS and GM Scab tests were poor as far as performance of A-1289 on CRS is concerned. This was in agreement with our earlier results indicating poor correlation between adhesion and corrosion results [9]

<dir>

<p><i>RA-IR</i> </p>
</dir>

  The A-1170 and A-1289 silane films were also characterized by RA-IR. The IR spectrum (fig.2) of A-1170 shows the presence of secondary aliphatic NH peak at 1144 cm<sup>-1</sup> and IR (fig.3) of A-1289 shows S-S peaks at 460 cm<sup>-1</sup>.
