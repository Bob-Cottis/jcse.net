<h4>Abstract</h4>
<p>There is much interest in the use of plasma polymer (PP) coatings to promote adhesion and/or inhibit corrosion of metals.[1] Here, we explore the use of carboxylic acid functionalised PP as adhesion promotion coatings on aluminium. The acid functionality, retained from the monomer structure in the plasma deposition process[2], is intended to form stable chemical bonds with both an aluminium oxide/hydroxide surface and epoxy based adhesives.[3]
Plasma polymerised acrylic acid and a plasma co-polymer of acrylic acid and octadiene[4], have been deposited onto AA6016 aluminium alloy using RF deposition apparatus described elsewhere.[2] An amine hardened epoxy-based adhesive formulation (Ciba Polymers XW1044-3) has been used to bond single lap shear (SLS) joints using PP-coated, or chromic acid anodised (CAA), alloy. The durability was probed by testing joints after storage in water at 60 <font FACE="Symbol">°</font>
C. The relative success of the adhesion promotion pretreatments can be gauged by combining tensile testing of SLS joints with locus-of-failure determination from TEM and imaging-XPS.
The failure strength of SLS joints bonded using carboxylic acid functionalised PP coated alloy was comparable with CAA. Examination of the fracture surfaces showed that all failures had occurred in the adhesive. Failure was visually at a near-interfacial region after ageing of the joints in water. TEM of ultramicrotomed sections from these areas indicated that failure occurred at the PP- adhesive interface. Imaging XPS indicated that failure also occurred at the PP-Al interface. Optimisation of the plasma pretreatment and the plasma polymer coatings is underway to obtain a durability superior to that of CAA.
The role of the of the PP chemistry in bonding was investigated through comparison with the non-functional plasma polymerised hexane surface. This exhibited a much reduced failure strain, implicating the carboxylic acid functionality in the bonding process. The application of this approach to the delineation of mechanical keying and interfacial chemical bonding is discussed.&nbsp;</p>
