<h2>A Simple AC Sensor for the Identification of Pores in Organic Coatings</h2>
</dir>


  J.D. Scantlebury<sup>1</sup>, C.A. Wright<sup>2</sup>, L.M. Callow<sup>3
<i>
<p>1</sup>Corrosion and Protection Centre, UMIST, PO Box 88, Manchester, M60 1QD. UK <SCRIPT>mailto2('scantlebury','manchester.ac.uk');</SCRIPT>

<sup>
