</i><b>Keywords:</b> AC sensor, Pores in coatings 
In early work on intact polystyrene lacquers on mild steel immersed in sodium chloride solutions, Mayne (1) showed that the corrosion under the lacquer was electrochemical with special separation of anodic and cathodic areas.  Cathodic reactions led to underfilm blistering, the blisters being filled with sodium hydroxide solution.  The anodic reaction was iron oxidation and iron ions were thought to migrate across the film at regions where ionic induction was easier.  These easy conduction pathways were further investigated by Mayne and his co-workers (2) and it was found that such areas had ionic resistance values which decreased as the bathing solution concentration increased and Mayne coined the term &quot;D&quot; area to stand for these paths of easy conduction.  This early work was concerned with unpigmented polymer lacquers but such &quot;D&quot; areas were also observed with polymers pigmented with iron oxide (3) and zinc oxide (4).  &quot;D&quot; areas were subsequently correlated with underfilm corrosion with the classical study undertaken by Mayne and Mills (5).
Since &quot;D&quot; areas were first proposed, there has been many attempts to explain the presence of D areas in terms of polymer structure and currently there are three likely explanations.  Firstly, most paints and organic polymer films cure by a chemical reaction after application that cross links the polymer to form a three dimensional network.  This cross-linking reaction can either be driven by atmospheric oxygen in the case of air-drying systems or by the reaction of two prepolymer components that are mixed just before application in the case of 2-pack epoxy systems.  In such a polymerisation process, there will be a spread of cross linking density and one possible explanation of &quot;D&quot; areas in regions of very low cross link density in the polymer film.  This hypothesis was shown to be true using a solvent-swelling technique in previously identified &quot;D&quot; areas (6).
A second explanation of &quot;D&quot; areas may be applied to polymers which carry polar side chains.  The most common example of such polymers used as the basis for organic coatings is the air drying alkyd which has a high concentration of fixed acid groups.  The explanation involves a high concentration of polar groups which might aggregate around a specific volume in the film and ion conduction would proceed more readily through these &quot;pipes&quot;.  Correlation between these features and conduction has been reported (7).
Shortly before his death, Mayne (8) had been studying solvent-free epoxy systems and investigating the role of solvent and solvent retention on coating performance in general and the presence of &quot;D&quot; areas in particular.  He concluded that &quot;in the absence of solvents, the films have much higher electrolytic resistance and a more homogenous structure consequently the elimination of solvents leads to films of increased protective value&quot;.
Such paths of easy ion conduction or &quot;D&quot; areas are one of the most important features of a corrosion protective film and the knowledge of their location and increase in number with time provides useful information on the mechanism of breakdown.
In this study, a rapid and simple AC driven probe was constructed to enable regions of high conductivity to be quickly identified. Such a technique although uncommon in the laboratory, has been used for many years for the identification of defects in coatings in pipelines. (9). 
<b>
  <h4>Experimental</h4>

</b>The AC signal at 2HK<sub>z</sub> amplitude 2V from a signal generator was connected to the metal substrate.  The other connector from the signal generator was fed through a specially constructed probe.  The probe was a felt tipped pen where the ink had been extracted with repeated flushing with methanol and replaced with 3% NaCl.  The detector was a cathode ray oscilloscope. The felt-tipped pen was gently moved across the whole surface of the coated specimen.  Where conduction was negligible, there was no response from the oscilloscope.  Where there was detectable conduction, a characteristic signal was observed on the oscilloscope. 
Such a probe has been used in several studies including; identification of conductive pathways in a clear alkyd and how they increase in number with immersion time and the effect of an anti-corrosion pigment, the identification of pores in lacquered cans for foodstuffs, powder epoxy coated rebar steel. Specific examples will be provided during the Conference which will be included in the full paper.
