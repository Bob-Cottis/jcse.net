<h4>Introduction</h4>
The use of Electrochemical Impedance Spectroscopy to make short-term predictions of the long-term performance of internally coated 2-piece food cans has been explored. In particular, the effects of packing with different food products and subsequent thermal processing of the cans have been investigated. 
So far, we have been able to demonstrate that: -
Relative performance of different experimental variables may be predicted using comparatively short-term impedance tests.
Different impedance parameters need to be used for the prediction of different modes of failure. Lacquer capacitance, pore resistance and charge transfer resistance are all useful as predictors derived from equivalent circuit analysis; the low frequency impedance magnitude can also be used.
It is more desirable to conduct impedance tests using the food product as electrolyte.
The most severe changes in impedance parameters occur as a result of the initial thermal processing of the cans; subsequent changes are comparatively slow.
However, the following problems have been encountered: -
A high level of scatter within variables is typical, due to the inherent variability of commercial materials and processes. 
The use of in-can electrodes to allow repeat measurements on a single can would be preferred, but experimental problems remain to be overcome
Differences in behaviour do not always manifest themselves until some time after filling, which limits the predictive capability of the technique.
