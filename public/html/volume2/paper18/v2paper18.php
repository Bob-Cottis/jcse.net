<h3><b>Abstract</b></h3>

<p>Two standard coatings containing inhibitive
pigments were scribed and the degree of protection afforded was investigated
when the specimens were immersed in 10% Harrison's solution. This was assessed
both visually, using ferricyanide solution and electrochemically. The
Electrochemical Noise Method was used to monitor corrosion and it was found
that the higher the value of R<sub>n</sub> the greater the degree of protection
afforded. The Scanning Reference Electrode Technique was also used on the same
scribed coatings. These results which provided an indication of the amount of
anodic activity along the scribe correlated well with the Electrochemical Noise
results. The effect of a pre-soak in de-mineralised water was also
investigated. It is suggested that the two methods when used together can
provide the means by which paint manufacturers can quickly and easily assess
the effectiveness of their systems in preventing corrosion at a break in the
coating.   </p>

<h3><b>Introduction</b></h3>

<p>At University College Northampton there has
been a programme of work investigating electrochemical techniques to examine
intact organic coatings on steel. Many researchers have tried to use the AC
impedance method for this [1]. Some of the fairly recent papers using this
method are summarised in a review by Murray [2]. Although this method is of
undoubted use it produces a lot of information which is sometimes difficult to
interpret and from an experimental disadvantage point of view, is electrically
intrusive. In the search for a relatively simple technique to replace the
successful DC resistance method (also electrically intrusive), the
Electrochemical Noise Method (ENM) was selected. This has now been quite extensively
investigated [3]. This followed on from previous work by Skerry [4,5] at
Sherwin-Williams, Cleveland and by Bierwagen's group [6,7] at NDSU in Fargo,
North Dakota. Recent work by two of the present authors has examined detached
and waterborne coatings on steel and aluminium [3,8]. The study has now
progressed to examine defects in coatings - deliberately created either using a
scribe or by laser ablation. Some interesting electrical responses have been
obtained and there is indirect evidence of processes occurring at the scribed
area such as protective film formation. However what was lacking, particularly
in the early stages of exposure, was direct evidence of microscopic and local
electrochemical activity as a function of time.</p>

<p>At Sheffield and elsewhere SRET (Scanning
Reference Electrode Technique) has been used and modified to study a variety of
corrosion processes [9-12]. With the ability to give localised, quantitative
information about anodic and cathodic processes it has proved a very useful
tool. Particularly useful for coatings evaluation is the rotational configured
SRET as application is easy and the problem of height variation is minimised. A
project in early 1998 looked at a Polyaniline coating on steel using SRET. As
expected from previous work with intact coatings, the high resistance of the
coating made it difficult to &quot;pick up&quot; anodic and cathodic areas, at
least during the initial stages of exposure. However when used to examine
deliberately created circular defects results proved quite promising. </p>

<p>The general aim of the work described in this
paper is to identify a fast laboratory test method to assess the effectiveness
of inhibitive materials within a coating to protect the substrate at a break.
Previous studies looking at point defects or scribes in coatings include work
by De Wit using AC Impedance [13] and work by Mills using the DC resistance
method [14]. A further objective of the work is to gain a mechanistic
understanding of the coating behaviour where protection takes place. Defects
are being investigated in several solvent-borne and water-borne primers on
steel using the Electrochemical Noise Method. Reported here are results for two
proprietary primers, an epoxy (CM) and an alkyd (RH). Red Lead in linseed oil
has also been investigated along with a control (water-borne unpigmented
epoxy). All apart from the control contain inhibitive pigments in the coatings.
Defects were produced mainly by scribing. 
Resistance (R<sub>n</sub>) measurements were monitored as a function of
time in either de-mineralised water or 10% Harrison's solution. It was found
that none of the inhibitive coatings were successful in preventing corrosion
upon immediate immersion in 10% Harrison’s solution. So coatings were given a
pre-soak by immersing for a time of 24 or 96 h in de-mineralised water. SRET
has also been used to examine scribed (deliberately damaged) areas in the same
solutions over a similar period of time. The expectation is that SRET would be
able to provide useful information about the initial stages of passivity, or of
substrate damage i.e. corrosion, at the (initially) bare metal area. </p>

<p><b>Experimental</b></p>

<p>The substrate used was steel Q panels or
steel circular (22mm diameter) SRET specimens. The two solvent-borne primers CM
(epoxy) and RH (alkyd) were expected to contain inhibitive pigments based on
zinc phosphate and zinc tetroxy chromate respectively. The solvent for the
amine portion in the unpigmented epoxy (IC 152) was water. This coating would
not be expected to contain any components that would lead to effective
inhibition at a scribe. The red lead was in linseed oil. It was used because it
was expected to be effective. Coatings were applied by spreader bar to Q panels
and by dipping for SRET specimens. Harrison’s solution (3.5% ammonium sulphate,
0.5% NaCl) was used unless indicated otherwise at 10% dilution for the tests
reported here. </p>

<p>Most of the Electrochemical Noise results for
CM and RH coatings were obtained from the same samples used for SRET (i.e.
cylinders). The beaker method was employed. This is shown in Figure 1. Fresh
scribes were made either with or without re-coating before SRET measurements.
Noise results from the red lead and IC 152 were obtained from Q panel samples
using the bridge method [3]. Blanking was achieved using a 75 / 25 mix of
beeswax and colophony resin. The scratch area was typically 0.02cm<sup>2 </sup>(10mm
x 0.2mm). The scribes were produced by a novel mechanical removal technique
using a specially designed tool that removes the area of coating rather than
displacing it by plastic/elastic deformation. The cutting edge of the blade
resembles a small lathe (part-off tool) when viewed under magnification. This
lifts the coating and shears the edges of the cut to give a square section
scribe with an easily calculable area and leads to more reproducible results.
Potassium ferricyanide indicator was used throughout this work to establish if
corrosion was actually taking place at a particular point at a particular time.
The indicator is very sensitive to Fe<sup>++</sup> ions, a blue colour can be
detected even when only traces of iron ions are present. </p>

<p>Electrochemical Noise was measured using a
zero resistance ammeter and data logger with dedicated software supplied by
ACM. Measurements are generally made every half-second for a period of 300 s.
The current between the two working electrodes is monitored and at the same
time the voltage of the pair is measured with respect to the reference
electrode. From the standard deviations of the resultant 300 data points, the
voltage noise and current noise are calculated. By dividing the former by the
latter, the parameter Noise Resistance (R<sub>n</sub>) is derived. Further
details can be found elsewhere [3,8].</p>

<p>SRET results were obtained by mapping over an
area of 1cm by 1.5cm that included the scribe. The specimen was rotated at
100rpm and the measurements were taken over a period of up to about thirty
hours. A diagram showing the experimental set-up is shown in Figure 2. Further
details on the experimental set-up can be found [9,10].  </p>

<h3><b>Results and discussion  </b></h3>

<h4>Electrochemical Noise</h4>

<p>Although not given here intact coatings were
also investigated which gave R<sub>n </sub>values that were considerably higher
than the scribed sample results. Thus all values here are attributed to the
scribe. Figure 3 shows the comparison between the clear epoxy and the CM
coating after just 2 h in solution. It can be seen that the R<sub>n </sub>value
of the CM coating is approximately an order of magnitude higher than the
unpigmented epoxy.  Reproducibility
among the four different pairs was very good. Figure 4 shows the effect of time
over a 24 h period for both CM and RH coatings in 10% Harrison’s solution.
Initially the CM coating provided a level of inhibition (corresponding to an
increase in the R<sub>n</sub> value). However within 24h both coatings were failing
to inhibit although the nature of the corrosion product was somewhat different
in the two cases. Nevertheless some degree of reduction in corrosion rate,
based on R<sub>n</sub> beyond that shown by the unpigmented epoxy, was evident.
Results for the red lead coating are shown in Figure 5. When immersed in 10%
Harrison’s solution this system was not very effectively in providing
inhibition, as shown by the ferricyanide test and low R<sub>n</sub> values.
However when pre-soaked for 96 h in water and then transferred to Harrison’s
solution, it maintained a sustained high value of R<sub>n</sub> and no visible
corrosion was seen at the scribe. This graph also shows that when the
Harrison’s solution was increased to 55% of full strength this overwhelmed the
inhibitor and visible corrosion ensued along with a drop in the R<sub>n</sub>
value to the levels of the non-inhibiting situation. Figure 6 shows a
comparison of R<sub>n </sub>value between red lead and the CM and RH coatings.
After a 24h soak in de-mineralised water the latter two primers failed to
afford much long-term (more than 24 h) protection at the scribe. However (as
already seen in figure 5) the red lead maintained a high R<sub>n</sub> value.</p>

<h4>SRET</h4>

<p>SRET is able to provide a spatially resolved
measurement of electrochemical activity at pre-selected intervals over a given
period of immersion. Note that although results given here are only for single
specimens, the effects described were reproducible i.e. very similar results
were obtained when they were rescribed and reimmersed and also from second,
nominally identical specimens. The region of the scratch is shown in Figure 7.
Some qualitative results obtained for the CM coating in 10% Harrison's are
shown in Figure 8. The green areas indicate anodic activity. It can be seen
that there is initial anodic activity in the centre of the scribe but this
decreases within 4h. If the specimen is re-scribed (after 24h) this anodic
activity recommences along the whole scribe but then falls away to almost zero
after 10 days (by this time there is some red corrosion product visible in the
base of the scribe). Results for RH coating are similar (see Figure 9). However
there is a greater degree of electrochemical activity overall which agrees with
the ENM results. Although after 10 days this has diminished, there was a quite
voluminous yellow corrosion product visible in the scribe of the RH coated
specimen.</p>

<p>The effect of a short pre-soak for the CM coating
was also investigated and the results are shown in a timed sequence over a
period of some 24 h in Figure 10 and 11. These results are presented in black
and white where the shade of grey corresponds to a probe output value in mV.
Note the darker the shade the higher the current density. </p>

<p>Although both sequences were obtained with CM
specimens in 10% Harrison's solution, the results in Figure 10 were obtained
after preliminary immersion in de-mineralised (DM) water for 2-3 h whereas
those shown in Figure 11 were obtained after immediate immersion in Harrison’s
solution. Corrosion can be seen to increase/decrease over 24h and corresponds
approximately with the R<sub>n</sub> results for the CM coatings as shown in
Figure 2. However note that there is somewhat decreased activity overall in the
pre-soaked sample showing that (as with red lead) there is some advantage in a
pre-soak with the CM coating. It should be noted that the electrochemical noise
and ferricyanide test results indicated that pre-immersion did not apparently
cause any long-term decrease in corrosion. However the greater sensitivity of
the SRET shows there is some effect at least in the early stages. </p>

<h3>Conclusions and further work</h3>

<ul>
  <li>Results obtained
using the Electrochemical Noise Method have been able to indicate the level of
protection afforded by a coating at a scribe. It would appear that a R<sub>n</sub>
value of about 2 - 4.5 x 10<sup>6</sup> is necessary for effective inhibition
on a 0.02cm<sup>2</sup> area in 10% Harrison's solution. This translates to a
value of about 4 x 10<sup>4</sup> to 9 x 10<sup>4 </sup>ohms-cm<sup>2</sup>
i.e. somewhat lower than the Bacon, Smith and Rugg criteria [15] for a
protective intact coating of 1 x 10<sup>6</sup> ohms-cm<sup>2</sup>). </li>
  <li>Results show that the CM and RH
coatings provide partial inhibition after about the first 24 hours where the CM
coating appears more effective than RH coating for immersion in 10% Harrison's
solution. Red lead is effective but requires a pre-soak in de-mineralised water
in the absence of a top-coat.</li>
  <li>The Scanning
Reference Electrode Technique has proved useful in providing direct evidence of
electrochemical activity. The technique is very sensitive and can provide both
quantitative and spatial information. The anodic/cathodic activity along the
scribe over a period of 24–48 hours as indicated by SRET illustrated the
effectiveness in retarding corrosion, of a coating containing active pigments.</li>
  <li>In this work the
effect of a pre-soak in de-mineralised water was successfully investigated
using both ENM and SRET. Hence using these two methods in combination, the
effect of different environments e.g. de-mineralised water, 10% Harrison’s
solution and 3% NaCl can be easily investigated.</li>
  <li>Further work is
currently being conducted with top-coated systems to assess whether the effects
observed here (particularly the effect of pre-soak) will still pertain. </li>
</ul>

