in water). The pressure was measured in situ. It was necessary to made a
calibration of the temperature before the experiments between an external regulation
thermocouple (K) and a thermocouple (K) set between the two windows of diamond.
Around a half an hour is necessary to reach the stability of the temperature
and pressure to begin an experiment. </p>

