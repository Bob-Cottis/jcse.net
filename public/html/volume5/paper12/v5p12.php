<p><b><font size=4 face="Times New Roman">Abstract</font></b></p>

<p><font size=4 face="Times New Roman">Ni, Fe, Cr and two Ni-Fe alloys thin foils
have been exposed to high pressure-high temperature aqueous oxidizing solutions
in order to characterize the difference of behaviour of Ni-base alloys between
sub- and supercritical thermodynamic conditions of aqueous solutions. The SEM
microstructures of the formed Ni, Fe and Cr oxides show that the Ni oxide would
promote the pitting corrosion more than Fe and Cr oxides.</font></p>

<p><b><font size=4 face="Times New Roman">Keywords: </font></b><font
size=4>corrosion, high pressure, high
temperature, Ni alloys, aqueous solutions.</font></p>

<p><b><font size=4 face="Times New Roman">Introduction</font></b></p>

<p><font size=4 face="Times New Roman">The development of crack corrosion in
Fe-Ni-Me (Me=Cr, Mo, Ti, V…) alloys in high pressure-high temperature aqueous
solutions with low concentrations of  oxygen or chlorine is a serious concern
of industrial processes and particularly the emerging waste treatment process
by supercritical water oxidation (SCWO). This corrosion phenomenon involves
gaseous species and deposits with possible contribution from oxides, sulphides
and chlorides. The corrosion of such as alloys in pure water in sub- and
supercritical conditions has been reported in several reports [ref1-ref4] but
it is still necessary to improve  research methods and develop new knowledge on
the action of the high pressure and high temperature aqueous solutions in
industrial situation. For instance only few corrosion kinetics studies have
been performed so far to prove the stability of the cracks and to measure the corresponding
corrosion rate of  nickel-base alloys which is of relevance for  industrial
applications [ref5-ref7]. Many chemical reactions can occur with different
types of corrosive species. Although these reactions are generally well known,
the transition between wet corrosion in sub- critical condition and dry
corrosion in supercritical condition modifies the local thermo-physical
properties in the fluid-solid interface and the mechanisms of
dissolution-precipitation,  causing strong damage in materials in a narrow
domain of temperature and pressure around the critical conditions (T~374°C;
P~22,1MPa).</font></p>

<p><font size=4
face="Times New Roman"> This note
reports on  <i>post mortem</i>
characterization of the products of local corrosion of metals and alloys at
high temperature and pressure using different compositions  of  water solutions
of hydrogen peroxide H<sub>2</sub>O<sub>2 </sub> as an oxidiser (between 3 and
20 wt%). The duration of the experiments is between 8 and 10 hours. </font></p>

