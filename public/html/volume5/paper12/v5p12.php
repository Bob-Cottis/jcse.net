<h4><b><font size=4 face="Times New Roman">Discussion and concluding remarks</font></b></h4>

<p><b><font size=4
face="Times New Roman">&nbsp;</font></b></p>

<p><font size=4
face="Times New Roman">&nbsp;</font></p>

<p><font size=4
face="Times New Roman">The
experimental conditions have been chosen to be sure to cross the critical point
between the two sets of experiments. The duration of the exposure of all the
samples was 10 hours. After experiments  the samples were examined using
scanning-electron microscopy (SEM). The surface of each material with the
corresponding microstructure are shown in <b>figure
4 </b>to<b> 8</b> .</font></p>

<p><font size=4 face="Times New Roman">These pictures show that the behaviour of metallic
materials is different in sub- and supercritical conditions. The concentration
of oxidizing agent increases this behaviour without strong modification of the
surface morphology. This feature is an interesting conclusion in the aim to
extrapolate the results to pure water. The difference of behaviour has been
discussed in a previous communication in terms of solubility involving
different mechanisms of dissolution-precipitation [7] of each part of the
critical point. Especially the change in the thermo-physical properties around
the critical point (the superficial energy and the viscosity are very weak in
the vicinity of the critical point) favours the crack corrosion. The pictures
show the variety of the local behaviour of the selected metals. The <b>figure 4. </b>shows the formation, in the case
of Ni, of particular sun-shape morphology, moreover decorative, but favourable
to the formation of pitting corrosion. The <b>figure
5. </b>shows less pronounced local morphology on Fe foils in the same
conditions. The <b>figure 6. </b>shows
the formation of a homogenous coating on Cr foils. Then Cr seems to be a good
alloying element to hinder the initiation of pitting effects in both sub- and
supercritical conditions. The two last pictures, <b>figure 7.</b> and <b>8.</b>
show the formation of  Ni pitting on AISI foils (10m% Ni) and mostly on Mumetal
foils (77m% Ni) with the typical form of nickel oxide morphology likely
favourable to the crack initiation.   </font></p>

<p><font size=4 face="Times New Roman">This note allows to confirm, by a simple observation
of samples treated in sub- and supercritical aqueous solutions, that the
behaviour of dissolution-precipitation were different on each part of the
critical point of  the solution. An other conclusion is the formation, by
oxidizing, of particular surface morphology of Ni  probably favourable to the
initiation of cracks. This feature is less evident with Fe and practically
absent with Cr. </font></p>

