<p><font size=4
face="Times New Roman">Oxidation
tests were performed at sub critical (T = 350 °C; P = 25 MPa) and
supercritical    (T = 390 °C; P = 25 MPa) conditions. This domain of pressure
and temperature is convenient to overcome salt precipitation and corrosion
effect. Figure 1. shows the high pressure (40 MPa) high temperature (700 K)
cell made of XN26TW. This cell, prior built for X-ray absorption experiments,
allows the avoidance of contact of the sample with the wall of the cell. A
ballast (100 cm<sup>3</sup>) at room temperature and high pressure in contact
with the experimental chamber allows the concentration of H<sub>2</sub>O<sub>2</sub>
to be kept constant during the experiments.</font></p>

<p><font size=4
face="Times New Roman">The metallic
samples were pure Fe, Cr and Ni , AISI 304 stainless steel and Mumetal (all
from Goodfellow Co.). The two alloys have been chosen on account of the large
differences in concentration in Fe and Ni (table 1). These materials  are also
still often used in reactors for sub- and supercritical water oxidation (SCWO)
of wastes. They can indeed give rise to useful indications according to the
oxidative context of the media of SCWO process</font></p>

